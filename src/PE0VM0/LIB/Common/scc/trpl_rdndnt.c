/*  U1      u1_g_TrplRdndntchk(const U4 * u4_ap_SMPL, U4 * u4_ap_maj)                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TrplRdndntchk(const U4 * u4_ap_SMPL, U4 * u4_ap_maj)
{
    U1          u1_t_stsbit;
    
    u1_t_stsbit = (U1)0U;
    if(u4_ap_SMPL[TRPL_RDNDNT_SMPL_1ST] == u4_ap_SMPL[TRPL_RDNDNT_SMPL_2ND]){
        u1_t_stsbit |= (U1)TRPL_RDNDNT_BIT_12_MTCH;
        (*u4_ap_maj) = u4_ap_SMPL[TRPL_RDNDNT_SMPL_1ST];
    }
    if(u4_ap_SMPL[TRPL_RDNDNT_SMPL_2ND] == u4_ap_SMPL[TRPL_RDNDNT_SMPL_3RD]){
        u1_t_stsbit |= (U1)TRPL_RDNDNT_BIT_23_MTCH;
        (*u4_ap_maj) = u4_ap_SMPL[TRPL_RDNDNT_SMPL_2ND];
    }
    if(u4_ap_SMPL[TRPL_RDNDNT_SMPL_3RD] == u4_ap_SMPL[TRPL_RDNDNT_SMPL_1ST]){
        u1_t_stsbit |= (U1)TRPL_RDNDNT_BIT_31_MTCH;
        (*u4_ap_maj) = u4_ap_SMPL[TRPL_RDNDNT_SMPL_3RD];
    }

    return(u1_t_stsbit);
}
/*===================================================================================================================================*/
/*  U4      u4_g_Majbit(const U4 * u4_ap_SMPL)                                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_Majbit(const U4 * u4_ap_SMPL)
{
    U4          u4_t_bit;

    u4_t_bit  = u4_ap_SMPL[TRPL_RDNDNT_SMPL_1ST] & u4_ap_SMPL[TRPL_RDNDNT_SMPL_2ND];
    u4_t_bit |= u4_ap_SMPL[TRPL_RDNDNT_SMPL_2ND] & u4_ap_SMPL[TRPL_RDNDNT_SMPL_3RD];
    u4_t_bit |= u4_ap_SMPL[TRPL_RDNDNT_SMPL_3RD] & u4_ap_SMPL[TRPL_RDNDNT_SMPL_1ST];

    return(u4_t_bit);
}
