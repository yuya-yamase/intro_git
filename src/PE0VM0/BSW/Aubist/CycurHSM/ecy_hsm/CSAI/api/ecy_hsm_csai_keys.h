/* Key properties   */
/**
 * No properties defined
 */
#define ecy_hsm_CSAI_KEYPROP_NONE                  (0x00000000UL)

/**
 * Indicates that this key has a PIN associated with it.
 * @note Flag is obsolete, do not use in CycurHSM2.x!
 */
#define ecy_hsm_CSAI_KEYPROP_HAS_PIN               (0x10000000UL)

/**
 * Flags a key as verify only. Verify only keys may only be used to verify signatures
 * but not to compute signatures.
 */
#define ecy_hsm_CSAI_KEYPROP_IS_VERIFY_ONLY        (0x00000001UL)

/**
 * Flags a key as signing key. Signing keys can only be generated by the HSM and are not
 * exportable. Signing keys cannot be used to encrypt data.
 */
#define ecy_hsm_CSAI_KEYPROP_IS_SIGNING_KEY        (0x00000002UL)

/**
 * Flags a key as public encryption key. Only public keys can be used to encrypt using an
 * asymmetric cipher or to validate a signature.
 */
#define ecy_hsm_CSAI_KEYPROP_IS_PUBLIC_KEY         (0x00000004UL)

/**
 * Flags a key as an identity key. Identity keys are the private part of an asymmetric key
 * pair and can only be used to sign data originating on the HSM.
 * These keys are comparable to the Endorsement Keys of the Trusted Platform
 * Module. Identity keys are typically generated during manufacturing and can only be
 * generated by a Security Officer session.  There is no such thing as a symmetric identity key,
 * nor an RSA public identity key.
 */
#define ecy_hsm_CSAI_KEYPROP_IS_IDENTITY_KEY       (0x00000008UL)

/**
 * Flags a key as an injected key. Injected keys are generated outside of the HSM and
 * injected to the HSM by the security officer role. Injected keys have no usage
 * restrictions, however, they cannot be flagged as identity keys.
 */
#define ecy_hsm_CSAI_KEYPROP_IS_INJECTED_KEY       (0x00000010UL)

/**
 * Flags a key as being read-only.  This means that the key cannot be updated or deleted
 */
#define ecy_hsm_CSAI_KEYPROP_IS_READONLY_KEY       (0x00000020UL)

/**
 * Flags a key as being delete-able.  This means that the key can be deleted.  In general
 * keys will not have this flag set.
 */
#define ecy_hsm_CSAI_KEYPROP_IS_DELETABLE_KEY      (0x00000040UL)

/**
 * Flags a key as not being update-able via SHE with a UID of zero.
 * This means that the correct UID for the device must be supplied in the key update message
 * if the key update is to be successful.
 */
#define ecy_hsm_CSAI_KEYPROP_DISABLE_WILDCARD      (0x00000080UL)

/**
 * Flags a key as having a usage of MAC generate/verify (if set).
 */
#define ecy_hsm_CSAI_KEYPROP_MAC_ONLY              (0x00000100UL)

/**
 * Flags a key as having been loaded into SHE as plain text, only applicable to the SHE RAM key
 */
#define ecy_hsm_CSAI_KEYPROP_SHE_LOADED_PLAIN      (0x00000200UL)

/**
 * Flags a key as having a usage of encrypt/decrypt
 */
#define ecy_hsm_CSAI_KEYPROP_ENCDEC_ONLY           (0x00000400UL)

/**
 * Flags a key as being available only if secure boot succeeds
 */
#define ecy_hsm_CSAI_KEYPROP_SECURE_BOOT_MUST_PASS (0x00000800UL)

/**
 * Flags a key as being available only without debugger connected
 */
#define ecy_hsm_CSAI_KEYPROP_LOCKED_ON_DEBUG       (0x00001000UL)

/**
 * Flags a key as being available to all open sessions
 * (without this a key is available only to the session(s) into which it has been injected
 * or loaded).
 */
#define ecy_hsm_CSAI_KEYPROP_ALL_SESSIONS          (0x00002000UL)

/**
 * Flags a object to contain private information.
 * Private objects may not be exported / forwarded to HOST side
 */
#define ecy_hsm_CSAI_KEYPROP_IS_PRIVATE_KEY        (0x00004000UL)

/**
 * Flags a object that it is an X509 certificate type
 */
#define ecy_hsm_CSAI_KEYPROP_CERTTYPE_X509         (0x00008000UL)

/**
 * Flags an object that it is only used by TPM services
 */
#define ecy_hsm_CSAI_KEYPROP_TPM_ONLY              (0x00010000UL)

/**
 * Flags an object that it can be used by SMART TB
 */
#define ecy_hsm_CSAI_KEYPROP_IS_TRUSTEDBOOT_KEY    (0x00020000UL)

