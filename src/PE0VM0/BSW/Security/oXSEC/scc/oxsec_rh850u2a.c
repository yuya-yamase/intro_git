/*===================================================================================================================================*/
/*  U1      u1_g_oXSECCryHaKeyGetValid(const U4 u4_a_KEYID)                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1    u1_g_oXSECCryHaKeyGetValid(const U4 u4_a_KEYID)
{
    Crypto_82_Hsm_Ext_ErrorStateType   u4_t_Crypto_ret;
    boolean                            u4_t_status;
    U1                                 u1_t_ret;
    
    u1_t_ret = (U1)FALSE;
    
    u4_t_Crypto_ret = Crypto_82_Hsm_Ext_KeyGetStatus(u4_a_KEYID, &u4_t_status);
    if(u4_t_Crypto_ret == CRYPTO_82_HSM_EXT_DONE){
        u1_t_ret = (U1)u4_t_status;  /* valid:1  Invalid:0 */
    }else{
        /* retuen CRYPTO_82_HSM_EXT_ERROR or CRYPTO_82_HSM_EXT_BUSY */
        u1_t_ret = (U1)FALSE;
    }
    
    return u1_t_ret;
}

