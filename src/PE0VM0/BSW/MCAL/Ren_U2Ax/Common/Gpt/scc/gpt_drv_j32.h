/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define GPT_J32_START_NUM_CFG                    (2U)
#define GPT_J32_START_CTRL                       (0U)
#define GPT_J32_START_PERI                       (1U)

#define GPT_J32_START_CTRL_BIT_ELVL              (0x00000003U)
#define GPT_J32_START_CTRL_ELVL_FA               (0x00000000U)  /* Falling Edge                      */
#define GPT_J32_START_CTRL_ELVL_RI               (0x00000001U)  /* Rising Edge                       */
#define GPT_J32_START_CTRL_ELVL_FA_RI            (0x00000002U)  /* Both Edge    FA : Start, RI Stop  */
#define GPT_J32_START_CTRL_ELVL_RI_FA            (0x00000003U)  /* Both Edge    RI : Start, FA Stop  */

#define GPT_J32_START_CTRL_BIT_IRQ_EN            (0x00000010U)
#define GPT_J32_START_CTRL_BIT_IRQ_CL            (0x00000020U)
#define GPT_J32_START_CTRL_BIT_TRG_ST            (0x00000040U)

/* ------------------------------------------------------------------------------------- */
/*  Attention :                                                                          */
/* ------------------------------------------------------------------------------------- */
/*  If a timer is configured as interval timer and 0xffffffff expects to be set to       */
/*  compare clear threshold, GPT_J32_PERI_MAX shall be used and passed as 2nd parameter  */
/*  of vd_g_Gpt_J32Start.                                                                */
/* ------------------------------------------------------------------------------------- */
#define GPT_J32_PERI_MAX                         (0U)

