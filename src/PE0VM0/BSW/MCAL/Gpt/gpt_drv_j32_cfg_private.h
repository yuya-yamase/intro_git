/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Literal Definitions                                                                                                              */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define GPT_J32_MDCTRL_CLKSRC_0                  (0x00000000U)
#define GPT_J32_MDCTRL_CLKSRC_1                  (0x00004000U)
#define GPT_J32_MDCTRL_CLKSRC_2                  (0x00008000U)
#define GPT_J32_MDCTRL_CLKSRC_3                  (0x0000c000U)

#define GPT_J32_MDCTRL_LSB_CMOR                  (0U)

#define GPT_J32_SRCCLK_LSB_BRS                   (16U)
#define GPT_J32_SRCCLK_LSB_TPS                   (0U)

#define GPT_J32_OUTPUT_LSB_TOE                   (12U)
#define GPT_J32_OUTPUT_LSB_TOM                   (8U)
#define GPT_J32_OUTPUT_LSB_TOC                   (4U)
#define GPT_J32_OUTPUT_LSB_TOL                   (0U)

#define GPT_J32_RELOAD_LSB_RDM                   (8U)
#define GPT_J32_RELOAD_LSB_RDE                   (0U)

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Macro Definitions                                                                                                                */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Type Definitions                                                                                                                 */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define GPT_J32_NUM_CH_BY_UNIT                   (4U)
typedef struct{
    volatile U4 *    u4p_base;
    U2               u2p_mdctrl[GPT_J32_NUM_CH_BY_UNIT];    /* bit 15 -  0 : TAUJnCMOR */

    U4               u4_srcclk;                             /* bit 23 - 16 : TAUJnBRS  */
                                                            /* bit 15 -  0 : TAUJnTPS  */

    U4               u4_output;                             /* bit 15 - 12 : TAUJnTOE  */
                                                            /* bit 11 -  8 : TAUJnTOM  */
                                                            /* bit  7 -  4 : TAUJnTOC  */
                                                            /* bit  3 -  0 : TAUJnTOL  */

    U2               u2_reload;
                                                            /* bit 11 -  8 : TAUJnRDM  */
                                                            /* bit  3 -  0 : TAUJnRDE  */
    U2               u2_irq_ch;
}ST_GPT_J32_UNIT;

