/*===================================================================================================================================*/
/*  uint8   CanSM_CbkJudgeFail( NetworkHandleType network )                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Aubist/cs Type Definitions                                                                                                       */
/*                  NetworkHandleType:  typedef unsigned char                                                                        */
/*===================================================================================================================================*/
uint8   CanSM_CbkJudgeFail( NetworkHandleType network )
{
    uint8 u1Ret;

    if (CANMBQ_GetSendLockFlg(CHM_ChCnvtr_Aub_To_LogCh[network]) == (uint8)CANMBQ_SET) {
        u1Ret = (uint8)CANSM_FAIL_USER;
    }
    else{
        u1Ret = (uint8)CANSM_FAIL_NONE;
    }

    return u1Ret;
}
