/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
Can_ReturnType Can_UEdgeGetStatus(uint8 Controller)
{
    U4          u4_t_irqbit;
    U1          u1_t_edge;         /* inc/Can.h:89:typedef uint8           Can_ReturnType; */

    u4_t_irqbit = u4_g_oXCANUsrhkExirIRQst(Controller);
    if(u4_t_irqbit != (U4)0U){
        u1_t_edge = (U1)CAN_OK;
    }
    else{
        u1_t_edge = (U1)CAN_NOT_OK;
    }

    return(u1_t_edge);
}
