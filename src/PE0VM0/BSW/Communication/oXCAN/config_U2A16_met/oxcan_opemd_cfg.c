/*  U4      u4_g_oXCANOpemdCfgMdfield(void)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_oXCANOpemdCfgMdfield(void)
{
    U4    u4_t_vehopemd;
    U1    u1_t_rx_sts;

    u4_t_vehopemd = u4_g_VehopemdMdfield();

    u1_t_rx_sts = u1_g_oXCANRxEnabled((U1)OXCAN_CH_0_CAN);
    
    u4_t_vehopemd |= (U4)((U4)u1_t_rx_sts << 31U);
    
    return(u4_t_vehopemd);
}
