const U4                        u4_gp_CAN_LPR_REGR_EN_BY_NET[CAN_LPR_NUM_NET * CAN_LPR_NSM_NUM_TRX] = {
    /* ComMConf_ComMChannel_CANFD_G2M_1_BUS (0U)    */
    /*   CAN_LPR_REGR_EN_SECOC_TX     (0x00000001U) */
    /*   CAN_LPR_REGR_EN_PHY_FQ_G2M1  (0x00000004U) */
    /*   CAN_LPR_REGR_EN_UDS_FQ_G2M1  (0x00000020U) */
    /*   CAN_LPR_REGR_EN_PHY_TX_G2M1  (0x00000040U) */
    /*   CAN_LPR_REGR_EN_PHY_RX_G2M1  (0x00000800U) */
    (U4)0x00000865U,                    /* FUL_EEE  */
    (U4)0x00000825U,                    /* FUL_DEE  */
    (U4)0x00000800U,                    /* SIL_DDE  */

    /* ComMConf_ComMChannel_CANFD_G2M_2_BUS (1U)    */
    /*   CAN_LPR_REGR_EN_PHY_RX_G2M2  (0x00001000U) */
    (U4)0x00001000U,                     /* FUL_EEE */
    (U4)0x00001000U,                     /* FUL_DEE */
    (U4)0x00001000U,                     /* SIL_DDE */

    /* ComMConf_ComMChannel_VCAN_VCC_1_BUS  (2U)    */
    /*   CAN_LPR_REGR_EN_SECOC_RX     (0x00000002U) */
    /*   CAN_LPR_REGR_EN_VIR_TX_0     (0x00000200U) */
    /*   CAN_LPR_REGR_EN_UDS_TX_VIR_0 (0x00000400U) */
    (U4)0x00000600U,                     /* FUL_EEE */
    /* ----------------------------------------------------------------------------------------------------- */
    /* Warning :                                                                                             */
    /* ----------------------------------------------------------------------------------------------------- */
    /* When the CanSM state of Virtual CAN-Bus channel is equal to "Full Communication and Tx is disabled",  */
    /* Virtual CAN Tx is intentionaly enabled since Virutal CAN-Bus itself is a shared memory and a kind of  */
    /* FIFO/Queue between virtual communication node.                                                        */
    /*                                                                                                       */
    /* If CAN messages are relayed and transmitted to Virtual CAN Bus RIGHT AFTER Virtual CAN-Bus is awaken, */
    /* the message could be discarded and lost.                                                              */
    /* It's because Virtual Rx nodes perform FIFO/Queue initialization RIGHT AFTER Virtual CAN-Bus awaken.   */
    /* The above behavior is permitted and expected in CANLpR design since it's rare case.                   */
    /*                                                                                                       */
    /* ----------------------------------------------------------------------------------------------------- */
    (U4)0x00000600U,                     /* FUL_DEE */
    (U4)0x00000000U,                     /* SIL_DDE */

    /* CAN_LPR_NET_LOCA  (3U)                       */
    /*   CAN_LPR_REGR_EN_PHY_FQ_LOCA  (0x00000010U) */
    /*   CAN_LPR_REGR_EN_PHY_TX_LOCA  (0x00000100U) */
    (U4)0x00000110U,                     /* FUL_EEE */
    (U4)0x00000010U,                     /* FUL_DEE */
    (U4)0x00000000U,                     /* SIL_DDE */

    /* ComMConf_ComMChannel_CANFD_G5M_BUS   (4U)    */
    /*   CAN_LPR_REGR_EN_SECOC_TX     (0x00000001U) */
    /*   CAN_LPR_REGR_EN_PHY_FQ_G5M   (0x00000008U) */
    /*   CAN_LPR_REGR_EN_PHY_TX_G5M   (0x00000080U) */
    (U4)0x00000089U,                     /* FUL_EEE */
    (U4)0x00000009U,                     /* FUL_DEE */
    (U4)0x00000000U                      /* SIL_DDE */

};

