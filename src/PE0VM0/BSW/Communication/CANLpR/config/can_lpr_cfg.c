/*  U4      u4_g_CANLpRCfgEgrEnabled(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_CANLpRCfgEgrEnabled(void)
{
    U4                         u4_t_en_port;
    
    u4_t_en_port = (U4)CAN_LPR_REGR_EN_BIT_PORT;
    
    if(u1_s_can_lpr_cfg_phy_txen == ((U1)FALSE)){
        u4_t_en_port = (U4)CAN_LPR_REGR_EN_BIT_DS28;
    }else{
        u4_t_en_port = (U4)CAN_LPR_REGR_EN_BIT_PORT;
    }
    
    return(u4_t_en_port);
}
