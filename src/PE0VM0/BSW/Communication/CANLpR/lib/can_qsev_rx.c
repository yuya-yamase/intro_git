/*  void    vd_g_CANQSEvRxMainTask(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CANQSEvRxMainTask(void)
{
    U4                                   u4_tp_qsev_rx[CAN_LPFQ_PDU_NW_MAX];

    U4                                   u4_t_lpcnt;
    U1                                   u1_t_eas_chk;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u2_g_CAN_QSEV_RX_ACK_LP_LM; u4_t_lpcnt++){

        u1_t_eas_chk = u1_g_CANLpFqDeqCh(u2_g_CAN_QSEV_RX_FQ_CH, &u4_tp_qsev_rx[0U], (U2)CAN_LPFQ_PDU_NW_MAX);
        if(u1_t_eas_chk == (U1)CAN_LPFQ_EAS_REQ_ACPT){
            vd_g_CANQSEvRxCfgRxAck(&u4_tp_qsev_rx[0U]);
        }
        else{
            break;
        }
    }
}
