/*  uint8   u1_g_CANQSEvRxFqCAN(const uint8 u1_a_CTRLR, const uint8 u1_a_MBOX, const CanMsgType * st_ap_CAN)                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
uint8   u1_g_CANQSEvRxFqCAN(const uint8 u1_a_CTRLR, const uint8 u1_a_MBOX, const CanMsgType * st_ap_CAN)
{
    const ST_CAN_QSEV_RX *               st_tp_QSEV_RX;
    U1                                   u1_t_canif_rx;

    u1_t_canif_rx = (U1)CAN_PROC_OK;
    if(u1_a_CTRLR < u1_g_CAN_QSEV_NUM_CTRLR){

        st_tp_QSEV_RX = &st_gp_CAN_QSEV_RX_BY_CTRLR[u1_a_CTRLR];
        if((u1_a_MBOX >= st_tp_QSEV_RX->u1_mb_min) &&
           (u1_a_MBOX <= st_tp_QSEV_RX->u1_mb_max)){

            (void)u1_g_CANLpFqEnqCAN(u2_g_CAN_QSEV_RX_FQ_CH, (U2)0U, st_ap_CAN);
            u1_t_canif_rx = (U1)CAN_PROC_NG;
        }
    }

    return(u1_t_canif_rx);
}
