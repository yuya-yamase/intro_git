/*  Type Definitions                                                                                                                 */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
typedef struct{
    U1 *            u1p_pdu;                                         /* protocol data unit */
    U4              u4_cbf;                                          /* control bit field  */
}ST_GP_I2C_MA_REQ;

