/*  U4      u4_g_oXCANOpemdSyschk(const U1 u1_a_TIE)                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_oXCANOpemdSyschk(const U1 u1_a_TIE)
{
    U4           u4_t_lpcnt;

    U4           u4_t_mdbit;
    U4           u4_t_vom_chk;
    U4           u4_t_tim_run;

    U4           u4_t_sys_chk;

    u4_t_sys_chk = (U4)0U;
    u4_t_mdbit   = u4_g_oXCANOpemdCfgMdfield();
    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)OXCAN_OPEMD_NUM_CHK; u4_t_lpcnt++){

        u4_t_vom_chk = u4_t_mdbit & st_gp_OXCAN_OPEMD_CHK[u4_t_lpcnt].u4_vom_chk; 
        u4_t_tim_run = st_gp_OXCAN_OPEMD_CHK[u4_t_lpcnt].u4_tim_run;
        if(u4_t_vom_chk != u4_t_tim_run){
            u2_sp_oxcan_opemd_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_OPEMD_TIM_INA;
        }
        else{
            if(u2_sp_oxcan_opemd_tim_elpsd[u4_t_lpcnt] >= (U2)OXCAN_OPEMD_TIM_INA){
                u2_sp_oxcan_opemd_tim_elpsd[u4_t_lpcnt] = (U2)0U;
            }
            else if(u2_sp_oxcan_opemd_tim_elpsd[u4_t_lpcnt] >= (U2)OXCAN_OPEMD_TIM_MAX){
                u2_sp_oxcan_opemd_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_OPEMD_TIM_MAX;
            }
            else if(u1_a_TIE == (U1)TRUE){
                u2_sp_oxcan_opemd_tim_elpsd[u4_t_lpcnt]++;
            }
            else{
                /* no processing */
            }
            u4_t_sys_chk |= st_gp_OXCAN_OPEMD_CHK[u4_t_lpcnt].u4_sys_act;
        }
    }

    return(u4_t_sys_chk);
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXCANOpemdNmAwkTout(const U2 u2_a_THRSH)                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXCANOpemdNmAwkTout(const U2 u2_a_THRSH)
{
    U1           u1_t_tout;

    if((u2_sp_oxcan_opemd_tim_elpsd[OXCAN_OPEMD_CHK_NM_AWK] < (U2)OXCAN_OPEMD_TIM_INA) &&
       (u2_sp_oxcan_opemd_tim_elpsd[OXCAN_OPEMD_CHK_NM_AWK] >= u2_a_THRSH            )){
        u1_t_tout = (U1)TRUE;
    }
    else{
        u1_t_tout = (U1)FALSE;
    }

    return(u1_t_tout);
}
