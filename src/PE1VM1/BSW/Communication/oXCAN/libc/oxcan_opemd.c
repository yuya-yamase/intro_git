/*  void    vd_g_oXCANOpemdInit(void)                                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXCANOpemdInit(void)
{
    U4           u4_t_lpcnt;

    u2_sp_oxcan_opemd_tim_elpsd[OXCAN_OPEMD_CHK_NM_AWK] = (U2)OXCAN_OPEMD_TIM_MAX;
    for(u4_t_lpcnt = (U4)1U; u4_t_lpcnt < (U4)OXCAN_OPEMD_NUM_CHK; u4_t_lpcnt++){
        u2_sp_oxcan_opemd_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_OPEMD_TIM_INA;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_oXCANOpemdEvTx(void)                                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXCANOpemdEvTx(void)
{
    U4           u4_t_lpcnt;
    U2           u2_t_elpsd;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_OXCAN_OPEMD_NUM_EVTX; u4_t_lpcnt++){

        u2_t_elpsd = st_gp_OXCAN_OPEMD_EVTX[u4_t_lpcnt].u2_tim_idx;
        u2_t_elpsd = u2_sp_oxcan_opemd_tim_elpsd[u2_t_elpsd];
        if((u2_t_elpsd                                    == st_gp_OXCAN_OPEMD_EVTX[u4_t_lpcnt].u2_tim_evt) &&
           (st_gp_OXCAN_OPEMD_EVTX[u4_t_lpcnt].fp_vd_EVTX != vdp_PTR_NA                                   )){

            (st_gp_OXCAN_OPEMD_EVTX[u4_t_lpcnt].fp_vd_EVTX)();
        }
    }
}
