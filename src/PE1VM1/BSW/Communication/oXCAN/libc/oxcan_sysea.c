/*  void    vd_g_oXCANOpemdInit(void)                                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXCANSysEaInit(void)
{
    U4           u4_t_lpcnt;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_OXCAN_SYSEA_NUM_TIM; u4_t_lpcnt++){
        u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_SYSEA_TIM_INA;
    }

#if (BSW_CANNM_NM_TYPE_USE(Y) == BSW_USE)
    u4_t_lpcnt = (U4)u1_g_OXCAN_SYSEA_TIM_NMWK;
    if(u4_t_lpcnt < u1_g_OXCAN_SYSEA_NUM_TIM){
        u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_SYSEA_TIM_MAX;
    }
#endif /* #if (BSW_CANNM_NM_TYPE_USE(Y) == BSW_USE) */
}
/*===================================================================================================================================*/
/*  void    vd_g_oXCANSysEaEvTx(void)                                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXCANSysEaEvTx(void)
{
    U4           u4_t_lpcnt;
    U2           u2_t_elpsd;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_OXCAN_SYSEA_NUM_EVTX; u4_t_lpcnt++){

        u2_t_elpsd = st_gp_OXCAN_SYSEA_EVTX[u4_t_lpcnt].u2_tim_idx;
        u2_t_elpsd = u2_gp_oxcan_sysea_tim_elpsd[u2_t_elpsd];
        if((u2_t_elpsd                                    == st_gp_OXCAN_SYSEA_EVTX[u4_t_lpcnt].u2_tim_evt) &&
           (st_gp_OXCAN_SYSEA_EVTX[u4_t_lpcnt].fp_vd_EVTX != vdp_PTR_NA                                   )){

            (st_gp_OXCAN_SYSEA_EVTX[u4_t_lpcnt].fp_vd_EVTX)();
        }
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_oXCANSysEaTimElpsd(const U4 u4_a_SYSBIT, const U1 u1_a_TIE)                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXCANSysEaTimElpsd(const U4 u4_a_SYSBIT, const U1 u1_a_TIE)
{
    U4           u4_t_lpcnt;
    U4           u4_t_tim_chk;
    U4           u4_t_tim_run;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_OXCAN_SYSEA_NUM_TIM; u4_t_lpcnt++){

        u4_t_tim_chk = u4_a_SYSBIT & st_gp_OXCAN_SYSEA_TIM[u4_t_lpcnt].u4_sys_chk; 
        u4_t_tim_run = st_gp_OXCAN_SYSEA_TIM[u4_t_lpcnt].u4_tim_run;
        if(u4_t_tim_chk != u4_t_tim_run){
            u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_SYSEA_TIM_INA;
        }
        else{
            if(u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] >= (U2)OXCAN_SYSEA_TIM_INA){
                u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)0U;
            }
            else if(u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] >= (U2)OXCAN_SYSEA_TIM_MAX){
                u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_SYSEA_TIM_MAX;
            }
            else if(u1_a_TIE == (U1)TRUE){
                u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt]++;
            }
            else{
                /* no processing */
            }
        }
    }
}
