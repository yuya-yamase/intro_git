/*  void    vd_g_iVDshFqIfInit(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_iVDshFqIfInit(void)
{
    U4                  u4_t_lpcnt;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_IVDSH_NUM_FQ_REA; u4_t_lpcnt++){

        st_gp_ivdsh_fq_rea[u4_t_lpcnt].u1_db_act = (U1)IVDSH_FQ_REA_DB_1;
        st_gp_ivdsh_fq_rea[u4_t_lpcnt].u1_log_ok = (U1)FALSE;

        (void)ehvm_vcc_clear_channel((U4)stp_g_IVDSH_FQ_REA[u4_t_lpcnt].u2_fq_ch);
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_iVDshFqIfWriCh(const U2 u2_a_FQ_CH, const U4 * const u4_ap_WRI, const U2 u2_a_NWORD)                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_iVDshFqIfWriCh(const U2 u2_a_FQ_CH, const U4 * const u4_ap_WRI, const U2 u2_a_NWORD)
{
    U4                  u4_t_nbyte;

    u4_t_nbyte = (U4)u2_a_NWORD << 2U;
    (void)ehvm_vcc_transmit((U4)u2_a_FQ_CH, u4_ap_WRI, u4_t_nbyte);
}
/*===================================================================================================================================*/
/*  U1      u1_g_iVDshFqIfReaCh(const U2 u2_a_FQ_CH, U4 * const u4_ap_rea, const U2 u2_a_NWORD)                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_iVDshFqIfReaCh(const U2 u2_a_FQ_CH, U4 * const u4_ap_rea, const U2 u2_a_NWORD)
{
    static const U1     u1_s_IVDSH_FQIF_DEQ_MAX = (U1)5U;

    U4                  u4_t_lpcnt;
    U4                  u4_t_nb_rea;
    U4                  u4_t_nb_deq;

    U1                  u1_t_eas_chk;
    U1                  u1_t_ok;

    u1_t_ok     = (U1)FALSE;
    u4_t_nb_rea = (U4)u2_a_NWORD << 2U;
    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_s_IVDSH_FQIF_DEQ_MAX; u4_t_lpcnt++){

        /* #define E_EHVM_OK                        ((ehvm_std_return_t)0x00U) */
        /* #define E_EHVM_NOT_OK                    ((ehvm_std_return_t)0x01U) */
        /* #define E_EHVM_RECEIVE_QUEUE_EMPTY       ((ehvm_std_return_t)0x03U) */
        /* #define E_EHVM_RECEIVE_OVERWRITE_OCCURS  ((ehvm_std_return_t)0x04U) */
        /* #define E_EHVM_HVC_ACC_DENY              ((ehvm_std_return_t)0x10U) */
        /* #define E_EHVM_MM_ACC_NG                 ((ehvm_std_return_t)0x20U) */
        u4_t_nb_deq  = (U4)0U;
        u1_t_eas_chk = ehvm_vcc_receive((U4)u2_a_FQ_CH, u4_ap_rea, u4_t_nb_rea, &u4_t_nb_deq);
        if(u1_t_eas_chk == (U1)E_EHVM_OK){

            if(u4_t_nb_deq == u4_t_nb_rea){
                u1_t_ok = (U1)TRUE;
            }
        }
        else if(u1_t_eas_chk == E_EHVM_RECEIVE_OVERWRITE_OCCURS){
            u1_t_ok = (U1)FALSE;
        }
        else{
            break;
        }
    }

    return(u1_t_ok);
}

