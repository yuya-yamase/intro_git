/*  U1      u1_d_NvmcMod_B1R3P1_RdnData(const ST_NVMC_CFGDATA * const st_ap_CFGDATA, U4 * u4_ap_rdnrslt, const U4 * const u4_ap_DATA, const U1 * const u1_ap_STATUS) */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      --> *st_ap_CFGDATA : Data configuration                                                                          */
/*                  --> *u4_ap_rdnrslt : RDN Data                                                                                    */
/*                  --> *u4_ap_DATA    : Read Buffer                                                                                 */
/*                  --> *u1_ap_STATUS  : Status                                                                                      */
/*  Return:         U1 u1_t_ret : Sts                                                                                                */
/*===================================================================================================================================*/
U1   u1_d_NvmcMod_B1R3P1_RdnData(const ST_NVMC_CFGDATA * const st_ap_CFGDATA, U4 * u4_ap_rdnrslt, const U4 * const u4_ap_DATA, const U1 * const u1_ap_STATUS)
{
    static const U1 u1_sp_NVMC_MOD_1BIT_TRPL_NODATA_TBL[NVMC_MOD_1BIT_TRPL_LOST_BIT_VAL] = {
        (U1)0U,  /* 000b */
        (U1)1U,  /* 001b */
        (U1)0U,  /* 010b */
        (U1)2U,  /* 011b */
        (U1)0U,  /* 100b */
        (U1)1U,  /* 101b */
        (U1)0U   /* 110b */
    };
    U1  u1_t_ret;
    U1  u1_t_sts_bit;
    U1  u1_t_index;
    U1  u1_t_bit1;
    U1  u1_t_bit2;

    u1_t_sts_bit = u1_d_NvmcMod_Lib_StatusCheck((U1)NVMC_MOD_1BIT_TRPL_RDNNUM, (U1)NVMC_MOD_1BIT_TRPL_DATA_SIZE, u1_ap_STATUS);
    u1_t_ret = (U1)NVMC_MOD_RSLT_NG;
    if(u1_t_sts_bit == (U1)0U){
        u1_t_bit1 = u1_d_NvmcMod_Lib_Major7Bit(u4_ap_DATA[NVMC_MOD_1BIT_TRPL_RDNDATA_1ST]);
        u1_t_bit2 = u1_d_NvmcMod_Lib_Major7Bit(u4_ap_DATA[NVMC_MOD_1BIT_TRPL_RDNDATA_2ND]);
        if(u1_t_bit1 == u1_t_bit2){
            u4_ap_rdnrslt[0] = (U4)u1_t_bit1;
        }
        else{
            u4_ap_rdnrslt[0] = (U4)u1_d_NvmcMod_Lib_Major7Bit(u4_ap_DATA[NVMC_MOD_1BIT_TRPL_RDNDATA_3RD]);
        }
        u1_t_ret = (U1)NVMC_MOD_RSLT_OK;
    }
    else if(u1_t_sts_bit < (U1)NVMC_MOD_1BIT_TRPL_LOST_BIT_VAL){
        u1_t_index = u1_sp_NVMC_MOD_1BIT_TRPL_NODATA_TBL[u1_t_sts_bit];
        u4_ap_rdnrslt[0] = (U4)u1_d_NvmcMod_Lib_Major7Bit(u4_ap_DATA[u1_t_index]);
        u1_t_ret = (U1)NVMC_MOD_RSLT_NODATA;
    }
    else if(u1_t_sts_bit == (U1)NVMC_MOD_1BIT_TRPL_LOST_BIT_VAL){
        u1_t_ret = (U1)NVMC_MOD_RSLT_LOST;
    }
    else{
        /* u1_t_ret = (U1)NVMC_MOD_RSLT_NG; */
    }
    return(u1_t_ret);
}

/*===================================================================================================================================*/
/*  void   vd_d_NvmcMod_B1R3P1_WrData(U4 * const u4_ap_writedata, const U1 u1_a_RDNNUM, const U1 u1_a_BLOCKNUM)                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      --> *u4_ap_writedata : Write Data                                                                                */
/*                  --> u1_a_RDNNUM      : RDN Nunm                                                                                  */
/*                  --> u1_a_BLOCKNUM    : Block Num                                                                                 */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void   vd_d_NvmcMod_B1R3P1_WrData(U4 * const u4_ap_writedata, const U1 u1_a_RDNNUM, const U1 u1_a_BLOCKNUM)
{
    U4  u4_t_wdata;

    u4_t_wdata = (U4)NVMC_MOD_1BIT_TRPL_7BIT_OFF_BIT;
    if(u4_ap_writedata[0] == (U4)TRUE){
        u4_t_wdata = (U4)NVMC_MOD_1BIT_TRPL_7BIT_ON_BIT;
    }

    u4_ap_writedata[0] = u4_t_wdata;
}

