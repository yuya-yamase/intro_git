/*  U1      u1_d_Nvmc_Eep_Adapt_ReadDrctBlock(const U2 u2_a_BLOCKID, U1 * u1_ap_data, const U2 u2_a_SIZE)                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      --> u2_a_BLOCKID : BlockId                                                                                       */
/*                  --> *u1_ap_data  : Read Data Pointer                                                                             */
/*                  --> u2_a_SIZE    : Data Size                                                                                     */
/*  Return:         U1 u1_t_ret : Result                                                                                             */
/*===================================================================================================================================*/
U1      u1_d_Nvmc_Eep_Adapt_ReadDrctBlock(const U2 u2_a_BLOCKID, U1 * u1_ap_data, const U2 u2_a_SIZE)
{
    U4  u4_t_data;
    U2  u2_t_data;
    U1  u1_t_ret;
    U1  u1_t_rslt;

    u1_t_ret  = (U1)NVMC_RESULT_NG;
    u4_t_data = (U4)0U;
    
    if(u1_ap_data != vdp_PTR_NA){
        if(u2_a_SIZE == (U2)NVMC_EEP_ADAPT_BLOCKSIZE){
            u1_t_rslt = u1_g_Nvm_Eep_Read(u2_a_BLOCKID, &u4_t_data);
            u2_t_data = (U2)u4_t_data;
            vd_g_MemcpyU1(u1_ap_data, (U1 *)&u2_t_data, (U4)NVMC_EEP_ADAPT_BLOCKSIZE);
            if(u1_t_rslt == (U1)TRUE){
                u1_t_ret = (U1)NVMC_RESULT_OK;
            }
        }
    }

    return(u1_t_ret);
}

