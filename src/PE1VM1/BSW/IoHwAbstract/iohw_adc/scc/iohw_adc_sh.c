/*  void    vd_g_IoHwAdcShInit(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_IoHwAdcShInit(void)
{
    U4 *                       u4_tp_lv_0;
    U4 *                       u4_tp_lv_1;
    U4                         u4_t_lpcnt;
    U4                         u4_t_num_word;

    u2_s_iohw_adc_sh_tocnt = (U2)U2_MAX;
    u1_s_iohw_adc_lv_rd    = (U1)U1_MAX;

    u4_t_num_word = ((U4)u1_g_IOHW_ADC_NUM_CH >> 1) + ((U4)u1_g_IOHW_ADC_NUM_CH & (U4)0x00000001U);

    u4_tp_lv_0 = u4p_gp_IOHW_ADC_LV[IOHW_ADC_SH_BUF_0];
    u4_tp_lv_1 = u4p_gp_IOHW_ADC_LV[IOHW_ADC_SH_BUF_1];
    for(u4_t_lpcnt = (U4)0; u4_t_lpcnt < u4_t_num_word; u4_t_lpcnt++){
        u4_tp_lv_0[u4_t_lpcnt] = (U4)IOHW_ADC_LV_U4_UNKNWN;
        u4_tp_lv_1[u4_t_lpcnt] = (U4)IOHW_ADC_LV_U4_UNKNWN;
    }

    vd_g_IoHwAdcShCfgInit();
}
/*===================================================================================================================================*/
/*  void    vd_g_IoHwAdshMainTask(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_IoHwAdcShMainTask(void)
{
    U1                         u1_t_rd;
    U1                         u1_t_ok;

    if(u2_s_iohw_adc_sh_tocnt < (U2)U2_MAX){
        u2_s_iohw_adc_sh_tocnt++;
    }

    u1_t_rd = (u1_s_iohw_adc_lv_rd + (U1)IOHW_ADC_SH_BUF_1) & (U1)IOHW_ADC_SH_BUF_1;
    u1_t_ok = u1_g_IoHwAdcShCfgLvRx(u4p_gp_IOHW_ADC_LV[u1_t_rd]);
    if(u1_t_ok == (U1)TRUE){
        u1_s_iohw_adc_lv_rd    = u1_t_rd;
        u2_s_iohw_adc_sh_tocnt = (U2)0U;
    }
    else if(u2_s_iohw_adc_sh_tocnt >= u2_g_IOHW_ADC_SH_TOUT){
        u1_s_iohw_adc_lv_rd    = (U1)U1_MAX;
    }
    else{
        /* do nothing */
    }
}
/*===================================================================================================================================*/
/*  U1      u1_g_IoHwAdcRead(const U1 u1_a_ADC_CH, U2 *u2_ap_lv)                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_IoHwAdcRead(const U1 u1_a_ADC_CH, U2 *u2_ap_lv)
{
    U2 *                       u2_tp_lv_rd;
    U2                         u2_t_lv;
    U1                         u1_t_valid;
    U1                         u1_t_rd;

    u1_t_valid = (U1)FALSE;

    u1_t_rd    = u1_s_iohw_adc_lv_rd;
    if((u1_a_ADC_CH < u1_g_IOHW_ADC_NUM_CH   ) &&
       (u1_t_rd     < (U1)IOHW_ADC_SH_NUM_BUF)){

        u2_tp_lv_rd = (U2 *)u4p_gp_IOHW_ADC_LV[u1_t_rd];
        u2_t_lv     = u2_tp_lv_rd[u1_a_ADC_CH];
        if(u2_t_lv <= u2_g_IOHW_ADC_LV_MAX){

            if(u2_ap_lv != vdp_PTR_NA){
                (*u2_ap_lv) = u2_t_lv;
            }
            u1_t_valid = (U1)TRUE;
        }
    }

    return(u1_t_valid);
}
/*===================================================================================================================================*/
/*  U2      u2_g_IoHwAdcLv(const U1 u1_a_ADC_CH)                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U2      u2_g_IoHwAdcLv(const U1 u1_a_ADC_CH)
{
    U2 *                       u2_tp_lv_rd;
    U2                         u2_t_lv;
    U1                         u1_t_rd;

    u2_t_lv = (U2)IOHW_ADC_LV_UNKNWN;

    u1_t_rd = u1_s_iohw_adc_lv_rd;
    if((u1_a_ADC_CH < u1_g_IOHW_ADC_NUM_CH   ) &&
       (u1_t_rd     < (U1)IOHW_ADC_SH_NUM_BUF)){

        u2_tp_lv_rd = (U2 *)u4p_gp_IOHW_ADC_LV[u1_t_rd];
        u2_t_lv     = u2_tp_lv_rd[u1_a_ADC_CH];
    }

    return(u2_t_lv);
}

