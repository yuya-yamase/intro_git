/* Function Name | Fee_ResetInitHook                                        */
/*               | (Initialization timing notification Hook at FEE reset)   */
/* Description   | Notify when to reset FEE                                 */
/* Preconditions |                                                          */
/* Parameters    | None                                                     */
/* Return Value  | None                                                     */
