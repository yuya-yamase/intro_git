/*  U1      u1_g_oXDoCANDidSup_2003(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANDidSup_2003(void)
{
    U1 u1_t_hud;
    U1 u1_t_sup;

    u1_t_hud = u1_g_VardefEsOptAvaByCh((U2)VDF_ESO_CH_GVIF2);

    if(u1_t_hud == (U1)TRUE){
        u1_t_sup = (U1)OXDC_XID_SUP_PUB;
    }
    else{
        u1_t_sup = (U1)OXDC_XID_SUP_NOT;
    }

    return(u1_t_sup);
}
