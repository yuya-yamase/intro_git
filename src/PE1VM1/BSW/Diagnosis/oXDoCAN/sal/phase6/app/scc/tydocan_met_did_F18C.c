/*  U1      u1_g_oXDoCANRebyId_F18C(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_F18C(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1                     u1_tp_pn_tx[OXDC_DATA_REA_ANS_NB_F18C];
    U1                     u1_t_xpn_sts;

    vd_g_MemfillU1(u1_tp_pn_tx, (U1)OXDC_F18C_INVALID, (U4)OXDC_DATA_REA_ANS_NB_F18C);
    u1_t_xpn_sts = u1_g_TyDoCANXpnTx((U1)TYDC_XPN_RX_ECU_PN, &u1_tp_pn_tx[0], (U1)TYDC_XPN_NB_ECU_PN);
    if(u1_t_xpn_sts == (U1)TYDC_XPN_UNKNOWN){
        vd_g_MemfillU1(&u1_tp_pn_tx[0], (U1)OXDC_F18C_UNKNOWN, (U4)OXDC_F188_ECU_PN_LEN);
    }
    else if(u1_t_xpn_sts == (U1)TYDC_XPN_INVALID){
        vd_g_MemfillU1(&u1_tp_pn_tx[0], (U1)OXDC_F18C_INVALID, (U4)OXDC_F188_ECU_PN_LEN);
    }
    else{
        /* Do Nothing */
    }

    u1_t_xpn_sts = u1_g_TyDoCANXpnTx((U1)TYDC_XPN_RX_SERI_N, &u1_tp_pn_tx[TYDC_SERI_ECU_LEN], (U1)TYDC_XPN_NB_SERI_N);
    if(u1_t_xpn_sts == (U1)TYDC_XPN_UNKNOWN){
        vd_g_MemfillU1(&u1_tp_pn_tx[TYDC_SERI_ECU_LEN], (U1)OXDC_F18C_UNKNOWN, (U4)OXDC_F188_SERI_N_LEN);
    }
    else if(u1_t_xpn_sts == (U1)TYDC_XPN_INVALID){
        vd_g_MemfillU1(&u1_tp_pn_tx[TYDC_SERI_ECU_LEN], (U1)OXDC_F18C_INVALID, (U4)OXDC_F188_SERI_N_LEN);
    }
    else{
        /* Do Nothing */
    }

    vd_g_MemcpyU1(u1_ap_ans, &u1_tp_pn_tx[0], (U4)OXDC_DATA_REA_ANS_NB_F18C);

    return((U1)OXDC_SAL_PROC_FIN);
}
