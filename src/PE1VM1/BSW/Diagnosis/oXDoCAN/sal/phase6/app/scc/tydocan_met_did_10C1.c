/*  U1      u1_g_oXDoCANRebyId_10C1(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_10C1(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    static volatile const U1 * u1p_sp_TYDC_SPDMET_ID[TYDC_SPDMET_ID_NUM] = {
        &u1_CALIB_MCUID0013_SPDID1,
        &u1_CALIB_MCUID0014_SPDID2,
        &u1_CALIB_MCUID0015_SPDID3,
        &u1_CALIB_MCUID0016_SPDID4,
        &u1_CALIB_MCUID0017_SPDID5,
        &u1_CALIB_MCUID0018_SPDID6,
        &u1_CALIB_MCUID0019_SPDID7,
        &u1_CALIB_MCUID0020_SPDID8
    };

    U4          u4_t_loop;

    for(u4_t_loop = (U4)0U; u4_t_loop < (U4)TYDC_SPDMET_ID_NUM; u4_t_loop++){
        if(u1p_sp_TYDC_SPDMET_ID[u4_t_loop] != vdp_PTR_NA){
            u1_ap_ans[u4_t_loop] = *(u1p_sp_TYDC_SPDMET_ID[u4_t_loop]);
        }
        else{
            u1_ap_ans[u4_t_loop] = (U1)TYDC_SPDMET_ID_UNK;
        }
    }

    return((U1)OXDC_SAL_PROC_FIN);
}
