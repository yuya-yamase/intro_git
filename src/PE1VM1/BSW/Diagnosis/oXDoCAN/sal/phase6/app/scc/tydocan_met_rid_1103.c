/*  U1      u1_g_oXDoCANRoutCorchk_1103(const U1 u1_a_SUBF, const U1 * u1_ap_COR)                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRoutCorchk_1103(const U1 u1_a_SUBF, const U1 * u1_ap_COR)
{
    U1            u1_t_proc;

    if((u1_a_SUBF    == (U1)OXDC_ROUT_SUBF_START  ) &&
       (u1_ap_COR[0] == (U1)TYDC_COR_HUD_MIR_RESET)){
        u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
    }
    else if(u1_a_SUBF == (U1)OXDC_ROUT_SUBF_RESULT){
        u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
    }

    return(u1_t_proc);
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRoutStart_1103(U1 * u1_ap_ans, const U2 u2_a_ELPSD, U2 * u2_ap_nbyte)                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRoutStart_1103(U1 * u1_ap_ans, const U2 u2_a_ELPSD, U2 * u2_ap_nbyte)
{
    U1  u1_t_sts;
    U1  u1_t_ret;

    u1_t_sts = u1_g_HudImgAdjRoutStartIsOK();

    if(u1_t_sts == (U1)TRUE){
        u1_t_ret = (U1)OXDC_SAL_PROC_FIN;
        u1_ap_ans[OXDC_ROUT_ANS_RINF_1103] = (U1)OXDC_1103_RINF;
    }
    else{
        u1_t_ret = (U1)OXDC_SAL_PROC_NR_24;
    }

    return(u1_t_ret);
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRoutRslt_1103(U1 * u1_ap_ans, const U2 u2_a_ELPSD, U2 * u2_ap_nbyte)                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRoutRslt_1103(U1 * u1_ap_ans, const U2 u2_a_ELPSD, U2 * u2_ap_nbyte)
{
    u1_ap_ans[OXDC_ROUT_ANS_RINF_1103]    = (U1)OXDC_1103_RINF;
    u1_ap_ans[OXDC_ROUT_ANS_RSTSREC_1103] = u1_g_HudImgAdjRoutRslt();

    return((U1)OXDC_SAL_PROC_FIN);
}
