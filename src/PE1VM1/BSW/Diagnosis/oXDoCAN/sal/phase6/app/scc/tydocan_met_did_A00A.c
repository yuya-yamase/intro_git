/*  U1      u1_g_oXDoCANDidSup_A00A(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANDidSup_A00A(void)
{
    U1  u1_t_ptsys;
    U1  u1_t_sup;

    u1_t_ptsys = u1_g_VardefPtsRx();

    if((u1_t_ptsys == (U1)VDF_PTS_RX_03_HYB    ) ||
       (u1_t_ptsys == (U1)VDF_PTS_RX_04_HYB_PLU) ||
       (u1_t_ptsys == (U1)VDF_PTS_RX_05_ELE_BAT) ||
       (u1_t_ptsys == (U1)VDF_PTS_RX_06_HYB_MOT) ||
       (u1_t_ptsys == (U1)VDF_PTS_RX_07_ELE_HYD) ||
       (u1_t_ptsys == (U1)VDF_PTS_RX_08_48V    )) {
        u1_t_sup = (U1)OXDC_XID_SUP_PUB;
   }
   else{
        u1_t_sup = (U1)OXDC_XID_SUP_NOT;
    }

    return(u1_t_sup);
}
