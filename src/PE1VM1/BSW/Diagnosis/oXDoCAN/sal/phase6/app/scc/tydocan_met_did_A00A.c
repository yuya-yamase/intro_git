/*  U1      u1_g_oXDoCANRebyId_A00A(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_A00A(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1              u1_t_msgsts;
    U1              u1_t_sgnl;
    U1              u1_t_result;

    u1_t_msgsts   = u1_g_oXCANRxdStat((U2)OXCAN_RXD_PDU_CAN_ECT1S93_CH0,
                                      (U4)OXCAN_SYS_IGR | (U4)OXCAN_SYS_IGP,
                                      (U2)U2_MAX) & (U1)COM_NO_RX;

    u1_t_sgnl     = (U1)0U;
    (void)Com_ReceiveSignal(ComConf_ComSignal_DRDYSTS, &u1_t_sgnl);

    if(u1_t_msgsts == (U1)COM_NO_RX){
        u1_t_sgnl = (U1)0U;
    }

    if(u1_t_sgnl == (U1)TYDC_A00A_REASYS_TT_ON){
        u1_t_result  = (U1)TYDC_A00A_ON;
    }else{
        u1_t_result  = (U1)TYDC_A00A_OFF;
    }

    u1_ap_ans[0] = u1_t_result;

    return((U1)OXDC_SAL_PROC_FIN);
}
