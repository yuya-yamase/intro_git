U1      u1_g_oXDoCANRebyId_11A2(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
#if 0    /* BEV Diag provisionally */
    ST_OXDC_DID_SSR          st_t_rec;
    U1 *                     u1_tp_tx;

    U4                       u4_t_offset;
    U4                       u4_t_lpcnt;
    U1                       u1_t_valid;

    vd_g_MemfillU1(&u1_ap_ans[0], (U1)0U, (U4)OXDC_DATA_REA_ANS_NB_11A2);
    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)OXDC_DID_SSR_NUM_REC_11A2; u4_t_lpcnt++){

        u4_t_offset  = u4_t_lpcnt * (U4)TYDC_SSR_SBR_NB_TX;
        u1_tp_tx     = &u1_ap_ans[u4_t_offset];
        u1_t_valid   = u1_g_oXDoCANDidSsrRead(u1_g_TYDC_SSR_CH_22_11A2, (U1)u4_t_lpcnt, &st_t_rec);
        if((u1_t_valid == (U1)TRUE) && (u4_t_offset < (U4)OXDC_DATA_REA_ANS_NB_11A2)){
            /* No array out of bounds exception occurred. Because the following condition
             (u4_t_lpcnt * (U4)TYDC_SSR_SBR_NB_TX + OXDC_TMSTP_GMT_NUM) < OXDC_DATA_REA_ANS_NB_11A2 */
            vd_g_MemcpyU1(u1_tp_tx, &st_t_rec.u1p_gmt[0], (U4)OXDC_TMSTP_GMT_NUM);

            u1_tp_tx[TYDC_SSR_SBR_TX_INFO_1] = (U1)(st_t_rec.u4p_ss[0] & (U4)U1_MAX);
            u1_tp_tx[TYDC_SSR_SBR_TX_INFO_2] = (U1)(st_t_rec.u4p_ss[1] & (U4)U1_MAX);
        }
    }

    return((U1)OXDC_SAL_PROC_FIN);
#else    /* BEV Diag provisionally */
    return((U1)OXDC_SAL_PROC_NR_22);
#endif    /* BEV Diag provisionally */
}
