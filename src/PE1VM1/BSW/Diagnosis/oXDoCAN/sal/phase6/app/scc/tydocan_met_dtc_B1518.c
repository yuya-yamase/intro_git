/*  U1    u1_g_oXDoCANDtcTrchk_B1518(const U2 u2_a_ELPSD)                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      void                                                                                                             */
/*  Return:         OXDC_DTC_TR_INA  (0x00U)  : DTC INActive                                                                         */
/*                  OXDC_DTC_TR_ACT  (0x01U)  : DTC ACTive                                                                           */
/*                  OXDC_DTC_TR_UNK  (0x02U)  : Unknown                                                                              */
/*                  OXDC_DTC_TR_N_A  (0x03U)  : DTC Not Applicable                                                                   */
/*===================================================================================================================================*/
U1    u1_g_oXDoCANDtcTrchk_B1518(const U2 u2_a_ELPSD)
{
    static const U1   u1_sp_TYDC_DTC_B1518[] = {
        (U1)OXDC_DTC_TR_UNK, /* 00b */
        (U1)OXDC_DTC_TR_UNK, /* 01b */
        (U1)OXDC_DTC_TR_INA, /* 10b */
        (U1)OXDC_DTC_TR_ACT  /* 11b */
    };
    static const U2   u2_s_TYDC_DTC_B1518_EVM_TOUT = (U2)1000U / (U2)OXDC_MAIN_TICK;  /* 1 seconds */

    U1                u1_t_gvif_chk;
    U1                u1_t_sup;

#if 0    /* BEV Diag provisionally */
    u1_t_sup = u1_g_VardefEsOptAvaByCh((U2)VDF_ESO_CH_GVIF2_METDISP);
#else    /* BEV Diag provisionally */
    u1_t_sup = (U1)FALSE;
#endif    /* BEV Diag provisionally */
    if(u1_t_sup == (U1)TRUE){
        if(u2_a_ELPSD >= u2_s_TYDC_DTC_B1518_EVM_TOUT){
            u1_t_gvif_chk = u1_g_XSpiDtcRx((U1)XSPI_DTC_RX_B1518) & ((U1)XSPI_DTC_RX_FAI |
                                                                            (U1)XSPI_DTC_RX_RUN);

            u1_t_gvif_chk = u1_sp_TYDC_DTC_B1518[u1_t_gvif_chk];
        }
        else{
            u1_t_gvif_chk = (U1)OXDC_DTC_TR_UNK;
        }
    }
    else{
        u1_t_gvif_chk = (U1)OXDC_DTC_TR_N_A;
    }

    return(u1_t_gvif_chk);
}
