U1      u1_g_oXDoCANRebyId_10AB(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
#if 0    /* BEV Diag provisionally */
    ST_OXDC_DID_SSR          st_t_rec;
    U1 *                     u1_tp_tx;   

    U4                       u4_t_offset;
    U4                       u4_t_lpcnt;
    U1                       u1_t_valid;
    U4                       u4_t_odo;


    u4_t_offset = (U4)TYDC_SSR_HR_NB_TX * (U4)OXDC_DID_SSR_NUM_REC_10AB;
    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)OXDC_DID_SSR_NUM_REC_10AB; u4_t_lpcnt++){

        u4_t_offset -= (U4)TYDC_SSR_HR_NB_TX;
        u1_tp_tx     = &u1_ap_ans[u4_t_offset];
        u1_t_valid   = u1_g_oXDoCANDidSsrRead(u1_g_TYDC_SSR_CH_22_10AB, (U1)u4_t_lpcnt, &st_t_rec);
        if(u1_t_valid == (U1)TRUE){
            u1_tp_tx[TYDC_SSR_HR_TX_LOG]   = (U1)st_t_rec.u4p_ss[OXDC_DID_SSR_SS_DW0];

            if(st_t_rec.u4_odo_0p1km == (U4)OXDC_TMSTP_ODO_UNK){
                u4_t_odo = (U4)OXDC_TMSTP_ODO_UNK;
            }
            else{
                u4_t_odo = st_t_rec.u4_odo_0p1km / (U4)TYDC_SSR_ODO_LSB;
                if(u4_t_odo >= (U4)TYDC_SSR_ODO_MAX){
                    u4_t_odo = (U4)TYDC_SSR_ODO_MAX;
                }
            }

            u1_tp_tx[TYDC_SSR_HR_TX_ODO_3] = (U1)((u4_t_odo >> OXDC_TMSTP_ODO_ANS_LSB_3RD) & (U4)U1_MAX);
            u1_tp_tx[TYDC_SSR_HR_TX_ODO_2] = (U1)((u4_t_odo >> OXDC_TMSTP_ODO_ANS_LSB_2ND) & (U4)U1_MAX);
            u1_tp_tx[TYDC_SSR_HR_TX_ODO_1] = (U1)(u4_t_odo                                 & (U4)U1_MAX);
        }
        else{
            u1_tp_tx[TYDC_SSR_HR_TX_LOG]   = (U1)0x00U;
            u1_tp_tx[TYDC_SSR_HR_TX_ODO_3] = (U1)U1_MAX;
            u1_tp_tx[TYDC_SSR_HR_TX_ODO_2] = (U1)U1_MAX;
            u1_tp_tx[TYDC_SSR_HR_TX_ODO_1] = (U1)U1_MAX;
        }
    }

    return((U1)OXDC_SAL_PROC_FIN);
#else    /* BEV Diag provisionally */
    return((U1)OXDC_SAL_PROC_NR_22);
#endif    /* BEV Diag provisionally */
}
