#define TYDC_SUP_ID_NVMC_NB                        (8U)
#define TYDC_SUP_ID_ARRAY_SIZE                     (4U)
#define TYDC_SUP_ID_MAX                            (0x270FU)    /* TRS_UDS-DA.5.1.10 : MAX 9999(dec) */
#define TYDC_SUP_ID_MASK                           (0x00FFU)

