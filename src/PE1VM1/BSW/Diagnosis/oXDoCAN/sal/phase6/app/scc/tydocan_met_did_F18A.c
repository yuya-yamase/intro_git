/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRebyId_F18A(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_F18A(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U2                 u2_tp_src[TYDC_SUP_ID_ARRAY_SIZE];
    U1                 u1_t_nvm_chk;
    U1                 u1_t_proc;

    u1_t_nvm_chk = u1_g_Nvmc_ReadOthrwithSts((U2)U2_MAX    /* BEV Diag provisionally */, (U2)TYDC_SUP_ID_NVMC_NB, (U1 *)&u2_tp_src[0]);

    if((u1_t_nvm_chk <  (U1)NVMC_STATUS_KIND_NG ) &&
       (u1_t_nvm_chk != (U1)NVMC_STATUS_ERRCOMP ) &&
       (u1_t_nvm_chk != (U1)NVMC_STATUS_CACHE_NG)){
        if(u2_tp_src[0] > (U2)TYDC_SUP_ID_MAX){
            u1_t_proc = (U1)OXDC_SAL_PROC_NR_22;
        }
        else{
            u1_ap_ans[0] = (U1)((U2)(u2_tp_src[0] >> 8) & (U2)TYDC_SUP_ID_MASK);
            u1_ap_ans[1] = (U1)(u2_tp_src[0] & (U2)TYDC_SUP_ID_MASK);
            u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
        }
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_22;
    }

    return(u1_t_proc);
}
