/*===================================================================================================================================*/
/*  void    vd_g_TyDoCANMrcBonInit(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_TyDoCANMrcBonInit(void)
{
    u1_s_tydc_mr_capt = (U1)0U;
    u1_s_tydc_mr_sta  = (U1)0U;
}
/*===================================================================================================================================*/
/*  void    vd_g_TyDoCANMrcRstwkInit(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_TyDoCANMrcRstwkInit(void)
{
    U1                         u1_t_mr;

    u1_t_mr = u1_g_TyDoCANMrcCfgReschk() & (U1)TYDC_MRC_SS_BIT_RST;
    if(u1_t_mr != (U1)0U){
        u1_s_tydc_mr_capt |= (u1_s_tydc_mr_sta & (U1)TYDC_MRC_SS_BIT_STA);
        u1_s_tydc_mr_capt |= u1_t_mr;
    }
    u1_s_tydc_mr_sta = (U1)0U;
}
/*===================================================================================================================================*/
/*  void    vd_g_TyDoCANMrcMainTask(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_TyDoCANMrcMainTask(void)
{
    u1_s_tydc_mr_sta = u1_g_TyDoCANMrcCfgVomSta();
}
/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANMrEvCapt(const U1 u1_a_ODO_UPDT, U4 * u4_ap_ss)                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANMrEvCapt(const U1 u1_a_ODO_UPDT, U4 * u4_ap_ss)
{
    U1                         u1_t_capt;

    if(u1_s_tydc_mr_capt != (U1)0U){
        u4_ap_ss[0] = (U4)u1_s_tydc_mr_capt;
        u1_t_capt   = (U1)TRUE;
    }
    else{
        u1_t_capt   = (U1)FALSE;
    }
    u1_s_tydc_mr_capt = (U1)0U;

    return(u1_t_capt);
}
