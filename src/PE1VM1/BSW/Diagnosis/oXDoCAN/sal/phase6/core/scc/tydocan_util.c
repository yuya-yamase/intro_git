/*  U1      u1_g_TyDoCANBiTx(U1 * u1_ap_ans_tx, const ST_TYDC_BITX_TX * st_ap_TX)                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANBiTx(U1 * u1_ap_ans_tx, const ST_TYDC_BITX_TX * st_ap_TX)
{
    U1 ( *                   fp_u1_bi_act)(const U2 u2_a_CH);

    const ST_TYDC_BITX_CH *  st_tp_CH;

    U4                       u4_t_lpcnt;
    U4                       u4_t_tx_nbyte;
    U4                       u4_t_num_ch;

    U1                       u1_t_act;
    U1                       u1_t_byte;
    U1                       u1_t_bit;

    U1                       u1_t_proc;

    u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
    if((u1_ap_ans_tx != vdp_PTR_NA) &&
       (st_ap_TX     != vdp_PTR_NA)){

        fp_u1_bi_act  = st_ap_TX->fp_u1_BI_ACT;
        st_tp_CH      = st_ap_TX->stp_CH;
        u4_t_num_ch   = (U4)st_ap_TX->u2_num_ch;
        u4_t_tx_nbyte = (U4)st_ap_TX->u2_tx_nbyte;

        if((fp_u1_bi_act  != vdp_PTR_NA) &&
           (st_tp_CH      != vdp_PTR_NA) &&
           (u4_t_num_ch   >  (U4)0U    ) &&
           (u4_t_tx_nbyte >  (U4)0U    )){
 
            for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < u4_t_tx_nbyte; u4_t_lpcnt++){
                u1_ap_ans_tx[u4_t_lpcnt] = (U1)0U;
            }

            for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < u4_t_num_ch; u4_t_lpcnt++){

                u1_t_act  = (*fp_u1_bi_act)(st_tp_CH[u4_t_lpcnt].u2_ch);
                u1_t_byte = st_tp_CH[u4_t_lpcnt].u1_byte;
                u1_t_bit  = st_tp_CH[u4_t_lpcnt].u1_bit;
                if(u1_t_act == (U1)TRUE){
                    u1_ap_ans_tx[u1_t_byte] |= u1_t_bit;
                    u1_t_byte++;
                    u1_ap_ans_tx[u1_t_byte] |= u1_t_bit;
                }
                else{
                    u1_ap_ans_tx[u1_t_byte] |= u1_t_bit;
                }
            }

            u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
        }
    }

    return(u1_t_proc);
}
/*===================================================================================================================================*/
/*  U4      u4_g_TyDoCANBi32(const U1 * u1_ap_B8, const U2 u2_a_NBYTE)                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_TyDoCANBi32(const U1 * u1_ap_B8, const U2 u2_a_NBYTE)
{
    U4                       u4_t_lpcnt;
    U4                       u4_t_lsb;
    U4                       u4_t_b32;

    u4_t_b32 = (U4)0U;
    if(u2_a_NBYTE <= (U2)TYDC_B32_NB_MAX){

        u4_t_lsb = (U4)0U;
        for(u4_t_lpcnt = (U4)1U; u4_t_lpcnt < (U4)u2_a_NBYTE; u4_t_lpcnt += (U2)TYDC_B8_BO_NEXT){
            u4_t_b32 |= (U4)((U4)u1_ap_B8[u4_t_lpcnt] << u4_t_lsb);
            u4_t_lsb += (U4)TYDC_B8_LB_NEXT;
        }
    }

    return(u4_t_b32);
}
/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANBi32Tx(U1 * u1_ap_ans_tx, const U4 * u4_ap_B32, const U2 u2_a_NBYTE)                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANBi32Tx(U1 * u1_ap_ans_tx, const U4 * u4_ap_B32, const U2 u2_a_NBYTE)
{
    U4                       u4_t_lpcnt;
    U4                       u4_t_offset;
    U4                       u4_t_lsb;

    U1                       u1_t_proc;

    if(u2_a_NBYTE <= (U2)TYDC_B32_NB_MAX){

        u4_t_lsb    = (U4)0U;
        u4_t_offset = (U4)0U;
        for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u2_a_NBYTE; u4_t_lpcnt += (U2)TYDC_B8_BO_NEXT){

            u1_ap_ans_tx[u4_t_lpcnt]   = (U1)(u4_ap_B32[TYDC_B32_WO_SU] >> u4_t_lsb);

            u4_t_offset                = u4_t_lpcnt + (U4)1U;

            u1_ap_ans_tx[u4_t_offset]  = (U1)(u4_ap_B32[TYDC_B32_WO_LA] >> u4_t_lsb);

            u4_t_lsb                  += (U4)TYDC_B8_LB_NEXT;
        }
        u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_22;
    }

    return(u1_t_proc);
}
/*===================================================================================================================================*/
/*  U4      u4_g_TyDoCANNu32(const U1 * u1_ap_N8, const U2 u2_a_NBYTE)                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_TyDoCANNu32(const U1 * u1_ap_N8, const U2 u2_a_NBYTE)
{
    U4                        u4_t_lpcnt;
    U4                        u4_t_n32;

    u4_t_n32 = (U4)0U;
    if(u2_a_NBYTE <= (U2)TYDC_N32_NB_MAX){

        for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u2_a_NBYTE; u4_t_lpcnt++){
            u4_t_n32 <<= TYDC_B8_LB_NEXT;
            u4_t_n32  |= (U4)u1_ap_N8[u4_t_lpcnt];
        }
    }

    return(u4_t_n32);
}
/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANNu32Tx(U1 * u1_ap_ans_tx, const U4 u4_a_N32, const U2 u2_a_NBYTE)                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNu32Tx(U1 * u1_ap_ans_tx, const U4 u4_a_N32, const U2 u2_a_NBYTE)
{
    U4                       u4_t_lpcnt;
    U4                       u4_t_lsb;

    U1                       u1_t_proc;

    if(u2_a_NBYTE <= (U2)TYDC_N32_NB_MAX){

        u4_t_lsb = (U4)u2_a_NBYTE * (U4)TYDC_B8_LB_NEXT;
        for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u2_a_NBYTE; u4_t_lpcnt++){
            u4_t_lsb                 -= (U4)TYDC_B8_LB_NEXT;
            u1_ap_ans_tx[u4_t_lpcnt]  = (U1)(u4_a_N32 >> u4_t_lsb);
        }

        u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_22;
    }

    return(u1_t_proc);
}
