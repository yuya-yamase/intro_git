/*  U1      u1_g_TyDoCANNvmIfRead(U1 * u1_ap_ans_tx, const U2 u2_a_DID)                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfRead(U1 * u1_ap_ans_tx, const U2 u2_a_DID)
{
    const ST_TYDC_NVM_IF *    st_tp_IF;
    U1                        u1_t_proc;

    u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
    if(u2_a_DID < u2_g_TYDC_NVM_NUM_DID){

        st_tp_IF = st_gp_TYDC_NVM_DID[u2_a_DID].stp_IF;
        if((st_tp_IF            != vdp_PTR_NA) &&
           (st_tp_IF->fp_u1_REA != vdp_PTR_NA)){
            u1_t_proc = (*st_tp_IF->fp_u1_REA)(u1_ap_ans_tx, u2_a_DID);
        }
    }

    return(u1_t_proc);
}
/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANNvmIfWrite(const U1 * u1_ap_REQ, const U2 u2_a_ELPSD, const U2 u2_a_DID)                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfWrite(const U1 * u1_ap_REQ, const U2 u2_a_ELPSD, const U2 u2_a_DID)
{
    static const U2           u2_s_TYDC_NVM_If_NVM_TOUT = (U2)5000U / (U2)10U;
    const ST_TYDC_NVM_IF *    st_tp_IF;
    U1                        u1_t_proc;

    u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
    if(u2_a_DID < u2_g_TYDC_NVM_NUM_DID){

        st_tp_IF = st_gp_TYDC_NVM_DID[u2_a_DID].stp_IF;
        if((st_tp_IF            != vdp_PTR_NA) &&
           (st_tp_IF->fp_u1_WRI != vdp_PTR_NA) &&
           (st_tp_IF->fp_u1_SYN != vdp_PTR_NA)){

            if(u2_a_ELPSD == (U2)0U){
                u1_t_proc = (*st_tp_IF->fp_u1_WRI)(u1_ap_REQ, u2_a_DID);
            }
            else if(u2_a_ELPSD >= u2_s_TYDC_NVM_If_NVM_TOUT){
                u1_t_proc = (U1)OXDC_SAL_PROC_NR_72;
            }
            else{
                u1_t_proc = (*st_tp_IF->fp_u1_SYN)(u2_a_DID);
            }
        }
    }

    return(u1_t_proc);
}
/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANNvmIfSynchk(const U2 u2_a_DID)                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfSynchk(const U2 u2_a_DID)
{
    U1                        u1_t_nvm_chk;
    U1                        u1_t_proc;

    u1_t_nvm_chk = u1_g_Nvmc_GetDataSts(st_gp_TYDC_NVM_DID[u2_a_DID].u2_nid);
    if(u1_t_nvm_chk == (U1)NVMC_STATUS_KIND_OK){
        u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
    }
    else if((u1_t_nvm_chk >= (U1)NVMC_STATUS_KIND_NG ) ||
            (u1_t_nvm_chk == (U1)NVMC_STATUS_ERRCOMP ) ||
            (u1_t_nvm_chk == (U1)NVMC_STATUS_CACHE_NG)){
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_72;
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
    }

    return(u1_t_proc);
}
