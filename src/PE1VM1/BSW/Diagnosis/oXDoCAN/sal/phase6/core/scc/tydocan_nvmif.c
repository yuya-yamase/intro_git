/*  U1      u1_g_TyDoCANNvmIfSup(const U2 u2_a_DID)                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfSup(const U2 u2_a_DID)
{
    const ST_TYDC_NVM_IF *    st_tp_IF;

    U4                        u4_t_max;
    U1                        u1_t_sup;

    u1_t_sup = (U1)OXDC_XID_SUP_NOT;
    if(u2_a_DID < u2_g_TYDC_NVM_NUM_DID){

        st_tp_IF = st_gp_TYDC_NVM_DID[u2_a_DID].stp_IF;
        if(st_tp_IF == vdp_PTR_NA){
            u4_t_max = (U4)0U;
        }
        else if(st_tp_IF->fp_u4_MAX != vdp_PTR_NA){
            u4_t_max = (*st_tp_IF->fp_u4_MAX)(u2_a_DID);
        }
        else{
            u4_t_max = (U4)U4_MAX;
        }            

        if(u4_t_max > (U4)0U){
            u1_t_sup = (U1)OXDC_XID_SUP_PUB;
        }
    }

    return(u1_t_sup);
}
