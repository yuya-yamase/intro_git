/*  U1      u1_g_TyDoCANNvmIfRea_Skn(U1 * u1_ap_ans_tx, const U2 u2_a_DID)                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfRea_Skn(U1 * u1_ap_ans_tx, const U2 u2_a_DID)
{
    U1      u1_t_nvm_chk;
    U1      u1_t_proc;

    u1_t_nvm_chk = u1_g_Nvmc_ReadOthrwithSts(st_gp_TYDC_NVM_DID[TYDC_NVM_BA_SKN].u2_nid,
                                             st_gp_TYDC_NVM_DID[TYDC_NVM_BA_SKN].u2_nbyte,
                                             u1_ap_ans_tx);

    if((u1_t_nvm_chk >= (U1)NVMC_STATUS_KIND_NG ) ||
       (u1_t_nvm_chk == (U1)NVMC_STATUS_ERRCOMP ) ||
       (u1_t_nvm_chk == (U1)NVMC_STATUS_CACHE_NG)){

        u1_t_proc = (U1)OXDC_SAL_PROC_NR_72;
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
    }

    return(u1_t_proc);
}
