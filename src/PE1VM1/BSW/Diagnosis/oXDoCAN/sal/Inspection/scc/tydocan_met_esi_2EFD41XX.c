#define OXDC_2EFD41XX_NB_SKN                         (16U)      /* Number of bytes for SAFEKEY NUMBER.  */
#define OXDC_2EFD41XX_SKN_INI                        (0xFFU)

#define OXDC_2EFD41XX_MT_PAS                         (0xF5U)
#define OXDC_2EFD41XX_MT_FAI                         (0xFAU)

