void vd_g_vCryClCfg_Callback(const U4 u4_a_JOBID, const U1 u1_a_CSM_RESULT)
{
     if((u4_a_JOBID == (U4)VCRYCL_JOBID_KEYUPDATE_MASTERECUKEY) ||
        (u4_a_JOBID == (U4)VCRYCL_JOBID_KEYUPDATE_MACKEY) ||
        (u4_a_JOBID == (U4)VCRYCL_JOBID_KEYELEMENTGET_MASTERECUKEY) ||
        (u4_a_JOBID == (U4)VCRYCL_JOBID_KEYELEMENTGET_MACKEY) ||
        (u4_a_JOBID == (U4)VCRYCL_JOBID_MACGENERATE_USEBYKEYVERIFY) ||
        (u4_a_JOBID == (U4)VCRYCL_JOBID_ENCRYPT_USEBYKEYVERIFY)){

         vd_g_TyDoCANAubIfKeyAck(u4_a_JOBID, (Crypto_ResultType)u1_a_CSM_RESULT);
     }
}

