#pragma ghs startdata
extern U4            __ghsbegin_bss_SHARE_COMPLEX_VEHOPEMD_SYSSTS;
#pragma ghs enddata
#define u4p_s_veh_opemd_vmshared                 ((U4 *)&__ghsbegin_bss_SHARE_COMPLEX_VEHOPEMD_SYSSTS)

static U1                       u1_s_veh_opemd_pts_chk;

