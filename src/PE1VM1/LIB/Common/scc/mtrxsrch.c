/*  U2      u2_g_MtrxSrch(const U2 u2_a_IDX_STA, const U4 * u4_ap_INST, const ST_MTRX_SRCH * const st_ap_SRCH)                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U2      u2_g_MtrxSrch(const U2 u2_a_IDX_STA, const U4 * u4_ap_INST, const ST_MTRX_SRCH * const st_ap_SRCH)
{
    const U4 *          u4_tp_MASK;
    const U4 *          u4_tp_CRIT; 
    U4                  u4_t_len_inst;
    U4                  u4_t_num_srch;
    U4                  u4_t_rslt;
    U4                  u4_t_jdgbit;
    U4                  u4_t_cmprpos;
    U4                  u4_t_instcnt;
    U4                  u4_t_equal;

    u4_t_len_inst = (U4)st_ap_SRCH->u2_len_inst;
    u4_t_num_srch = (U4)st_ap_SRCH->u2_num_srch;

    u4_t_rslt = (U4)U2_MAX;
    if((u4_t_len_inst > (U4)0U          ) &&
       (u4_t_num_srch > (U4)0U          ) &&
       (u4_t_num_srch > (U4)u2_a_IDX_STA)){

        u4_t_cmprpos = (U4)u2_a_IDX_STA;
        do{
            u4_t_instcnt = u4_t_len_inst * u4_t_cmprpos;
            u4_tp_MASK = &(st_ap_SRCH->u4p_MASK[u4_t_instcnt]);
            u4_tp_CRIT = &(st_ap_SRCH->u4p_CRIT[u4_t_instcnt]);

            u4_t_equal = (U4)TRUE;
            u4_t_instcnt = (U4)0U;
            do{
                u4_t_jdgbit = u4_ap_INST[u4_t_instcnt] & u4_tp_MASK[u4_t_instcnt];
                if(u4_t_jdgbit != u4_tp_CRIT[u4_t_instcnt]){
                    u4_t_equal = (U4)FALSE;
                }
                u4_t_instcnt++;
            }while((u4_t_equal   == (U4)TRUE     ) &&
                   (u4_t_instcnt  < u4_t_len_inst));

            if(u4_t_equal == (U4)TRUE){
                u4_t_rslt = u4_t_cmprpos;
            }
            else{
                u4_t_cmprpos++;
                if(u4_t_cmprpos >= u4_t_num_srch){
                    u4_t_cmprpos = (U4)0U;
                }
            }
        }while((u4_t_rslt    >= (U4)U2_MAX      ) &&
               (u4_t_cmprpos != (U4)u2_a_IDX_STA));
    }

    return((U2)u4_t_rslt);
}
