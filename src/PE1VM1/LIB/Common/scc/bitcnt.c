/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Literal Definitions                                                                                                              */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define BITCNT_SHFT_1ST                          (1U)
#define BITCNT_SHFT_2ND                          (2U)
#define BITCNT_SHFT_3RD                          (4U)
#define BITCNT_SHFT_4TH                          (8U)
#define BITCNT_SHFT_5TH                          (16U)
#define BITCNT_MASK_1ST                          (0x55555555U)
#define BITCNT_MASK_2ND                          (0x33333333U)
#define BITCNT_MASK_3RD                          (0x0F0F0F0FU)
#define BITCNT_MAX_MASK                          (0x0000003FU)

