/*===================================================================================================================================*/
/*  U1      u1_g_BitCnt(const U4 u4_a_BIT)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:        -                                                                                                              */
/*  Return:           -                                                                                                              */
/*===================================================================================================================================*/
U1      u1_g_BitCnt(const U4 u4_a_BIT)
{
    U4        u4_t_bit;

    u4_t_bit = (u4_a_BIT & (U4)BITCNT_MASK_1ST) + ((u4_a_BIT >> BITCNT_SHFT_1ST) & (U4)BITCNT_MASK_1ST);
    u4_t_bit = (u4_t_bit & (U4)BITCNT_MASK_2ND) + ((u4_t_bit >> BITCNT_SHFT_2ND) & (U4)BITCNT_MASK_2ND);
    u4_t_bit = (u4_t_bit & (U4)BITCNT_MASK_3RD) + ((u4_t_bit >> BITCNT_SHFT_3RD) & (U4)BITCNT_MASK_3RD);
    u4_t_bit = u4_t_bit + (u4_t_bit >> BITCNT_SHFT_4TH);
    u4_t_bit = (u4_t_bit + (u4_t_bit >> BITCNT_SHFT_5TH)) & (U4)BITCNT_MAX_MASK;

    return((U1)u4_t_bit);
}
