/*===================================================================================================================================*/
/*  U4      u4_g_GetCoreId(void)                                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:        -                                                                                                              */
/*  Return:           coreid                                                                                                         */
/*===================================================================================================================================*/
U4 u4_g_GetCoreId(void)
{
    U4      u4_t_peid;
    U4      u4_t_coreid;

    /* PEID register could be read in user mode */
    u4_t_peid = __STSR( (S4)BSWLIB_REGID_SR0 , (S4)BSWLIB_SELID_2 );    /* get coreid from PEID register */
    u4_t_coreid = ( u4_t_peid & (U4)BSWLIB_MASK_PEID );                 /* get PEID field data */

    return( u4_t_coreid );
}
