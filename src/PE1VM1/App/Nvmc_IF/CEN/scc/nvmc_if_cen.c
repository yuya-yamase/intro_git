/* void    vd_g_NvmcIfCENBonInit(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_NvmcIfCENBonInit(void)
{
    u4_s_PwrCtrl_OtaReq = (U4)PWRCTRL_OTA_INIT;

    return;
}

