/*  Constant Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static const U2   u2_sp_XSPI_NVM_16B[XSPI_NVM_NUM_16B] = {
    (U2)U2_MAX,                             /* Provisional for CV-R */
    (U2)U2_MAX                              /* Provisional for CV-R */
    /* (U2)NVMCID_OTR_RCAR_HMI_16B_0, */    /* Provisional for CV-R */
    /* (U2)NVMCID_OTR_RCAR_HMI_16B_1  */    /* Provisional for CV-R */
};

