/*===================================================================================================================================*/
/*  void    vd_g_XSpiNvmInit(void)                                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_XSpiNvmInit(void)
{
    U4 *                 u4_tp_nvm_n32;

    U4                   u4_t_lpcnt;
    U4                   u4_t_woff;
    U2                   u2_t_nid;
    U1                   u1_t_nvm_chk;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)XSPI_NVM_NUM_16B; u4_t_lpcnt++){

        u4_t_woff     = u4_t_lpcnt * (U4)XSPI_NVM_16B_NWORD;
        u4_tp_nvm_n32 = &u4_sp_xspi_nvm_n32[u4_t_woff];

        u4_tp_nvm_n32[XSPI_NVM_WO_0] = (U4)U4_MAX;
        u4_tp_nvm_n32[XSPI_NVM_WO_1] = (U4)U4_MAX;
        u4_tp_nvm_n32[XSPI_NVM_WO_2] = (U4)U4_MAX;
        u4_tp_nvm_n32[XSPI_NVM_WO_3] = (U4)U4_MAX;

        u2_t_nid      = u2_sp_XSPI_NVM_16B[u4_t_lpcnt];
        u1_t_nvm_chk  = u1_g_Nvmc_ReadOthrwithSts(u2_t_nid, (U2)XSPI_NVM_16B_NBYTE, (U1 *)u4_tp_nvm_n32);
        if((u1_t_nvm_chk >= (U1)NVMC_STATUS_KIND_NG ) ||
           (u1_t_nvm_chk == (U1)NVMC_STATUS_ERRCOMP ) ||
           (u1_t_nvm_chk == (U1)NVMC_STATUS_CACHE_NG)){

            u4_tp_nvm_n32[XSPI_NVM_WO_0] = (U4)U4_MAX;
            u4_tp_nvm_n32[XSPI_NVM_WO_1] = (U4)U4_MAX;
            u4_tp_nvm_n32[XSPI_NVM_WO_2] = (U4)U4_MAX;
            u4_tp_nvm_n32[XSPI_NVM_WO_3] = (U4)U4_MAX;
        }
    }

    u2_s_xspi_nvm_syn_elpsd = (U2)U2_MAX;
}
/*===================================================================================================================================*/
/*  void    vd_g_XSpiNvmMainTask(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_XSpiNvmMainTask(void)
{
    static const U2      u2_s_XSPI_NVM_SYN_NEXT = (U2)10000U / (U2)10U; /* 10 seconds */

    U4 *                 u4_tp_nex;

    U4                   u4_t_lpcnt;
    U4                   u4_t_woff;
    U4                   u4_tp_las[XSPI_NVM_16B_NWORD];

    U2                   u2_t_nid;

    U1                   u1_t_nvm_chk;
    U1                   u1_t_esi_chk;

#if 0   /* BEV BSW provisionally */
    u1_t_esi_chk = u1_g_ESInspectMdBfield();
#else
    u1_t_esi_chk = (U1)FALSE;
#endif
    if(u2_s_xspi_nvm_syn_elpsd >= (U2)U2_MAX){
     /* u2_s_xspi_nvm_syn_elpsd = (U2)U2_MAX; */
    }
    else if(u1_t_esi_chk != (U1)0U){
        u2_s_xspi_nvm_syn_elpsd = (U2)U2_MAX;
    }
    else{
        u2_s_xspi_nvm_syn_elpsd++;
    }

    if(u2_s_xspi_nvm_syn_elpsd == u2_s_XSPI_NVM_SYN_NEXT){

        for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)XSPI_NVM_NUM_16B; u4_t_lpcnt++){

            u4_t_woff = u4_t_lpcnt * (U4)XSPI_NVM_16B_NWORD;
            u4_tp_nex = &u4_sp_xspi_nvm_n32[u4_t_woff];

            u4_tp_las[XSPI_NVM_WO_0] = (U4)U4_MAX;
            u4_tp_las[XSPI_NVM_WO_1] = (U4)U4_MAX;
            u4_tp_las[XSPI_NVM_WO_2] = (U4)U4_MAX;
            u4_tp_las[XSPI_NVM_WO_3] = (U4)U4_MAX;

            u2_t_nid     = u2_sp_XSPI_NVM_16B[u4_t_lpcnt];
            u1_t_nvm_chk = u1_g_Nvmc_ReadOthrwithSts(u2_t_nid, (U2)XSPI_NVM_16B_NBYTE, (U1 *)&u4_tp_las[0]);
            if((u1_t_nvm_chk                 >= (U1)NVMC_STATUS_KIND_NG     ) ||
               (u1_t_nvm_chk                 == (U1)NVMC_STATUS_ERRCOMP     ) ||
               (u1_t_nvm_chk                 == (U1)NVMC_STATUS_CACHE_NG    ) ||
               (u4_tp_las[XSPI_NVM_WO_0] != u4_tp_nex[XSPI_NVM_WO_0]) ||
               (u4_tp_las[XSPI_NVM_WO_1] != u4_tp_nex[XSPI_NVM_WO_1]) ||
               (u4_tp_las[XSPI_NVM_WO_2] != u4_tp_nex[XSPI_NVM_WO_2]) ||
               (u4_tp_las[XSPI_NVM_WO_3] != u4_tp_nex[XSPI_NVM_WO_3])){

                vd_g_Nvmc_ClearShtdwnTimer();
                vd_g_Nvmc_WriteOthr(u2_t_nid, (U2)XSPI_NVM_16B_NBYTE, (U1 *)&u4_tp_nex[0]);
            }
        }
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_XSpiNvmRx(const U4 * u4_ap_PDU_RX)                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_XSpiNvmRx(const U4 * u4_ap_PDU_RX)
{
    const U4 *    u4_tp_NVM_RX;

    U4            u4_t_lpcnt;
    U4            u4_t_last;
    U4            u4_t_next;

    u4_tp_NVM_RX = &u4_ap_PDU_RX[57];
    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)XSPI_NVM_NUM_N32; u4_t_lpcnt++){

        u4_t_last = u4_sp_xspi_nvm_n32[u4_t_lpcnt];
        u4_t_next = u4_tp_NVM_RX[u4_t_lpcnt];
        if((u4_t_next != (U4)U4_MAX) &&
           (u4_t_last != u4_t_next )){

            u4_sp_xspi_nvm_n32[u4_t_lpcnt] = u4_t_next; 
            u2_s_xspi_nvm_syn_elpsd        = (U2)0U;
        }
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_XSpiNvmTx(U4 * u4_ap_pdu_tx)                                                                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_XSpiNvmTx(U4 * u4_ap_pdu_tx)
{
    U4 *          u4_tp_nvm_tx;
    U4            u4_t_lpcnt;

    u4_tp_nvm_tx = &u4_ap_pdu_tx[101];
    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)XSPI_NVM_NUM_N32; u4_t_lpcnt++){
        u4_tp_nvm_tx[u4_t_lpcnt] = u4_sp_xspi_nvm_n32[u4_t_lpcnt];
    }
}
