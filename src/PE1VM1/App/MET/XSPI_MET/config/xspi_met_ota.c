/*===================================================================================================================================*/
/*  void    vd_g_XSpiOtaInit(void)                                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_XSpiOtaInit(void)
{
    vd_g_MemfillU4(&u4_sp_xspi_ota_rx[0], (U4)0U, (U4)XSPI_OTA_NW_RX);

    u2_s_xspi_ota_rxtocnt = (U2)U2_MAX;
    u2_s_xspi_ota_syncnt  = (U2)U2_MAX;

    u1_s_xspi_ota_blo_rx  = (U1)0x00U;
    u1_s_xspi_ota_log_rx  = (U1)U1_MAX;
}
/*===================================================================================================================================*/
/*  void    vd_g_XSpiOtaMainTask(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_XSpiOtaMainTask(void)
{
    static const U2  u2_s_XSPI_OTA_RX_TOUT = (U2)200U / (U2)10U;

    if(u2_s_xspi_ota_rxtocnt < (U2)U2_MAX){
        u2_s_xspi_ota_rxtocnt++;
    }

    if(u2_s_xspi_ota_rxtocnt >= u2_s_XSPI_OTA_RX_TOUT){
        u2_s_xspi_ota_syncnt  = (U2)U2_MAX;
        u1_s_xspi_ota_blo_rx  = (U1)0x00U;
        u1_s_xspi_ota_log_rx  = (U1)U1_MAX;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_XSpiOtaRx(const U4 * u4_ap_PDU_RX)                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_XSpiOtaRx(const U4 * u4_ap_PDU_RX)
{
    static const U2  u2_sp_XSPI_OTA_NW_BY_RX[XSPI_OTA_LOG_NUM_BLO] = {
        (U2)36U,                     /* 0 */
        (U2)64U,                     /* 1 */ /* 256 byte */
        (U2)64U,                     /* 2 */ /* 256 byte */
        (U2)64U,                     /* 3 */ /* 256 byte */
        (U2)64U,                     /* 4 */ /* 256 byte */
        (U2)64U,                     /* 5 */ /* 256 byte */
        (U2)58U                      /* 6 */ /* 232 byte */
    };

    static const U2  u2_sp_XSPI_OTA_DST_BY_RX[XSPI_OTA_LOG_NUM_BLO << 1U] = {
        (U2)0U,                          /*  0 */
        (U2)XSPI_OTA_22_0119_BLO_0,  /*  1 */
        (U2)100U,                        /*  2 */
        (U2)164U,                        /*  3 */
        (U2)228U,                        /*  4 */
        (U2)292U,                        /*  5 */
        (U2)356U,                        /*  6 */

        (U2)0U,                          /*  7 */
        (U2)XSPI_OTA_22_0119_BLO_1,  /*  8 */
        (U2)478U,                        /*  9 */
        (U2)542U,                        /* 10 */
        (U2)606U,                        /* 11 */
        (U2)670U,                        /* 12 */
        (U2)734U                         /* 13 */
    };

    const U4 *       u4_tp_OTA_RX;

    U2               u2_t_cnt;
    U2               u2_t_dst;

    u4_tp_OTA_RX = &u4_ap_PDU_RX[0];

    u2_t_cnt = (U2)u4_tp_OTA_RX[0];
    if(u2_t_cnt != u2_s_xspi_ota_syncnt){
        u1_s_xspi_ota_blo_rx &= (U1)XSPI_OTA_BLO_RX_AB_F020;
    }
    u2_s_xspi_ota_syncnt = u2_t_cnt;

    u2_t_cnt = (U2)(u4_tp_OTA_RX[0] >> XSPI_OTA_CNT_LSB_BLO);
    if(u2_t_cnt < (U2)XSPI_OTA_LOG_NUM_BLO){

        if(u1_s_xspi_ota_log_rx == (U1)XSPI_OTA_LOG_RX_1){
            u2_t_dst = u2_t_cnt + (U2)XSPI_OTA_LOG_NUM_BLO;
        }
        else{
            u2_t_dst = u2_t_cnt; 
        }
        u2_t_dst = u2_sp_XSPI_OTA_DST_BY_RX[u2_t_dst];
        vd_g_MemcpyU4(&u4_sp_xspi_ota_rx[u2_t_dst], &u4_tp_OTA_RX[1], (U4)u2_sp_XSPI_OTA_NW_BY_RX[u2_t_cnt]);

        u1_s_xspi_ota_blo_rx |= ((U1)0x01U << u2_t_cnt);
        if(u1_s_xspi_ota_blo_rx >= (U1)XSPI_OTA_BLO_RX_22_0119){
            u1_s_xspi_ota_blo_rx &= (U1)XSPI_OTA_BLO_RX_AB_F020;
            u1_s_xspi_ota_log_rx  = (u1_s_xspi_ota_log_rx + (U1)1U) & (U1)XSPI_OTA_LOG_RX_1;
        }

        u2_s_xspi_ota_rxtocnt = (U2)0U;
    }
}
/*===================================================================================================================================*/
/*  U1      u1_g_XSpiOtaRobNumRecord(const U1 u1_a_REQ_MAX)                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_XSpiOtaRobNumRecord(const U1 u1_a_REQ_MAX)
{
    U1               u1_t_rx;
    U1               u1_t_rec;

    if(u1_a_REQ_MAX == (U1)TRUE){
        u1_t_rec = (U1)XSPI_OTA_NUM_REC_AB_F020;
    }
    else{

        u1_t_rx  = u1_s_xspi_ota_blo_rx & (U1)XSPI_OTA_BLO_RX_AB_F020;
        u1_t_rec = (U1)u4_sp_xspi_ota_rx[0];
        if((u1_t_rx  == (U1)0x00U                       ) ||
           (u1_t_rec >  (U1)XSPI_OTA_NUM_REC_AB_F020)){
            u1_t_rec = (U1)0U;
        }
    }

    return(u1_t_rec);
}
/*===================================================================================================================================*/
/*  void    vd_g_XSpiOtaRobSsrRead(const U1 u1_a_REC_IDX, U1 * u1_ap_ssr)                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_XSpiOtaRobSsrRead(const U1 u1_a_REC_IDX, U1 * u1_ap_ssr)
{
    const U1 *       u1_tp_SSR;
    U2               u2_t_src;
    U1               u1_t_rx;

    u1_t_rx = u1_s_xspi_ota_blo_rx & (U1)XSPI_OTA_BLO_RX_AB_F020;
    if((u1_t_rx      != (U1)0x00U                       ) &&
       (u1_a_REC_IDX <  (U1)XSPI_OTA_NUM_REC_AB_F020)){

        u2_t_src  = (U2)u1_a_REC_IDX * (U2)XSPI_OTA_NB_AB_F020;
        u1_tp_SSR = (const U1 *)&u4_sp_xspi_ota_rx[1];
        vd_g_MemcpyU1(u1_ap_ssr, &u1_tp_SSR[u2_t_src], (U4)XSPI_OTA_NB_AB_F020);
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_XSpiOtaLogRead(U1 * u1_ap_log_rx)                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_XSpiOtaLogRead(U1 * u1_ap_log_rx)
{
    if(u1_ap_log_rx == vdp_PTR_NA){
    }
    else if(u1_s_xspi_ota_log_rx == (U1)XSPI_OTA_LOG_RX_0){
        vd_g_MemcpyU1(u1_ap_log_rx, (const U1 *)&u4_sp_xspi_ota_rx[XSPI_OTA_22_0119_BLO_1], (U4)XSPI_OTA_NB_22_0119);
    }
    else if(u1_s_xspi_ota_log_rx == (U1)XSPI_OTA_LOG_RX_1){
        vd_g_MemcpyU1(u1_ap_log_rx, (const U1 *)&u4_sp_xspi_ota_rx[XSPI_OTA_22_0119_BLO_0], (U4)XSPI_OTA_NB_22_0119);
    }
    else{
        vd_g_MemfillU1(u1_ap_log_rx, (U1)0U, (U4)XSPI_OTA_NB_22_0119);
    }
}
