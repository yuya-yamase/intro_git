/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Literal Definitions                                                                                                              */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define XSPI_OTA_CNT_LSB_BLO                 (16U)
#define XSPI_OTA_CNT_LSB_SYN                 (0U)

/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define XSPI_OTA_NW_RX                       (792U)         /* 144 + (1512 * 2) = 3168 byte */

#define XSPI_OTA_LOG_NUM_BLO                 (7U)
#define XSPI_OTA_LOG_RX_0                    (0U)
#define XSPI_OTA_LOG_RX_1                    (1U)

#define XSPI_OTA_22_0119_BLO_0               (36U)
#define XSPI_OTA_22_0119_BLO_1               (414U)

#define XSPI_OTA_BLO_RX_22_0119              (0x7eU)
#define XSPI_OTA_BLO_RX_AB_F020              (0x01U)

