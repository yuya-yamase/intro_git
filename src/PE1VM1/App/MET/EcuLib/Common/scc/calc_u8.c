/*===================================================================================================================================*/
/*  U1      u1_g_MulU8(const U4 * u4_ap_TERM, U4 * u4_ap_rslt)                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      const U4 u4_ap_TERM, U4 u4_ap_rslt : calucation result(Lo/Hi)                                                    */
/*  Return:         u1_t_rsltbit : See calc_8.h "CALC_U8_RSLT_XXXX"                                                                  */
/*===================================================================================================================================*/
U1      u1_g_MulU8(const U4 * u4_ap_TERM, U4 * u4_ap_rslt)
{
    U4  u4_t_uhi;
    U4  u4_t_ulo;
    U4  u4_t_vhi;
    U4  u4_t_vlo;
    U4  u4_t_whi;
    U4  u4_t_wlo;
    U1  u1_t_rsltbit;

    if((u4_ap_TERM[CALC_U8_LO] != (U4)0U) &&
       (u4_ap_TERM[CALC_U8_HI] != (U4)0U)){

        u4_ap_rslt[CALC_U8_LO] = u4_ap_TERM[CALC_U8_LO] * u4_ap_TERM[CALC_U8_HI];

        u4_t_ulo = u4_ap_TERM[CALC_U8_LO] & (U4)CALC_U8_BITMSK_U2;
        u4_t_uhi = u4_ap_TERM[CALC_U8_LO] >> CALC_U8_BITLEN_U2;
        u4_t_vlo = u4_ap_TERM[CALC_U8_HI] & (U4)CALC_U8_BITMSK_U2;
        u4_t_vhi = u4_ap_TERM[CALC_U8_HI] >> CALC_U8_BITLEN_U2;

        u4_t_whi = ((u4_t_ulo * u4_t_vlo) >> CALC_U8_BITLEN_U2) + (u4_t_uhi * u4_t_vlo);
        u4_t_wlo = (u4_t_whi & (U4)CALC_U8_BITMSK_U2) + (u4_t_ulo * u4_t_vhi);

        u4_ap_rslt[CALC_U8_HI] = u4_t_uhi * u4_t_vhi;
        u4_ap_rslt[CALC_U8_HI] += (u4_t_whi >> CALC_U8_BITLEN_U2);
        u4_ap_rslt[CALC_U8_HI] += (u4_t_wlo >> CALC_U8_BITLEN_U2);

        if(u4_ap_rslt[CALC_U8_HI] > (U4)0U){
            u1_t_rsltbit = (U1)CALC_U8_RSLT_OVRFLW_U4;
        }
        else{
            u1_t_rsltbit = (U1)0U;
        }
    }
    else{
        u1_t_rsltbit = (U1)CALC_U8_RSLT_ZERO;
        u4_ap_rslt[CALC_U8_LO] = (U4)0U;
        u4_ap_rslt[CALC_U8_HI] = (U4)0U;
    }

    return(u1_t_rsltbit);
}
/*===================================================================================================================================*/
/*  U1      u1_g_DivU8(const U4 * u4_ap_TERM, U4 * u4_ap_rslt)                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      const U4 u4_ap_TERM, U4 u4_ap_rslt : calucation result(Lo/Hi)                                                    */
/*  Return:         u1_t_rsltbit : See calc_8.h "CALC_U8_RSLT_XXXX"                                                                  */
/*===================================================================================================================================*/
U1      u1_g_DivU8(const U4 * u4_ap_TERM, U4 * u4_ap_rslt)
{
    U4   u4_t_cnt;
    U4   u4_t_bor;
    U4   u4_t_res;
    U4   u4_t_quo;
    U4   u4_t_div;
    U1   u1_t_rsltbit;

    if(u4_ap_TERM[CALC_U8_DIV] == (U4)0U){
        u4_ap_rslt[CALC_U8_QUO] = (U4)U4_MAX;
        u4_ap_rslt[CALC_U8_RES] = (U4)0U;
        u1_t_rsltbit = ((U1)CALC_U8_RSLT_DIVBYZERO | (U1)CALC_U8_RSLT_OVRFLW_U4);
    }
    else if((u4_ap_TERM[CALC_U8_HI] == (U4)0U) &&
            (u4_ap_TERM[CALC_U8_LO] == (U4)0U)){
        u4_ap_rslt[CALC_U8_QUO] = (U4)0U;
        u4_ap_rslt[CALC_U8_RES] = (U4)0U;
        u1_t_rsltbit = (U1)CALC_U8_RSLT_ZERO;
    }
    else if(u4_ap_TERM[CALC_U8_HI] >= u4_ap_TERM[CALC_U8_DIV]){
        u4_ap_rslt[CALC_U8_QUO] = (U4)U4_MAX;
        u4_ap_rslt[CALC_U8_RES] = (U4)U4_MAX;
        u1_t_rsltbit = (U1)CALC_U8_RSLT_OVRFLW_U4;
    }
    else{
        u4_t_res = u4_ap_TERM[CALC_U8_HI];
        u4_t_quo = u4_ap_TERM[CALC_U8_LO];
        u4_t_div = u4_ap_TERM[CALC_U8_DIV];

        for(u4_t_cnt = (U4)0U; u4_t_cnt < (U4)CALC_U8_DIV_LOOP_MAX; u4_t_cnt++){
            /* 1st ----------------------------------------------------------------------------------*/
            u4_t_bor = (U4)((S4)u4_t_res >> (CALC_U8_BITLEN_U4 - 1U));          /* 31 bit right shift */
            u4_t_res = (u4_t_res << 1U) | (u4_t_quo >> (CALC_U8_BITLEN_U4 - 1U));
            u4_t_bor |= u4_t_res;
            u4_t_quo <<= 1U;
            if(u4_t_bor >= u4_t_div){
                u4_t_res -= u4_t_div;
                u4_t_quo |= (U4)1U;
            }

            /* 2nd ----------------------------------------------------------------------------------*/
            u4_t_bor = (U4)((S4)u4_t_res >> (CALC_U8_BITLEN_U4 - 1U));          /* 31 bit right shift */
            u4_t_res = (u4_t_res << 1U) | (u4_t_quo >> (CALC_U8_BITLEN_U4 - 1U));
            u4_t_bor |= u4_t_res;
            u4_t_quo <<= 1U;
            if(u4_t_bor >= u4_t_div){
                u4_t_res -= u4_t_div;
                u4_t_quo |= (U4)1U;
            }
            /* 3rd ----------------------------------------------------------------------------------*/
            u4_t_bor = (U4)((S4)u4_t_res >> (CALC_U8_BITLEN_U4 - 1U));          /* 31 bit right shift */
            u4_t_res = (u4_t_res << 1U) | (u4_t_quo >> (CALC_U8_BITLEN_U4 - 1U));
            u4_t_bor |= u4_t_res;
            u4_t_quo <<= 1U;
            if(u4_t_bor >= u4_t_div){
                u4_t_res -= u4_t_div;
                u4_t_quo |= (U4)1U;
            }
            /* 4th ----------------------------------------------------------------------------------*/
            u4_t_bor = (U4)((S4)u4_t_res >> (CALC_U8_BITLEN_U4 - 1U));          /* 31 bit right shift */
            u4_t_res = (u4_t_res << 1U) | (u4_t_quo >> (CALC_U8_BITLEN_U4 - 1U));
            u4_t_bor |= u4_t_res;
            u4_t_quo <<= 1U;
            if(u4_t_bor >= u4_t_div){
                u4_t_res -= u4_t_div;
                u4_t_quo |= (U4)1U;
            }
        }

        u4_ap_rslt[CALC_U8_QUO] = u4_t_quo;
        u4_ap_rslt[CALC_U8_RES] = u4_t_res;
        u1_t_rsltbit = (U1)0U;
    }

    return(u1_t_rsltbit);
}
/*===================================================================================================================================*/
/*  U1      u1_g_MuldivU8(const U4 * u4_ap_TERM, U4 * u4_ap_rslt)                                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      const U4 u4_ap_TERM, U4 u4_ap_rslt : calucation result(Lo/Hi)                                                    */
/*  Return:         u1_t_rsltbit : See calc_8.h "CALC_U8_RSLT_XXXX"                                                                  */
/*===================================================================================================================================*/
U1      u1_g_MuldivU8(const U4 * u4_ap_TERM, U4 * u4_ap_rslt)
{
    U4   u4_tp_mulrslt[CALC_U8_NUM_TERM];
    U1   u1_t_rsltbit;

    (void)u1_g_MulU8(u4_ap_TERM, &u4_tp_mulrslt[0]);
    u4_tp_mulrslt[CALC_U8_DIV] = u4_ap_TERM[CALC_U8_DIV];
    u1_t_rsltbit = u1_g_DivU8(&u4_tp_mulrslt[0], u4_ap_rslt);

    return(u1_t_rsltbit);
}
