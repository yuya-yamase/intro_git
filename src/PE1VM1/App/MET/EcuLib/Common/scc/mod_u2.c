/*  U2      u2_g_ModU2(const U2 u2_a_NUM, const U2 u2_a_DIV)                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U2      u2_g_ModU2(const U2 u2_a_NUM, const U2 u2_a_DIV)
{
    U2      u2_t_quo;

    u2_t_quo = (U2)MOD_U2_DIV_BY_ZERO;
    if(u2_a_DIV > (U2)0U){
        u2_t_quo = u2_a_NUM % u2_a_DIV;
    }

    return(u2_t_quo);
}
