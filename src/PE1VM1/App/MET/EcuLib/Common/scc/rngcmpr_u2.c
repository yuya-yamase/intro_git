/*  U2      u2_g_RngCmprU2(const U2 u2_a_IDX, const U2 u2_a_SMPL, const ST_RNG_CMPR_U2 * const st_ap_CMPR)                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U2      u2_g_RngCmprU2(const U2 u2_a_IDX, const U2 u2_a_SMPL, const ST_RNG_CMPR_U2 * const st_ap_CMPR)
{
    const U2 *          u2_tp_UP;
    const U2 *          u2_tp_LO;
    U2                  u2_t_num_rng;
    U2                  u2_t_idx;

    u2_t_idx = (U2)RNG_CMPR_HALF_RNG_UNKNWN;

    u2_t_num_rng = st_ap_CMPR->u2_num_rng;
    if(u2_t_num_rng < (U2)RNG_CMPR_HALF_UDR_LO_LMT){

        if(u2_a_IDX < u2_t_num_rng){
            u2_t_idx = u2_a_IDX;
        }
        else{
            u2_t_idx = st_ap_CMPR->u2_idx_init;
        }

        u2_tp_LO = st_ap_CMPR->u2p_LIMIT;
        u2_tp_UP = &u2_tp_LO[u2_t_num_rng];

        if(u2_a_SMPL > u2_tp_UP[u2_t_idx]){

            do{
                u2_t_idx++;
            }while((u2_t_idx  < u2_t_num_rng      ) &&
                   (u2_a_SMPL > u2_tp_UP[u2_t_idx]));

            if(u2_t_idx >= u2_t_num_rng){
                u2_t_idx = (U2)RNG_CMPR_HALF_OVR_UP_LMT;
            }
        }
        else if(u2_a_SMPL >= u2_tp_LO[0]){

            while((u2_t_idx  > (U2)0U            ) &&
                  (u2_a_SMPL < u2_tp_LO[u2_t_idx])){
                u2_t_idx--;
            }
        }
        else{
            u2_t_idx = (U2)RNG_CMPR_HALF_UDR_LO_LMT;
        }
    }

    return(u2_t_idx);
}
