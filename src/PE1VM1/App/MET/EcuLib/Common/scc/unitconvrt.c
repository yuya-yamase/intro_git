/*  U4      u4_g_UnitconvrtConvert(const U4 u4_a_ORG, const U1 u1_a_METHOD)                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:                                                                                                                       */
/*  Return:                                                                                                                          */
/*===================================================================================================================================*/
U4      u4_g_UnitconvrtConvert(const U4 u4_a_ORG, const U1 u1_a_METHOD)
{
    U4                             u4_t_rslt;
    U4                             u4_t_div;
    U4                             u4_t_mlt;

    u4_t_rslt = (U4)U4_MAX;
    if((u1_a_METHOD < u1_g_UNIT_CONVRT_NUM_METHOD) &&
       (u4_a_ORG <= st_gp_UNIT_CONVRT_CFG[u1_a_METHOD].u4_max)){

        u4_t_div = st_gp_UNIT_CONVRT_CFG[u1_a_METHOD].u4_div;
        u4_t_mlt = st_gp_UNIT_CONVRT_CFG[u1_a_METHOD].u4_mlt;
        u4_t_rslt = (u4_a_ORG / u4_t_div) * u4_t_mlt;
        u4_t_rslt += ((u4_a_ORG % u4_t_div) * u4_t_mlt) / u4_t_div;
    }

    return(u4_t_rslt);
}
