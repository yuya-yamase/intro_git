/*  U2      u2_g_LerpU2(const U2 u2_a_X, const U2 * u2_ap_MAP)                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U2      u2_g_LerpU2(const U2 u2_a_X, const U2 * u2_ap_MAP)
{
    const U2 *      u2_tp_MAP_X;
    const U2 *      u2_tp_MAP_Y;

    U4              u4_t_wka;
    U4              u4_t_wkb;

    U2              u2_t_num_pt;

    U2              u2_t_xlo;
    U2              u2_t_xup;

    U2              u2_t_ylo;
    U2              u2_t_yup;

    U2              u2_t_ilo;
    U2              u2_t_iup;
    U2              u2_t_ichk;
    U2              u2_t_iend;

    U2              u2_t_y;

    u2_t_y = (U2)0U;

    u2_t_num_pt = u2_ap_MAP[LERP_HALF_MAP_IDX_NUM_PT];
    if((u2_t_num_pt >= (U2)LERP_HALF_MAP_MIN_NUM_PT) &&
       (u2_t_num_pt <  (U2)U2_MAX              )){

        u2_tp_MAP_X = &u2_ap_MAP[LERP_HALF_MAP_IDX_X_0];

        u2_t_iup    = u2_t_num_pt - (U2)1U;
        u2_t_ilo    = u2_t_num_pt + (U2)1U;  /* y min */
        u2_tp_MAP_Y = &u2_ap_MAP[u2_t_ilo];

        if(u2_a_X <= u2_tp_MAP_X[0]){
            u2_t_y = u2_tp_MAP_Y[0];
        }
        else if(u2_a_X >= u2_tp_MAP_X[u2_t_iup]){
            u2_t_y = u2_tp_MAP_Y[u2_t_iup];
        }
        else{

            u2_t_ilo  = (U2)0U;
            u2_t_iend = (U2)1U;
            do{
                u2_t_ichk = (u2_t_ilo + u2_t_iup) >> 1U;
                if(u2_a_X >= u2_tp_MAP_X[u2_t_ichk]){
                    u2_t_ilo  = u2_t_ichk;
                    u2_t_iend = u2_t_ichk + (U2)1U;
                }
                else{
                    u2_t_iup  = u2_t_ichk;
                }
            }
            while(u2_t_iend < u2_t_iup);

            u2_t_xlo = u2_tp_MAP_X[u2_t_ilo];
            u2_t_xup = u2_tp_MAP_X[u2_t_iup];

            u2_t_ylo = u2_tp_MAP_Y[u2_t_ilo];
            u2_t_yup = u2_tp_MAP_Y[u2_t_iup];

            u4_t_wka = ((U4)u2_t_ylo * ((U4)u2_t_xup - (U4)u2_a_X)) + ((U4)u2_t_yup * ((U4)u2_a_X - (U4)u2_t_xlo));
            u4_t_wkb = (U4)u2_t_xup - (U4)u2_t_xlo;
            if(u4_t_wkb > (U4)0U){
                u2_t_y = (U2)(u4_t_wka / u4_t_wkb);
            }
        }
    }

    return(u2_t_y);
}
