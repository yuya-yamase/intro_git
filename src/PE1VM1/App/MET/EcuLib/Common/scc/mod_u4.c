/*  U4      u4_g_ModU4(const U4 u4_a_NUM, const U4 u4_a_DIV)                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_ModU4(const U4 u4_a_NUM, const U4 u4_a_DIV)
{
    U4      u4_t_quo;

    u4_t_quo = (U4)MOD_U4_DIV_BY_ZERO;
    if(u4_a_DIV > (U4)0U){
        u4_t_quo = u4_a_NUM % u4_a_DIV;
    }

    return(u4_t_quo);
}
