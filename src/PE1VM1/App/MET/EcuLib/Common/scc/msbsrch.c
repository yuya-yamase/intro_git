/*  Literal Definitions                                                                                                              */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define MSB_SRCH_SHFT_1ST                        (1U)
#define MSB_SRCH_SHFT_2ND                        (2U)
#define MSB_SRCH_SHFT_3RD                        (4U)
#define MSB_SRCH_SHFT_4TH                        (8U)
#define MSB_SRCH_SHFT_5TH                        (16U)
#define MSB_SRCH_MASK_1ST                        (0x55555555U)
#define MSB_SRCH_MASK_2ND                        (0x33333333U)
#define MSB_SRCH_MASK_3RD                        (0x0F0F0F0FU)
#define MSB_SRCH_OFFSET                          (1U)
#define MSB_SRCH_MAX_MASK                        (0x0000001FU)

