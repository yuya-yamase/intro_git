/*===================================================================================================================================*/
/*  U1      u1_g_MsbSrch(const U4 u4_a_BIT)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:        -                                                                                                              */
/*  Return:           -                                                                                                              */
/*===================================================================================================================================*/
U1      u1_g_MsbSrch(const U4 u4_a_BIT)
{
    U4        u4_t_bit;

    if(u4_a_BIT == (U4)0U){
        u4_t_bit = (U4)MSB_SRCH_NOT_FOUND;
    }
    else{
        u4_t_bit  = u4_a_BIT | (u4_a_BIT >> MSB_SRCH_SHFT_1ST);
        u4_t_bit |= (u4_t_bit >> MSB_SRCH_SHFT_2ND);
        u4_t_bit |= (u4_t_bit >> MSB_SRCH_SHFT_3RD);
        u4_t_bit |= (u4_t_bit >> MSB_SRCH_SHFT_4TH);
        u4_t_bit |= (u4_t_bit >> MSB_SRCH_SHFT_5TH);
        u4_t_bit  = (u4_t_bit & (U4)MSB_SRCH_MASK_1ST) + ((u4_t_bit >> MSB_SRCH_SHFT_1ST) & (U4)MSB_SRCH_MASK_1ST);
        u4_t_bit  = (u4_t_bit & (U4)MSB_SRCH_MASK_2ND) + ((u4_t_bit >> MSB_SRCH_SHFT_2ND) & (U4)MSB_SRCH_MASK_2ND);
        u4_t_bit  = (u4_t_bit & (U4)MSB_SRCH_MASK_3RD) + ((u4_t_bit >> MSB_SRCH_SHFT_3RD) & (U4)MSB_SRCH_MASK_3RD);
        u4_t_bit  = u4_t_bit + (u4_t_bit >> MSB_SRCH_SHFT_4TH);
        u4_t_bit  = u4_t_bit + (u4_t_bit >> MSB_SRCH_SHFT_5TH);
        u4_t_bit  = (u4_t_bit - (U4)MSB_SRCH_OFFSET) & (U4)MSB_SRCH_MAX_MASK;
    }

    return((U1)u4_t_bit);
}
