/*===================================================================================================================================*/
/*  Main Routine                                                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void vd_g_HdimmgrRoutine(void)
{
#ifdef HDIMDTCTR_H
    ST_HDIMDTCTR_DIMSTSINF   st_t_dimstsinf;
#endif /* HDIMDTCTR_H */
    U1                       u1_t_sts;

/*---------------------------------------------------------------------------*/
/* HudDimmer Input Update (Priority:High)                                    */
/*---------------------------------------------------------------------------*/
    vd_g_HdimmgrCfgInptUpdtHiPri();

/*---------------------------------------------------------------------------*/
/* HudDimmer Input Update (Priority:Low)                                     */
/*---------------------------------------------------------------------------*/
    vd_g_HdimmgrCfgInptUpdtLoPri();

/*---------------------------------------------------------------------------*/
/* HudDimmer Output Update                                                   */
/*---------------------------------------------------------------------------*/
#ifdef HDIMADJ_H
    vd_g_HdimadjDutyUpdt(&u2_s_hdimmgr_tgtduty , &u2_s_hdimmgr_adjduty);
#else
    u2_s_hdimmgr_tgtduty = (U2)0;
    u2_s_hdimmgr_adjduty = u2_g_HdimmgrIfGetAdjduty();
#endif /* HDIMADJ_H */

#ifdef HDIMDTCTR_H
    vd_g_HdimdtctrDimStsUpdt(&st_t_dimstsinf);
    vd_g_HdimdtctrCfgDimStsAct(u2_s_hdimmgr_adjduty , &st_t_dimstsinf , &u2_s_hdimmgr_otptduty);
    u1_t_sts = st_t_dimstsinf.u1_sts;
#else
    u2_s_hdimmgr_otptduty = u2_g_HdimmgrIfGetOutduty();
    u1_t_sts = (U1)U1_MAX;
#endif /* HDIMDTCTR_H */

    vd_g_HdimmgrCfgSetOtptDuty(u2_s_hdimmgr_otptduty);

/*---------------------------------------------------------------------------*/
/* Sleep Judgment                                                            */
/*---------------------------------------------------------------------------*/
    u1_s_hdimmgr_sleep_ok = u1_g_HdimmgrCfgIsSlpOK(u1_t_sts);
}

