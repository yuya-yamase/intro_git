/*===================================================================================================================================*/
/*  Duty Exchange to Percent                                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments : u2_a_DUTY:Duty                                                                                                       */
/*  Return:   : u2_t_prcnt:Percent Data(LSB:HDIMMGR_IF_DUTYLSB)                                                                      */
/*===================================================================================================================================*/
U2 u2_g_HdimmgrDutyExchgBaseToIfLSB(const U2 u2_a_DUTY)
{
    U2  u2_t_prcnt;

#if (HDIMMGR_IF_DUTYLSB != HDIMMGR_BASE_DUTYLSB)
    if(u2_a_DUTY >= (U2)HDIMMGR_BASE_DUTYMAX){
        u2_t_prcnt = (U2)HDIMMGR_IF_DUTYMAX;
    }
    else{
        u2_t_prcnt = (U2)((((U4)u2_a_DUTY * (U4)HDIMMGR_IF_DUTYMAX) + (U4)HDIMMGR_BASE_DTP_ROUND) / (U4)HDIMMGR_BASE_DUTYLSB);         /* PRQA S 3383 # over detection */
    }
#else
    u2_t_prcnt = u2_a_DUTY;
#endif

    return(u2_t_prcnt);
}

/*===================================================================================================================================*/
/*  Duty Exchange to Percent                                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments : u2_a_DUTY:Duty                                                                                                       */
/*  Return:   : u2_t_prcnt:Percent Data(LSB:0.01percent)                                                                             */
/*===================================================================================================================================*/
static U2 u2_s_HdimmgrDutyExchgToPrcnt(const U2 u2_a_DUTY)
{
    U2  u2_t_prcnt;

    if(u2_a_DUTY >= (U2)HDIMMGR_IF_DUTYMAX){
        u2_t_prcnt = (U2)HDIMMGR_PER_MAX;
    }
    else{
        u2_t_prcnt = (U2)(((U4)u2_a_DUTY * (U4)HDIMMGR_PER_MAX) / (U4)HDIMMGR_IF_DUTYLSB);         /* PRQA S 3383 # over detection */
    }

    return(u2_t_prcnt);
}

