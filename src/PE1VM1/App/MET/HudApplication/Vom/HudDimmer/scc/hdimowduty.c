/*  Defines                                                                                                                          */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define HDIMOWDUTY_OW_ACTTO                         (5000 / HDIMMGR_BASE_TASKTIM)
#define HDIMOWDUTY_OW_INACTV                        (0x0000)
#define HDIMOWDUTY_OW_ACTV                          (0xffff)

#define HDIMOWDUTY_DUTYLSB                          (HDIMMGR_BASE_DUTYLSB)      /* Don't change! */
#define HDIMOWDUTY_DUTYMAX                          (HDIMMGR_BASE_DUTYMAX)      /* Don't change! */
#define HDIMOWDUTY_DIAGPERLSB                       (1000)                      /* Percent LSB(0.1percent) */

