/*===================================================================================================================================*/
/*  Initialize                                                                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimowdutyInit(void)
{
    vd_g_HdimowdutyOwDeAct();
}

/*===================================================================================================================================*/
/*  Dimmer Overwirte duty Update                                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimowdutyUpdt(void)
{
    if(u2_s_hdimowduty_tocnt < (U2)U2_MAX){
        u2_s_hdimowduty_tocnt++;
    }

    u4_s_hdimowduty_ow_unlock = (U4)0;

    if(u2_s_hdimowduty_tocnt > (U2)HDIMOWDUTY_OW_ACTTO){
        vd_g_HdimowdutyOwDeAct();
    }
}

/*===================================================================================================================================*/
/*  Dimmer Overwrite Duty                                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   * u2_ap_owduty : Overwrite duty                                                                                      */
/*  Return      u1_t_isact : Overwrite mode                                                                                          */
/*===================================================================================================================================*/
U1    u1_g_HdimowdutyGetVal(U2 * u2_ap_owduty)
{
    U4  u4_t_duty;
    U1  u1_t_isact;

    u4_t_duty = ((U4)u2_s_hdimowduty_owduty * (U4)HDIMOWDUTY_DUTYLSB) / (U4)HDIMOWDUTY_DIAGPERLSB;            /* PRQA S 3383 # over detection */
    if(u4_t_duty > (U4)HDIMOWDUTY_DUTYMAX){
        u4_t_duty = (U4)HDIMOWDUTY_DUTYMAX;
    }
    (*u2_ap_owduty) = u2_g_HdimmgrDutyExchgBaseToIfLSB((U2)u4_t_duty);

    u1_t_isact = (U1)FALSE;
    if(u2_s_hdimowduty_ow_act == (U2)HDIMOWDUTY_OW_ACTV){
        u1_t_isact = (U1)TRUE;
    }
    return(u1_t_isact);
}

/*===================================================================================================================================*/
/*  void    vd_g_HdimowdutyOwUnlock(const U4 u4_a_KEY)                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimowdutyOwUnlock(const U4 u4_a_KEY)
{
    u4_s_hdimowduty_ow_unlock = u4_a_KEY;
}

/*===================================================================================================================================*/
/*  void    vd_g_HdimowdutyOwAct(const U2 u2_a_REQ)                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimowdutyOwAct(const U2 u2_a_REQ)
{
    if(u4_s_hdimowduty_ow_unlock == (U4)HDIMOWDUTY_OW_UNLOCK){
        u2_s_hdimowduty_ow_act = (U2)HDIMOWDUTY_OW_ACTV;
        u2_s_hdimowduty_tocnt  = (U2)0;
        u2_s_hdimowduty_owduty = u2_a_REQ;
    }
    u4_s_hdimowduty_ow_unlock = (U4)0;
}

/*===================================================================================================================================*/
/*  void    vd_g_HdimowdutyOwDeAct(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimowdutyOwDeAct(void)
{
    u2_s_hdimowduty_ow_act    = (U2)HDIMOWDUTY_OW_INACTV;
    u2_s_hdimowduty_tocnt     = (U2)U2_MAX;
    u2_s_hdimowduty_owduty    = (U2)0;
    u4_s_hdimowduty_ow_unlock = (U4)0;
}

