/*===================================================================================================================================*/
/*  U1      u1_g_HdimstepCfgReadStep(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/* Arguments    -                                                                                                                    */
/* Return       -                                                                                                                    */
/*===================================================================================================================================*/
U1      u1_g_HdimstepCfgReadStep(void)
{
    U1      u1_t_step;

    u1_t_step = (U1)u4_g_McstBf((U1)MCST_BFI_HUDILL);

    return(u1_t_step);
}

/*===================================================================================================================================*/
/*  U1      u1_g_HdimstepCfgWriteStep(const U1 u1_a_STEP)                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/* Arguments    -                                                                                                                    */
/* Return       -                                                                                                                    */
/*===================================================================================================================================*/
U1      u1_g_HdimstepCfgWriteStep(const U1 u1_a_STEP)
{
    U1      u1_t_isok;

    vd_g_McstBfPut((U1)MCST_BFI_HUDILL, (U4)u1_a_STEP);
    u1_t_isok = (U1)TRUE;
    return(u1_t_isok);
}

