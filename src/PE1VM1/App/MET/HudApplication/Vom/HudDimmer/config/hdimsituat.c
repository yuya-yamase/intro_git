/*===================================================================================================================================*/
/*  Initialize                                                                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimsituatInit(void)
{
    u1_s_hdimsituat_mode = (U1)HDIMSITUAT_HEAD_OFF;
    u1_s_hdimsituat_sts = (U1)HDIMMGR_INSTS_UNDET;
}

/*===================================================================================================================================*/
/*  HudDimmer Situation Update                                                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimsituatUpdt(void)
{
    U1          u1_t_headison;
    U1          u1_t_msgsts;

    u1_t_msgsts = Com_GetIPDUStatus((PduIdType)MSG_BDB1S03_RXCH0);
    u1_t_msgsts &= (U1)(COM_TIMEOUT | COM_NO_RX);
    if(u1_t_msgsts == (U1)0){
        (void)Com_ReceiveSignal(ComConf_ComSignal_HEDL, &u1_t_headison);
        if(u1_t_headison == (U1)TRUE){
            u1_s_hdimsituat_mode = (U1)HDIMSITUAT_HEAD_ON;
        }
        else{
            u1_s_hdimsituat_mode = (U1)HDIMSITUAT_HEAD_OFF;
        }
        u1_s_hdimsituat_sts = (U1)HDIMMGR_INSTS_NML;
    }
    else if(u1_t_msgsts == (U1)COM_NO_RX){
        u1_s_hdimsituat_mode = (U1)HDIMSITUAT_HEAD_OFF;
    }
    else{
        /* sitiation status hold */
        u1_s_hdimsituat_sts = (U1)HDIMMGR_INSTS_ERR;
    }
}

/*===================================================================================================================================*/
/*  HudDimmer Situation Data                                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   * u4_a_situat : Situation Information                                                                                */
/*  Return      u1_s_hdimsituat_sts : Situation Status                                                                               */
/*===================================================================================================================================*/
U1    u1_g_HdimsituatGetSituatVal(U4 * u4_a_situat)
{
    (*u4_a_situat) = (U4)u1_s_hdimsituat_mode;

    return(u1_s_hdimsituat_sts);
}

