/*===================================================================================================================================*/
/*  Setting Output Duty                                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments : u2_ap_DUTY : Output Duty                                                                                             */
/*  Return:   -                                                                                                                      */
/*===================================================================================================================================*/
void vd_g_HdimmgrCfgSetOtptDuty(const U2 u2_a_DUTY)
{
#ifdef HDIMDTCTR_H
    U2  u2_t_duty;

    if(u2_a_DUTY >= (U2)HDIMMGR_IF_DUTYMAX){
        u2_t_duty = (U2)PWM_DRV_DUTY_MAX;
    }
    else{
        u2_t_duty = (U2)(((U4)u2_a_DUTY * (U4)PWM_DRV_DUTY_MAX) / (U4)HDIMMGR_IF_DUTYLSB);         /* PRQA S 3383 # over detection */
    }
    vd_g_BlledSetDuty(u2_t_duty);
#endif /* HDIMDTCTR_H */
}

/*===================================================================================================================================*/
/*  HudDimmer Input Information                                                                                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   : --> u1_a_ID   : HudDimmer Input Information ID                                                                     */
/*              : <-- *u4_ap_val: HudDimmer Input Value                                                                              */
/*  Return      :     u1_t_is_valid : HudDimmer Input Status(TRUE:valid,FALSE:invalid)                                               */
/*===================================================================================================================================*/
U1 u1_g_HdimmgrCfgGetInSig(const U1 u1_a_ID , U4 *u4_ap_val)
{
    U1  u1_t_is_valid;
    U1  u1_t_sts;

    u1_t_is_valid = (U1)FALSE;
    (*u4_ap_val) = (U4)0U;
    u1_t_sts = (U1)HDIMMGR_INSTS_UNDET;
    switch(u1_a_ID){
#ifdef HDIMADJ_H
        case (U1)HDIMMGR_INSIG_EXTRNLSTEP:
            u1_t_sts = u1_g_HdimstepGetExtrlStepVal(u4_ap_val);
            break;

        case (U1)HDIMMGR_INSIG_ILLMN:
            u1_t_sts = u1_g_HdimillmnGetIllmnVal(u4_ap_val);                    /* LSB : 0.01 lx */
            break;

        case (U1)HDIMMGR_INSIG_ILLMNSNSR:
            u1_t_sts = u1_g_HdimillmnGetIllmnSnsrVal(u4_ap_val);                /* LSB : 0.01 lx */
            break;

        case (U1)HDIMMGR_INSIG_CNLGHTSNSR:
            u1_t_sts = u1_g_HdimillmnGetCnlghtSnsrRawVal(u4_ap_val);            /* LSB : 0.01 lx */
            break;

        case (U1)HDIMMGR_INSIG_OTHFRILL:
            if (u1_CALIB_MCUID0583_ATSSAORNA != (U1)HDIMADJ_FRILL_NOTSUP) {
                u1_t_sts = u1_g_HdimadjFrillGetOthfrillRawVal(u4_ap_val);      /* LSB : 0.001 cd/m2 */
            }
            else {
                /* no processing */
            }
            break;
#endif /* HDIMADJ_H */

#ifdef HDIMDTCTR_H
        case (U1)HDIMMGR_INSIG_LMTDUTY:
            u1_t_sts = u1_g_HdimlmtdutyGetLmtDutyVal(u4_ap_val);                /* LSB : see:HDIMMGR_IF_DUTYLSB */
            break;
#endif /* HDIMDTCTR_H */

        default:
            /* no processing */
            break;
    }

    if(u1_t_sts == (U1)HDIMMGR_INSTS_NML){
        u1_t_is_valid = (U1)TRUE;
    }
    return(u1_t_is_valid);
}

/*===================================================================================================================================*/
/*  HudDimmer Sleep OK Judgment                                                                                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments : u1_a_HDIMSTS                                                                                                         */
/*  Return:   : TRUE:Sleep OK,FALSE:Sleep NG                                                                                         */
/*===================================================================================================================================*/
U1 u1_g_HdimmgrCfgIsSlpOK(const U1 u1_a_HDIMSTS)
{
#ifdef HDIMDTCTR_H
    static const U1 u1_sp_HDIMMGRCFG_SLPJDG[HDIMDTCTR_DIMSTS_NUM] = {
        (U1)TRUE,       /* HDIMDTCTR_DIMSTS_PWOFF      */
        (U1)FALSE,      /* HDIMDTCTR_DIMSTS_PWON       */
        (U1)FALSE,      /* HDIMDTCTR_DIMSTS_FADEIN     */
        (U1)FALSE,      /* HDIMDTCTR_DIMSTS_FADEOUT    */
        (U1)FALSE       /* HDIMDTCTR_DIMSTS_BLANK      */
    };

    U1              u1_t_is_slpok;

    u1_t_is_slpok = (U1)TRUE;
    if(u1_a_HDIMSTS < (U1)HDIMDTCTR_DIMSTS_NUM){
        u1_t_is_slpok = u1_sp_HDIMMGRCFG_SLPJDG[u1_a_HDIMSTS];
    }

    return(u1_t_is_slpok);
#else
    return((U1)TRUE);
#endif /* HDIMDTCTR_H */
}

/*===================================================================================================================================*/
/*  HudDimmer Backlight ON/OFF                                                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments : -                                                                                                                    */
/*  Return:   : (TRUE:Backlight ON / FALSE:Backlight OFF)                                                                            */
/*===================================================================================================================================*/
U1 u1_g_HdimmgrCfgIsBacklighOn(void)
{
#ifdef HDIMDTCTR_H
    U1 u1_t_ret;
    U1 u1_t_dvcsts;
    U1 u1_t_dimaplreq;

    u1_t_ret = (U1)FALSE;
    u1_t_dvcsts = u1_g_BlledIsDeviceSts();
    u1_t_dimaplreq = u1_g_HdimdtctrCfgIsBlOnReq();

    if((u1_t_dimaplreq == (U1)TRUE) && (u1_t_dvcsts == (U1)BLLED_DEVSTS_RUN)){
       u1_t_ret = (U1)TRUE;
    }
    return(u1_t_ret);
#else
    return((U1)TRUE);
#endif /* HDIMDTCTR_H */
}

