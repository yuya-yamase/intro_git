/*===================================================================================================================================*/
/*  U1      u1_g_HdimmgrIfGetOwduty(U2 * u2_ap_owduty)                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
U1      u1_g_HdimmgrIfGetOwduty(U2 * u2_ap_owduty)
{
    U2      u2_t_owduty;
    U1      u1_t_isactv;

    u2_t_owduty = (U2)0;
    u1_t_isactv = u1_g_HdimowdutyGetVal(&u2_t_owduty);                  /* LSB : see:HDIMMGR_IF_DUTYLSB */
    (*u2_ap_owduty) = u2_t_owduty;                                      /* LSB : see:HDIMMGR_IF_DUTYLSB */
    return(u1_t_isactv);
}
/*===================================================================================================================================*/
/*  U1      u1_g_HdimmgrIfGetIllStepVal(void)                                                                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
U1      u1_g_HdimmgrIfGetIllStepVal(void)
{
    static const U1    u1_sp_HDIMSTEP_CNVRT[(U1)HDIMSTEP_STEP_MAX + (U1)1]={
        (U1)U1_MAX,  /* Don't change!!! */
        (U1) 0,                                                         /* lvl 01 */
        (U1) 1,                                                         /* lvl 02 */
        (U1) 2,                                                         /* lvl 03 */
        (U1) 3,                                                         /* lvl 04 */
        (U1) 4,                                                         /* lvl 05 */ 
        (U1) 5,                                                         /* lvl 06 */
        (U1) 6,                                                         /* lvl 07 */
        (U1) 7,                                                         /* lvl 08 */
        (U1) 8,                                                         /* lvl 09 */
        (U1) 9,                                                         /* lvl 10 */
        (U1)10                                                          /* lvl 11 */
    };

    U4      u4_t_step;
    U1      u1_t_step;

    u4_t_step = (U4)0U;

    (void)u1_g_HdimstepGetExtrlStepVal(&u4_t_step);
    u1_t_step = u1_sp_HDIMSTEP_CNVRT[(U1)HDIMSTEP_STEP_DEF];

    if(((U4)HDIMSTEP_STEP_MIN <= u4_t_step            ) &&
       (u4_t_step             <= (U4)HDIMSTEP_STEP_MAX)){
        u1_t_step = u1_sp_HDIMSTEP_CNVRT[u4_t_step];
    }

    return(u1_t_step);
}

