void    vd_g_HdimmgrIfSetOutduty(const U2 u2_a_OUTDUTY)
{
    u2_s_hdimif_outduty = u2_a_OUTDUTY;
    u2_s_hdimif_timout = (U2)0;
}

/*===================================================================================================================================*/
/*  void    vd_g_HdimmgrIfSetIllStepVal(const U1 u1_a_STEP)                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimmgrIfSetIllStepVal(const U1 u1_a_STEP)
{
    U1      u1_t_updtdstep;

    u1_t_updtdstep = u1_a_STEP;
    if(u1_t_updtdstep <= (U1)HDIM_IF_STEP_IND_MAX){
        u1_t_updtdstep += (U1)HDIM_IF_STEP_IND_OFFSET;
        u1_s_hdimif_step_ind = u1_t_updtdstep;
    }
    else{
        u1_s_hdimif_step_ind = (U1)U1_MAX;
    }

    u2_s_hdimif_timout = (U2)0;
}

