static U2   u2_s_hdimif_timout;
static U2   u2_s_hdimif_outduty;
static U1   u1_s_hdimif_step_ind;
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  Initialize                                                                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimmgrIfInit(void)
{
    u2_s_hdimif_timout    = (U2)HDIM_IFTIMEOUT_MAX;
    u2_s_hdimif_outduty   = (U2)0;
    u1_s_hdimif_step_ind  = (U1)HDIM_IF_STEP_IND_UNDEF;
}

/*===================================================================================================================================*/
/*  Dimmer Interface Update                                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_HdimmgrIfUpdt(void)
{
    static const U2 u2_s_HDIM_IF_TIMOUT = (U2)50U;       /* U2_MAX: hold, 1sec: LSB:20ms */

    if(u2_s_hdimif_timout < (U2)HDIM_IFTIMEOUT_MAX){
        u2_s_hdimif_timout += (U2)1;
    }
    else{
        u2_s_hdimif_timout = (U2)HDIM_IFTIMEOUT_MAX;
    }
    if(u2_s_hdimif_timout >= u2_s_HDIM_IF_TIMOUT){
        vd_g_HdimmgrIfInit();
    }
}

