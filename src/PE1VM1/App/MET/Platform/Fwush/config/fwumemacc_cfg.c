/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Segment Definitions                                                                                                              */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  LB Configuration Table                                                                                                           */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static const ST_FWUMEMACC_LB_INFO st_s_lb_info_table[] = {
    {
        (U1)FWUSH_VERI_LB1,                                               /* lb_id */
        (U4)0x00020000U,                                                  /* start_address */
        (U4)0x007A0000U                                                   /* length: 7808KB */
    },
    {
        (U1)FWUSH_VERI_LB2,                                               /* lb_id */
        (U4)0x0001c000U,                                                  /* start_address */
        (U4)0x00004000U                                                   /* length: 16KB */
    }
};

#define FWUMEMACC_CFG_LB_TABLE_COUNT  (sizeof(st_s_lb_info_table) / sizeof(st_s_lb_info_table[0]))

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/

/*===================================================================================================================================*/
/*  U1 u1_g_FwuMemAccCfgGetLbInfo(U1 u1_a_lb_id, U4* u4p_a_start_address, U4* u4p_a_length)                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      U1 u1_a_lb_id           : LB ID                                                                                  */
/*                  U4* u4p_a_start_address  : Start address output pointer                                                          */
/*                  U4* u4p_a_length         : Length output pointer                                                                  */
/*  Return:         U1 : FWUMEMACC_CFG_RET_OK / FWUMEMACC_CFG_RET_NG                                                                 */
/*===================================================================================================================================*/
U1 u1_g_FwuMemAccCfgGetLbInfo(U1 u1_a_lb_id, U4* u4p_a_start_address, U4* u4p_a_length)
{
    U1 u1_t_ret;
    U2 u2_t_idx;

    u1_t_ret = (U1)FWUMEMACC_CFG_RET_NG;

    for (u2_t_idx = (U2)0U; u2_t_idx < (U2)FWUMEMACC_CFG_LB_TABLE_COUNT; u2_t_idx++) {
        if (st_s_lb_info_table[u2_t_idx].u1_lb_id == u1_a_lb_id) {
            *u4p_a_start_address = st_s_lb_info_table[u2_t_idx].u4_start_address;
            *u4p_a_length = st_s_lb_info_table[u2_t_idx].u4_length;
            u1_t_ret = (U1)FWUMEMACC_CFG_RET_OK;
            break;
        }
    }

    return(u1_t_ret);
}

