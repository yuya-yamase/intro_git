/*  Periodic processing                                                                                                              */
/*  Scope : Public                                                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments: -                                                                                                                     */
/*  Return:    -                                                                                                                     */
/*===================================================================================================================================*/
void vd_g_GatewayCxpiMainTask(void)
{
    U1    u1_t_pwrsts;        /*  Power Status              */
    U1    u1_t_baon;          /*  BA ON Status              */
    U1    u1_t_accon;         /*  ACC ON Status             */
    U1    u1_t_igron;         /*  IGR ON Status             */
    U1    u1_t_igpon;         /*  IGP ON Status             */
    U4    u4_t_lpcnt;         /*  Loop Counter              */

    u1_t_baon  = u1_g_VehopemdBaOn();
    u1_t_accon = u1_g_VehopemdAccOn();
    u1_t_igron = u1_g_VehopemdIgnOn();
    u1_t_igpon = u1_g_VehopemdIgnpOn();

    u1_t_pwrsts  = (U1)GWCXCFG_PWRSTS_B_BITPOS;
    u1_t_pwrsts |= (U1)(u1_t_baon << GWCX_PWRSTS_BA_BITPOS);
    u1_t_pwrsts |= (U1)(u1_t_accon << GWCX_PWRSTS_ACC_BITPOS);
    u1_t_pwrsts |= (U1)(u1_t_igron << GWCX_PWRSTS_IGR_BITPOS);
    u1_t_pwrsts |= (U1)(u1_t_igpon << GWCX_PWRSTS_IGP_BITPOS);

    /* ErrorCheck Task */
    for(u4_t_lpcnt = (U4)0; u4_t_lpcnt < (U4)u1_g_GWCX_ERRCHKTASK_NUM; u4_t_lpcnt++){
        st_gp_GWCX_ERRCHKIF[u4_t_lpcnt].fp_vd_errchk();
    }

    /* MainTask */
    for(u4_t_lpcnt = (U4)0; u4_t_lpcnt < (U4)u1_g_GWCX_WRTMSG_NUM; u4_t_lpcnt++){
        st_gp_GWCX_MSGIF[u4_t_lpcnt].fp_vd_send(u1_t_pwrsts);
    }
}

