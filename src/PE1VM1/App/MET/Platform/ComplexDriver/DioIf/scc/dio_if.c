/*  void    vd_g_DioIfOwUnlock(const U4 u4_a_KEY)                                                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_DioIfOwUnlock(const U4 u4_a_KEY)
{
    u4_s_dio_if_ow_unlock = u4_a_KEY;
}
/*===================================================================================================================================*/
/*  void    vd_g_DioIfOwAct(const U1 u1_a_GR, const U2 u2_a_OW_CH, const U2 u2_a_OW_ACT)                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_DioIfOwAct(const U1 u1_a_GR, const U2 u2_a_OW_CH, const U2 u2_a_OW_ACT)
{
    if((u4_s_dio_if_ow_unlock == (U4)DIO_IF_OW_UNLOCK) &&
       (u1_a_GR               <  u1_g_DIO_IF_NUM_GR  )){

        st_gp_dio_if_gr[u1_a_GR].u2_ow_tocnt = (U2)U2_MAX;
        st_gp_dio_if_gr[u1_a_GR].u2_ow_ch    = u2_a_OW_CH;
        st_gp_dio_if_gr[u1_a_GR].u2_ow_act   = u2_a_OW_ACT;
    }
    u4_s_dio_if_ow_unlock = (U4)0x00000000U;
}
/*===================================================================================================================================*/
/*  void    vd_g_DioIfOwDeAct(const U1 u1_a_GR)                                                                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_DioIfOwDeAct(const U1 u1_a_GR)
{

    if(u1_a_GR < u1_g_DIO_IF_NUM_GR){

        st_gp_dio_if_gr[u1_a_GR].u2_ow_tocnt  = (U2)DIO_IF_OW_TOC_MAX;
        st_gp_dio_if_gr[u1_a_GR].u2_ow_ch     = (U2)0x0000U;
        st_gp_dio_if_gr[u1_a_GR].u2_ow_act    = (U2)0x0000U;

    }
    u4_s_dio_if_ow_unlock = (U4)0x00000000U;
}
