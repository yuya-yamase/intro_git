/*===================================================================================================================================*/
/*  void    vd_g_DioIfInit(void)                                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_DioIfInit(void)
{
    U4             u4_t_lpcnt;

    u4_s_dio_if_ow_unlock = (U4)0x00000000U;
    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_DIO_IF_NUM_GR; u4_t_lpcnt++){
        st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt = (U2)DIO_IF_OW_TOC_MAX;
        st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_ch    = (U2)0x0000U;
        st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_act   = (U2)0x0000U;
        st_gp_dio_if_gr[u4_t_lpcnt].u2_ch_act   = (U2)0x0000U;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_DioIfMainIn(void)                                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_DioIfMainIn(void)
{
    U4             u4_t_lpcnt;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_DIO_IF_GR_DO_MIN; u4_t_lpcnt++){

        if(st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt > (U2)DIO_IF_OW_TOC_MAX){
            st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt = (U2)0U;
        }
        else if(st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt < (U2)DIO_IF_OW_TOC_MAX){
            st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt++;
        }
        else{
            /* Do nothing */
        }

        if(st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt >= u2_g_DIO_IF_OW_TOUT){
            st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_ch  = (U2)0x0000U;
            st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_act = (U2)0x0000U;
        }

        st_gp_dio_if_gr[u4_t_lpcnt].u2_ch_act = (U2)0x0000U;
        if(st_gp_DIO_IF_RW_CFG[u4_t_lpcnt].fp_u2_REA != vdp_PTR_NA){
            st_gp_dio_if_gr[u4_t_lpcnt].u2_ch_act = (*st_gp_DIO_IF_RW_CFG[u4_t_lpcnt].fp_u2_REA)();
        }
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_DioIfMainOut(void)                                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_DioIfMainOut(void)
{
    U4             u4_t_lpcnt;

    U2             u2_t_ch_act;
    U2             u2_t_ow_ch;
    U2             u2_t_ow_act;

    for(u4_t_lpcnt = (U4)u1_g_DIO_IF_GR_DO_MIN; u4_t_lpcnt < (U4)u1_g_DIO_IF_NUM_GR; u4_t_lpcnt++){

        if(st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt > (U2)DIO_IF_OW_TOC_MAX){
            st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt = (U2)0U;
        }
        else if(st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt < (U2)DIO_IF_OW_TOC_MAX){
            st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt++;
        }
        else{
            /* Do nothing */
        }

        u2_t_ch_act = (U2)0x0000U;
        if(st_gp_DIO_IF_RW_CFG[u4_t_lpcnt].fp_u2_REA != vdp_PTR_NA){
            u2_t_ch_act = (*st_gp_DIO_IF_RW_CFG[u4_t_lpcnt].fp_u2_REA)();
        }
        st_gp_dio_if_gr[u4_t_lpcnt].u2_ch_act = u2_t_ch_act;
        if(st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_tocnt >= u2_g_DIO_IF_OW_TOUT){

            st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_ch  = (U2)0x0000U;
            st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_act = (U2)0x0000U;

            u2_t_ow_act  = u2_t_ch_act;
        }
        else{

            u2_t_ow_ch   = st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_ch;
            u2_t_ow_act  = st_gp_dio_if_gr[u4_t_lpcnt].u2_ow_act & u2_t_ow_ch;
            u2_t_ow_act |= (u2_t_ch_act & ((U2)U2_MAX ^ u2_t_ow_ch));
        }

        if(st_gp_DIO_IF_RW_CFG[u4_t_lpcnt].fp_vd_WRI != vdp_PTR_NA){
            (*st_gp_DIO_IF_RW_CFG[u4_t_lpcnt].fp_vd_WRI)(u2_t_ow_act);
        }
    }
}
/*===================================================================================================================================*/
/*  U1      u1_g_DioIfChAct(const U2 u2_a_CH, const U1 u1_a_OW_EN)                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_DioIfChAct(const U2 u2_a_CH, const U1 u1_a_OW_EN)
{
    U2             u2_t_gr;
    U2             u2_t_bn;
    U1             u1_t_ch_act;

    u2_t_gr = u2_DIO_IF_CH_TO_GR(u2_a_CH);
    if(u2_t_gr < (U2)u1_g_DIO_IF_NUM_GR){

        u2_t_bn     = u2_DIO_IF_CH_TO_BN(u2_a_CH);
        u1_t_ch_act = (U1)(st_gp_dio_if_gr[u2_t_gr].u2_ow_ch >> u2_t_bn) & u1_a_OW_EN;
        if(u1_t_ch_act == (U1)TRUE){
            u1_t_ch_act = (U1)(st_gp_dio_if_gr[u2_t_gr].u2_ow_act >> u2_t_bn) & (U1)TRUE;
        }
        else{
            u1_t_ch_act = (U1)(st_gp_dio_if_gr[u2_t_gr].u2_ch_act >> u2_t_bn) & (U1)TRUE;
        }
    }
    else{
        u1_t_ch_act = (U1)FALSE;
    }

    return(u1_t_ch_act);
}
/*===================================================================================================================================*/
/*  U2      u2_g_DioIfGrAct(const U1 u1_a_GR, const U1 u1_a_OW_EN)                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U2      u2_g_DioIfGrAct(const U1 u1_a_GR, const U1 u1_a_OW_EN)
{
    U2             u2_t_ch_act;

    if(u1_a_GR >= u1_g_DIO_IF_NUM_GR){
        u2_t_ch_act = (U2)0x00U;
    }
    else if(u1_a_OW_EN != (U1)TRUE){
        u2_t_ch_act  = st_gp_dio_if_gr[u1_a_GR].u2_ch_act;
    }
    else{
        u2_t_ch_act  = st_gp_dio_if_gr[u1_a_GR].u2_ch_act;
        u2_t_ch_act &= ((U2)U2_MAX ^ st_gp_dio_if_gr[u1_a_GR].u2_ow_ch);
        u2_t_ch_act |= (st_gp_dio_if_gr[u1_a_GR].u2_ow_act & st_gp_dio_if_gr[u1_a_GR].u2_ow_ch);
    }

    return(u2_t_ch_act);
}
