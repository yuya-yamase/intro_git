/*  void    vd_g_ESInspectBonInit(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_ESInspectBonInit(void)
{

    u2_s_esi_md_tocnt  = (U2)U2_MAX;
    u1_s_esi_md_ctrl   = (U1)ES_INSPECT_MD_CHK;
    u1_s_esi_md_bfield = (U1)0U;

    u1_s_esi_md_req_rx = (U1)0U;

    vd_g_ESInspectCfgInit(u1_s_esi_md_ctrl, (U1)TRUE);
}
/*===================================================================================================================================*/
/*  void    vd_g_ESInspectRstwkInit(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
