/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_ESInspectRstwkInit(void)
{

    u2_s_esi_md_tocnt  = (U2)ES_INSPECT_TOC_MAX;
    u1_s_esi_md_ctrl   = (U1)ES_INSPECT_MD_INA;
    u1_s_esi_md_req_rx = (U1)0U;
    u1_s_esi_md_bfield = (U1)0U;

    vd_g_ESInspectCfgInit(u1_s_esi_md_ctrl, (U1)FALSE);
}
/*===================================================================================================================================*/
/*  void    vd_g_ESInspectRunInit(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_ESInspectRunInit(void)
{
    U1                 u1_t_nuo_di;

    u1_t_nuo_di = u1_g_ESInspectCfgNuoDisabled() & (U1)ES_INSPECT_MDBF_NUO_DI;
    u1_s_esi_md_bfield |= u1_t_nuo_di;
}
/*===================================================================================================================================*/
/*  void    vd_g_ESInspectMainTask(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_ESInspectMainTask(void)
{
    U1                 u1_t_last;
    U1                 u1_t_nuo_di;

    u1_t_last = u1_s_esi_md_ctrl;
    if(u1_t_last < (U1)ES_INSPECT_NUM_MD){

        vd_s_ESInsectMdRun(u1_t_last);
    }
    else{

        if(u1_s_esi_md_bfield != (U1)0U){
            u1_t_nuo_di         = u1_g_ESInspectCfgNuoDisabled() & (U1)ES_INSPECT_MDBF_NUO_DI;
            u1_s_esi_md_bfield &= u1_t_nuo_di;
        }

        vd_g_ESInspectCfgDeInit();
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_ESInspectReqRx(const U1 u1_a_REQ, const U1 u1_a_MD_CHK)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_ESInspectReqRx(const U1 u1_a_REQ, const U1 u1_a_MD_CHK)
{
    if((u1_s_esi_md_ctrl   >= u1_a_MD_CHK          ) &&
       (u1_s_esi_md_ctrl   < (U1)ES_INSPECT_MD_FIN ) &&
       (u1_s_esi_md_req_rx < (U1)ES_INSPECT_REQ_FIN) &&
       (u1_a_REQ           > u1_s_esi_md_req_rx    )){

        u1_s_esi_md_req_rx = u1_a_REQ;
    }
}
/*===================================================================================================================================*/
