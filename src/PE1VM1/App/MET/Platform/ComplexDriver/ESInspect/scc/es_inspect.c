/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_ESInspectMdchk(void)
{
    return(u1_s_esi_md_ctrl);
}
/*===================================================================================================================================*/
/*  U1      u1_g_ESInspectMdBfield(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_ESInspectMdBfield(void)
{
    return(u1_s_esi_md_bfield & ((U1)ES_INSPECT_MDBF_NUO_DI | (U1)ES_INSPECT_MDBF_SI_ACT));
}
/*===================================================================================================================================*/
/*  static void    vd_s_ESInsectMdRun(const U1 u1_a_LAST)                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void    vd_s_ESInsectMdRun(const U1 u1_a_LAST)
{
    static const U1    u1_sp_ESI_MC_NEXT[] = {
        (U1)ESI_MC_NEXT_CHK,    (U1)ESI_MC_NEXT_RUN,    (U1)ESI_MC_NEXT_FIN,
        (U1)ESI_MC_NEXT_TO_RUN, (U1)ESI_MC_NEXT_TO_RUN, (U1)ESI_MC_NEXT_FIN,
        (U1)ESI_MC_NEXT_TO_INA, (U1)ESI_MC_NEXT_TO_FIN, (U1)ESI_MC_NEXT_TO_INA
    };

    U1                 u1_t_nuo_di;
    U1                 u1_t_eas_chk;
    U1                 u1_t_next;
    U1                 u1_t_toci;

    if(u2_s_esi_md_tocnt >= (U2)U2_MAX){
        u2_s_esi_md_tocnt = (U2)0U;
    }
    else if(u2_s_esi_md_tocnt >= (U2)ES_INSPECT_TOC_MAX){
        u2_s_esi_md_tocnt = (U2)ES_INSPECT_TOC_MAX;
    }
    else{
        u2_s_esi_md_tocnt++;
    }

    u1_t_nuo_di = u1_s_esi_md_bfield & (U1)ES_INSPECT_MDBF_NUO_DI;
    vd_g_ESInsepctCfgMainStart(u1_a_LAST, u1_t_nuo_di, u2_s_esi_md_tocnt);

    u1_t_eas_chk = u1_s_esi_md_req_rx & (U1)ES_INSPECT_REQ_MASK;
    u1_s_esi_md_req_rx = (U1)0U;

    if(u2_s_esi_md_tocnt >= u2_gp_ESI_TOUT_BY_MD[u1_a_LAST]){
        u1_t_eas_chk = (U1)ES_INSPECT_REQ_FIN;
    }

    u1_t_next  = (u1_t_eas_chk * (U1)ES_INSPECT_NUM_MD) + u1_a_LAST;
    u1_t_next  = u1_sp_ESI_MC_NEXT[u1_t_next];
    u1_t_toci  = u1_t_next & (U1)ESI_MC_NEXT_BIT_TOC;
    u1_t_next &= (U1)ESI_MC_NEXT_BIT_MD;
    if(u1_t_toci != (U1)0U){
        u2_s_esi_md_tocnt = (U2)0U;
    }

    if(u1_t_next < (U1)ES_INSPECT_NUM_MD){
        u1_s_esi_md_ctrl    = u1_t_next;
        if((u1_a_LAST == (U1)ESI_MC_NEXT_CHK) && (u1_t_next == (U1)ESI_MC_NEXT_RUN)){
            u1_s_esi_md_bfield |= (U1)ES_INSPECT_MDBF_SI_ACT;
            vd_g_ESInspectCfgInit(u1_s_esi_md_ctrl, (U1)FALSE);
        }
        vd_g_ESInsepctCfgMainFinish(u1_t_next, u1_t_nuo_di, u2_s_esi_md_tocnt);
    }
    else{
        u1_s_esi_md_ctrl   = (U1)ES_INSPECT_MD_INA; 
        u1_s_esi_md_bfield = u1_g_ESInspectCfgNuoDisabled() & (U1)ES_INSPECT_MDBF_NUO_DI;
        vd_g_ESInspectCfgDeInit();
    }
}
