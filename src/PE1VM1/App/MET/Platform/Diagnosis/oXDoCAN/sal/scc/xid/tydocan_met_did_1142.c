/*  U1      u1_g_oXDoCANRebyId_1142(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1142(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1  u1_t_sts;
    U4  u4_t_cnlill_0p01;


    u4_t_cnlill_0p01 = (U4)0U;
    u1_t_sts         = u1_g_HdimmgrGetInSig((U1)HDIMMGR_INSIG_CNLGHTSNSR, &u4_t_cnlill_0p01);

    if(u1_t_sts == (U1)TRUE){
        u4_t_cnlill_0p01 = u4_t_cnlill_0p01 / (U4)TYDC_CNL_ILL_0P01_TO_1;
        if(u4_t_cnlill_0p01 >= (U4)TYDC_CNL_ILL_MAX){
            u4_t_cnlill_0p01 = (U4)TYDC_CNL_ILL_MAX;
        }
    }
    else{
        u4_t_cnlill_0p01 = (U4)TYDC_CNL_ILL_INV;
    }

    u1_ap_ans[TYDC_CNL_ILL_ANS_04] = (U1)((u4_t_cnlill_0p01 >> 8) & (U4)0xFFU);
    u1_ap_ans[TYDC_CNL_ILL_ANS_05] = (U1)((u4_t_cnlill_0p01     ) & (U4)0xFFU);

    return((U1)OXDC_SAL_PROC_FIN);
}
