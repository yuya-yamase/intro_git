/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRebyId_1042(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1042(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U2              u2_t_rpm;
    U1              u1_t_sts_chk;

    u2_t_rpm     = (U2)0U;
    u1_t_sts_chk = u1_g_EngspdRpmInst(&u2_t_rpm);

    if(u1_t_sts_chk != (U1)ENGSPD_STSBIT_VALID){
        u1_ap_ans[0] = (U1)TYDC_ENGSPD_50_RPM_UNK;
    }
    else if(u2_t_rpm >= (U2)TYDC_ENGSPD_1_RPM_MAX){
        u1_ap_ans[0] = (U1)TYDC_ENGSPD_50_RPM_MAX;
    }
    else{
        u1_ap_ans[0] = (U1)(u2_t_rpm / (U2)TYDC_ENGSPD_50_RPM);
    }

    return((U1)OXDC_SAL_PROC_FIN);
}
