/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1003(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1  u1_t_sts;

    u1_ap_ans[0] = (U1)0x80U;
    u1_t_sts = u1_g_IoHwDifltSwitch((U1)IOHW_DISGNL_HUD_MAIN_SW_IN);
    if(u1_t_sts == (U1)TYDC_HUDSW_STS_UNK){
        u1_t_sts = (U1)0U;
    }
    u1_ap_ans[1] = u1_t_sts;

    return((U1)OXDC_SAL_PROC_FIN);
}
