/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANHudCaRead(U1 * u1_ap_ans_tx, const U1 u1_a_DID)                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANHudCaRead(U1 * u1_ap_ans_tx, const U1 u1_a_DID)
{
    U1                        u1_t_proc;

    if(u1_a_DID < (U1)TYDC_HUD_CA_NUM_DID){
        (*u1_ap_ans_tx) = (U1)s1_g_HudCaLastByDs22(u1_sp_TYDC_HUD_CA_PRM[u1_a_DID]);
        u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
    }

    return(u1_t_proc);
}
/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANHudCaWrite(const U1 * u1_ap_REQ, const U2 u2_a_ELPSD, const U1 u1_a_DID)                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANHudCaWrite(const U1 * u1_ap_REQ, const U2 u2_a_ELPSD, const U1 u1_a_DID)
{
    static const U2           u2_s_TYDC_HUD_CA_SYN_TOUT = (U2)5000U / (U2)OXDC_MAIN_TICK;

    U1                        u1_t_syn;
    U1                        u1_t_proc;

    if(u1_a_DID >= (U1)TYDC_HUD_CA_NUM_DID){
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
    }
    else if(u2_a_ELPSD == (U2)0U){

        u1_t_syn = u1_g_HudCaNextByDs2E(u1_sp_TYDC_HUD_CA_PRM[u1_a_DID], (S1)(*u1_ap_REQ));
        if(u1_t_syn == (U1)HUD_CA_SYN_REQ){
            u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
        }
        else{
            u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
        }
    }
    else if(u2_a_ELPSD >= u2_s_TYDC_HUD_CA_SYN_TOUT){
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_72;
    }
    else{

        u1_t_syn = u1_g_HudCaPrmSynchk(u1_sp_TYDC_HUD_CA_PRM[u1_a_DID]);
        if(u1_t_syn == (U1)HUD_CA_SYN_SUC){
            u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
        }
        else if(u1_t_syn >= (U1)HUD_CA_SYN_FAI){
            u1_t_proc = (U1)OXDC_SAL_PROC_NR_72;
        }
        else{
            u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
        }
    }

    return(u1_t_proc);
}
