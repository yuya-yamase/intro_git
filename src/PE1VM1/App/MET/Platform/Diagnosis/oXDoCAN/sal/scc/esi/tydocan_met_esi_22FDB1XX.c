/*  U1      u1_g_TyDoCANEsiTRx_22FDB0XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANEsiTRx_22FDB1XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)
{
    static const U2 u2_s_TYDC_ESI_DLPCHK_TOUT = (U2)5000 / (U2)10U;
    U1  u1_t_rslt;
    U1  u1_t_ret;

    if(u2_a_ELPSD == (U2)0U){
        vd_g_DspbldEsiDlpchkStart();
        u1_t_ret = (U1)OXDC_SAL_PROC_RUN;
    }
    else if(u2_a_ELPSD >= u2_s_TYDC_ESI_DLPCHK_TOUT){
        u1_ap_ans_tx[3] = (U1)0xFAU;
        u1_t_ret        = (U1)OXDC_SAL_PROC_FIN;
    }
    else{
        u1_t_rslt = u1_g_DspbldEsiDlpchkResult(u1_ap_ans_tx);
        if(u1_t_rslt == (U1)DSPBLD_ESI_PROC_FIN){
            u1_ap_ans_tx[3]     = (U1)0xF5U;
            u1_t_ret            = (U1)OXDC_SAL_PROC_FIN;

        }else if(u1_t_rslt == (U1)DSPBLD_ESI_PROC_NRC){
            u1_ap_ans_tx[3]     = (U1)0xFAU;
            u1_t_ret            = (U1)OXDC_SAL_PROC_FIN;

        }else{
            u1_t_ret            = (U1)OXDC_SAL_PROC_RUN;
        }
    }

    u1_ap_ans_tx[0]         = (U1)0x62U;
    u1_ap_ans_tx[1]         = (U1)0xFDU;
    u1_ap_ans_tx[2]         = (U1)0xB1U;

    return(u1_t_ret);
}
