/*  U1      u1_g_oXDoCANRebyId_11A3(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_11A3(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1  u1_t_turn_l;
    U1  u1_t_turn_r;

    /* Support Info */
    u1_ap_ans[0] = (U1)0xC0U;

    /* TURN_L_IN */
    u1_t_turn_l = u1_g_IoHwDifltSwitch((U1)IOHW_DISGNL_TURN_L_IN);
    if(u1_t_turn_l == (U1)TYDC_TURN_L_IN_STS_UNK){
        u1_t_turn_l = (U1)0U;
    }
    u1_ap_ans[1]  = (U1)( (U4)u1_t_turn_l << (U4)TYDC_TURN_L_SFT );

    /* TURN_R_IN */
    u1_t_turn_r = u1_g_IoHwDifltSwitch((U1)IOHW_DISGNL_TURN_R_IN);
    if(u1_t_turn_r == (U1)TYDC_TURN_R_IN_STS_UNK){
        u1_t_turn_r = (U1)0U;
    }
    u1_ap_ans[1] |= (U1)( (U4)TYDC_BIT6_MSK & (U4)((U4)u1_t_turn_r << (U4)TYDC_TURN_R_SFT) );

    return((U1)OXDC_SAL_PROC_FIN);
}
