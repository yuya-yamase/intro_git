/*  U1      u1_g_oXDoCANRebyId_1141(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1141(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U2             u2_t_cel;
    U2             u2_t_fah;
    U1             u1_t_sts_chk;

    u2_t_cel      = (U2)0U;
    u2_t_fah      = (U2)0U;
    u1_t_sts_chk  = u1_g_AmbtmpCelInst(&u2_t_cel);
    u1_t_sts_chk |= u1_g_AmbtmpFahInst(&u2_t_fah);

    if(u1_t_sts_chk != (U1)AMBTMP_STS_VALID){
        u1_ap_ans[TYDC_AMBTMP_ANS_CEL] = (U1)TYDC_AMBTMP_DEG_UNK;
        u1_ap_ans[TYDC_AMBTMP_ANS_FAH] = (U1)TYDC_AMBTMP_DEG_UNK;
    }
    else{
        u2_t_cel /= (U2)TYDC_AMBTMP_0P5_CEL;
        if(u2_t_cel >= (U2)TYDC_AMBTMP_CEL_MAX){
            u1_ap_ans[TYDC_AMBTMP_ANS_CEL] = (U1)TYDC_AMBTMP_CEL_MAX;
        }
        else{
            u1_ap_ans[TYDC_AMBTMP_ANS_CEL] = (U1)u2_t_cel;
        }
        u2_t_fah /= (U2)TYDC_AMBTMP_1_FAH;
        if(u2_t_fah >= (U2)TYDC_AMBTMP_FAH_MAX){
            u1_ap_ans[TYDC_AMBTMP_ANS_FAH] = (U1)TYDC_AMBTMP_FAH_MAX;
        }
        else{
            u1_ap_ans[TYDC_AMBTMP_ANS_FAH] = (U1)u2_t_fah;
        }
    }

    return((U1)OXDC_SAL_PROC_FIN);
}
