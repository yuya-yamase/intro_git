#define TYDC_AMBTMP_1_FAH                        (100U)

#define TYDC_AMBTMP_CEL_MAX                      (254U)
#define TYDC_AMBTMP_FAH_MAX                      (254U)

#define TYDC_AMBTMP_ANS_CEL                      (0U)
#define TYDC_AMBTMP_ANS_FAH                      (1U)

