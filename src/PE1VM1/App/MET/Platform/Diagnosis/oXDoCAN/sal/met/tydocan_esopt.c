/*  U1      u1_g_TyDoCANEsOptAnsTx(U1 * u1_ap_ans_tx, const U2 u2_a_TX)                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANEsOptAnsTx(U1 * u1_ap_ans_tx, const U2 u2_a_TX)
{
    U1                       u1_t_proc;

    if(u2_a_TX < u2_g_TYDC_ESOPT_NUM_TX){
        u1_t_proc = u1_g_TyDoCANBiTx(u1_ap_ans_tx, &st_gp_TYDC_ESOPT_TX[u2_a_TX]);
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
    }

    return(u1_t_proc);
}
