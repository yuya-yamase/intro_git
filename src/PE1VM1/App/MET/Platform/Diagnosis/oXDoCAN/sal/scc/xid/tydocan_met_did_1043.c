/*  U1      u1_g_oXDoCANRebyId_1043(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1043(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1      u1_t_sw;

    u1_ap_ans[0] = (U1)0xF8U;

    u1_t_sw = u1_g_HmiDiagSwGet((U1)HMIDIAG_DIM_UP);
    if(u1_t_sw == (U1)0xFFU){
        u1_t_sw = (U1)0U;
    }
    u1_ap_ans[1] = (U1)(u1_t_sw << 7);

    u1_t_sw = u1_g_HmiDiagSwGet((U1)HMIDIAG_DIM_DOWN);
    if(u1_t_sw == (U1)0xFFU){
        u1_t_sw = (U1)0U;
    }
    u1_ap_ans[1] |= (U1)(u1_t_sw << 6);

    u1_t_sw = u1_g_HmiDiagSwGet((U1)HMIDIAG_VIPOS_UP);
    if(u1_t_sw == (U1)0xFFU){
        u1_t_sw = (U1)0U;
    }
    u1_ap_ans[1] |= (U1)(u1_t_sw << 5);

    u1_t_sw = u1_g_HmiDiagSwGet((U1)HMIDIAG_VIPOS_DN);
    if(u1_t_sw == (U1)0xFFU){
        u1_t_sw = (U1)0U;
    }
    u1_ap_ans[1] |= (U1)(u1_t_sw << 4);

    u1_t_sw = u1_g_HmiDiagSwGet((U1)HMIDIAG_HUD_MSW);
    if(u1_t_sw == (U1)0xFFU){
        u1_t_sw = (U1)0U;
    }
    u1_ap_ans[1] |= (U1)(u1_t_sw << 3);

    return((U1)OXDC_SAL_PROC_FIN);
}
