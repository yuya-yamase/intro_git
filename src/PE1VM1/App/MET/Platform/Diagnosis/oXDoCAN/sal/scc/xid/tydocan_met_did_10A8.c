/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_10A8(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    static const ST_TYDC_10A8 st_sp_TYDC_10A8[] = {
        /* mcst_id,                             u1_dst,         u1_msk,         u1_sft */
        {(U1)MCST_DIAG_HUD               ,      (U1)0U,         (U1)0x01U,      (U1)7 },
        {(U1)MCST_DIAG_HUD_ILL           ,      (U1)0U,         (U1)0x1fU,      (U1)2 },
        {(U1)MCST_DIAG_HUD_ST            ,      (U1)1U,         (U1)0x03U,      (U1)6 },
        {(U1)MCST_DIAG_MM_ANALOG_MET     ,      (U1)1U,         (U1)0x07U,      (U1)3 },
        {(U1)MCST_DIAG_MM_MET_TYP        ,      (U1)2U,         (U1)0x03U,      (U1)6 },
        {(U1)MCST_DIAG_MM_TACHO          ,      (U1)2U,         (U1)0x03U,      (U1)4 },
        {(U1)MCST_DIAG_RHEO              ,      (U1)3U,         (U1)0x1fU,      (U1)3 },
/*        {(U1)U1_MAX                      ,      (U1)3U,         (U1)0x03U,      (U1)1 }, */  /* #7 bit2-1 is no available */
        {(U1)MCST_DIAG_MM_ECO_IND        ,      (U1)4U,         (U1)0x01U,      (U1)7 },
        {(U1)MCST_DIAG_MM_EV_IND         ,      (U1)4U,         (U1)0x01U,      (U1)6 },
        {(U1)MCST_DIAG_MM_REV_IND        ,      (U1)4U,         (U1)0x01U,      (U1)5 },
        {(U1)MCST_DIAG_MM_ECO_DRV_IND    ,      (U1)4U,         (U1)0x01U,      (U1)4 },
        {(U1)MCST_DIAG_MM_EV_IND_DSP     ,      (U1)4U,         (U1)0x01U,      (U1)3 },
        {(U1)MCST_DIAG_MM_HV_SYS_IND     ,      (U1)4U,         (U1)0x01U,      (U1)2 },
        {(U1)MCST_DIAG_MM_REV_ST         ,      (U1)5U,         (U1)0x7fU,      (U1)1 },
        {(U1)MCST_DIAG_MM_REV_PK         ,      (U1)5U,         (U1)0x01U,      (U1)0 },
        {(U1)MCST_DIAG_MM_MET_DSP        ,      (U1)6U,         (U1)0x1fU,      (U1)3 },
/*        {(U1)MCST_DIAG_MM_MET_DSP_7INCH  ,      (U1)7U,         (U1)0x1fU,      (U1)3 }, */  /* #11 bit7-3 is no available */
        {(U1)MCST_DIAG_MM_MET_DSP_LT     ,      (U1)8U,         (U1)0x1fU,      (U1)3 },
        {(U1)MCST_DIAG_MM_MET_DSP_CT     ,      (U1)9U,         (U1)0x1fU,      (U1)3 },
        {(U1)MCST_DIAG_MM_MET_DSP_RT     ,      (U1)10U,        (U1)0x1fU,      (U1)3 },
        {(U1)MCST_DIAG_MM_MET_DSP_PRESET ,      (U1)11U,        (U1)0x07U,      (U1)5 },
        {(U1)MCST_DIAG_MM_MET_LAYOUT     ,      (U1)11U,        (U1)0x0fU,      (U1)1 },
        {(U1)MCST_DIAG_MM_MET_DESIGN     ,      (U1)12U,        (U1)0x0fU,      (U1)4 },
/*        {(U1)MCST_DIAG_MM_CURCNT_LBYTE   ,      (U1)13U,        (U1)0xffU,      (U1)0 }, */  /* #17 bit7-0 is provided by LComSPI */
/*        {(U1)MCST_DIAG_MM_CURCNT_HBYTE   ,      (U1)14U,        (U1)0xffU,      (U1)0 }, */  /* #18 bit7-0 is provided by LComSPI */
        {(U1)MCST_DIAG_MM_FUEL_FLG       ,      (U1)15U,        (U1)0x07U,      (U1)5 },
        {(U1)MCST_DIAG_MM_ELEC_FLG       ,      (U1)15U,        (U1)0x07U,      (U1)2 },
        {(U1)MCST_DIAG_MM_POP_CROSS      ,      (U1)16U,        (U1)0x01U,      (U1)7 },
        {(U1)MCST_DIAG_MM_POP_TEL        ,      (U1)16U,        (U1)0x01U,      (U1)6 },
        {(U1)MCST_DIAG_MM_POP_AUDIO      ,      (U1)16U,        (U1)0x01U,      (U1)5 },
        {(U1)MCST_DIAG_MM_POP_VOLUME     ,      (U1)16U,        (U1)0x01U,      (U1)4 },
        {(U1)MCST_DIAG_MM_POP_VOICE      ,      (U1)16U,        (U1)0x01U,      (U1)3 },
        {(U1)MCST_DIAG_MM_POP_TIME       ,      (U1)16U,        (U1)0x01U,      (U1)2 },
        {(U1)MCST_DIAG_MM_POP_NOTIFY     ,      (U1)16U,        (U1)0x01U,      (U1)1 },
        {(U1)MCST_DIAG_MM_POP_DIM        ,      (U1)17U,        (U1)0x01U,      (U1)7 },
        {(U1)MCST_DIAG_MM_EV_DRVINF      ,      (U1)17U,        (U1)0x03U,      (U1)5 },
        {(U1)MCST_DIAG_MM_DRVINF_CALC    ,      (U1)17U,        (U1)0x03U,      (U1)3 },
        {(U1)MCST_DIAG_MM_DRVINF_UP      ,      (U1)18U,        (U1)0x03U,      (U1)6 },
        {(U1)MCST_DIAG_MM_DRVINF_DN      ,      (U1)18U,        (U1)0x03U,      (U1)4 },
        {(U1)MCST_DIAG_MM_MET_TRIP_A_UP  ,      (U1)18U,        (U1)0x03U,      (U1)2 },
        {(U1)MCST_DIAG_MM_MET_TRIP_A_DN  ,      (U1)18U,        (U1)0x03U,      (U1)0 },
        {(U1)MCST_DIAG_MM_MET_TRIP_B_UP  ,      (U1)19U,        (U1)0x03U,      (U1)6 },
        {(U1)MCST_DIAG_MM_MET_TRIP_B_DN  ,      (U1)19U,        (U1)0x03U,      (U1)4 },
        {(U1)MCST_DIAG_MM_ENG_MONI       ,      (U1)19U,        (U1)0x01U,      (U1)3 },
        {(U1)MCST_DIAG_MM_4WD_AWD        ,      (U1)19U,        (U1)0x01U,      (U1)2 }
    };
    static const U1 u1_s_TYDC_NUM_10A8 = ((U1)sizeof(st_sp_TYDC_10A8) / (U1)sizeof(ST_TYDC_10A8));
    U4      u4_t_lpcnt;
    U1      u1_t_bf;

    U1      u1_t_id;
    U1      u1_t_dst;
    U1      u1_t_msk;
    U1      u1_t_sft;

    U2      u2_t_val;

    vd_g_MemfillU1(u1_ap_ans, (U1)0U, (U4)OXDC_DATA_REA_ANS_NB_10A8);

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_s_TYDC_NUM_10A8; u4_t_lpcnt++){
        u1_t_id  = st_sp_TYDC_10A8[u4_t_lpcnt].u1_mcst_id;
        u1_t_dst = st_sp_TYDC_10A8[u4_t_lpcnt].u1_dst;
        u1_t_msk = st_sp_TYDC_10A8[u4_t_lpcnt].u1_msk;
        u1_t_sft = st_sp_TYDC_10A8[u4_t_lpcnt].u1_sft;

        u1_t_bf  = u1_g_McstDiag(u1_t_id);
        if(u1_t_bf != (U1)MCST_DIAG_UNK){
            u1_t_bf             &= u1_t_msk;
            u1_ap_ans[u1_t_dst] |= (U1)(u1_t_bf << u1_t_sft);
        }
    }

    u2_t_val = u2_g_TyDoCANCursorCount();
    u1_ap_ans[(U1)TYDC_10A8_TX_BYTE_13] = (U1)((u2_t_val >> (U1)8U) & (U1)U1_MAX);
    u1_ap_ans[(U1)TYDC_10A8_TX_BYTE_14] = (U1)(u2_t_val             & (U1)U1_MAX);

    u1_ap_ans[(U1)TYDC_10A8_TX_BYTE_7] = u1_g_TyDoCANGetDisplaySwitchCntt();

    return((U1)OXDC_SAL_PROC_FIN);
}
