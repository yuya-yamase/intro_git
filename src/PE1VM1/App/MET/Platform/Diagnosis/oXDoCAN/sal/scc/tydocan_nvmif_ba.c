/*  U1      u1_g_TyDoCANNvmIfAns_Rta(const U2 u2_a_DID, const U1 u1_a_NVM_OK, U4 * u4_ap_b32_tx)                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfAns_Rta(const U2 u2_a_DID, const U1 u1_a_NVM_OK, U4 * u4_ap_b32_tx)
{
    S2            s2_t_subsec;

    s2_t_subsec = (S2)u4_ap_b32_tx[TYDC_B32_WO_LA];
    if((u1_a_NVM_OK != (U1)TRUE            ) ||
       (s2_t_subsec <  (S2)DATE_CLK_SCA_MIN) ||
       (s2_t_subsec >  (S2)DATE_CLK_SCA_MAX)){

        u4_ap_b32_tx[TYDC_B32_WO_LA] = (U4)0U;
    }

    return((U1)OXDC_SAL_PROC_FIN);
}
