/*  U1      u1_g_TyDoCANNvmIfAns_Tpa(const U2 u2_a_DID, const U1 u1_a_NVM_OK, U4 * u4_ap_b32_tx)                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfAns_Da(const U2 u2_a_DID, const U1 u1_a_NVM_OK, U4 * u4_ap_b32_tx)
{
    U1      u1_t_adj;
    U1      u1_t_dir;

    u1_t_adj = (U1)(u4_ap_b32_tx[TYDC_B32_WO_LA]        & (U4)TYDC_NVMIF_BA_DA_ADJ_MSK);
    u1_t_dir = (U1)((u4_ap_b32_tx[TYDC_B32_WO_LA] >> 4) & (U4)TYDC_NVMIF_BA_DA_DIR_MSK);

    if((u1_a_NVM_OK != (U1)TRUE                     ) ||
       (u1_t_adj     > (U1)TYDC_NVMIF_BA_DA_ADJ_MAX ) ||
       ((u1_t_dir   != (U1)TYDC_NVMIF_BA_DA_DIR_PLS ) &&
        (u1_t_dir   != (U1)TYDC_NVMIF_BA_DA_DIR_MNU ))){

        u4_ap_b32_tx[TYDC_B32_WO_LA] = (U4)0U;
    }

    return((U1)OXDC_SAL_PROC_FIN);
}
