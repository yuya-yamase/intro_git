/*  U1      u1_g_oXDoCANRebyId_1041(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1081(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U2             u2_t_vehspd;
    U2             u2_t_kmph;
    U1             u1_t_sts_chk;

    u2_t_vehspd  = (U2)0U;
    u1_t_sts_chk = u1_g_VehspdKmphInst(&u2_t_vehspd, (U1)TRUE) & ((U1)VEHSPD_STSBIT_UNKNOWN | (U1)VEHSPD_STSBIT_INVALID);
    u2_t_kmph    = u2_g_GagdstNxmphDsplyd((U1)GAGDST_NXMPH_KMPH);

    if(u1_t_sts_chk != (U1)VEHSPD_STSBIT_VALID){
        u1_ap_ans[0] = (U1)TYDC_GAGDST_KMPH_UNK;
    }
    else if(u2_t_kmph >= (U2)TYDC_GAGDST_KMPH_MAX){
        u1_ap_ans[0] = (U1)TYDC_GAGDST_KMPH_MAX;
    }
    else{
        u1_ap_ans[0] = (U1)u2_t_kmph;
    }

    return((U1)OXDC_SAL_PROC_FIN);
}
