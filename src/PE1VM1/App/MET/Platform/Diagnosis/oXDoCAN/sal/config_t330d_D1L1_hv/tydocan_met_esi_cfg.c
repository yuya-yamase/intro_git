/*  static U2    u2_s_TydcMetEsi_22FDC040(void)                                                                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U2    u2_s_TydcMetEsi_22FDC040(void)
{
    U4                 u4_t_km;
    U2                 u2_t_rslt;
    U1                 u1_t_chk;

    u4_t_km  = (U4)U4_MAX;
    u1_t_chk = u1_g_OdoKm(&u4_t_km);
    if((u1_t_chk == (U1)ODO_STSBIT_VALID   ) &&
       (u4_t_km  <  (U4)ODO_TRIP_MIN_TO_NVM)){
        u2_t_rslt  = (U2)TYDC_MET_ESI_OTZ_ODO;
    }
    else{
        u2_t_rslt  = (U2)0U;
    }

    u4_t_km  = (U4)U4_MAX;
    u1_t_chk = u1_g_OdoTripKm((U1)ODO_TRIP_CH_A, &u4_t_km);
    if((u1_t_chk == (U1)ODO_STSBIT_VALID   ) &&
       (u4_t_km  <  (U4)ODO_TRIP_MIN_TO_NVM)){
        u2_t_rslt |= (U2)TYDC_MET_ESI_OTZ_A;
    }

    u4_t_km  = (U4)U4_MAX;
    u1_t_chk = u1_g_OdoTripKm((U1)ODO_TRIP_CH_B, &u4_t_km);
    if((u1_t_chk == (U1)ODO_STSBIT_VALID   ) &&
       (u4_t_km  <  (U4)ODO_TRIP_MIN_TO_NVM)){
        u2_t_rslt |= (U2)TYDC_MET_ESI_OTZ_B;
    }

    u4_t_km  = (U4)U4_MAX;
    u1_t_chk = u1_g_OdoTripKm((U1)ODO_TRIP_CH_OM, &u4_t_km);
    if((u1_t_chk == (U1)ODO_STSBIT_VALID   ) &&
       (u4_t_km  <  (U4)ODO_TRIP_MIN_TO_NVM)){
        u2_t_rslt |= (U2)TYDC_MET_ESI_OTZ_OM;
    }

    return((U2)U2_MAX ^ u2_t_rslt);
}
