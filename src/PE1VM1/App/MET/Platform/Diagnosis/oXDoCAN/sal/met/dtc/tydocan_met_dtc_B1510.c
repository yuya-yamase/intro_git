/*===================================================================================================================================*/
/*  void    u1_g_oXDoCANDtcTrchk_B1510(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      void                                                                                                             */
/*  Return:         OXDC_DTC_TR_INA  (0x00U)  : DTC INActive                                                                         */
/*                  OXDC_DTC_TR_ACT  (0x01U)  : DTC ACTive                                                                           */
/*                  OXDC_DTC_TR_N_A  (0x02U)  : DTC Not Applicable                                                                   */
/*===================================================================================================================================*/
U1    u1_g_oXDoCANDtcTrchk_B1510(const U2 u2_a_ELPSD)
{
    static const U1   u1_sp_TYDC_DTC_B1510[] = {
        (U1)OXDC_DTC_TR_UNK, /* 00b */
        (U1)OXDC_DTC_TR_UNK, /* 01b */
        (U1)OXDC_DTC_TR_INA, /* 10b */
        (U1)OXDC_DTC_TR_ACT  /* 11b */
    };
    static const U2   u2_s_TYDC_DTC_B1510_EVM_TOUT = (U2)1000U / (U2)OXDC_MAIN_TICK;  /* 1 seconds */

    U1                u1_t_gvif_chk;

    if(u2_a_ELPSD >= u2_s_TYDC_DTC_B1510_EVM_TOUT){
        u1_t_gvif_chk = u1_g_LComSpiDtcRx((U1)LCOM_SPI_DTC_RX_B1510) & ((U1)LCOM_SPI_DTC_RX_FAI |
                                                                        (U1)LCOM_SPI_DTC_RX_RUN);

        u1_t_gvif_chk = u1_sp_TYDC_DTC_B1510[u1_t_gvif_chk];
    }
    else{
        u1_t_gvif_chk = (U1)OXDC_DTC_TR_UNK;
    }

    return(u1_t_gvif_chk);
}
