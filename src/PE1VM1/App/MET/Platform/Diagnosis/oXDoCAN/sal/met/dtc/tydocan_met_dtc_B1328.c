/*===================================================================================================================================*/
/*  void    u1_g_oXDoCANDtcTrchk_B1328(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      void                                                                                                             */
/*  Return:         OXDC_DTC_TR_INA  (0x00U)  : DTC INActive                                                                         */
/*                  OXDC_DTC_TR_ACT  (0x01U)  : DTC ACTive                                                                           */
/*                  OXDC_DTC_TR_N_A  (0x02U)  : DTC Not Applicable                                                                   */
/*===================================================================================================================================*/
U1    u1_g_oXDoCANDtcTrchk_B1328(const U2 u2_a_ELPSD)
{
    static const U1   u1_sp_TYDC_DTC_B1328[] = {
        (U1)OXDC_DTC_TR_N_A, /* 000b */
        (U1)OXDC_DTC_TR_N_A, /* 001b */
        (U1)OXDC_DTC_TR_N_A, /* 010b */
        (U1)OXDC_DTC_TR_N_A, /* 011b */

        (U1)OXDC_DTC_TR_UNK, /* 100b */
        (U1)OXDC_DTC_TR_UNK, /* 101b */
        (U1)OXDC_DTC_TR_INA, /* 110b */
        (U1)OXDC_DTC_TR_ACT  /* 111b */
    };
    static const U2   u2_s_TYDC_DTC_B1328_EVM_TOUT = (U2)10000U / (U2)OXDC_MAIN_TICK; /* 10 seconds */

    U1                u1_eth_chk;
    U1                u1_mthd_chk;

    u1_mthd_chk = u1_g_VardefMmMthd();
    if(u1_mthd_chk == (U1)VDF_MM_MTHD_ETHER){

        u1_eth_chk = u1_g_LComSpiDtcRx((U1)LCOM_SPI_DTC_RX_B1328) & ((U1)LCOM_SPI_DTC_RX_FAI |
                                                                     (U1)LCOM_SPI_DTC_RX_RUN |
                                                                     (U1)LCOM_SPI_DTC_RX_ENA);

        u1_eth_chk = u1_sp_TYDC_DTC_B1328[u1_eth_chk];
        if((u1_eth_chk   < (U1)OXDC_DTC_TR_UNK         ) &&
           (u2_a_ELPSD   < u2_s_TYDC_DTC_B1328_EVM_TOUT)){

            u1_eth_chk = (U1)OXDC_DTC_TR_UNK;
        }

    }
    else{
        u1_eth_chk = (U1)OXDC_DTC_TR_UNK;
    }

    return(u1_eth_chk);
}
