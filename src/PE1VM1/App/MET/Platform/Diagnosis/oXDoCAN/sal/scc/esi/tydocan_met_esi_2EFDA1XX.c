/*  U1      u1_g_TyDoCANEsiTRx_2EFDA1XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANEsiTRx_2EFDA1XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)
{
    static const U2         u2_sp_2FFDACXX_TOUT = (U2)1000U / (U2)10U;    

    U1                      u1_t_proc;

    U1                      u1_t_offset;
    U1                      u1_t_cmplt;

    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B0] = (U1)TYDC_MET_ESI_ANS_2E;
    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B1] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B1];
    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B2] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B2];
    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B3];
    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B4] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B4];

    u1_t_offset = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B4]; /*  Request data will be 1byte */

    if(u2_a_ELPSD == (U2)0U){

        u1_t_proc   = (U1)OXDC_SAL_PROC_RUN;
        vd_g_MechptrSMZcaZpOffsetStart((U1)MECH_GAG_CH_TACH, u1_t_offset);
    }
    else if (u2_a_ELPSD < u2_sp_2FFDACXX_TOUT){

        u1_t_cmplt = u1_g_MechptrSMZcaZpOffsetCmplt((U1)MECH_GAG_CH_TACH);
        if(u1_t_cmplt == (U1)TRUE){

            u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
        }
        else{
            u1_t_proc   = (U1)OXDC_SAL_PROC_RUN;
        }
    }
    else{

        u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
    }

    return(u1_t_proc);
}
