/*  U1      u1_g_TyDoCANRobSsr_F020(const U2 u2_a_FRN, U1 * u1_ap_ans, const U4 u4_a_ANS_NBYTE, const U2 u2_a_ELPSD)                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANRobSsr_F020(const U2 u2_a_FRN, U1 * u1_ap_ans, const U4 u4_a_ANS_NBYTE, const U2 u2_a_ELPSD)
{
    U1 *                     u1_tp_tx;

    U4                       u4_t_nbyte;
    U4                       u4_t_rec_idx;
    U4                       u4_t_lpcnt;

    if(u4_a_ANS_NBYTE >= (U4)TYDC_ROB_SSR_NB_F020){

        if(u2_a_FRN > (U2)0U){
            u4_t_nbyte   = (U4)TYDC_ROB_SSR_NB_F020;
            u4_t_rec_idx = (U4)u2_a_FRN - (U4)1U;
        }
        else{
            u4_t_nbyte   = u4_a_ANS_NBYTE;
            u4_t_rec_idx = (U4)0U;
        }

        u4_t_lpcnt = (U4)0U;
        while(u4_t_lpcnt < u4_t_nbyte){

            u1_tp_tx = &u1_ap_ans[u4_t_lpcnt];

            u1_tp_tx[TYDC_ROB_OTA_ANS_FRN_H] = (U1)0U;
            u1_tp_tx[TYDC_ROB_OTA_ANS_FRN_L] = (U1)u4_t_rec_idx + (U1)1U;
            u1_tp_tx[TYDC_ROB_OTA_ANS_NDID]  = (U1)TYDC_ROB_OTA_NUM_DID;

            vd_g_MemfillU1(&u1_tp_tx[TYDC_ROB_OTA_ANS_SSR], (U1)U1_MAX, (U4)LCOM_SPI_OTA_NB_AB_F020);
            vd_g_LComSpiOtaRobSsrRead((U1)u4_t_rec_idx, &u1_tp_tx[TYDC_ROB_OTA_ANS_SSR]);

            u4_t_rec_idx++;
            u4_t_lpcnt += (U4)TYDC_ROB_SSR_NB_F020;
        }
    }

    return((U1)OXDC_SAL_PROC_FIN);
}
