/*  U1      u1_g_TyDoCANNvmIfSyn_2E(const U2 u2_a_DID)                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfSyn_2E(const U2 u2_a_DID)
{
    U1       u1_t_proc;

    u1_t_proc = u1_g_TyDoCANNvmIfSynchk(u2_a_DID);
    if(u1_t_proc != (U1)OXDC_SAL_PROC_RUN){
        vd_g_VardefDs2E_Syn32(u2_a_DID);
    }

    return(u1_t_proc);
}
/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANNvmIfAns_2E(const U2 u2_a_DID, const U1 u1_a_NVM_OK, U4 * u4_ap_b32_tx)                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfAns_2E(const U2 u2_a_DID, const U1 u1_a_NVM_OK, U4 * u4_ap_b32_tx)
{
    u4_ap_b32_tx[TYDC_B32_WO_LA] = u4_g_VardefDs2E_Las32(u2_a_DID);

    return((U1)OXDC_SAL_PROC_FIN);
}
/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANNvmIfReq_2E(const U2 u2_a_DID, U4 * u4_ap_b32_rx)                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfReq_2E(const U2 u2_a_DID, U4 * u4_ap_b32_rx)
{
    U1       u1_t_proc;

    if(u4_ap_b32_rx[TYDC_B32_WO_LA] > (U4)TYDC_NVM_2E_MAX){
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
    }

    return(u1_t_proc);
}
