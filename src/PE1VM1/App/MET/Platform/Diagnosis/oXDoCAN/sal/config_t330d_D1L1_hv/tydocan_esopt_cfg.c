/*===================================================================================================================================*/
/*  static U1      u1_s_TydcEsopt_11A1(const U2 u2_a_CH)                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U1      u1_s_TydcEsopt_11A1(const U2 u2_a_CH)
{
    static const U2 u2_sp_TYDC_11A1[] = {
        (U2)VDF_ESO_CH_AHS,
        (U2)U2_MAX
    };

    U1                  u1_t_ava;

    if(u2_sp_TYDC_11A1[u2_a_CH] != (U2)U2_MAX){
        u1_t_ava = u1_g_VardefEsOptAvaByCh(u2_sp_TYDC_11A1[u2_a_CH]);
    }
    else{
        u1_t_ava = (U1)TRUE;
    }

    return(u1_t_ava);
}

