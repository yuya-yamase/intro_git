/*  U1      u1_g_oXDoCANDidSup_1001(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANDidSup_1001(void)
{
    U1              u1_t_sup;
    U1              u1_t_calib;

    u1_t_sup   = (U1)OXDC_XID_SUP_NOT;
    u1_t_calib = u1_CALIB_MCUID0268_SWNUM;
    if((u1_t_calib == (U1)CALIB_MCUID0268_BUTTON_1) ||
       (u1_t_calib == (U1)CALIB_MCUID0268_BUTTON_2)){
        u1_t_sup = (U1)OXDC_XID_SUP_PUB;
    }

    u1_t_calib = u1_CALIB_MCUID0430_RHEOSW;
    if((u1_t_calib == (U1)CALIB_MCUID0430_1_INPUT) ||
       (u1_t_calib == (U1)CALIB_MCUID0430_2_INPUT) ||
       (u1_t_calib == (U1)CALIB_MCUID0430_THUMB_WHEEL)){
        u1_t_sup = (U1)OXDC_XID_SUP_PUB;
    }

    u1_t_calib = u1_CALIB_MCUID0432_LIDINFO;
    if(u1_t_calib == (U1)CALIB_MCUID0432_LST1){
        u1_t_sup = (U1)OXDC_XID_SUP_PUB;
    }

    return(u1_t_sup);
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRebyId_1001(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1001(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1              u1_t_proc;
    U1              u1_t_calib;

    u1_t_proc = u1_g_TyDoCANDilAnsTx(u1_ap_ans, (U2)TYDC_DIL_TX_1001);
    /*#4 #5 Bit 7 Mask                  */
    u1_t_calib = u1_CALIB_MCUID0268_SWNUM;
    if((u1_t_calib != (U1)CALIB_MCUID0268_BUTTON_1) &&
       (u1_t_calib != (U1)CALIB_MCUID0268_BUTTON_2)){
        u1_ap_ans[0] &= ((U1)U1_MAX ^ (U1)0x80U);
        u1_ap_ans[1] &= ((U1)U1_MAX ^ (U1)0x80U);
    }
    /*#4 #5 Bit 4 Mask                  */
    u1_t_calib = u1_CALIB_MCUID0430_RHEOSW;
    if(u1_t_calib != (U1)CALIB_MCUID0430_THUMB_WHEEL){
        u1_ap_ans[0] &= ((U1)U1_MAX ^ (U1)0x10U);
        u1_ap_ans[1] &= ((U1)U1_MAX ^ (U1)0x10U);
    }
    /*#4 #5 Bit 3,2 Mask                */
    if((u1_t_calib  != (U1)CALIB_MCUID0430_2_INPUT) &&
       (u1_t_calib  != (U1)CALIB_MCUID0430_1_INPUT)){
        u1_ap_ans[0] &= ((U1)U1_MAX ^ (U1)0x0CU);
        u1_ap_ans[1] &= ((U1)U1_MAX ^ (U1)0x0CU);
    }
    /*#4 #5 Bit 0 Mask                  */
    u1_t_calib = u1_CALIB_MCUID0432_LIDINFO;
    if(u1_t_calib != (U1)CALIB_MCUID0432_LST1){
        u1_ap_ans[0] &= ((U1)U1_MAX ^ (U1)0x01U);
        u1_ap_ans[1] &= ((U1)U1_MAX ^ (U1)0x01U);
    }

    return(u1_t_proc);
}

