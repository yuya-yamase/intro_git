/*  U1      u1_g_oXDoCANRebyId_1641(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1641(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    S2          s2_t_pct;
    U2          u2_t_tx;
    U1          u1_t_sts_chk;

    s2_t_pct     = (S2)0;
    u1_t_sts_chk = u1_g_DrvIndHvPctInst(&s2_t_pct);
    if(u1_t_sts_chk != (U1)DRVIND_HV_STSBIT_VALID){
        u2_t_tx = (U2)TYDC_HYBSYS_UNK;
    }
    else{
        u2_t_tx = (U2)s2_t_pct & (U2)TYDC_MYBSYS_MAX;
    }

    return(u1_g_TyDoCANNu32Tx(u1_ap_ans, (U4)u2_t_tx, (U2)OXDC_DATA_REA_ANS_NB_1641));
}
