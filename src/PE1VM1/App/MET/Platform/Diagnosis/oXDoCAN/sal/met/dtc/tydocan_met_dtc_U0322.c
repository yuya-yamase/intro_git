/*  void    u1_g_oXDoCANDtcTrchk_U0322(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      void                                                                                                             */
/*  Return:         OXDC_DTC_TR_INA  (0x00U)  : DTC INActive                                                                         */
/*                  OXDC_DTC_TR_ACT  (0x01U)  : DTC ACTive                                                                           */
/*                  OXDC_DTC_TR_N_A  (0x02U)  : DTC Not Applicable                                                                   */
/*===================================================================================================================================*/
U1    u1_g_oXDoCANDtcTrchk_U0322(const U2 u2_a_ELPSD)
{
    U1    u1_t_rsichk;

    u1_t_rsichk = u1_g_TyDoCANRsiTrchk();

    if(u1_t_rsichk == (U1)TRUE){
        u1_t_rsichk = (U1)OXDC_DTC_TR_ACT;
    }
    else{
        u1_t_rsichk = (U1)OXDC_DTC_TR_INA;
    }

    return(u1_t_rsichk);
}
