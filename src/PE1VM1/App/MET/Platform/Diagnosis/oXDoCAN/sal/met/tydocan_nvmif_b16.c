/*  U1      u1_g_TyDoCANNvmIfWriB16(const U1 * u1_ap_REQ, const U2 u2_a_DID)                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfWriB16(const U1 * u1_ap_REQ, const U2 u2_a_DID)
{
    const ST_TYDC_NVM_IF *    st_tp_IF;

    U4                        u4_tp_b32_rx[TYDC_B32_NWORD];

    U4                        u4_t_sup;
    U4                        u4_t_req;
    U4                        u4_t_unk;

    U2                        u2_t_nbyte;
    U2                        u2_t_nid;
    U2                        u2_t_nvm_b16;

    U1                        u1_t_nvm_chk;
    U1                        u1_t_proc;

    st_tp_IF   = st_gp_TYDC_NVM_DID[u2_a_DID].stp_IF;
    u2_t_nbyte = st_gp_TYDC_NVM_DID[u2_a_DID].u2_nbyte;
    if((st_tp_IF            == vdp_PTR_NA            ) ||
       (st_tp_IF->fp_u4_MAX == vdp_PTR_NA            ) ||
       (st_tp_IF->fp_u1_REQ == vdp_PTR_NA            ) ||
       (u2_t_nbyte          >  (U2)TYDC_NVM_IF_NB_B16)){

        u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
    }
    else{

        u4_t_sup = (*st_tp_IF->fp_u4_MAX)(u2_a_DID);
        u4_t_req = u4_g_TyDoCANBi32(u1_ap_REQ, u2_t_nbyte);
        u4_t_unk = u4_t_req & ((U4)U4_MAX ^ u4_t_sup);
        if(u4_t_unk == (U4)0U){

            u4_tp_b32_rx[TYDC_B32_WO_SU] = u4_t_sup;
            u4_tp_b32_rx[TYDC_B32_WO_LA] = u4_t_req & u4_t_sup;
            u1_t_proc = (*st_tp_IF->fp_u1_REQ)(u2_a_DID, &u4_tp_b32_rx[0]);
        }
        else{
            u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
        }

        if(u1_t_proc == (U1)OXDC_SAL_PROC_RUN){

            u2_t_nid     = st_gp_TYDC_NVM_DID[u2_a_DID].u2_nid;
            u2_t_nvm_b16 = (U2)U2_MAX ^ (U2)u4_tp_b32_rx[TYDC_B32_WO_LA];
            u1_t_nvm_chk = u1_g_Nvmc_ReadU2withSts(u2_t_nid, &u2_t_nvm_b16);
            if((u1_t_nvm_chk == (U1)NVMC_STATUS_KIND_OK         ) &&
               (u2_t_nvm_b16 == (U2)u4_tp_b32_rx[TYDC_B32_WO_LA])){

                u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
            }
            else{
                vd_g_Nvmc_WriteU2(u2_t_nid, (U2)u4_tp_b32_rx[TYDC_B32_WO_LA]);
            }
        }
    }

    return(u1_t_proc);
}
