/*  U1      u1_g_TyDoCANNvmIfReaB16(U1 * u1_ap_ans_tx, const U2 u2_a_DID)                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfReaB16(U1 * u1_ap_ans_tx, const U2 u2_a_DID)
{
    const ST_TYDC_NVM_IF *    st_tp_IF;

    U4                        u4_tp_b32_tx[TYDC_B32_NWORD];

    U2                        u2_t_nvm_b16;
    U2                        u2_t_nbyte;

    U1                        u1_t_nvm_chk;
    U1                        u1_t_nvm_ok;
    U1                        u1_t_proc;

    st_tp_IF   = st_gp_TYDC_NVM_DID[u2_a_DID].stp_IF;
    u2_t_nbyte = st_gp_TYDC_NVM_DID[u2_a_DID].u2_nbyte;
    if((st_tp_IF            == vdp_PTR_NA            ) ||
       (st_tp_IF->fp_u4_MAX == vdp_PTR_NA            ) ||
       (st_tp_IF->fp_u1_ANS == vdp_PTR_NA            ) ||
       (u2_t_nbyte          >  (U2)TYDC_NVM_IF_NB_B16)){

        u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
    }
    else{

        u4_tp_b32_tx[TYDC_B32_WO_SU] = (*st_tp_IF->fp_u4_MAX)(u2_a_DID);

        u2_t_nvm_b16 = (U2)0U;
        u1_t_nvm_chk = u1_g_Nvmc_ReadU2withSts(st_gp_TYDC_NVM_DID[u2_a_DID].u2_nid, &u2_t_nvm_b16);
        if((u1_t_nvm_chk >= (U1)NVMC_STATUS_KIND_NG ) ||
           (u1_t_nvm_chk == (U1)NVMC_STATUS_ERRCOMP ) ||
           (u1_t_nvm_chk == (U1)NVMC_STATUS_CACHE_NG)){

            u1_t_nvm_ok                  = (U1)FALSE;
            u4_tp_b32_tx[TYDC_B32_WO_LA] = (U4)0U;
        }
        else{

            u1_t_nvm_ok                  = (U1)TRUE;
            u4_tp_b32_tx[TYDC_B32_WO_LA] = (U4)u2_t_nvm_b16 & u4_tp_b32_tx[TYDC_B32_WO_SU];
        }

        u1_t_proc = (*st_tp_IF->fp_u1_ANS)(u2_a_DID, u1_t_nvm_ok, &u4_tp_b32_tx[0]);
        if(u1_t_proc == (U1)OXDC_SAL_PROC_FIN){

            (void)u1_g_TyDoCANBi32Tx(u1_ap_ans_tx, &u4_tp_b32_tx[0], u2_t_nbyte);
        }
    }

    return(u1_t_proc);
}
