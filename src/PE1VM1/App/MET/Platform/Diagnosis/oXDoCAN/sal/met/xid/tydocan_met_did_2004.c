/*  U1      u1_g_oXDoCANDidSup_2004(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANDidSup_2004(void)
{
    U1                        u1_t_sup;

    u1_t_sup = (U1)OXDC_XID_SUP_NOT;

    if(u4_g_TyDoCANNvmIfMax_2E((U2)TYDC_NVM_2E_2004) > (U4)0U){
        u1_t_sup = (U1)OXDC_XID_SUP_PUB;
    }

    return(u1_t_sup);
}
#ifndef MCST_BFI_TAST_FLG
#define MCST_BFI_TAST_FLG (0xFFU)
#endif
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRebyId_2004(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_2004(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U4                        u4_tp_b32_tx[TYDC_B32_NWORD];
    U4                        u4_t_nvm_b32;

    u4_tp_b32_tx[TYDC_B32_WO_SU] = u4_g_TyDoCANNvmIfMax_2E((U2)TYDC_NVM_2E_2004);

    u4_t_nvm_b32 = u4_g_McstBfU4((U1)MCST_BFI_TAST_FLG);

    if(u4_t_nvm_b32 == (U4)TYDC_INIT_TAST_FLG){
        u4_tp_b32_tx[TYDC_B32_WO_LA] = (U4)0U;
    }
    else{
        u4_tp_b32_tx[TYDC_B32_WO_LA] = u4_t_nvm_b32 & u4_tp_b32_tx[TYDC_B32_WO_SU];
    }

    return(u1_g_TyDoCANBi32Tx(u1_ap_ans, &u4_tp_b32_tx[0], (U2)OXDC_DATA_REA_ANS_NB_2004));
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANWrbyId_2004(const U1 * u1_ap_REQ, const U2 u2_a_ELPSD)                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANWrbyId_2004(const U1 * u1_ap_REQ, const U2 u2_a_ELPSD)
{
    U4                        u4_t_nvm_b32;

    U4                        u4_t_sup;
    U4                        u4_t_req;
    U4                        u4_t_unk;

    U1                        u1_t_proc;

    u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;

    u4_t_sup = u4_g_TyDoCANNvmIfMax_2E((U2)TYDC_NVM_2E_2004);
    u4_t_req = u4_g_TyDoCANBi32(u1_ap_REQ, (U2)OXDC_DATA_WRI_REQ_NB_2004);
    u4_t_unk = u4_t_req & ((U4)U4_MAX ^ u4_t_sup);
    if(u4_t_unk == (U4)0U){

        u1_t_proc = (U1)OXDC_SAL_PROC_FIN;

        u4_t_req = u4_t_req & u4_t_sup;
        u4_t_nvm_b32 = u4_g_McstBfU4((U1)MCST_BFI_TAST_FLG);
        if(u4_t_nvm_b32 != u4_t_req){

            vd_g_McstBfPutU4((U1)MCST_BFI_TAST_FLG, u4_t_req);
        }
    }

    return(u1_t_proc);
}
