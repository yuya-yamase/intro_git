/*  U1      u1_g_oXDoCANRebyId_AA00(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_AA00(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U4                u4_tp_b32[TYDC_B32_NWORD];
    U1                u1_t_ok;
    U1                u1_t_proc;

    u4_tp_b32[TYDC_B32_WO_SU] = (U4)0U;
    u4_tp_b32[TYDC_B32_WO_LA] = (U4)0U;
    u1_t_ok = u1_g_TyDoCANTesMd32(&u4_tp_b32[0]);
    if(u1_t_ok == (U1)TRUE){
        u1_t_proc = u1_g_TyDoCANBi32Tx(u1_ap_ans, &u4_tp_b32[0], (U2)OXDC_DATA_REA_ANS_NB_AA00);
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
    }

    return(u1_t_proc);
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANWrbyId_AA00(const U1 * u1_ap_REQ, const U2 u2_a_ELPSD)                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANWrbyId_AA00(const U1 * u1_ap_REQ, const U2 u2_a_ELPSD)
{
    U4                u4_t_b32;
    U1                u1_t_ok;
    U1                u1_t_proc;

    u4_t_b32 = u4_g_TyDoCANBi32(u1_ap_REQ, (U2)OXDC_DATA_WRI_REQ_NB_AA00);
    u1_t_ok  = u1_g_TyDoCANTesMdNext(u4_t_b32);
    if(u1_t_ok == (U1)TRUE){
        u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_22;
    }

    return(u1_t_proc);
}
