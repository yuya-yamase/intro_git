/*  U1      u1_g_oXDoCANRebyId_1044(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1044(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1  u1_t_sts;
    U4  u4_t_camill_0p001;

    u4_t_camill_0p001 = (U4)0U;
    u1_t_sts          = u1_g_HdimmgrGetInSig((U1)HDIMMGR_INSIG_OTHFRILL, &u4_t_camill_0p001);

    if(u1_t_sts == (U1)TRUE){
        u4_t_camill_0p001 = u4_t_camill_0p001 / (U4)TYDC_CAM_ILL_0P001_TO_0P04;
        if(u4_t_camill_0p001 >= (U4)TYDC_CAM_ILL_MAX){
            u4_t_camill_0p001 = (U4)TYDC_CAM_ILL_MAX;
        }
    }
    else{
        u4_t_camill_0p001 = (U4)TYDC_CAM_ILL_INV;
    }

    u1_ap_ans[TYDC_CAM_ILL_ANS_04] = (U1)((u4_t_camill_0p001 >> 16) & (U4)0xFFU);
    u1_ap_ans[TYDC_CAM_ILL_ANS_05] = (U1)((u4_t_camill_0p001 >>  8) & (U4)0xFFU);
    u1_ap_ans[TYDC_CAM_ILL_ANS_06] = (U1)((u4_t_camill_0p001      ) & (U4)0xFFU);

    return((U1)OXDC_SAL_PROC_FIN);
}
