#define TYDC_CAM_ILL_ANS_07                     (3U)

#define TYDC_CAM_ILL_0P001_TO_0P04              (40U)       /* 0.001 -> 0.04 */
#define TYDC_CAM_ILL_MAX                        (0x0F4240U)
#define TYDC_CAM_ILL_INV                        (0x0FFFFFU)
