/*  U1      u1_g_oXDoCANDidSup_280C(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_280C(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1 u1_t_ret;
    U1 u1_t_sts;
    U1 u1_t_step;

    u1_t_step = (U1)0U;

    u1_t_sts = u1_g_HudImgAdjReadDataImgPos(&u1_t_step);
    u1_t_ret = (U1)OXDC_SAL_PROC_NR_31;

    if((u1_t_sts  == (U1)TRUE) &&
       ((u1_t_step <= 0x03U  ) || 
        (u1_t_step >= 0xFDU  ))){ 
            u1_ap_ans[0] = u1_t_step;
            u1_t_ret = (U1)OXDC_SAL_PROC_FIN;
    }
    else{
        /* OXDC_SAL_PROC_NR_31 */
    }

    return(u1_t_ret);
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANIocCsmchk_280C(const U1 u1_a_PRM, const U1 * u1_ap_CSM)                                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANIocCsmchk_280C(const U1 u1_a_PRM, const U1 * u1_ap_CSM)
{
    U1 u1_t_ret;
    U1 u1_t_rslt;

    u1_t_ret = (U1)OXDC_SAL_PROC_NR_31;

    if(u1_a_PRM == (U1)OXDC_IOC_PRM_CTE){
        if(u1_ap_CSM == vdp_PTR_NA){
            u1_t_rslt = (U1)TRUE;
        }
        else{
            u1_t_rslt = u1_s_TyDoCANIocCsmchkCTE_280C(u1_ap_CSM);
        }
        if(u1_t_rslt == (U1)TRUE){
            u1_t_ret = (U1)OXDC_SAL_PROC_RUN;
        }
    }
    else{
        u1_t_rslt = u1_s_TyDoCANIocCsmchkSTA_280C(u1_ap_CSM);
        if(u1_t_rslt == (U1)TRUE){
            u1_t_ret               = (U1)OXDC_SAL_PROC_RUN;
            u1_s_tydc_ioc_280C_csm = u1_ap_CSM[0];
        }
    }
    return(u1_t_ret);
}
