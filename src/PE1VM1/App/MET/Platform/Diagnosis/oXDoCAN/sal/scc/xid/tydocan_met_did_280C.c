/*===================================================================================================================================*/
/*  static U1   u1_s_TyDoCANIocCsmchkCTE_280C(const U1 * u1_ap_CSM)                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U1   u1_s_TyDoCANIocCsmchkCTE_280C(const U1 * u1_ap_CSM)
{
    U1  u1_t_ret;

    u1_t_ret = (U1)FALSE;

    /* Check controlMask on CTE is valid */
    if (u1_ap_CSM[0] == (U1)TYDOCAN_IOC_280C_CEM){
        u1_t_ret = (U1)TRUE;
    }
    return (u1_t_ret);
}
/*===================================================================================================================================*/
/*  static U1   u1_s_TyDoCANIocCsmchkSTA_280C(const U1 * u1_ap_CSM)                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U1   u1_s_TyDoCANIocCsmchkSTA_280C(const U1 * u1_ap_CSM)
{
    U1  u1_t_ret;

    u1_t_ret = (U1)FALSE;

    /* Check controlState/Mask on STA is valid */
    if (u1_ap_CSM[1] == (U1)TYDOCAN_IOC_280C_CEM){
        if((u1_ap_CSM[0] <= 0x03U) || (u1_ap_CSM[0] >= 0xFDU)){ 
            u1_t_ret = (U1)TRUE;
        }
    }
    return (u1_t_ret);
}
