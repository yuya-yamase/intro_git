/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRoutCorchk_1101(const U1 u1_a_SUBF, const U1 * u1_ap_COR)                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRoutCorchk_1101(const U1 u1_a_SUBF, const U1 * u1_ap_COR)
{
    U1            u1_t_proc;

    if((u1_a_SUBF    == (U1)OXDC_ROUT_SUBF_START) &&
       (u1_ap_COR[0] == (U1)TYDC_COR_DTE_RESET  )){
        u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
    }
    else{
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
    }

    return(u1_t_proc);
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRoutStart_1101(U1 * u1_ap_ans, const U2 u2_a_ELPSD, U2 * u2_ap_nbyte)                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRoutStart_1101(U1 * u1_ap_ans, const U2 u2_a_ELPSD, U2 * u2_ap_nbyte)
{
    u1_ap_ans[OXDC_ROUT_ANS_RINF_1101] = (U1)OXDC_1101_RINF;

    vd_g_DteAccmltClr();
    (void)u1_g_DteEdCgResetReq();
    return((U1)OXDC_SAL_PROC_FIN);
}
