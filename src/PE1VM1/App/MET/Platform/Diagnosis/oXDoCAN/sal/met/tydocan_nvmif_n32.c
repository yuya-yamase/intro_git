/*  U1      u1_g_TyDoCANNvmIfWriN32(const U1 * u1_ap_REQ, const U2 u2_a_DID)                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANNvmIfWriN32(const U1 * u1_ap_REQ, const U2 u2_a_DID)
{
    const ST_TYDC_NVM_IF *    st_tp_IF;

    U4                        u4_tp_b32_rx[TYDC_B32_NWORD];
    U4                        u4_t_nvm_n32;
    U4                        u4_t_max;

    U2                        u2_t_nbyte;
    U2                        u2_t_nid;

    U1                        u1_t_nvm_chk;
    U1                        u1_t_proc;

    st_tp_IF   = st_gp_TYDC_NVM_DID[u2_a_DID].stp_IF;
    u2_t_nbyte = st_gp_TYDC_NVM_DID[u2_a_DID].u2_nbyte;
    if((st_tp_IF            == vdp_PTR_NA            ) ||
       (st_tp_IF->fp_u1_REQ == vdp_PTR_NA            ) ||
       (u2_t_nbyte          >  (U2)TYDC_NVM_IF_NB_N32)){

        u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
    }
    else{

        u4_t_nvm_n32 = u4_g_TyDoCANNu32(u1_ap_REQ, u2_t_nbyte);
        if(st_tp_IF->fp_u4_MAX != vdp_PTR_NA){
            u4_t_max = (*st_tp_IF->fp_u4_MAX)(u2_a_DID);
        }
        else{
            u4_t_max = (U4)U4_MAX;
        }

        if(u4_t_nvm_n32 > u4_t_max){

            u1_t_proc = (U1)OXDC_SAL_PROC_NR_31;
        }
        else{

            u4_tp_b32_rx[TYDC_B32_WO_SU] = u4_t_max;
            u4_tp_b32_rx[TYDC_B32_WO_LA] = u4_t_nvm_n32;
            u1_t_proc = (*st_tp_IF->fp_u1_REQ)(u2_a_DID, &u4_tp_b32_rx[0]);
        }

        if(u1_t_proc == (U1)OXDC_SAL_PROC_RUN){

            u2_t_nid     = st_gp_TYDC_NVM_DID[u2_a_DID].u2_nid;
            u4_t_nvm_n32 = (U4)U4_MAX ^ u4_tp_b32_rx[TYDC_B32_WO_LA];
            u1_t_nvm_chk = u1_g_Nvmc_ReadU4withSts(u2_t_nid, &u4_t_nvm_n32);
            if((u1_t_nvm_chk == (U1)NVMC_STATUS_KIND_OK     ) &&
               (u4_t_nvm_n32 == u4_tp_b32_rx[TYDC_B32_WO_LA])){

                u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
            }
            else{
                vd_g_Nvmc_WriteU4(u2_t_nid, u4_tp_b32_rx[TYDC_B32_WO_LA]);
            }
        }
    }

    return(u1_t_proc);
}
