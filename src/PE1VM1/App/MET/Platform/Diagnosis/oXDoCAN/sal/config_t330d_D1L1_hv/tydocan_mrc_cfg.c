/*  U1      u1_g_TyDoCANMrcCfgVomSta(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANMrcCfgVomSta(void)
{
#ifdef ComConf_ComSignal_B_ST
    U1                     u1_t_rx_chk;
    U1                     u1_t_b_st;
    U1                     u1_t_ss;

    u1_t_b_st  = (U1)0U;
    u1_t_rx_chk = Com_GetIPDUStatus(MSG_ENG1G92_RXCH0) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);
#if 0   /* BEV BSW provisionally */
    (void)Com_ReceiveSignal(ComConf_ComSignal_B_ST, &u1_t_b_st);
#endif
    if((u1_t_rx_chk == (U1)0U) &&
       (u1_t_b_st   != (U1)0U)){

        u1_t_ss = (U1)TYDC_MRC_SS_BIT_STA;
    }
    else{
        u1_t_ss = (U1)0U;
    }

    return(u1_t_ss);
#else
    return((U1)0U);
#endif /* #ifdef ComConf_ComSignal_B_ST */
}
