/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANMrcCfgReschk(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANMrcCfgReschk(void)
{
    U4                     u4_t_reason;
    U4                     u4_t_bit;
    U1                     u1_t_ss;

    u4_t_reason = u4_g_EcuMMcurstReason();
    if(u4_t_reason == (U4)MCU_RESET_BY_EXT){
        u1_t_ss = (U1)TYDC_MRC_SS_BIT_EXT;
    }
    else{
        u1_t_ss = (U1)0U; 
    }

    u4_t_bit = u4_t_reason & ((U4)MCU_RESET_BY_WDG_1 | (U4)MCU_RESET_BY_WDG_0);
    if(u4_t_bit != (U4)0U){
        u1_t_ss |= (U1)TYDC_MRC_SS_BIT_WDG;
    }

    u4_t_bit = u4_t_reason & (U4)MCU_RESET_BY_ISOPWR;
    if(u4_t_bit != (U4)0U){
        u1_t_ss |= (U1)TYDC_MRC_SS_BIT_LVD;
    }

    return(u1_t_ss);
}
