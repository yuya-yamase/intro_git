/*  U1      u1_g_oXDoCANRebyId_1241(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1241(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U2           u2_t_0p01cel;
    U1           u1_t_sts_chk;

    u2_t_0p01cel = (U2)0U;
    u1_t_sts_chk = u1_g_PtsctmpCelInst(&u2_t_0p01cel) & ((U1)PTSCTMP_STSBIT_UNKNOWN | (U1)PTSCTMP_STSBIT_INVALID);

    if(u2_t_0p01cel >= (U2)PTSCTMP_CEL_OFFSET){
        u2_t_0p01cel -= (U2)PTSCTMP_CEL_OFFSET;
    }
    else{
        u2_t_0p01cel = (U2)0U;
    }

    if(u1_t_sts_chk != (U1)PTSCTMP_STSBIT_VALID){
        u1_ap_ans[0] = (U1)TYDC_PTSCTMP_0P5_UNK;
    }
    else if(u2_t_0p01cel >= (U2)TYDC_PTSCTMP_0P01_MAX){
        u1_ap_ans[0] = (U1)TYDC_PTSCTMP_0P5_MAX;
    }
    else{
        u1_ap_ans[0] = (U1)(u2_t_0p01cel / (U2)TYDC_PTSCTMP_0P5_CEL); 
    }

    return((U1)OXDC_SAL_PROC_FIN);
}
