/*  U1      u1_g_oXDoCANDidSup_1022(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANDidSup_1022(void)
{
    U1              u1_t_sup;

    u1_t_sup = u1_g_TyDoCANXidSup_Fuel((U1)TYDC_FUEL_CH_MA);
    if(u1_t_sup == (U1)TRUE){
        u1_t_sup = (U1)OXDC_XID_SUP_PUB;
    }
    else{
        u1_t_sup = (U1)OXDC_XID_SUP_NOT;
    }

    return(u1_t_sup);
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRebyId_1022(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_1022(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U2              u2_t_lit;

    u2_t_lit = u2_g_TyDoCANFuelLit32((U1)TYDC_FUEL_CH_MA);

    u1_ap_ans[TYDC_FUEL_LIT_ANS_HI] = (U1)((u2_t_lit >> (U2)TYDC_FUEL_LIT_LSB_HI) & (U2)U1_MAX);
    u1_ap_ans[TYDC_FUEL_LIT_ANS_LO] = (U1)(u2_t_lit & (U2)U1_MAX);

    return((U1)OXDC_SAL_PROC_FIN);
}
