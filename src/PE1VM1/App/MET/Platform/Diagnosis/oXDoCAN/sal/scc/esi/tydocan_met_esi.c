/*  U1      u1_g_oXDoCANEsiTRx_22FDC0XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANEsiTRx_22FDC0XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)
{
    U2                            u2_t_mem;
    U1                            u1_t_mid;
    U1                            u1_t_proc;

    u1_t_mid = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B3];
    if((u1_t_mid                        <  (U1)TYDC_MET_ESI_NUM_MEM) &&
       (fp_gp_u2_TYDC_ESI_MEM[u1_t_mid] != vdp_PTR_NA              )){

        u2_t_mem = (*fp_gp_u2_TYDC_ESI_MEM[u1_t_mid])();

        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B0] = (U1)TYDC_MET_ESI_ANS_22;
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B1] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B1];
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B2] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B2];
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3] = (U1)TYDC_MET_ESI_MEM_B3;
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B4] = (U1)(u2_t_mem >> TYDC_MET_ESI_LSB_B1);
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B5] = (U1)u2_t_mem;

        u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
    }
    else{
        u1_t_proc  = (U1)OXDC_SAL_PROC_NR_10;
    }

    return(u1_t_proc);
}
