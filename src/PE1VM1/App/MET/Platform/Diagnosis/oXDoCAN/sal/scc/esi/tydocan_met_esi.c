/*  U1      u1_g_oXDoCANEsiTRx_22FD11XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANEsiTRx_22FD11XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)
{
    U1                            u1_t_proc;
    U1                            u1_t_mt;

    u1_t_mt = u1_g_ESIMemTestResult((U1)ESI_MEM_TEST_SEQ_D1X, (U1)TRUE);
    if(u1_t_mt == (U1)ESI_MEM_TEST_UNK){

        u1_t_mt = u1_g_ESIMemTestStart((U1)ESI_MEM_TEST_SEQ_D1X);
        if(u1_t_mt == (U1)ESI_MEM_TEST_RUN){
            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3] = (U1)TYDC_MET_ESI_MT_INI;
            u1_t_proc                         = (U1)OXDC_SAL_PROC_FIN;
        }
        else{
            u1_t_proc                         = (U1)OXDC_SAL_PROC_NR_10;
        }
    }
    else if(u1_t_mt == (U1)ESI_MEM_TEST_RUN){
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3]     = (U1)TYDC_MET_ESI_MT_INI;
        u1_t_proc                             = (U1)OXDC_SAL_PROC_FIN;
    }
    else if(u1_t_mt == (U1)ESI_MEM_TEST_PAS){
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3]     = (U1)TYDC_MET_ESI_MT_PAS;
        u1_t_proc                             = (U1)OXDC_SAL_PROC_FIN;
    }
    else{
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3]     = (U1)TYDC_MET_ESI_MT_FAI;
        u1_t_proc                             = (U1)OXDC_SAL_PROC_FIN;
    }

    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B0]         = (U1)TYDC_MET_ESI_ANS_22;
    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B1]         = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B1];
    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B2]         = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B2];

    return(u1_t_proc);
}
