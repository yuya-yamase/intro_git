U1      u1_g_TyDoCANEsiTRx_22FDAAXX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)
{
    U1          u1_t_cnt;

    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B0] = (U1)TYDC_MET_ESI_ANS_22;
    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B1] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B1];
    u1_ap_ans_tx[TYDC_MET_ESI_TRX_B2] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B2];

    u1_t_cnt = u1_g_MechptrSMZpdRtrycnt();

    if(u1_t_cnt != (U1)0U){
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3] = (U1)TYDC_ESI_ZPD_RETRY_EXIST;
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B4] = u1_t_cnt;
    }
    else{
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3] = (U1)TYDC_ESI_ZPD_RETRY_NONE;
        u1_ap_ans_tx[TYDC_MET_ESI_TRX_B4] = (U1)0U;
    }

    return((U1)OXDC_SAL_PROC_FIN);
}
