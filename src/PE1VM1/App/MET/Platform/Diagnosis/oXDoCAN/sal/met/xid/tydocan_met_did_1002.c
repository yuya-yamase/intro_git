/*  U1      u1_g_oXDoCANDidSup_280A(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANDidSup_1002(void)
{
    U1  u1_t_mpxsw;
    U1  u1_t_sup;

    u1_t_mpxsw = u1_g_TyDoCANXidSup_MpxSw();

    if(u1_t_mpxsw == (U1)TRUE){
        u1_t_sup = (U1)OXDC_XID_SUP_NOT;
    }
    else{
        u1_t_sup = (U1)OXDC_XID_SUP_PUB;
    }

    return(u1_t_sup);
}
