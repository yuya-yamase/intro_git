/*  U1      u1_g_TyDoCANEsiTRx_22FDC1XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANEsiTRx_22FDC1XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)
{
    U4                            u4_t_trx;
    U4                            u4_t_rsl;
    U1                            u1_t_proc;

    if(u2_a_ELPSD == (U2)0U){
        
        u4_t_trx = u4_g_TyDoCANNu32(&u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B3], (U2)TYDC_MET_ESI_ETHR_RW_NB_RX);
        vd_g_LComSpiDsrUnlock((U4)LCOM_SPI_DSR_UNLOCK);
        vd_g_LComSpiDsrTx((U1)LCOM_SPI_DSR_BA_ETH, u4_t_trx);

        u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
    }
    else if(u2_a_ELPSD >= u2_s_TYDC_ETHR_RW_TOUT){
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
    }
    else{

        u4_t_trx = u4_g_LComSpiDsBA_EthrRWRslt();
        u4_t_rsl = u4_t_trx & (U4)LCOM_SPI_ETHR_RW_RSL_SU;
        if(u4_t_trx >= (U4)LCOM_SPI_ETHR_RW_UNK){
            u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
        }
        else if(u4_t_rsl == (U4)LCOM_SPI_ETHR_RW_RSL_SU){

            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B0] = (U1)TYDC_MET_ESI_ANS_22;
            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B1] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B1];
            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B2] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B2];

            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3] = (U1)TYDC_MET_ESI_ETHR_RW_OK;
            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B4] = (U1)(u4_t_trx >> TYDC_MET_ESI_LSB_B1);
            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B5] = (U1)u4_t_trx;

            u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
        }
        else{
            u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
        }
    }

    return(u1_t_proc);
}

/*===================================================================================================================================*/
/*  U1      u1_g_TyDoCANEsiTRx_2EFDC1XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_TyDoCANEsiTRx_2EFDC1XX(const U1 * u1_ap_REQ_RX, U1 * u1_ap_ans_tx, const U2 u2_a_ELPSD)
{
    U4                            u4_t_trx;
    U4                            u4_t_rsl;
    U1                            u1_t_proc;

    if(u2_a_ELPSD == (U2)0U){

        u4_t_trx  = u4_g_TyDoCANNu32(&u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B3], (U2)TYDC_MET_ESI_ETHR_RW_NB_RX);
        u4_t_trx |= (U4)LCOM_SPI_ETHR_RW_DIR_WR;
        vd_g_LComSpiDsrUnlock((U4)LCOM_SPI_DSR_UNLOCK);
        vd_g_LComSpiDsrTx((U1)LCOM_SPI_DSR_BA_ETH, u4_t_trx);

        u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
    }
    else if(u2_a_ELPSD >= u2_s_TYDC_ETHR_RW_TOUT){
        u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
    }
    else{

        u4_t_trx = u4_g_LComSpiDsBA_EthrRWRslt();
        u4_t_rsl = u4_t_trx & (U4)LCOM_SPI_ETHR_RW_RSL_SU;
        if(u4_t_trx >= (U4)LCOM_SPI_ETHR_RW_UNK){
            u1_t_proc = (U1)OXDC_SAL_PROC_RUN;
        }
        else if(u4_t_rsl == (U4)LCOM_SPI_ETHR_RW_RSL_SU){

            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B0] = (U1)TYDC_MET_ESI_ANS_2E;
            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B1] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B1];
            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B2] = u1_ap_REQ_RX[TYDC_MET_ESI_TRX_B2];
            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B3] = (U1)TYDC_MET_ESI_ETHR_RW_OK;
            u1_ap_ans_tx[TYDC_MET_ESI_TRX_B4] = (U1)0x00U;

            u1_t_proc = (U1)OXDC_SAL_PROC_FIN;
        }
        else{
            u1_t_proc = (U1)OXDC_SAL_PROC_NR_10;
        }
    }

    return(u1_t_proc);
}
