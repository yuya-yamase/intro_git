/*  void    u1_g_oXDoCANDtcTrchk_B1503(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      void                                                                                                             */
/*  Return:         OXDC_DTC_TR_INA  (0x00U)  : DTC INActive                                                                         */
/*                  OXDC_DTC_TR_ACT  (0x01U)  : DTC ACTive                                                                           */
/*                  OXDC_DTC_TR_N_A  (0x02U)  : DTC Not Applicable                                                                   */
/*===================================================================================================================================*/
U1    u1_g_oXDoCANDtcTrchk_B1503(const U2 u2_a_ELPSD)
{
    U2    u2_t_0p01cel;
    U1    u1_t_tmpret;

    u2_t_0p01cel = (U2)0U;
    u1_t_tmpret  = (u1_g_PtsctmpCelFltrd(&u2_t_0p01cel) & (U1)PTSCTMP_STSBIT_WTSW_DTCACT);

    if(u1_t_tmpret == (U1)PTSCTMP_STSBIT_WTSW_DTCACT){
        u1_t_tmpret = (U1)OXDC_DTC_TR_ACT;
    }
    else{
        u1_t_tmpret = (U1)OXDC_DTC_TR_INA;
    }

    return(u1_t_tmpret);
}
