#define TYDOCAN_280B_MAX_VAL                (1000U)     /* 100% */
#define TYDOCAN_280B_CSM_SIZE               (2U)
#define TYDOCAN_280B_CEM                    (0x80U)

#define TYDOCAN_280B_VAL_LSB                (50U)       /* 0.002% -> 0.1% */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Macro Definitions                                                                                                                */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
