/*===================================================================================================================================*/
void    vd_g_oXDoCANIocDeAct_280B(void)
{
    vd_g_HdimowdutyOwDeAct();
}
/*===================================================================================================================================*/
/*  static U1   u1_s_TyDoCANIocCsmchkCTE_280B(const U1 * u1_ap_CSM)                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U1   u1_s_TyDoCANIocCsmchkCTE_280B(const U1 * u1_ap_CSM)
{
    U1  u1_t_ret;

    u1_t_ret = (U1)FALSE;

    /* Check controlMask on CTE is valid */
    if (u1_ap_CSM[0] == (U1)TYDOCAN_280B_CEM){
        u1_t_ret = (U1)TRUE;
    }
    return (u1_t_ret);
}
/*===================================================================================================================================*/
/*  static U1   u1_s_TyDoCANIocCsmchkSTA_280B(const U1 * u1_ap_CSM)                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U1   u1_s_TyDoCANIocCsmchkSTA_280B(const U1 * u1_ap_CSM)
{
    U1  u1_t_ret;

    u1_t_ret = (U1)FALSE;

    /* Check controlMask on STA is valid */
    if (u1_ap_CSM[2] == (U1)TYDOCAN_280B_CEM){
        u1_t_ret = (U1)TRUE;
    }
    return (u1_t_ret);
}
