/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANRebyId_280B(U1 * u1_ap_ans, const U2 u2_a_ELPSD)                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANRebyId_280B(U1 * u1_ap_ans, const U2 u2_a_ELPSD)
{
    U1 u1_t_ret;
    U2 u2_t_pct;

    u2_t_pct = (U2)0;
    (void)u1_g_HdimmgrGetPrcnt((U1)HDIMMGR_OTPTSIG_OTPTDUTY, &u2_t_pct);
    u2_t_pct = ( u2_t_pct / (U2)TYDOCAN_280B_VAL_LSB);

    u1_ap_ans[0] = (U1)(u2_t_pct >> 8);
    u1_ap_ans[1] = (U1)(u2_t_pct & (U2)0x00FFU);

    u1_t_ret = (U1)OXDC_SAL_PROC_FIN;

    return(u1_t_ret);
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANIocCsmchk_280B(const U1 u1_a_PRM, const U1 * u1_ap_CSM)                                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANIocCsmchk_280B(const U1 u1_a_PRM, const U1 * u1_ap_CSM)
{
    U1 u1_t_ret;
    U1 u1_t_rslt;

    u1_t_ret = (U1)OXDC_SAL_PROC_NR_31;

    if(u1_a_PRM == (U1)OXDC_IOC_PRM_CTE){
        if(u1_ap_CSM == vdp_PTR_NA){
            u1_t_rslt = (U1)TRUE;
        }
        else{
            u1_t_rslt = u1_s_TyDoCANIocCsmchkCTE_280B(u1_ap_CSM);
        }
        if(u1_t_rslt == (U1)TRUE){
            u1_t_ret = (U1)OXDC_SAL_PROC_RUN;
        }
    }
    else{
        u1_t_rslt = u1_s_TyDoCANIocCsmchkSTA_280B(u1_ap_CSM);
        if(u1_t_rslt == (U1)TRUE){
            u1_t_ret                                   = (U1)OXDC_SAL_PROC_RUN;
            u1_s_tydc_ioc_280B_csm[0] = u1_ap_CSM[0];
            u1_s_tydc_ioc_280B_csm[1] = u1_ap_CSM[1];
        }
    }
    return(u1_t_ret);
}
/*===================================================================================================================================*/
/*  void    vd_g_oXDoCANIocAct_280B(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXDoCANIocAct_280B(void)
{
    U2 u2_t_val;

    /* Convert U1[] -> U2 */
    u2_t_val  = (U2)((U2)u1_s_tydc_ioc_280B_csm[0] << 8);
    u2_t_val |= (U2)(u1_s_tydc_ioc_280B_csm[1]);

    if(u2_t_val >= (U2)TYDOCAN_280B_MAX_VAL) {
        u2_t_val = (U2)TYDOCAN_280B_MAX_VAL;
    }

    vd_g_HdimowdutyOwUnlock((U4)HDIMOWDUTY_OW_UNLOCK);
    vd_g_HdimowdutyOwAct(u2_t_val);
}
/*===================================================================================================================================*/
