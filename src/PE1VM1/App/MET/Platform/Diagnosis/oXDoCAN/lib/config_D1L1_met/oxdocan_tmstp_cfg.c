/*  U2      u2_g_oXDoCANTmstpCfgTricRx(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U2      u2_g_oXDoCANTmstpCfgTricRx(void)
{
    U2                      u2_t_tric_rx;
    U1                      u1_t_rx_stat;

    u2_t_tric_rx = (U2)OXDC_TMSTP_TRIC_UNK;
    u1_t_rx_stat = Com_GetIPDUStatus((U2)MSG_BDB1F03_RXCH0) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);
    (void)Com_ReceiveSignal(ComConf_ComSignal_TRIP_CNT, &u2_t_tric_rx);
    if(u1_t_rx_stat != (U1)0U){
        u2_t_tric_rx = (U2)OXDC_TMSTP_TRIC_UNK;
    }

    return(u2_t_tric_rx);
}
/*===================================================================================================================================*/
/*  void    vd_g_oXDoCANTmstpCfgOdoRx(U4 * u4_ap_odo_0p1km)                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXDoCANTmstpCfgOdoRx(U4 * u4_ap_odo_0p1km)
{
    U4                      u4_t_odo_0p001km;
    U1                      u1_t_odo_stat;

    u4_t_odo_0p001km = (U4)0U;
    u1_t_odo_stat    = u1_g_OdoKm(&u4_t_odo_0p001km);

    if(u1_t_odo_stat == (U1)ODO_STSBIT_VALID){
        (*u4_ap_odo_0p1km) = u4_t_odo_0p001km / (U4)100U;   /* [0.001km] -> [0.1km] */
    }
    else{
        (*u4_ap_odo_0p1km) = (U4)OXDC_TMSTP_ODO_UNK;
    }
}
/*===================================================================================================================================*/
/*  U1      u1_g_oXDoCANTmstpCfgGmtRx(U1 * u1_ap_gmt)                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_oXDoCANTmstpCfgGmtRx(U1 * u1_ap_gmt)
{
#ifdef MSG_RSE1G20_RXCH0
    U1                      u1_t_rx_stat;
    U1                      u1_t_gmt_chk;
    U1                      u1_tp_rx[OXDC_TMSTP_GMT_RX_NBYTE];

    u1_t_rx_stat = Com_GetIPDUStatus((U2)MSG_RSE1G20_RXCH0) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);
    if(u1_t_rx_stat == (U1)0U){

        u1_t_gmt_chk = (U1)TRUE;
        (void)Com_ReadIPDU(MSG_RSE1G20_RXCH0, &u1_tp_rx[0]);
        u1_ap_gmt[OXDC_TMSTP_GMT_IDX_YE] = u1_tp_rx[OXDC_TMSTP_GMT_RX_YE];
        u1_ap_gmt[OXDC_TMSTP_GMT_IDX_MO] = u1_tp_rx[OXDC_TMSTP_GMT_RX_MO];
        u1_ap_gmt[OXDC_TMSTP_GMT_IDX_DA] = u1_tp_rx[OXDC_TMSTP_GMT_RX_DA];
        u1_ap_gmt[OXDC_TMSTP_GMT_IDX_HO] = u1_tp_rx[OXDC_TMSTP_GMT_RX_HO];
        u1_ap_gmt[OXDC_TMSTP_GMT_IDX_MI] = u1_tp_rx[OXDC_TMSTP_GMT_RX_MI];
        u1_ap_gmt[OXDC_TMSTP_GMT_IDX_SE] = u1_tp_rx[OXDC_TMSTP_GMT_RX_SE];
    }
    else{
        u1_t_gmt_chk = (U1)FALSE;
    }

    return(u1_t_gmt_chk);
#else
    return((U1)FALSE);
#endif
}
