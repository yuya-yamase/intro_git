/*  U1   u1_g_TyDoCANSecurityCfgGetSeed(const U1 u1_a_ID, const Dcm_OpStatusType OpStatus, U1 * u1_ap_seed)                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1   u1_g_TyDoCANSecurityCfgGetSeed(const U1 u1_a_ID, const Dcm_OpStatusType OpStatus, U1 * u1_ap_seed)
{
    U1                      u1_t_ret;

    switch(OpStatus)
    {
    case DCM_INITIAL:
        if((u1_a_ID >= (U1)OXDC_SECURITY_ID01) &&
           (u1_a_ID <= (U1)OXDC_SECURITY_ID02)){
            u1_t_ret = u1_g_TyDoCANSecurityAesSeedJobStat(st_sp_TYDC_SECURITY_KEYCFG[u1_a_ID - 1U].u4_key_adr);
        }
        else{
            u1_t_ret = (U1)E_NOT_OK;
        }
        break;
    case DCM_CANCEL:
        vd_g_TyDoCANSecurityAesSeedInit();
        u1_t_ret = (U1)E_OK;
        break;
    case DCM_PENDING:
        u1_t_ret = u1_g_TyDoCANSecurityAesSeedJobProc(u1_ap_seed);
        break;
    default:
        vd_g_TyDoCANSecurityAesSeedInit();
        u1_t_ret = (U1)E_NOT_OK;
        break;
    }

    return(u1_t_ret);
}
