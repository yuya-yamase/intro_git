/*  uint8   u1_g_oXDoCANAubIfSaSeed(const uint8 u1_a_ID, const Dcm_OpStatusType OpStatus, uint8 * u1_ap_seed, uint8 * u1_ap_err)     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
uint8   u1_g_oXDoCANAubIfSaSeed(const uint8 u1_a_ID, const Dcm_OpStatusType OpStatus, uint8 * u1_ap_seed, uint8 * u1_ap_err)
{
    U1                       u1_t_ret;

    if(u1_ap_seed != vdp_PTR_NA){
        u1_t_ret = (U1)E_OK;

        if(OpStatus == DCM_INITIAL){
            u1_t_ret = u1_g_oXDoCANSecurityAccessChk((U1)OXDC_SEC_KIND_SEED, u1_a_ID);
            if(u1_t_ret != (U1)E_OK){
                *u1_ap_err = (U1)DCM_E_CONDITIONSNOTCORRECT;      /* NRC 0x22 */
            }
        }

        if(u1_t_ret == (U1)E_OK){
            u1_t_ret = u1_g_TyDoCANSecurityCfgGetSeed(u1_a_ID, OpStatus, u1_ap_seed);
            if(u1_t_ret == (U1)E_NOT_OK){
                *u1_ap_err = (U1)DCM_E_CONDITIONSNOTCORRECT;      /* NRC 0x22 */
            }
        }
    }
    else{
        u1_t_ret   = (U1)E_NOT_OK;
        *u1_ap_err = (U1)DCM_E_CONDITIONSNOTCORRECT;              /* NRC 0x22 */
    }

    return(u1_t_ret);
}
/*===================================================================================================================================*/
/*  uint8   u1_g_oXDoCANAubIfSaKeyOk(const uint8 u1_a_ID, const uint8 * u1_ap_KEY, uint8 * u1_ap_err)                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:                                                                                                                       */
/*  Return:                                                                                                                          */
/*===================================================================================================================================*/
uint8   u1_g_oXDoCANAubIfSaKeyOk(const uint8 u1_a_ID, const uint8 * u1_ap_KEY, uint8 * u1_ap_err)
{
    U1                       u1_t_ret;

    if(u1_ap_KEY != vdp_PTR_NA){
        u1_t_ret = u1_g_oXDoCANSecurityAccessChk((U1)OXDC_SEC_KIND_KEY, u1_a_ID);
        if(u1_t_ret == (U1)E_OK){
            u1_t_ret = u1_g_TyDoCANSecurityCfgCompareKey(u1_a_ID, u1_ap_KEY);
            if(u1_t_ret != (U1)E_OK){
                *u1_ap_err = (U1)DCM_E_INVALIDKEY;                /* NRC 0x35 */
            }
        }
        else{
            *u1_ap_err = (U1)DCM_E_CONDITIONSNOTCORRECT;          /* NRC 0x22 */
        }
    }
    else{
        u1_t_ret   = (U1)E_NOT_OK;
        *u1_ap_err = (U1)DCM_E_CONDITIONSNOTCORRECT;              /* NRC 0x22 */
    }

    return(u1_t_ret);
}
