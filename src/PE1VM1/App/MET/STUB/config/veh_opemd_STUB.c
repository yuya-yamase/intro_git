/*  U4      u4_g_VehopemdMdfield(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_VehopemdConvertMdfield(void)
{
    U4 u4_t_mdbit;
    U4 u4_t_convbit;

    u4_t_mdbit = u4_g_VehopemdMdfield();
    u4_t_convbit = (U4)0U;

    if ((u4_t_mdbit & (U4)VEH_OPEMD_MDBIT_ACC) != (U4)0U){
        u4_t_convbit |= (U4)VEH_OPEMD_STUB_MDBIT_ACC;
    }
    if ((u4_t_mdbit & (U4)VEH_OPEMD_MDBIT_IGN) != (U4)0U){
        u4_t_convbit |= (U4)VEH_OPEMD_STUB_MDBIT_IGN;
    }
    if ((u4_t_mdbit & (U4)VEH_OPEMD_MDBIT_STA) != (U4)0U){
        u4_t_convbit |= (U4)VEH_OPEMD_STUB_MDBIT_STA;
    }
    if ((u4_t_mdbit & (U4)VEH_OPEMD_MDBIT_PBA) != (U4)0U){
        u4_t_convbit |= (U4)VEH_OPEMD_STUB_MDBIT_BA;
    }
    if ((u4_t_mdbit & (U4)VEH_OPEMD_MDBIT_IGNP) != (U4)0U){
        u4_t_convbit |= (U4)VEH_OPEMD_STUB_MDBIT_IGNP;
    }
    if ((u4_t_mdbit & (U4)VEH_OPEMD_MDBIT_RDY) != (U4)0U){
        u4_t_convbit |= (U4)VEH_OPEMD_STUB_MDBIT_RDY;
    }
    if ((u4_t_mdbit & (U4)VEH_OPEMD_MDBIT_TST) != (U4)0U){
        u4_t_convbit |= (U4)VEH_OPEMD_STUB_MDBIT_TST;
    }
    
    return(u4_t_convbit);
}