/*  void    vd_g_CanTxAppMET1S70_Init(void)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CanTxAppMET1S70_Init(void)
{
    U4                 u4_t_loop;

    for(u4_t_loop = (U4)0U; u4_t_loop < (U4)CANTXAPP_NBYTE_PAYLOAD8; u4_t_loop++){
        u4_sp_cantxapp_met1s70data[u4_t_loop] = (U4)0x00U;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_CanTxAppMET1S70_Send(void)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CanTxAppMET1S70_Send(void)
{
    vd_s_CanTxAppSend_LCASGAPR();
    vd_s_CanTxAppSend_PKSSVOL();
    vd_s_CanTxAppSend_PLUSSW();
    vd_s_CanTxAppSend_LTASSWOP();
    vd_s_CanTxAppSend_STRSWAP();
    vd_s_CanTxAppSend_ADSEGDSW();
    vd_s_CanTxAppSend_AECOSWBB();
    vd_s_CanTxAppSend_METMDSEL();
    vd_s_CanTxAppSend_TRLC_YN();
}
/*===================================================================================================================================*/
/*  void    vd_g_CanTxAppMET1S70_Put(const U4 * u4_ap_pck_rx, const U1 u1_a_BUFSIZE)                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u4_ap_pck_rx: xspi buffer                                                                                        */
/*                  u1_a_BUFSIZE: buffer size                                                                                        */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CanTxAppMET1S70_Put(const U4 * u4_ap_pck_rx, const U1 u1_a_BUFSIZE)
{
    U4                 u4_t_loop;

    for(u4_t_loop = (U4)0U; u4_t_loop < (U4)u1_a_BUFSIZE; u4_t_loop++){
        u4_sp_cantxapp_met1s70data[u4_t_loop] = u4_ap_pck_rx[u4_t_loop];
    }
}
