/*  void    vd_g_CanTxAppMET1S02_Init(void)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CanTxAppMET1S02_Init(void)
{
    U4                 u4_t_loop;

    for(u4_t_loop = (U4)0U; u4_t_loop < (U4)CANTXAPP_NBYTE_PAYLOAD2; u4_t_loop++){
        u4_sp_cantxapp_met1s02data[u4_t_loop] = (U4)0U;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_CanTxAppMET1S02_Send(void)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CanTxAppMET1S02_Send(void)
{
    vd_s_CanTxAppSend_HCPCLOP();
    vd_s_CanTxAppSend_HCSUPEN2();
    vd_s_CanTxAppSend_HCSDWEN2();
    vd_s_CanTxAppSend_HCSUPEN();
    vd_s_CanTxAppSend_HCSDWEN();
    vd_s_CanTxAppSend_HCSMNSW();
    vd_s_CanTxAppSend_HCSSTPOP();
}
/*===================================================================================================================================*/
/*  void    vd_g_CanTxAppMET1S02_Put(const U4 * u4_ap_pck_rx, const U1 u1_a_BUFSIZE)                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u4_ap_pck_rx: xspi buffer                                                                                        */
/*                  u1_a_BUFSIZE: buffer size                                                                                        */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CanTxAppMET1S02_Put(const U4 * u4_ap_pck_rx, const U1 u1_a_BUFSIZE)
{
    U4                 u4_t_loop;

    for(u4_t_loop = (U4)0U; u4_t_loop < (U4)u1_a_BUFSIZE; u4_t_loop++){
        if(u1_a_BUFSIZE <= (U1)CANTXAPP_NBYTE_PAYLOAD2){
            u4_sp_cantxapp_met1s02data[u4_t_loop] = u4_ap_pck_rx[u4_t_loop];
        }
    }
}
/*===================================================================================================================================*/
/*  static void    vd_s_CanTxAppSend_HCPCLOP(void)                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*                                                                                                                                   */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void    vd_s_CanTxAppSend_HCPCLOP(void)
{
    U1                 u1_t_tx;
    U1                 u1_t_pre_tx;

    u1_t_tx = u1_CANTXAPP_MET_READ_BIT(u4_sp_cantxapp_met1s02data[0], (U1)CANTXAPP_POS_1_7, (U1)CANTXAPP_LEN_1);
    u1_t_pre_tx = (U1)0U;

    (void)Com_ReceiveSignal(ComConf_ComSignal_HCPCLOP, &u1_t_pre_tx);
    (void)Com_SendSignal(ComConf_ComSignal_HCPCLOP, &u1_t_tx);

    if(u1_t_pre_tx != u1_t_tx){
        (void)Com_TriggerIPDUSend(MSG_MET1S02_TXCH0);
    }
}
