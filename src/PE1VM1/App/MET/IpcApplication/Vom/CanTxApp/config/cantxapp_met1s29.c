/*  void    vd_g_CanTxAppMET1S29_Init(void)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CanTxAppMET1S29_Init(void)
{
    U4                 u4_t_loop;

    for(u4_t_loop = (U4)0U; u4_t_loop < (U4)CANTXAPP_NBYTE_PAYLOAD2; u4_t_loop++){
        u4_sp_cantxapp_met1s29data[u4_t_loop] = (U4)0U;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_CanTxAppMET1S29_Send(void)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CanTxAppMET1S29_Send(void)
{
    vd_s_CanTxAppSend_WVRU_BB();
    vd_s_CanTxAppSend_WVRD_BB();
    vd_s_CanTxAppSend_WRSW_BB();
    vd_s_CanTxAppSend_WROF_BB();
    vd_s_CanTxAppSend_WRIN_BB();
    vd_s_CanTxAppSend_WRLO_BB();
}
/*===================================================================================================================================*/
/*  void    vd_g_CanTxAppMET1S29_Put(const U4 * u4_ap_pck_rx, const U1 u1_a_BUFSIZE)                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u4_ap_pck_rx: xspi buffer                                                                                        */
/*                  u1_a_BUFSIZE: buffer size                                                                                        */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_CanTxAppMET1S29_Put(const U4 * u4_ap_pck_rx, const U1 u1_a_BUFSIZE)
{
    U4                 u4_t_loop;

    for(u4_t_loop = (U4)0U; u4_t_loop < (U4)u1_a_BUFSIZE; u4_t_loop++){
        if(u1_a_BUFSIZE <= (U1)CANTXAPP_NBYTE_PAYLOAD2){
            u4_sp_cantxapp_met1s29data[u4_t_loop] = u4_ap_pck_rx[u4_t_loop];
        }
    }
}
