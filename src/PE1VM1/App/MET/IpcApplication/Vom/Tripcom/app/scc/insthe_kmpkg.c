/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_InstHeUpdt(const U1 u1_a_CNTTID)
{
    vd_g_InstEconUpdt(&st_s_INSTHE_CNTTS_CFG, (U1)INSTECON_ENGYTYPE_HYDR);
}

/*===================================================================================================================================*/
/* U1              u1_g_InstHeKmpkg(U4 * u4p_a_kmpl)                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1              u1_g_InstHeKmpkg(U4 * u4p_a_kmpkg)
{
    if (u4p_a_kmpkg != vdp_PTR_NA) {
        (*u4p_a_kmpkg) = u4_g_TripcomMsGetAccmltVal(st_s_INSTHE_CNTTS_CFG.u1_ms_dspval_id);
    }
    return (st_s_insthe_var.u1_status);
}

/*===================================================================================================================================*/
/* U2              u2_g_InstHeCalcTx(const U1 u1_a_CNTTID, const U1 u1_a_UNIT)                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U2              u2_g_InstHeCalcTx(const U1 u1_a_CNTTID, const U1 u1_a_UNIT)
{
    U2          u2_t_txval;


    if ((st_s_insthe_var.u1_status & (U1)TRIPCOM_STSBIT_INVALID) != (U1)0U) {
        u2_t_txval = (U2)TRIPCOM_CANTX_TIMEOUT;
    }
    else {
        u2_t_txval = u2_g_InstEconCalcTx(&st_s_INSTHE_CNTTS_CFG, st_s_insthe_var.u1_calcstsbit, u1_a_UNIT, (U1)INSTECON_ENGYTYPE_HYDR);
    }

    return (u2_t_txval);
}

