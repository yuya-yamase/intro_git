/*===================================================================================================================================*/
/* U1              u1_g_InstHeCalcTrnst(const U1 u1_a_CNTTID, const U2 * u2_ap_STSFIELD)                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1              u1_g_InstHeCalcTrnst(const U1 u1_a_CNTTID, const U2 * u2_ap_STSFIELD)
{
    U1          u1_t_update;
    U1          u1_t_snsrsts;
    U2          u2_t_jdgbit;


    u1_t_snsrsts = (U1)TRIPCOM_STSBIT_VALID;
    u2_t_jdgbit = u2_ap_STSFIELD[TRIPCOM_STSFIELD_SNSR] & ((U2)TRIPCOM_SNSRBIT_ODOCNT_UNKNOWN | (U2)TRIPCOM_SNSRBIT_HYDRUSD_UNKNOWN);
    if (u2_t_jdgbit != (U2)0U) {
        u1_t_snsrsts  = (U1)TRIPCOM_STSBIT_UNKNOWN;
    }
    u2_t_jdgbit = u2_ap_STSFIELD[TRIPCOM_STSFIELD_SNSR] & ((U2)TRIPCOM_SNSRBIT_ODOCNT_INVALID | (U2)TRIPCOM_SNSRBIT_HYDRUSD_INVALID);
    if (u2_t_jdgbit != (U2)0U) {
        u1_t_snsrsts |= (U1)TRIPCOM_STSBIT_INVALID;
    }
    u1_s_insthe_prevsts = st_s_insthe_var.u1_status;

    u1_t_update = u1_g_InstEconCalcTrnst(&st_s_INSTHE_CNTTS_CFG, &st_s_insthe_var, u2_ap_STSFIELD, u1_t_snsrsts);

    return (u1_t_update);
}

/*===================================================================================================================================*/
/* void            vd_g_InstHeAccmlt(const U1 u1_a_CNTTID, const U2 * u2_ap_STSFIELD, const U4 * u4_ap_DELTA)                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_InstHeAccmlt(const U1 u1_a_CNTTID, const U2 * u2_ap_STSFIELD, const U4 * u4_ap_DELTA)
{
    U4          u4_t_hydrusd;
    U4          u4_t_odocnt;


    u4_t_hydrusd = u4_ap_DELTA[TRIPCOM_DELTA_DC_HYDRUSD_PC];
    u4_t_odocnt  = u4_ap_DELTA[TRIPCOM_DELTA_ANY_ODOCNT_PC];

    vd_g_InstEconAccmlt(&st_s_INSTHE_CNTTS_CFG, st_s_insthe_var.u1_ocinit, u4_t_hydrusd, u4_t_odocnt);
}

/*===================================================================================================================================*/
/* void            vd_g_InstHeUpdt(const U1 u1_a_CNTTID)                                                                             */
