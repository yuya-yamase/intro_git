/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1              u1_g_SaveFsCalcTrnst(const U1 u1_a_CNTTID, const U2 * u2_ap_STSFIELD)
{
    return ((U1)TRUE);
}

/*===================================================================================================================================*/
/* void            vd_g_SaveFsApplTask(const U2 * u2_ap_STSFIELD)                                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_SaveFsApplTask(const U2 * u2_ap_STSFIELD)
{
    U2          u2_t_jdgbit;


    u1_s_savefs_applsts = (U1)TRIPCOM_STSBIT_VALID;
    u2_t_jdgbit  =  u2_ap_STSFIELD[TRIPCOM_STSFIELD_VSTS] & (U2)TRIPCOM_VEHSTS_ECORUNUNK;
    u2_t_jdgbit |= (u2_ap_STSFIELD[TRIPCOM_STSFIELD_SNSR] & (U2)TRIPCOM_SNSRBIT_ELPSDTM_UNKNOWN);
    if (u2_t_jdgbit != (U2)0U) {
        u1_s_savefs_applsts  = (U1)TRIPCOM_STSBIT_UNKNOWN;
    }
    u2_t_jdgbit  =  u2_ap_STSFIELD[TRIPCOM_STSFIELD_VSTS] & (U2)TRIPCOM_VEHSTS_ECORUNINV;
    u2_t_jdgbit |= (u2_ap_STSFIELD[TRIPCOM_STSFIELD_SNSR] & (U2)TRIPCOM_SNSRBIT_ELPSDTM_INVALID);
    if (u2_t_jdgbit != (U2)0U) {
        u1_s_savefs_applsts |= (U1)TRIPCOM_STSBIT_INVALID;
    }
}

/*===================================================================================================================================*/
/* void            vd_g_SaveFsUpdt(const U1 u1_a_CNTTID)                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_SaveFsUpdt(const U1 u1_a_CNTTID)
{
    static const U1 u1_s_SAVEFSRA_RES = (U1)0U;
    const ST_SAVEFS_CNTT *                      stp_t_CNTT;
    U4                                          u4_t_second;
    U4                                          u4_t_ml;
    U4                                          u4_t_ratio;


    if (u1_a_CNTTID < (U1)SAVEFS_NUM_CNTTS) {
        stp_t_CNTT  = &st_sp_SAVEFS_CNTTS_CFG[u1_a_CNTTID];

        u4_t_second = u4_g_TripcomMsGetAccmltVal(stp_t_CNTT->u1_ms_ecostptm_id);
        u4_t_ratio = u1_s_SAVEFSRA_RES;
        u4_t_ml     = u4_g_TripcomCalcSavefsMl(u4_t_second, u4_t_ratio, (U4)TRIPCOM_SAVEFSRA_RES);

        vd_g_TripcomMsSetAccmltVal(stp_t_CNTT->u1_ms_fuelsupply_id, u4_t_ml);
    }
}

/*===================================================================================================================================*/
/* U1              u1_g_SaveFsMl(const U1 u1_a_SAVE_FS_CH, U4 * u4p_a_ml)                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1              u1_g_SaveFsMl(const U1 u1_a_SAVE_FS_CH, U4 * u4p_a_ml)
{
    U1          u1_t_status;


    u1_t_status = (U1)TRIPCOM_STSBIT_INVALID;
    if (u1_a_SAVE_FS_CH < (U1)SAVEFS_NUM_CNTTS) {
        if (u4p_a_ml != vdp_PTR_NA) {
            (*u4p_a_ml) = u4_g_TripcomMsGetAccmltVal(st_sp_SAVEFS_CNTTS_CFG[u1_a_SAVE_FS_CH].u1_ms_fuelsupply_id);
        }
        u1_t_status = u1_s_savefs_applsts;
    }

    return (u1_t_status);
}

