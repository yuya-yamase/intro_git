static U4  u4_s_odo_vslmt_km_inst;                 /*  1/5096km        */
static U2  u2_s_odo_vslmt_tim_elpsd;               /*  1 seconds timer */
static U1  u1_s_odo_vslmt_incr_en;                 /*  TRUE/FALSE      */

