/*===================================================================================================================================*/
U1      u1_g_AttmpCfgRxCnt(void)
{
#ifdef OXCAN_PDU_RX_CAN_ECT1G92_RXCH0
    return(u1_g_oXCANRxEvcnt((U2)OXCAN_PDU_RX_CAN_ECT1G92_RXCH0));
#else
    return((U1)OXCAN_RX_RXEV_CNT_UNK);
#endif
}

