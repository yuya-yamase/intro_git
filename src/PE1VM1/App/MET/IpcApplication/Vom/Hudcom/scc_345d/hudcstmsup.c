static void         vd_s_HudCstmdTachdspChk(void);
static U1           u1_s_HudCstmCstmzCal(U1 u1_sig);
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  HUD カスタマイズ表示用変数初期化処理                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void        vd_g_HudCstmStsInit(void)
{
    st_s_hudsts.u1_aud = (U1)0;
    st_s_hudsts.u1_lane = (U1)0;
    st_s_hudsts.u1_nav = (U1)0;
    st_s_hudsts.u1_ahs = (U1)0;

    st_s_hudsts.u1_cmp = (U1)0;
    st_s_hudsts.u1_blk = (U1)0;
    st_s_hudsts.u1_hvsys = (U1)0;
    st_s_hudsts.u1_ecodrv = (U1)0;

    st_s_hudsts.u1_tacho = (U1)0;
    st_s_hudsts.u1_onoff = (U1)0;
    st_s_hudsts.u1_pobrt = (U1)0;
    st_s_hudsts.u1_rotat = (U1)0;
}

/*===================================================================================================================================*/
/*  定期処理                                                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void        vd_g_HudCstmStsRoutine(void)
{
    U1          u1_sup;
    U1          u1_t_sig;
    U1          u1_t_disp;
    U1          u1_t_igon;
    U1          u1_t_msgsts;

    u1_sup = u1_HUDCSTMSUP_CFG_HUD_IS_SUPD();
    if(u1_sup == (U1)TRUE){
        u1_t_msgsts = u1_HUDCSTMSUP_CFG_CANMSGSTS_HUD1S01();
        u1_t_msgsts &= (U1)(HUDCSTMSUP_ST_TIMEOUT | HUDCSTMSUP_ST_NO_RX);
        if(u1_t_msgsts == (U1)0){
            vd_g_HudCstmStsInit();
            vd_s_HudCstmAdjustChk();
            u1_t_sig = u1_HUDCSTMSUP_CFG_HUDSW_DC();
            u1_t_disp = u1_s_HudCstmCstmzCal(u1_t_sig);
            if(u1_t_disp == (U1)TRUE){
                vd_s_HudCstmCstmzChk();
                vd_s_HudCstmdTachdspChk();
            }
        }
    }
    else{
        vd_g_HudCstmStsInit();
    }
}

/*===================================================================================================================================*/
/*  HUD Adjustment Customize                                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void vd_s_HudCstmAdjustChk(void)
{
    U1          u1_t_sig;

    /* ON/OFF                     */
    st_s_hudsts.u1_onoff = u1_HUDCSTMSUP_CFG_HUDSW_PW();
    /* Position & Brightness      */
    st_s_hudsts.u1_pobrt = u1_HUDCSTMSUP_CFG_HUDSW_PS();
    /* Rotation correction        */
    u1_t_sig = u1_HUDCSTMSUP_CFG_HUDSW_RT();
    st_s_hudsts.u1_rotat = u1_s_HudCstmCstmzCal(u1_t_sig);
}

/*===================================================================================================================================*/
/*  HUD 表示ON/OFFカスタマイズ情報作成処理                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void vd_s_HudCstmCstmzChk(void)
{
    st_s_hudsts.u1_aud = u1_HUDCSTMSUP_CFG_CTAUDEXT();
    st_s_hudsts.u1_lane = u1_HUDCSTMSUP_CFG_CTLANEXT();
    st_s_hudsts.u1_nav = u1_HUDCSTMSUP_CFG_CTNAVEXT();
    st_s_hudsts.u1_ahs = u1_HUDCSTMSUP_CFG_CTADSEXT();
    st_s_hudsts.u1_cmp = u1_HUDCSTMSUP_CFG_CTCMPEXT();
}

/*===================================================================================================================================*/
/*  HUD Tachometer Settings                                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void vd_s_HudCstmdTachdspChk(void)
{
    U1          u1_t_tachdsp;

    u1_t_tachdsp = u1_HUDCSTMSUP_CFG_TACH_DSP();

    switch(u1_t_tachdsp){
        case (U1)HUDCSTMSUP_CFG_TACHDSP_ECO_ON:
            st_s_hudsts.u1_blk = (U1)TRUE;
            st_s_hudsts.u1_tacho = (U1)TRUE;
            st_s_hudsts.u1_ecodrv = (U1)TRUE;
            break;
        case (U1)HUDCSTMSUP_CFG_TACHDSP_HV_ON:
            st_s_hudsts.u1_blk = (U1)TRUE;
            st_s_hudsts.u1_tacho = (U1)TRUE;
            st_s_hudsts.u1_hvsys = (U1)TRUE;
            break;
        case (U1)HUDCSTMSUP_CFG_TACHDSP_OFF:
        default:
            /* Do Nothing */
            break;
    }
}

/*===================================================================================================================================*/
/*  HUD 表示ON/OFFカスタマイズ情報有無判定処理                                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U1   u1_s_HudCstmCstmzCal(U1 u1_sig)
{
    U1          u1_ret;

    switch(u1_sig){
        case (U1)HUDCSTMSUP_CFG_STAT_ON:
        case (U1)HUDCSTMSUP_CFG_STAT_ON_GOUT:
            u1_ret = (U1)TRUE;
            break;
        case (U1)HUDCSTMSUP_CFG_STAT_UNDEF:
        case (U1)HUDCSTMSUP_CFG_STAT_NOUSE:
        default:
            u1_ret = (U1)FALSE;
            break;
    }
    return(u1_ret);
}

