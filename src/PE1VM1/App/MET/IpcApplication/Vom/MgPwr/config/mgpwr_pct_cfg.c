/*===================================================================================================================================*/
/*  U1      u1_g_MgPwrCfgComRxMGPWRMET(S1 * const s1p_a_mgpwrmet)                                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      U1 * const s1p_a_mgpwrmet : Receiving Value                                                                      */
/*  Return:         U1         u1_t_stsbit    : Receiving Status                                                                     */
/*===================================================================================================================================*/
U1      u1_g_MgPwrCfgComRxMGPWRMET(S1 * const s1p_a_mgpwrmet)
{
#if defined(ComConf_ComSignal_MGPWRMET)
    U1                  u1_t_stsbit;                                            /* Receiving Status                                  */

    u1_t_stsbit = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_EHV1F04_RXCH0,
                                        (U2)(OXCAN_RX_SYS_NRX_POE | OXCAN_RX_SYS_TOE_POE),
                                        (U2)MGPWR_EHV1F04_TO_THRSH) & (U1)(COM_TIMEOUT | COM_NO_RX);

    (void)Com_ReceiveSignal(ComConf_ComSignal_MGPWRMET, s1p_a_mgpwrmet);

    return(u1_t_stsbit);
#else
    return((U1)MGPWR_STSBIT_UNKNOWN);
#endif
}

/*===================================================================================================================================*/
/*  U1      u1_g_MgPwrCfgComRxEvcnt(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_MgPwrCfgComRxEvcnt(void)
{
#if defined(OXCAN_PDU_RX_CAN_EHV1F04_RXCH0)
    U1                  u1_t_rxev_cnt;

    u1_t_rxev_cnt = u1_g_oXCANRxEvcnt((U2)OXCAN_PDU_RX_CAN_EHV1F04_RXCH0);

    return(u1_t_rxev_cnt);
#else
    return((U1)OXCAN_RX_RXEV_CNT_UNK);
#endif
}

