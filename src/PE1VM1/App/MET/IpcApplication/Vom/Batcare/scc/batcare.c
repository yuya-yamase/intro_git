/*  void    vd_g_BatcareBonInit(void)                                                                                                */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Arguments:  -                                                                                                                    */
/*  Return   :  -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_BatcareBonInit(void)
{
    u1_s_batcare_bsoce     = (U1)BATCARE_BSOCE_INIT;
    u2_s_batcare_bsoh      = (U2)BATCARE_BSOH_INIT;
    vd_g_Batcare_RimU1Write((U2)BATCARE_RIMID_U1_BSOCE, (U1)BATCARE_BSOCE_INIT);
    vd_g_Batcare_RimU2Write((U2)BATCARE_RIMID_U2_BSOH,  (U2)BATCARE_BSOH_INIT);
}

/*===================================================================================================================================*/
/*  void    vd_g_BatcareRstwkInit(void)                                                                                              */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Arguments:  -                                                                                                                    */
/*  Return   :  -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_BatcareRstwkInit(void)
{
    U1                          u1_t_bsoce;
    U2                          u2_t_bsoh;
    U1                          u1_t_bsoce_rimsts;
    U1                          u1_t_bsoh_rimsts;

    u1_t_bsoce         = (U1)BATCARE_BSOCE_INIT;
    u2_t_bsoh          = (U2)BATCARE_BSOH_INIT;
    u1_t_bsoce_rimsts  = u1_g_Batcare_RimU1Read((U2)BATCARE_RIMID_U1_BSOCE, &u1_t_bsoce);
    u1_t_bsoh_rimsts   = u1_g_Batcare_RimU2Read((U2)BATCARE_RIMID_U2_BSOH,  &u2_t_bsoh);

    if((u1_t_bsoce_rimsts & (U1)BATCARE_RIM_STATUS_MASK) == (U1)BATCARE_RIM_READ_OK) {
        u1_s_batcare_bsoce = u1_t_bsoce;
    }
    else{
        u1_s_batcare_bsoce = (U1)BATCARE_BSOCE_INIT;
    }

    if((u1_t_bsoh_rimsts & (U1)BATCARE_RIM_STATUS_MASK) == (U1)BATCARE_RIM_READ_OK) {
        u2_s_batcare_bsoh  = u2_t_bsoh;
    }
    else{
        u2_s_batcare_bsoh  = (U2)BATCARE_BSOH_INIT;
    }
}

/*===================================================================================================================================*/
/*  void     vd_g_BatcareMainTask(void)                                                                                              */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Arguments:  -                                                                                                                    */
/*  Return   :  -                                                                                                                    */
/*===================================================================================================================================*/
void    vd_g_BatcareMainTask(void)
{
    U1                          u1_t_bsoce_pre;
    U2                          u2_t_bsoh_pre;
    U1                          u1_t_bsoce_rimsts;
    U1                          u1_t_bsoh_rimsts;
    U1                          u1_t_sts;

    u1_t_bsoce_pre    = (U1)BATCARE_BSOCE_INIT;
    u2_t_bsoh_pre     = (U2)BATCARE_BSOH_INIT;
    u1_t_bsoce_rimsts = u1_g_Batcare_RimU1Read((U2)BATCARE_RIMID_U1_BSOCE, &u1_t_bsoce_pre);
    u1_t_bsoh_rimsts  = u1_g_Batcare_RimU2Read((U2)BATCARE_RIMID_U2_BSOH,  &u2_t_bsoh_pre);
    u1_t_sts          = u1_g_BatcareStatus() & ((U1)BATCARE_STSBIT_UNKNOWN | (U1)BATCARE_STSBIT_INVALID);

    if((u1_t_bsoce_rimsts & (U1)BATCARE_RIM_STATUS_MASK) != (U1)BATCARE_RIM_READ_OK) {
        u1_t_bsoce_pre = (U1)BATCARE_BSOCE_INIT;
    }

    if((u1_t_bsoh_rimsts & (U1)BATCARE_RIM_STATUS_MASK) != (U1)BATCARE_RIM_READ_OK) {
        u2_t_bsoh_pre  = (U2)BATCARE_BSOH_INIT;
    }

    if(u1_t_sts == (U1)BATCARE_STSBIT_VALID){
        vd_g_BatcareBSOCE(&u1_s_batcare_bsoce);
        if(u1_s_batcare_bsoce != u1_t_bsoce_pre){
            vd_g_Batcare_RimU1Write((U2)BATCARE_RIMID_U1_BSOCE, u1_s_batcare_bsoce);
        }

        vd_g_BatcareBSOH(&u2_s_batcare_bsoh);
        if(u2_s_batcare_bsoh != u2_t_bsoh_pre){
            vd_g_Batcare_RimU2Write((U2)BATCARE_RIMID_U2_BSOH, u2_s_batcare_bsoh);
        }
    }
    else{
        u1_s_batcare_bsoce = u1_t_bsoce_pre;
        u2_s_batcare_bsoh  = u2_t_bsoh_pre;
    }
}

