/*  U1      u1_g_Unit(const U1 u1_a_IDX)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_Unit(const U1 u1_a_IDX)
{
    static const U1 u1_sp_LOCALE_UNIT_MAX_VAL[UNIT_NUM_IDX] = {
        (U1)UNIT_NUM_VAL_DIST,      /*  UNIT_IDX_DIST      */
        (U1)UNIT_NUM_VAL_SPEED,     /*  UNIT_IDX_SPEED     */
        (U1)UNIT_NUM_VAL_FUECO,     /*  UNIT_IDX_FUECO     */
        (U1)UNIT_NUM_VAL_ELECO,     /*  UNIT_IDX_ELECO     */
        (U1)UNIT_NUM_VAL_AMBTMP     /*  UNIT_IDX_AMBTMP    */
    };
    U1  u1_t_unit;

    u1_t_unit = (U1)U1_MAX;
    if(u1_a_IDX < (U1)UNIT_NUM_IDX){
        u1_t_unit  = u1_g_LocaleCfgUnit(u1_a_IDX);
        if(u1_t_unit >= u1_sp_LOCALE_UNIT_MAX_VAL[u1_a_IDX]){
            u1_t_unit = u1_g_UnitDef(u1_a_IDX);
        }
    }
    return(u1_t_unit);
}
/*===================================================================================================================================*/
/*  U1      u1_g_UnitDef(const U1 u1_a_IDX)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_UnitDef(const U1 u1_a_IDX)
{
    U1          u1_t_unit;

    u1_t_unit = (U1)U1_MAX;
    if(u1_a_IDX < (U1)UNIT_NUM_IDX){
        u1_t_unit  = u1_g_LocaleCfgUnitdef(u1_a_IDX);
    }
    return(u1_t_unit);
}
