/*  U1      u1_g_Language(const U1 u1_a_MET)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_Language(const U1 u1_a_MET)
{
    U1  u1_t_lang;

    u1_t_lang = u1_g_LocaleCfgLang();

    if(u1_t_lang >= u1_g_LANG_NUM){
        u1_t_lang = u1_g_LanguageDef();
    }
    else{
        if(u1_a_MET == (U1)TRUE){
            u1_t_lang = u1_g_LocaleCfgMmLangChk(u1_t_lang);
        }
    }

    return(u1_t_lang);
}
