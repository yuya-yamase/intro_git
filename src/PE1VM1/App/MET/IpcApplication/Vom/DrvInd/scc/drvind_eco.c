/*  void    vd_g_DrvIndEcoInit(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_DrvIndEcoInit(void)
{
    u1_s_drvind_eco_sts = (U1)DRVIND_ECO_STS_OFF;
    u1_s_drvind_eco_pct = (U1)DRVIND_ECO_PCT_OFF;
}
/*===================================================================================================================================*/
/*  void    vd_g_DrvIndEcoMainTask(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_DrvIndEcoMainTask(void)
{
    U1          u1_t_igon;                                                      /* Ignition on Status                                */
    U1          u1_t_stsbit;                                                    /* B_IECOCR Status                                   */
    U1          u1_t_ecopct;                                                    /* Eco Percent                                       */
    U1          u1_t_ecosts;                                                    /* Eco Status                                        */

    u1_t_igon = u1_g_DrvIndEcoIgnOn();
    if(u1_t_igon == (U1)TRUE){

        u1_t_ecopct = (U1)DRVIND_ECO_PCT_OFF;
        u1_t_stsbit = u1_g_DrvIndEcoCfgComRxBIECOCR(&u1_t_ecopct);

        if(u1_t_stsbit == (U1)DRVIND_ECO_STSBIT_VALID){
            switch(u1_t_ecopct){
            case (U1)DRVIND_ECO_PCT_OFF:
                u1_t_ecosts = (U1)DRVIND_ECO_STS_OFF;
                break;
            case (U1)DRVIND_ECO_PCT_CANCEL1:
             /* break; */
            case (U1)DRVIND_ECO_PCT_CANCEL2:
                u1_t_ecosts = (U1)DRVIND_ECO_STS_CANCEL;
                break;
            default:
                if(u1_t_ecopct <= (U1)DRVIND_ECO_PCT_ECOMAX){
                    u1_t_ecosts = (U1)DRVIND_ECO_STS_ECO;
                }
                else{
                    u1_t_ecosts = (U1)DRVIND_ECO_STS_NOTECO;
                }
                break;
            }
        }
        else if((u1_t_stsbit & (U1)DRVIND_ECO_STSBIT_INVALID) != (U1)0U){
            u1_t_ecopct = (U1)DRVIND_ECO_PCT_CANCEL2;
            u1_t_ecosts = (U1)DRVIND_ECO_STS_CANCEL;
        }
        else{
            u1_t_ecopct = (U1)DRVIND_ECO_PCT_OFF;
            u1_t_ecosts = (U1)DRVIND_ECO_STS_OFF;
        }
        u1_s_drvind_eco_pct = u1_t_ecopct;
        u1_s_drvind_eco_sts = u1_t_ecosts;
    }
    else{
        vd_g_DrvIndEcoInit();
    }
}
/*===================================================================================================================================*/
/*  U1      u1_g_DrvIndEcoPct(U1 * const u1p_a_PCT)                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      U1 * const u1p_a_PCT           : Eco Percent                                                                     */
/*  Return:         U1         u1_s_drvind_eco_sts : Eco Status                                                                      */
/*===================================================================================================================================*/
U1      u1_g_DrvIndEcoPct(U1 * const u1p_a_PCT)
{
    if (u1p_a_PCT != vdp_PTR_NA) {
       (*u1p_a_PCT) = u1_s_drvind_eco_pct;
    }

   return(u1_s_drvind_eco_sts);
}
