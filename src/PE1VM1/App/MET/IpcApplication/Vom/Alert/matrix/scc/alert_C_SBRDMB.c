/*===================================================================================================================================*/
/*  static U4      u4_s_AlertC_sbrdmbSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U4      u4_s_AlertC_sbrdmbSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
    static const U1 u1_s_ALERT_B_DMODE_LSB_MSGSTS  = (U1)1U;
    static const U1 u1_s_ALERT_B_DMODE_MSGSTS_NML  = (U1)0x00U;
    static const U1 u1_s_ALERT_C_SBRDMB_REQ_NONE   = (U1)0x00U;
    static const U1 u1_s_ALERT_C_SBRDMB_REQ_CHANGE = (U1)0x01U;
    U4              u4_t_src_chk;
    U1              u1_t_msgsts;
    U1              u1_t_sgnl;                                                 /* Request status from signal edge check              */
    U1              u1_t_alert_c_sbrdmb_dmode_curr;                            /* Current Drive Mode data                            */

    u1_t_msgsts   = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_ENG1G90_RXCH0,
                                     (U2)OXCAN_RX_SYS_NRX_IGR,
                                     (U2)U2_MAX) & (U1)COM_NO_RX;

    u1_t_sgnl                      = u1_s_ALERT_C_SBRDMB_REQ_NONE;
    u1_t_alert_c_sbrdmb_dmode_curr = (U1)0U;

    (void)Com_ReceiveSignal(ComConf_ComSignal_B_DMODE, &u1_t_alert_c_sbrdmb_dmode_curr);

    if (u1_t_msgsts == (U1)u1_s_ALERT_B_DMODE_MSGSTS_NML) {
        if (u1_s_alert_c_sbrdmb_dmode_prev != u1_t_alert_c_sbrdmb_dmode_curr) {
            u1_t_sgnl = u1_s_ALERT_C_SBRDMB_REQ_CHANGE;
        }
        u1_s_alert_c_sbrdmb_dmode_prev = u1_t_alert_c_sbrdmb_dmode_curr;
    }

    u4_t_src_chk  = (U4)u1_t_sgnl;
    u4_t_src_chk |= ((U4)u1_t_msgsts << u1_s_ALERT_B_DMODE_LSB_MSGSTS);

    return(u4_t_src_chk);
}

