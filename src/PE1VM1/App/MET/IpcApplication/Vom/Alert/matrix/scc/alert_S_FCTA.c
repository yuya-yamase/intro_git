/*===================================================================================================================================*/
static U4      u4_s_AlertS_fctaSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
#if defined(OXCAN_PDU_RX_CAN_DS11S40_RXCH0) && defined(ComConf_ComSignal_CTABUZF)
    static const U2  u2_s_ALERT_S_FCTA_TO_THRESH  = ((U2)5000U / (U2)OXCAN_MAIN_TICK);
    static const U1  u1_s_ALERT_S_FCTA_LSB_MSGSTS = (U1)2U;
    U1               u1_t_msgsts;
    U1               u1_t_sgnl;
    U4               u4_t_src_chk;

    u1_t_msgsts   = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_DS11S40_RXCH0,
                                          (U2)OXCAN_RX_SYS_NRX_POE | (U2)OXCAN_RX_SYS_TOE_POE,
                                          u2_s_ALERT_S_FCTA_TO_THRESH) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);
    u1_t_sgnl     = (U1)0U;
    (void)Com_ReceiveSignal(ComConf_ComSignal_CTABUZF, &u1_t_sgnl);
    u4_t_src_chk  = (U4)u1_t_sgnl;
    u4_t_src_chk |= ((U4)u1_t_msgsts << u1_s_ALERT_S_FCTA_LSB_MSGSTS);

    return(u4_t_src_chk);
#else /* defined(OXCAN_PDU_RX_CAN_DS11S40_RXCH0) && defined(ComConf_ComSignal_CTABUZF) */
    return((U4)0U);
#endif /* defined(OXCAN_PDU_RX_CAN_DS11S40_RXCH0) && defined(ComConf_ComSignal_CTABUZF) */
}

