/*===================================================================================================================================*/
/*  static U4      u4_s_AlertB_thesenSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U4      u4_s_AlertB_thesenSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
    static const U2 u2_s_ALERT_B_THESEN_TO_THRESH  = ((U2)5000U / (U2)OXCAN_MAIN_TICK);
    static const U1 u1_s_ALERT_B_THESEN_LSB_MSGSTS = (U1)2U;
    static const U4 u4_s_ALERT_B_THESEN_BIT_BAT_WT = (U4)0x00000008U;
    U1              u1_t_trns_fact;
    U1              u1_t_sgnl;
    U4              u4_t_src_chk;

    u1_t_trns_fact = u1_g_oXCANRxdStat((U2)OXCAN_RXD_PDU_CAN_BDB1F01_CH0,
                                      (U4)OXCAN_SYS_PNC_ALL,
                                      u2_s_ALERT_B_THESEN_TO_THRESH) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);

    if((u1_a_VOM & (U1)ALERT_VOM_IGN_ON) == (U1)0U){
        u1_t_trns_fact |= (U1)ALERT_BRX_FACT_NO_RX;
    }
    vd_g_AlertBRxTrnsSts(&u1_s_alert_b_thesen_msgsts_issd, u1_t_trns_fact);

    u1_t_sgnl      = (U1)0U;
#if defined(ComConf_ComSignal_ISSD_BDB)
    (void)Com_ReceiveSignal(ComConf_ComSignal_ISSD_BDB, &u1_t_sgnl);
#endif /* defined(ComConf_ComSignal_ISSD_BDB) */

    u4_t_src_chk   = (U4)u1_t_sgnl;
    u4_t_src_chk  |= (((U4)u1_s_alert_b_thesen_msgsts_issd & (U4)COM_NO_RX) << u1_s_ALERT_B_THESEN_LSB_MSGSTS);

    if((u1_a_VOM & (U1)ALERT_VOM_BAT_WT) != (U1)0U){
        u4_t_src_chk   |= u4_s_ALERT_B_THESEN_BIT_BAT_WT;
    }

    return(u4_t_src_chk);
}

