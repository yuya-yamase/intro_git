/*  static U4      u4_s_AlertD_launchSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U4      u4_s_AlertD_launchSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
    static const U2 u2_s_ALERT_LAUNCH_TO_THRESH    = ((U2)3600U / (U2)OXCAN_MAIN_TICK);
    static const U1 u1_s_ALERT_D_LAUNCH_LSB_MSGSTS = (U1)2U;
    U4              u4_t_src_chk;
    U1              u1_t_msgsts;
    U1              u1_t_sgnl;

    u1_t_sgnl = (U1)0U;

    u1_t_msgsts  = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_ENG1S98,
                                    (U2)OXCAN_RX_SYS_NRX_IGR | (U2)OXCAN_RX_SYS_TOE_IGR,
                                    u2_s_ALERT_LAUNCH_TO_THRESH) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);

    if (u1_t_msgsts == (U1)0U){
#if 0   /* BEV BSW provisionally */
        (void)Com_ReceiveSignal(ComConf_ComSignal_STSLNCH_4, &u1_t_sgnl);
#endif
    }

    u4_t_src_chk  = (U4)u1_t_sgnl;
    u1_t_sgnl     = (U1)0U;
 
    u1_t_msgsts  = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_EHV1F04_RXCH0,
                                    (U2)OXCAN_RX_SYS_NRX_IGR | (U2)OXCAN_RX_SYS_TOE_IGR,
                                    u2_s_ALERT_LAUNCH_TO_THRESH) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);

    if (u1_t_msgsts == (U1)0U){
#if 0   /* BEV BSW provisionally */
        (void)Com_ReceiveSignal(ComConf_ComSignal_STSLNCH_0, &u1_t_sgnl);
#endif
    }

    u4_t_src_chk |= ((U4)u1_t_sgnl << u1_s_ALERT_D_LAUNCH_LSB_MSGSTS);

    return(u4_t_src_chk);
}

