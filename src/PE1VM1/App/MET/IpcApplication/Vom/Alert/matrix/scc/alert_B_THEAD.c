/*===================================================================================================================================*/
/*  static U4      u4_s_AlertB_theadSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U4      u4_s_AlertB_theadSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
    static const U4 u4_s_ALERT_B_THEAD_DLY_THRESH  = ((U4)300U  / (U4)ALERT_CH_TICK);
    static const U4 u4_s_ALERT_B_THEAD_BIT_DLYELPS = (U4)0x00000001U;
    static const U2 u2_s_ALERT_B_THEAD_TO_THRESH   = ((U2)5000U / (U2)OXCAN_MAIN_TICK);
    static const U1 u1_s_ALERT_B_THEAD_LSB_MSGSTS  = (U1)2U;
    static const U1 u1_s_ALERT_B_THEAD_LSB_SIGNAL  = (U1)1U;
    U4              u4_t_src_chk;
    U1              u1_t_sgnl;
    U1              u1_t_trns_fact;
    U1              u1_t_isenable;

    u1_t_trns_fact = u1_g_oXCANRxdStat((U2)OXCAN_RXD_PDU_CAN_BDB1S03_CH0,
                                           (U4)ALERT_CAN_SYS_PNC_ALL,
                                           u2_s_ALERT_B_THEAD_TO_THRESH) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);

    if((u1_a_VOM & (U1)ALERT_VOM_IGN_ON) == (U1)0U){
        u1_t_trns_fact |= (U1)ALERT_BRX_FACT_NO_RX;
    }
    vd_g_AlertBRxTrnsSts(&u1_s_alert_b_thead_msgsts, u1_t_trns_fact);

    u1_t_isenable  = u1_g_AlertCfgHEDLIsEnable();

    u1_t_sgnl      = (U1)0U;
    if(u1_t_isenable == (U1)TRUE){
        (void)Com_ReceiveSignal(ComConf_ComSignal_HEDL, &u1_t_sgnl);
    }
    u4_t_src_chk   = ((U4)u1_t_sgnl << u1_s_ALERT_B_THEAD_LSB_SIGNAL);

    if((u1_t_sgnl                                   == (U1)0U       ) ||
       ((u1_s_alert_b_thead_msgsts & (U1)COM_NO_RX) == (U1)COM_NO_RX)){
        u4_s_alert_b_thead_dlytm = (U4)0U;
    }
    else if(u4_s_alert_b_thead_dlytm >= u4_s_ALERT_B_THEAD_DLY_THRESH){
        u4_t_src_chk            |= u4_s_ALERT_B_THEAD_BIT_DLYELPS;
        u4_s_alert_b_thead_dlytm = u4_s_ALERT_B_THEAD_DLY_THRESH;
    }
    else{
        u4_s_alert_b_thead_dlytm++;
    }

    u4_t_src_chk  |= (((U4)u1_s_alert_b_thead_msgsts & (U4)COM_NO_RX) << u1_s_ALERT_B_THEAD_LSB_MSGSTS);

    return(u4_t_src_chk);
}

