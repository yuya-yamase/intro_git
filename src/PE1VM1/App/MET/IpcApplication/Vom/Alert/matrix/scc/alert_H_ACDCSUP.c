/*===================================================================================================================================*/
/*  static U4      u4_s_AlertH_acdcsupSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U4      u4_s_AlertH_acdcsupSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
    static const U2 u2_s_ALERT_H_ACDCSUP_TRSH_PL10   = ((U2)5000U / (U2)OXCAN_MAIN_TICK);
    static const U1 u1_s_ALERT_H_ACDCSUP_LSB_PLG1G10 = (U1)9U;
    static const U1 u1_s_ALERT_H_ACDCSUP_LSB_PISP    = (U1)8U;
    U4              u4_t_src_chk;
    U1              u1_t_msgsts;
    U1              u1_t_sgnl;

    u1_t_msgsts   = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_PLG1G10,
                                     (U2)OXCAN_RX_SYS_NRX_BAT | (U2)OXCAN_RX_SYS_TOE_BAT,
                                     u2_s_ALERT_H_ACDCSUP_TRSH_PL10) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);
    vd_g_AlertBRxTrnsSts(&u1_s_alert_h_acdcsup_plg1g10_sts, u1_t_msgsts);

    u4_t_src_chk  = ((U4)u1_s_alert_h_acdcsup_plg1g10_sts << u1_s_ALERT_H_ACDCSUP_LSB_PLG1G10);

    u1_t_sgnl     = (U1)0U;
    (void)Com_ReceiveSignal(ComConf_ComSignal_PIINFDSP, &u1_t_sgnl);
    u4_t_src_chk |= ((U4)u1_t_sgnl                        << u1_s_ALERT_H_ACDCSUP_LSB_PISP);

    u1_t_sgnl     = (U1)0U;
    (void)Com_ReceiveSignal(ComConf_ComSignal_PIINFD08, &u1_t_sgnl);
    u4_t_src_chk |= (U4)u1_t_sgnl;

    return(u4_t_src_chk);
}

