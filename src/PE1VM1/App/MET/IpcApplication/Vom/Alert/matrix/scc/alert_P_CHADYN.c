/*  static U4      u4_s_AlertP_chadynSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U4      u4_s_AlertP_chadynSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
    static const U1 u1_s_ALERT_P_CHADYN_LSB_MSGSTS = (U1)2U;
    U4              u4_t_src_chk;
    U1              u1_t_sgnl;
    U1              u1_t_msgsts;
#ifdef OXCAN_PDU_RX_CAN_ENG1F03
    u1_t_msgsts   = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_ENG1F03,
                                     (U2)OXCAN_RX_SYS_NRX_POE,
                                     (U2)U2_MAX) & (U1)COM_NO_RX;
#endif /* OXCAN_PDU_RX_CAN_ENG1F03 */
#ifdef OXCAN_PDU_RX_CAN_ENG1G90_RXCH0
    u1_t_msgsts   = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_ENG1G90_RXCH0,
                                     (U2)OXCAN_RX_SYS_NRX_POE,
                                     (U2)U2_MAX) & (U1)COM_NO_RX;
#endif /* OXCAN_PDU_RX_CAN_ENG1G90_RXCH0 */

    u1_t_sgnl     = (U1)0U;
#ifdef ComConf_ComSignal_CDYMD
    (void)Com_ReceiveSignal(ComConf_ComSignal_CDYMD, &u1_t_sgnl);
#endif /* ComConf_ComSignal_CDYMD */

    u4_t_src_chk  = (U4)u1_t_sgnl;
    u4_t_src_chk |= ((U4)u1_t_msgsts << u1_s_ALERT_P_CHADYN_LSB_MSGSTS);
    return(u4_t_src_chk);
}

