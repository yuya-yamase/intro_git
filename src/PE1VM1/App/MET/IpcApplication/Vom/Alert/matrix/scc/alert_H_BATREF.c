/*===================================================================================================================================*/
/*  static U4      u4_s_AlertH_batrefSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U4      u4_s_AlertH_batrefSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
    static const U2 u2_s_ALERT_H_BATREF_TO_THRESH  = ((U2)5000U / (U2)OXCAN_MAIN_TICK);
    static const U1 u1_s_ALERT_H_BATREF_LSB_MSGSTS = (U1)1U;
    static const U4 u4_s_ALERT_H_BATREF_BIT_BAT_WT = (U4)0x00000004U;
    U1              u1_t_trns_fact;
    U1              u1_t_sgnl;
    U4              u4_t_src_chk;

    u1_t_trns_fact   = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_BAT1S01_RXCH0,
                                        (U2)OXCAN_RX_SYS_NRX_PBA | (U2)OXCAN_RX_SYS_TOE_PBA,
                                        u2_s_ALERT_H_BATREF_TO_THRESH) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);

    if((u1_a_VOM & (U1)ALERT_VOM_IGN_ON) == (U1)0U){
        u1_t_trns_fact |= (U1)ALERT_BRX_FACT_NO_RX;
    }
    vd_g_AlertBRxTrnsSts(&u1_s_alert_h_batref_msgsts, u1_t_trns_fact);

    u1_t_sgnl     = (U1)0U;
    (void)Com_ReceiveSignal(ComConf_ComSignal_FCHGSUG, &u1_t_sgnl);

    u4_t_src_chk  = (U4)u1_t_sgnl;
    u4_t_src_chk |= (((U4)u1_s_alert_h_batref_msgsts & (U4)COM_NO_RX) << u1_s_ALERT_H_BATREF_LSB_MSGSTS);

    if((u1_a_VOM & (U1)ALERT_VOM_BAT_WT) != (U1)0U){
        u4_t_src_chk   |= u4_s_ALERT_H_BATREF_BIT_BAT_WT;
    }

    return(u4_t_src_chk);
}

