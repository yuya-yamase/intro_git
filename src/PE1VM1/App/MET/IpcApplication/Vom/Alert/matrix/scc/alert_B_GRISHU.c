static U4      u4_s_AlertB_grishuSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
    static const U1 u1_s_ALERT_B_GRISHU_LSB_ENG1S99 = (U1)1U;
    U1              u1_t_msgsts;
    U1              u1_t_sgnl;
    U4              u4_t_src_chk;

    u1_t_msgsts   = u1_g_oXCANRxdStat((U2)OXCAN_RXD_PDU_CAN_ENG1S99_CH0,
                                      (U4)OXCAN_SYS_IGR | (U4)OXCAN_SYS_IGP,
                                      (U2)U2_MAX) & (U1)COM_NO_RX;

    u1_t_sgnl     = (U1)0U;
#if defined(ComConf_ComSignal_GRSMET_R) /* _840B_CAN_ */
    (void)Com_ReceiveSignal(ComConf_ComSignal_GRSMET_R, &u1_t_sgnl);
#endif /* defined(ComConf_ComSignal_GRSMET_R) */
    u4_t_src_chk  = (U4)u1_t_sgnl;
    u4_t_src_chk |= ((U4)u1_t_msgsts << u1_s_ALERT_B_GRISHU_LSB_ENG1S99);

    return(u4_t_src_chk);
}

