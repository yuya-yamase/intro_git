/*  static U4      u4_s_AlertS_adbzr_fctaSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U4      u4_s_AlertS_adbzr_fctaSrcchk(const U1 u1_a_VOM, const U4 u4_a_IGN_TM, const U1 u1_a_LAS)
{
    static const U2  u2_s_ALERT_S_ADBZR_FCTA_TO     = ((U2)5000U / (U2)OXCAN_MAIN_TICK);                  /*For THRESH/Judgement time*/
    static const U1  u1_s_ALERT_S_ADBZR_FCTA_MSGSTS = (U1)2U;                                             /*LSB for CAN Frame        */
    U1               u1_t_msgsts;
    U1               u1_t_sgnl;
    U4               u4_t_src_chk;

    u1_t_msgsts   = u1_g_oXCANRxdStat((U2)OXCAN_RXD_PDU_CAN_DS11S40_CH0,
                                      (U4)OXCAN_SYS_IGR | (U4)OXCAN_SYS_IGP,
                                      u2_s_ALERT_S_ADBZR_FCTA_TO) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);
    u1_t_sgnl     = (U1)0U;
    (void)Com_ReceiveSignal(ComConf_ComSignal_CTABUZF, &u1_t_sgnl);
    u4_t_src_chk  = (U4)u1_t_sgnl;
    u4_t_src_chk |= ((U4)u1_t_msgsts << u1_s_ALERT_S_ADBZR_FCTA_MSGSTS);

    return(u4_t_src_chk);
}

