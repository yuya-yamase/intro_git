/*  void           vd_g_AlertBRxTrnsSts(U1 * u1p_a_msgsts, const U1 u1_a_TRNS_FACT)                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void           vd_g_AlertBRxTrnsSts(U1 * u1p_a_msgsts, const U1 u1_a_TRNS_FACT)
{
    static const U1  u1_sp_ALERT_MSG_STS_TRNS_TBL[ALERT_BRX_NUM_MSG_TRNS_TBL] = {
        (U1)0U                       , (U1)0U                       , (U1)0U                       , (U1)0U                       ,
        (U1)0U                       , (U1)COM_NO_RX                , (U1)COM_TIMEOUT              , (U1)(COM_NO_RX | COM_TIMEOUT),
        (U1)COM_TIMEOUT              , (U1)(COM_NO_RX | COM_TIMEOUT), (U1)COM_TIMEOUT              , (U1)(COM_NO_RX | COM_TIMEOUT),
        (U1)COM_TIMEOUT              , (U1)(COM_NO_RX | COM_TIMEOUT), (U1)COM_TIMEOUT              , (U1)(COM_NO_RX | COM_TIMEOUT),
        (U1)0U                       , (U1)0U                       , (U1)0U                       , (U1)0U                       ,
        (U1)0U                       , (U1)COM_NO_RX                , (U1)COM_TIMEOUT              , (U1)(COM_NO_RX | COM_TIMEOUT),
        (U1)(COM_NO_RX | COM_TIMEOUT), (U1)(COM_NO_RX | COM_TIMEOUT), (U1)(COM_NO_RX | COM_TIMEOUT), (U1)(COM_NO_RX | COM_TIMEOUT),
        (U1)(COM_NO_RX | COM_TIMEOUT), (U1)(COM_NO_RX | COM_TIMEOUT), (U1)(COM_NO_RX | COM_TIMEOUT), (U1)(COM_NO_RX | COM_TIMEOUT)
    };

    static const U1  u1_s_ALERT_BRX_MSK_STS  = (U1)0x03U;
    static const U1  u1_s_ALERT_BRX_MSK_FACT = (U1)0x07U;
    static const U1  u1_s_ALERT_BRX_LSB_FACT = (U1)2U;

    U1               u1_t_idx;
    U1               u1_t_fact;

    u1_t_idx      = *u1p_a_msgsts  & u1_s_ALERT_BRX_MSK_STS;
    u1_t_fact     = u1_a_TRNS_FACT & u1_s_ALERT_BRX_MSK_FACT;
    u1_t_idx     |= (U1)(u1_t_fact << u1_s_ALERT_BRX_LSB_FACT);

    *u1p_a_msgsts = u1_sp_ALERT_MSG_STS_TRNS_TBL[u1_t_idx];
}
