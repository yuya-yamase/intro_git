/* U1              u1_g_BattpowCfgPtsysJudge(void)                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1              u1_g_BattpowCfgPtsysJudge(void)
{
    U1                  u1_t_judge_ptsys;                                            /* Judge PTSYS Value                            */
    U1                  u1_t_get_ptsys;                                              /* Get   PTSYS Value                            */

    u1_t_judge_ptsys  = (U1)BATTPOW_SOCMON_OTHER;
    u1_t_get_ptsys    = u1_g_VardefPtsRx();

    if(u1_t_get_ptsys   == (U1)VDF_PTS_RX_04_HYB_PLU){
        u1_t_judge_ptsys = (U1)BATTPOW_SOCMON_PHEV;
    }
    else if (u1_t_get_ptsys   == (U1)VDF_PTS_RX_05_ELE_BAT){
       u1_t_judge_ptsys = (U1)BATTPOW_SOCMON_BEV;
    }
    else {
        /* Do nothing  */
    }

    return (u1_t_judge_ptsys);
}

/*===================================================================================================================================*/
/* U1              u1_g_BattpowCfgSOCDSP(U1 * const u1p_a_socdsp)                                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1              u1_g_BattpowCfgSOCDSP(U1 * const u1p_a_socdsp)
{
    U1                  u1_t_sts;

    (void)Com_ReceiveSignal(ComConf_ComSignal_SOC_DSP, u1p_a_socdsp);
    u1_t_sts = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_PLG1S06_RXCH0,
                                ((U2)OXCAN_RX_SYS_NRX_BAT |
                                 (U2)OXCAN_RX_SYS_TOE_BAT),
                                u2_OXCAN_RXTO_THRSH(5000U)) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);
    return(u1_t_sts);
}

/*===================================================================================================================================*/
/* U1              u1_g_BattpowCfgSOCINDLL(U1 * const u1p_a_socindll)                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1              u1_g_BattpowCfgSOCINDLL(U1 * const u1p_a_socindll)
{
    U1                  u1_t_sts;

    (void)Com_ReceiveSignal(ComConf_ComSignal_SOCINDLL, u1p_a_socindll);
    u1_t_sts = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_EHV1S31_RXCH0,
                                ((U2)OXCAN_RX_SYS_NRX_IGP |
                                 (U2)OXCAN_RX_SYS_TOE_IGP),
                                u2_OXCAN_RXTO_THRSH(5000U)) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);

    return(u1_t_sts);
}

