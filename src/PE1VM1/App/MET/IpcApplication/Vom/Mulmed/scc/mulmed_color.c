/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Variable Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static U1    u1_s_mulmed_color;
static U4    u4_s_mulmed_tx_elps;
static U1    u1_s_mulmed_tx_flg;

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Static Function Prototypes                                                                                                       */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Constant Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  void    vd_g_MulmedColorInit(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_MulmedColorInit(void)
{
    u1_s_mulmed_color   = (U1)U1_MAX;
    u4_s_mulmed_tx_elps = (U4)U4_MAX;
    u1_s_mulmed_tx_flg  = (U1)0U;
}
/*===================================================================================================================================*/
/*  void    vd_g_MulmedColorMainTask(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_MulmedColorMainTask(void)
{
    static const U4        u4_s_MULMED_COLOR_TXTIM = ((U4)1000U / (U4)10U) - (U4)1U;

    if(u4_s_mulmed_tx_elps < (U4)U4_MAX){
        u4_s_mulmed_tx_elps++;
    }

    if(u1_s_mulmed_tx_flg == (U1)MULMED_COLOR_SENDCND){
        u4_s_mulmed_tx_elps = (U4)0U;
        vd_g_MulmedColorCfgSendM_CLRCHG((U1)TRUE);
        u1_s_mulmed_tx_flg = (U1)0U;
    }
    
    if(u4_s_mulmed_tx_elps >= u4_s_MULMED_COLOR_TXTIM){
        vd_g_MulmedColorCfgSendM_CLRCHG((U1)FALSE);
        u4_s_mulmed_tx_elps = (U4)U4_MAX;
    }

}
/*===================================================================================================================================*/
/*  U1      u1_g_MulmedColor(void)                                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_MulmedColor(void)
{
    return(u1_s_mulmed_color);
}
/*===================================================================================================================================*/
/*  void    vd_g_MulmedColorPut(const U1 u1_a_COLOR)                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_MulmedColorPut(const U1 u1_a_COLOR)
{
    if(u1_s_mulmed_color != u1_a_COLOR){
        u1_s_mulmed_color   = u1_a_COLOR;
        vd_g_MulmedColorCfgSendM_CLRCHG((U1)TRUE);
        u1_s_mulmed_tx_flg |= (U1)MULMED_COLOR_CHANGE;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_MulmedColorTxHk(const U2 u2_a_MSG)                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_MulmedColorTxHk(const U2 u2_a_MSG)
{
    if(u2_a_MSG == (U2)u2_g_MULMED_COLOR_MSGID_MET1S19){
        u1_s_mulmed_tx_flg |= (U1)MULMED_COLOR_COM_TXFIN;
    }
}
