#define TIMEZONE_TZ_SIGN_MINUS       (0x00U)
#define TIMEZONE_TZ_SIGN_PLUS        (0x01U)
#define TIMEZONE_TZ_SIGN_UNUSE       (0x02U)
#define TIMEZONE_TZ_SIGN_UNDEF       (0x03U)

#define TIMEZONE_DST_S30_INVALID     (0x00U)
#define TIMEZONE_DST_S30_VALID       (0x01U)
#define TIMEZONE_DST_S30_UNUSE       (0x02U)
#define TIMEZONE_DST_S30_UNDEF       (0x03U)

#define TIMEZONE_TZ_MAX_VAL          (0x3EU)

