static U1   u1_sp_tz_rx[TIMEZONE_NUM_RX];
static S4   s4_s_utcdifsec;

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Static Function Prototypes                                                                                                       */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static void    vd_s_TimeZoneUtcDiffSec(void);

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Constant Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static const U1   u1_sp_TIMEZONE_DEF[TIMEZONE_NUM_RX] = {
    (U1)0U,
    (U1)TIMEZONE_TZ_SIGN_UNDEF,
    (U1)TIMEZONE_DST_S30_INVALID
};

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  void    vd_g_TimeZoneBonInit(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_TimeZoneBonInit(void)
{
    U4  u4_t_loop;

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)TIMEZONE_NUM_RX ; u4_t_loop++){
        u1_sp_tz_rx[u4_t_loop] = u1_sp_TIMEZONE_DEF[u4_t_loop];
        vd_g_Rim_WriteU1(u2_gp_TIMEZONE_RIMID[u4_t_loop], u1_sp_TIMEZONE_DEF[u4_t_loop]);
    }
    s4_s_utcdifsec = (S4)0;
}
/*===================================================================================================================================*/
/*  void    vd_g_TimeZoneRstWkupInit(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_TimeZoneRstWkupInit(void)
{
    U4  u4_t_loop;
    U1  u1_t_sts;

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)TIMEZONE_NUM_RX ; u4_t_loop++){
        u1_t_sts = u1_g_Rim_ReadU1withStatus(u2_gp_TIMEZONE_RIMID[u4_t_loop],
                                             &u1_sp_tz_rx[u4_t_loop]) & (U1)RIM_RESULT_KIND_MASK;
        if(u1_t_sts != (U1)RIM_RESULT_KIND_OK){
            u1_sp_tz_rx[u4_t_loop] = u1_sp_TIMEZONE_DEF[u4_t_loop];
        }
    }

    vd_s_TimeZoneUtcDiffSec();
}
/*===================================================================================================================================*/
/*  void    vd_g_TimeZoneMainTask(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_TimeZoneMainTask(void)
{
    U1              u1_t_sts;
    U1              u1_t_rx;

    u1_t_rx  = (U1)0U;
    u1_t_sts = u1_g_TimeZoneCfgRxTZ(&u1_t_rx);
    if((u1_t_sts == (U1)0U) &&
       (u1_t_rx  <= (U1)TIMEZONE_TZ_MAX_VAL)){
        u1_sp_tz_rx[TIMEZONE_RX_TZ] = u1_t_rx;
        vd_g_Rim_WriteU1(u2_gp_TIMEZONE_RIMID[TIMEZONE_RX_TZ], u1_t_rx);
    }

    u1_t_rx  = (U1)0U;
    u1_t_sts = u1_g_TimeZoneCfgRxTZ_SIGN(&u1_t_rx);
    if((u1_t_sts == (U1)0U) &&
       (u1_t_rx  <= (U1)TIMEZONE_TZ_SIGN_PLUS)){
        u1_sp_tz_rx[TIMEZONE_RX_TZ_SIGN] = u1_t_rx;
        vd_g_Rim_WriteU1(u2_gp_TIMEZONE_RIMID[TIMEZONE_RX_TZ_SIGN], u1_t_rx);
    }

    u1_t_rx  = (U1)0U;
    u1_t_sts = u1_g_TimeZoneCfgRxDST_S30(&u1_t_rx);
    if((u1_t_sts == (U1)0U) &&
       (u1_t_rx  <= (U1)TIMEZONE_DST_S30_VALID)){
        u1_sp_tz_rx[TIMEZONE_RX_DST_S30] = u1_t_rx;
        vd_g_Rim_WriteU1(u2_gp_TIMEZONE_RIMID[TIMEZONE_RX_DST_S30], u1_t_rx);
    }

    vd_s_TimeZoneUtcDiffSec();
}
/*===================================================================================================================================*/
/*  S4  s4_g_TimeZoneUtcDiffSec(void)                                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
S4  s4_g_TimeZoneUtcDiffSec(void)
{
    return(s4_s_utcdifsec);
}
/*===================================================================================================================================*/
/*  static void    vd_s_TimeZoneUtcDiffSec(void)                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void    vd_s_TimeZoneUtcDiffSec(void)
{
    static const S4 s4_s_TZ_HR2SEC  = (S4)3600;
    static const U4 u4_s_TZ_RES2SEC = (U4)900U; /* 15 * 60 */
    U4              u4_t_diff_wo_sign;
    S4              s4_t_dstsec;

    u4_t_diff_wo_sign  = (U4)u1_sp_tz_rx[TIMEZONE_RX_TZ] * u4_s_TZ_RES2SEC;
                                        /* The type of u1_sp_tz_rx[] is U1, so max value of u1_sp_tz_rx is 255.   */
                                        /* The value of u4_s_TZ_RES2SEC is 900.                                   */
                                        /* Therefor (u1_sp_tz_rx[] * u4_s_TZ_RES2SEC) is always lower than U4_MAX */
    if(u1_sp_tz_rx[TIMEZONE_RX_TZ_SIGN] == (U1)TIMEZONE_TZ_SIGN_MINUS){
        s4_s_utcdifsec = (S4)(u4_t_diff_wo_sign) * (S4)(-1);
    }
    else{
        s4_s_utcdifsec = (S4)(u4_t_diff_wo_sign);
    }

    if(u1_sp_tz_rx[TIMEZONE_RX_DST_S30] == (U1)TIMEZONE_DST_S30_VALID ){
        s4_t_dstsec = s4_s_TZ_HR2SEC;
    }
    else{
        s4_t_dstsec = (S4)0;
    }
    s4_s_utcdifsec += s4_t_dstsec;
}
