/* U4              u4_g_PitchRollCfgCalcU8MulDiv(const U4 u4_a_mullo, const U4 u4_a_mulhi, const U4 u4_a_div)                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4              u4_g_PitchRollCfgCalcU8MulDiv(const U4 u4_a_mullo, const U4 u4_a_mulhi, const U4 u4_a_div)
{
    U4          u4_tp_trem[CALC_U8_NUM_TERM];
    U4          u4_tp_rslt[CALC_U8_NUM_RSLT];
    U4          u4_t_rand;
    U4          u4_t_calcrslt;
    U1          u1_t_rsltsts;

    u4_t_rand = ((u4_a_div + (U4)1U) >> 1);
    u4_tp_trem[CALC_U8_LO]  = u4_a_mullo;
    u4_tp_trem[CALC_U8_HI]  = u4_a_mulhi;
    u4_tp_trem[CALC_U8_DIV] = u4_a_div;
    u4_tp_rslt[CALC_U8_QUO] = (U4)0U;
    u4_tp_rslt[CALC_U8_RES] = (U4)0U;

    u1_t_rsltsts = u1_g_MuldivU8(&u4_tp_trem[0], &u4_tp_rslt[0]);
    if(((u1_t_rsltsts & (U1)(CALC_U8_RSLT_DIVBYZERO | CALC_U8_RSLT_OVRFLW_U4)) != (U1)0U) ||
        (u4_tp_rslt[CALC_U8_QUO] == (U4)U4_MAX)){
        u4_t_calcrslt = (U4)U4_MAX;
    }
    else if(u4_tp_rslt[CALC_U8_RES] < u4_t_rand){
        u4_t_calcrslt = u4_tp_rslt[CALC_U8_QUO];
    }
    else{
        u4_t_calcrslt = u4_tp_rslt[CALC_U8_QUO] + (U4)1U;
    }

    return(u4_t_calcrslt);
}

