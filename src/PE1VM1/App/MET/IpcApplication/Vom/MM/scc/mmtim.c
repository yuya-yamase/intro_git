/*===================================================================================================================================*/
/*  50ms定期処理                                                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void vd_g_MMTim_Cycle(void)
{
    U1 u1_t_timerid;

    /* 全てのタイマーを確認する */
    for(u1_t_timerid = (U1)0; (U1)MMTIM_TIMERID_MAX > u1_t_timerid; u1_t_timerid++)
    {
        /* 動作中のタイマーのみ処理する */
        if((U1)MMTIM_STATE_RUNNING == st_tbl_timer_control[u1_t_timerid].u1_state)
        {
            if((U2)U2_MAX > st_tbl_timer_control[u1_t_timerid].u2_count)
            {
                /* タイマーカウンタをインクリメント */
                st_tbl_timer_control[u1_t_timerid].u2_count++;

                if(((U2)U2_MAX/(U2)MMTIM_CYCLE_INTERVAL) >= st_tbl_timer_control[u1_t_timerid].u2_count)
                {
                    /* タイムアウトしたらタイマイベントを通知 */
                    if(st_tbl_timer_info[u1_t_timerid].u2_time <=
                        (st_tbl_timer_control[u1_t_timerid].u2_count * (U2)MMTIM_CYCLE_INTERVAL))
                    {
                        /* タイマーイベント通知 */
                        st_tbl_timer_info[u1_t_timerid].fp_notify_timerevent_func();

                        /* タイマーカウンタを初期化 */
                        st_tbl_timer_control[u1_t_timerid].u2_count = (U2)0;

                        /* ワンショットタイマーの場合はタイマーを止める */
                        if((U1)MMTIM_TYPE_ONESHOT == st_tbl_timer_info[u1_t_timerid].u1_type)
                        {
                            st_tbl_timer_control[u1_t_timerid].u1_state = (U1)MMTIM_STATE_STOP;
                        }
                    }
                }
                else{
                    /* タイマーイベント通知 */
                    st_tbl_timer_info[u1_t_timerid].fp_notify_timerevent_func();

                    /* タイマーカウンタを初期化 */
                    st_tbl_timer_control[u1_t_timerid].u2_count = (U2)0;

                    /* ワンショットタイマーの場合はタイマーを止める */
                    if((U1)MMTIM_TYPE_ONESHOT == st_tbl_timer_info[u1_t_timerid].u1_type)
                    {
                        st_tbl_timer_control[u1_t_timerid].u1_state = (U1)MMTIM_STATE_STOP;
                    }
                }
            }
            else{
                /* タイマーカウンタを初期化 */
                st_tbl_timer_control[u1_t_timerid].u2_count = (U2)0;
            }
        }
        /* 予約状態のタイマーを起動する */
        else if((U1)MMTIM_STATE_RESERVED == st_tbl_timer_control[u1_t_timerid].u1_state)
        {
            /* タイマー状態を「実行中」にする */
            st_tbl_timer_control[u1_t_timerid].u1_state = (U1)MMTIM_STATE_RUNNING;
        }
        else
        {
            /* DO NOTHING */
        }
    }
}

/*===================================================================================================================================*/
/*  タイマー起動                                                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments   -                                                                                                                    */
/*  Return      -                                                                                                                    */
/*===================================================================================================================================*/
void vd_g_MMTim_StartTimer(U1 u1_a_timerid)
{
    /* タイマーIDが正常な場合だけ処理する */
    if((U1)MMTIM_TIMERID_MAX > u1_a_timerid)
    {
        /* タイマーIDに該当するタイマー状態を「実行中」にする */
        st_tbl_timer_control[u1_a_timerid].u1_state = (U1)MMTIM_STATE_RUNNING;

        /* タイマーIDに該当するカウンタをクリアする */
        st_tbl_timer_control[u1_a_timerid].u2_count = (U2)0;
    }
}

