static U2       u2_s_engspddet_rpm_inst;
static U2       u2_s_engspddet_rpm_fltrd;

static U2       u2_sp_engspddet_smpl[ENDSPD_DET_MVAVG_TMS];
static U1       u1_s_engspddet_pos_oldest;
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Static Function Prototypes                                                                                                       */
