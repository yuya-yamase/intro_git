/*  void    vd_g_EngspdDetInit(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_EngspdDetInit(void)
{
    u1_s_engspddet_stsbit       = (U1)ENGSPD_DET_STSBIT_UNKNOWN;
    u2_s_engspddet_rpm_fltrd    = (U2)0U;
    u2_s_engspddet_rpm_inst     = (U2)0U;

    vd_s_EngspdDetMvAvgRamClr();
}

/*===================================================================================================================================*/
/*  void    vd_g_EngspdDetMainTask(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_EngspdDetMainTask(void)
{
    U1                  u1_t_igon;
    U1                  u1_t_stsbit;
    U2                  u2_t_fltrd;
    U2                  u2_t_inst;

    u2_t_fltrd  = (U2)0U;

    u1_t_igon = u1_g_EngspdDetRpmCfgIgOn();

    if(u1_t_igon == (U1)TRUE){
        u2_t_inst   = (U2)0U;
        u1_t_stsbit = u1_g_EngspdDetRpmCfgRpmInst(&u2_t_inst) & ((U1)ENGSPD_DET_STSBIT_INVALID | (U1)ENGSPD_DET_STSBIT_UNKNOWN);

        if((u1_t_stsbit & (U1)ENGSPD_DET_STSBIT_INVALID) != (U1)0U){
            u2_t_fltrd = u2_s_EngspdDetMvAvg(u2_s_engspddet_rpm_inst);
        }
        else if((u1_t_stsbit & (U1)ENGSPD_DET_STSBIT_UNKNOWN) != (U1)0U){
            /* u2_t_fltrd = (U2)0U; */
            vd_s_EngspdDetMvAvgRamClr();
        }
        else{
            u2_s_engspddet_rpm_inst = u2_t_inst;
            u2_t_fltrd = u2_s_EngspdDetMvAvg(u2_t_inst);
        }

        u2_s_engspddet_rpm_fltrd    = u2_t_fltrd;
        u1_s_engspddet_stsbit       = u1_t_stsbit;
    }
    else{
        vd_g_EngspdDetInit();
    }
}

/*===================================================================================================================================*/
/*  U1      u1_g_EngspdDetRpm(U2 * u2p_a_rpm)                                                                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_EngspdDetRpm(U2 * u2p_a_rpm)
{
    U1                  u1_t_unk;

    u1_t_unk = u1_s_engspddet_stsbit & (U1)ENGSPD_DET_STSBIT_UNKNOWN;
    if((u1_t_unk  == (U1)0U    ) &&
       (u2p_a_rpm != vdp_PTR_NA)){
        (*u2p_a_rpm) = u2_s_engspddet_rpm_fltrd;
    }

    return(u1_s_engspddet_stsbit);
}

/*===================================================================================================================================*/
/*  static void    vd_s_EngspdDetMvAvgRamClr(void)                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void    vd_s_EngspdDetMvAvgRamClr(void)
{
    U1                  u1_t_cnt;

    for(u1_t_cnt = (U1)0U; u1_t_cnt < (U1)ENDSPD_DET_MVAVG_TMS; u1_t_cnt++){
        u2_sp_engspddet_smpl[u1_t_cnt] = (U2)0U;
    }
    u1_s_engspddet_pos_oldest = (U1)0U;
}

/*===================================================================================================================================*/
/*  static U2      u2_s_EngspdDetMvAvg(const U2 u2_a_RPM)                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U2      u2_s_EngspdDetMvAvg(const U2 u2_a_RPM)
{
    U4                  u4_t_sum;
    U1                  u1_t_cnt;
    U4                  u4_t_rpm;

    if(u1_s_engspddet_pos_oldest < ((U1)ENDSPD_DET_MVAVG_TMS - (U1)1U)){
        u2_sp_engspddet_smpl[u1_s_engspddet_pos_oldest] = u2_a_RPM;
        u1_s_engspddet_pos_oldest++;
    }
    else if(u1_s_engspddet_pos_oldest < (U1)ENDSPD_DET_MVAVG_TMS){
        u2_sp_engspddet_smpl[u1_s_engspddet_pos_oldest] = u2_a_RPM;
        u1_s_engspddet_pos_oldest = (U1)0U;
    }
    else{
        vd_s_EngspdDetMvAvgRamClr();
    }

    for(u1_t_cnt = (U1)0U, u4_t_sum = (U4)0U; u1_t_cnt < (U1)ENDSPD_DET_MVAVG_TMS; u1_t_cnt++){
        if(((U4)U4_MAX - u2_sp_engspddet_smpl[u1_t_cnt]) > u4_t_sum){
            u4_t_sum += (U4)u2_sp_engspddet_smpl[u1_t_cnt];
        }
        else{
            u4_t_sum = (U4)U4_MAX;
            break;
        }
    }

    u4_t_rpm = u4_t_sum / (U4)ENDSPD_DET_MVAVG_TMS;
    if(u4_t_rpm > (U4)U2_MAX){
        u4_t_rpm = (U4)U2_MAX;
    }

    return((U2)u4_t_rpm);
}

