/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  U1          u1_g_EcoJdgCfgScrInst(U1 * u1p_a_id, U1 * u1p_a_scr, U1 * u1p_a_cnt)                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1          u1_g_EcoJdgCfgScrInst(U1 * u1p_a_id, U1 * u1p_a_scr, U1 * u1p_a_cnt)
{
    static const U1 u1_sp_ECOJDG_ID_CHK[ECOJDG_ESPOHVID_NUM] ={
        (U1)ECOJDG_ID_STP,
        (U1)ECOJDG_ID_STR,
        (U1)ECOJDG_ID_RUN,
        (U1)ECOJDG_ID_SUS,
        (U1)ECOJDG_ID_DEC,
        (U1)ECOJDG_ID_SUS,
        (U1)ECOJDG_ID_SUS,
        (U1)ECOJDG_ID_SUS
    };

    U1      u1_t_espohvid;
    U1      u1_t_espohvvl;
    U1      u1_t_rx_cnt;
    U1      u1_t_rx_sts;

    u1_t_espohvid = (U1)0U;
    u1_t_espohvvl = (U1)0U;
#if defined(ComConf_ComSignal_ESPOHVID)
    (void)Com_ReceiveSignal(ComConf_ComSignal_ESPOHVID, &u1_t_espohvid);
#endif/* >>ComConf_ComSignal_ESPOHVID */
#if defined(ComConf_ComSignal_ESPOHVVL)
    (void)Com_ReceiveSignal(ComConf_ComSignal_ESPOHVVL, &u1_t_espohvvl);
#endif/* >>ComConf_ComSignal_ESPOHVVL */

#if defined(OXCAN_PDU_RX_CAN_EHV1S23)
    u1_t_rx_cnt = u1_g_oXCANRxEvcnt((U2)OXCAN_PDU_RX_CAN_EHV1S23);
    u1_t_rx_sts = u1_g_oXCANRxStat((U2)OXCAN_PDU_RX_CAN_EHV1S23,
                                   (U2)(OXCAN_RX_SYS_NRX_IGP | OXCAN_RX_SYS_TOE_IGP),
                                   (U2)ECOJDG_EHV1S23_TO_THRSH) & ((U1)COM_TIMEOUT | (U1)COM_NO_RX);
#else
    u1_t_rx_cnt = (U1)OXCAN_RX_RXEV_CNT_UNK;
    u1_t_rx_sts = (U1)COM_NO_RX;
#endif/* >>OXCAN_PDU_RX_CAN_EHV1S23 */
    if(u1_t_espohvid < (U1)ECOJDG_ESPOHVID_NUM){
        *u1p_a_id = u1_sp_ECOJDG_ID_CHK[u1_t_espohvid];
    }
    *u1p_a_scr  = (U1)(u1_t_espohvvl * (U1)ECOJDG_ESPOHVVL_LSB);
    *u1p_a_cnt  = u1_t_rx_cnt;

    return(u1_t_rx_sts);
}
/*===================================================================================================================================*/
/*  U1          u1_g_EcoJdgCfgRxCnt(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1          u1_g_EcoJdgCfgRxCnt(void)
{
#if defined(OXCAN_PDU_RX_CAN_EHV1S23)
    return(u1_g_oXCANRxEvcnt((U2)OXCAN_PDU_RX_CAN_EHV1S23));
#else
    return((U1)OXCAN_RX_RXEV_CNT_UNK);
#endif/* >>OXCAN_PDU_RX_CAN_EHV1S23 */
}
