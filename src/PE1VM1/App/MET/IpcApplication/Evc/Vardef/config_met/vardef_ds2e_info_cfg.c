/*  U4      u4_g_VardefDs2E_info_2004(void)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_VardefDs2E_info_2004(void)
{
    U4        u4_t_max;

#if (__DIAGSUP_DID2004_TYPE__ == 1)
    u4_t_max = (U4)VDF_DS_2E_PC_FLAG1;
#elif (__DIAGSUP_DID2004_TYPE__ == 2)
    u4_t_max = (U4)VDF_DS_2E_PC_FLAG2;
#elif (__DIAGSUP_DID2004_TYPE__ == 3)
    u4_t_max = (U4)VDF_DS_2E_PC_FLAG1 |
               (U4)VDF_DS_2E_PC_FLAG2;
#else
    u4_t_max = (U4)VDF_DS_2E_UNK;
#endif

    return(u4_t_max);
}

