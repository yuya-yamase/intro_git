/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_SW_AC100V_TESTMODE(void)                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_SW_AC100V_TESTMODE(void)
{
    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;

    u1_t_rx     = (U1)0U;
    u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_INA;
    (void)Com_ReceiveSignal(ComConf_ComSignal_ACINVSSW, &u1_t_rx);

    if(u1_t_rx != (U1)0U){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }

    return(u1_t_ava_rx);
}

