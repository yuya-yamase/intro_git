/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_D_DPTZ(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_D_DPTZ(void)
{
    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;

    u1_t_rx = (U1)0U;
#if 0   /* BEV Rebase provisionally */
    (void)Com_ReceiveSignal(ComConf_ComSignal_DBKLAB, &u1_t_rx);
#endif   /* BEV Rebase provisionally */

    u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    if((u1_t_rx != (U1)VDF_ESO_BKL_SW_STS_UNDTRMIND) &&
       (u1_t_rx == u1_s_vdf_eso_dbklab_rx_las      )){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }

    u1_s_vdf_eso_dbklab_rx_las = u1_t_rx;

    return(u1_t_ava_rx);
}
