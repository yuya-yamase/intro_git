/*  U1      u1_g_VdfEsoRx_BSM(void)                                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_BSM(void)
{
    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;

    u1_t_rx = (U1)0U;
    (void)Com_ReceiveSignal(ComConf_ComSignal_BSD_TTR, &u1_t_rx);
    if(u1_t_rx != (U1)0U){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_INA;
    }

    return(u1_t_ava_rx);
}
