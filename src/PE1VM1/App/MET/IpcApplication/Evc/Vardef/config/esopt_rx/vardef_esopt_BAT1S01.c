/*  U1      u1_g_VdfEsoRx_SW_BATWSW(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_SW_BATWSW(void)
{
    static const U1 u1_s_VDF_ESO_SW_BATWSW_ACT = (U1)0U;
    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;
    U1                 u1_t_opt;

    u1_t_opt = u1_g_VardefEsOptAvaByCh((U2)VDF_ESO_CH_MOP);

    u1_t_rx = (U1)0U;
    (void)Com_ReceiveSignal(ComConf_ComSignal_TBW_EN, &u1_t_rx);
    if((u1_t_rx  == u1_s_VDF_ESO_SW_BATWSW_ACT) && (u1_t_opt == (U1)FALSE)){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_INA;
    }

    return(u1_t_ava_rx);
}
