/*  U1      u1_g_VdfEsoRx_HVSYSIND(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_HVSYSIND(void)
{
    U1                 u1_t_brand;
    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;

    u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    u1_t_brand  = u1_s_Vdf_HVSYSINDCalibU1NumChk(u1_CALIB_MCUID0024_BRAND, (U1)CALIB_MCUID0024_NUM, (U1)CALIB_MCUID0024_DEF);
    u1_t_rx     = u1_g_VardefPtsRx();

    if(u1_t_brand != (U1)CALIB_MCUID0024_LEXUS){
        if((u1_t_rx == (U1)VDF_PTS_RX_03_HYB     ) ||
           (u1_t_rx == (U1)VDF_PTS_RX_04_HYB_PLU ) ||
           (u1_t_rx == (U1)VDF_PTS_RX_06_HYB_MOT )){
            u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
        }
    }

    return(u1_t_ava_rx);
}
/*===================================================================================================================================*/
/*  static  U1      u1_s_Vdf_HVSYSINDCalibU1NumChk(const U1 u1_a_CALIBID, const U1 u1_a_NUM, const U1 u1_a_DEF)                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static inline  U1 u1_s_Vdf_HVSYSINDCalibU1NumChk(const U1 u1_a_CALIBID, const U1 u1_a_NUM, const U1 u1_a_DEF)
{
    U1 u1_t_ret;

    u1_t_ret = u1_a_CALIBID;
    if(u1_t_ret >= u1_a_NUM){
        u1_t_ret = u1_a_DEF;
    }

    return(u1_t_ret);
}
