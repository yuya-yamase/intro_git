/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_TPMS(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_TPMS(void)
{
    static const U1 u1_s_VDF_ESO_TPM_VAR = (U1)1U;

    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;

    u1_t_rx = (U1)0U;
#if 0   /* BEV Rebase provisionally */
    (void)Com_ReceiveSignal(ComConf_ComSignal_TPM_VAR, &u1_t_rx);
#endif   /* BEV Rebase provisionally */
    if(u1_t_rx == u1_s_VDF_ESO_TPM_VAR){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    }

    return(u1_t_ava_rx);
}
/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_TPMSCHN(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_TPMSCHN(void)
{
    static const U1 u1_s_VDF_ESO_TPM_DISC_CHN     = (U1)1U;
    static const U1 u1_s_VDF_ESO_TPM_DISC_NOT_CHN = (U1)0U;

    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;

    u1_t_rx = (U1)0U;
#if 0   /* BEV Rebase provisionally */
    (void)Com_ReceiveSignal(ComConf_ComSignal_TPM_DISC, &u1_t_rx);
#endif   /* BEV Rebase provisionally */

    if(u1_t_rx == (U1)u1_s_VDF_ESO_TPM_DISC_CHN){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else if(u1_t_rx == (U1)u1_s_VDF_ESO_TPM_DISC_NOT_CHN){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_INA;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    }

    return(u1_t_ava_rx);
}
