/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_GERPOS(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_GERPOS(void)
{
    U1                 u1_t_sts;
    U1                 u1_t_ava_rx;

    u1_t_sts = u1_s_VdfEso_GERPOSCalibU1NumChk(u1_CALIB_MCUID0025_SPORTS, (U1)CALIB_MCUID0025_NUM, (U1)CALIB_MCUID0025_DEF);

    if ((u1_t_sts == (U1)CALIB_MCUID0025_GR    ) ||
        (u1_t_sts == (U1)CALIB_MCUID0025_GRMN  ) ||
        (u1_t_sts == (U1)CALIB_MCUID0025_FSPORT) ||
        (u1_t_sts == (U1)CALIB_MCUID0025_F     ) ||
        (u1_t_sts == (U1)CALIB_MCUID0025_070D  ))
    {
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    }

    return(u1_t_ava_rx);
}
/*===================================================================================================================================*/
/*  static inline U1    u1_s_VdfEso_GERPOSCalibU1NumChk(const U1 u1_a_CALIBID, const U1 u1_a_NUM, const U1 u1_a_DEF)                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      const U1 u1_a_CALIBID                                                                                            */
/*                  const U1 u1_a_NUM                                                                                                */
/*                  const U1 u1_a_DEF                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static inline U1    u1_s_VdfEso_GERPOSCalibU1NumChk(const U1 u1_a_CALIBID, const U1 u1_a_NUM, const U1 u1_a_DEF)
{
    U1 u1_t_ret;

    u1_t_ret = u1_a_CALIBID;
    if(u1_t_ret >= u1_a_NUM){
        u1_t_ret = u1_a_DEF;
    }

    return(u1_t_ret);
}
