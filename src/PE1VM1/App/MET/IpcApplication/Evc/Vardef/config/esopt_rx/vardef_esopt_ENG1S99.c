/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_OILMNT(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_OILMNT(void)
{
    static const S2 s2_s_VDF_ESO_SW_OILMNT_INACT = (S2)0x8000;
    S2                 s2_t_rx;
    U1                 u1_t_ava_rx;

    s2_t_rx = (S2)0;
    (void)Com_ReceiveSignal(ComConf_ComSignal_OILMNTMI, &s2_t_rx);
    if(s2_t_rx != s2_s_VDF_ESO_SW_OILMNT_INACT){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_INA;
    }

    return(u1_t_ava_rx);
}
/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_OILLVG(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_OILLVG(void)
{
    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;

    u1_t_rx = (U1)0U;
    (void)Com_ReceiveSignal(ComConf_ComSignal_B_EOILVL, &u1_t_rx);
    if (u1_t_rx > (U1)0U) {
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else {
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    }

    return(u1_t_ava_rx);
}
