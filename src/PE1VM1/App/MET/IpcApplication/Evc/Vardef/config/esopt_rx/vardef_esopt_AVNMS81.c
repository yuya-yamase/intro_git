/*  U1      u1_g_VdfEsoRx_MM_MAP(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_MM_MAP(void)
{
    static const U1 u1_s_VDF_ESO_MM_MAP_ACT_NAV_INI     = (U1)0U;
    static const U1 u1_s_VDF_ESO_MM_MAP_ACT_NAV_READY   = (U1)1U;
    static const U1 u1_s_VDF_ESO_MM_MAP_ACT_NAV_UNREADY = (U1)3U;
    static const U1 u1_s_VDF_ESO_MM_MAP_ACT_MAP_INI     = (U1)0U;
    static const U1 u1_s_VDF_ESO_MM_MAP_ACT_MAP_READY   = (U1)1U;
    static const U1 u1_s_VDF_ESO_MM_MAP_ACT_MAP_UNREADY = (U1)3U;

    U1                 u1_t_rx_navi;
    U1                 u1_t_rx_map;
    U1                 u1_t_ava_rx;
    U1                 u1_t_msgsts;

    u1_t_rx_navi = u1_s_VDF_ESO_MM_MAP_ACT_NAV_INI;
    u1_t_rx_map  = u1_s_VDF_ESO_MM_MAP_ACT_MAP_INI;
    u1_t_msgsts = (U1)Com_GetIPDUStatus(MSG_AVNMS81_RXCH1) & (U1)COM_NO_RX;

    (void)Com_ReceiveSignal(ComConf_ComSignal_V_OUT_NAV_STS, &u1_t_rx_navi);
    (void)Com_ReceiveSignal(ComConf_ComSignal_V_OUT_PROJ_STS, &u1_t_rx_map);

    if(u1_t_msgsts == (U1)0U){
        if((u1_t_rx_navi      == u1_s_VDF_ESO_MM_MAP_ACT_NAV_INI) &&
           (u1_t_rx_map       == u1_s_VDF_ESO_MM_MAP_ACT_MAP_INI)){
            u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
        }
        else if((u1_t_rx_navi == u1_s_VDF_ESO_MM_MAP_ACT_NAV_READY)   ||
                (u1_t_rx_navi == u1_s_VDF_ESO_MM_MAP_ACT_NAV_UNREADY) ||
                (u1_t_rx_map  == u1_s_VDF_ESO_MM_MAP_ACT_MAP_READY)   ||
                (u1_t_rx_map  == u1_s_VDF_ESO_MM_MAP_ACT_MAP_UNREADY)){
            u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
        }
        else{
            u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_INA;
        }
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    }

    return(u1_t_ava_rx);
}
