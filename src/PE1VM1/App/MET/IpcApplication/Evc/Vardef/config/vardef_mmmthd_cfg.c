/*===================================================================================================================================*/
/*  U1      u1_g_VardefMmMthdCfgComSts(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VardefMmMthdCfgComSts(void)
{
    U1                 u1_t_sts;
    U1                 u1_t_ret;

    u1_t_sts = u1_g_LcomSpiEtherComSts();

    if(u1_t_sts == (U1)LCOM_SPI_ETHER_COMSCCS){
        u1_t_ret = (U1)VDF_MMMTHD_STS_COMSCCS;
    }
    else{
        u1_t_ret = (U1)VDF_MMMTHD_STS_COMFAIL;
    }

    return(u1_t_ret);
}
