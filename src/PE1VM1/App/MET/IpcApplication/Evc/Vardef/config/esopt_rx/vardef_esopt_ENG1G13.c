/*  U1      u1_g_VdfEsoRx_ECOFLAG(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_ECOFLAG(void)
{
    static const U1 u1_s_VDF_ESO_PTSYS_ISS = (U1)2U;

    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;

    u1_t_rx = (U1)0U;
#if 0   /* BEV Rebase provisionally */
    (void)Com_ReceiveSignal(ComConf_ComSignal_PTSYS, &u1_t_rx);
#endif   /* BEV Rebase provisionally */
    if(u1_t_rx == u1_s_VDF_ESO_PTSYS_ISS){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_INA;
    }

    return(u1_t_ava_rx);
}
