/*  U1      u1_g_VdfEsoRx_MT(void)                                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_MT(void)
{
    U1                 u1_t_rx;
    U1                 u1_t_ava_rx;
    U2                 u2_t_chk;

    u1_t_rx = (U1)0U;
#if 0   /* BEV Rebase provisionally */
    (void)Com_ReceiveSignal(ComConf_ComSignal_VARTRM1, &u1_t_rx);
#endif   /* BEV Rebase provisionally */
    u2_t_chk        = ((U2)TRUE << u1_t_rx);

    if(u1_t_rx == (U1)VDF_ESO_VARTRM1_MT_ACT){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else if(((u2_t_chk & (U2)VDF_ESO_VARTRM1_AT_ACT     ) != (U2)0U) ||
            ((u2_t_chk & (U2)VDF_ESO_VARTRM1_MMT_SMT_ACT) != (U2)0U)){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_INA;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    }

    return(u1_t_ava_rx);
}
