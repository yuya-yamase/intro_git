#define VDF_ESO_GVIF_LINKDOWN                       (0U)
#define VDF_ESO_GVIF_LINKNML                        (1U)
#define VDF_ESO_GVIF_UNDEF1                         (2U)
#define VDF_ESO_GVIF_UNDEF2                         (3U)
