/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_GVIF(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_GVIF(void)
{
    U1                 u1_t_sts;
    U1                 u1_t_ava_rx;

    u1_t_sts = u1_g_XSpiGvifSts();
    
    if(u1_t_sts == (U1)XSPI_GVIF_LINKNML){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    }

    return(u1_t_ava_rx);
}
/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_GVIF_METDISP(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_GVIF_METDISP(void)
{
    U1                 u1_t_sts;
    U1                 u1_t_ava_rx;

    u1_t_sts = u1_g_XSpiGvifSts_METDISP();

    if(u1_t_sts == (U1)XSPI_GVIF_METDSP_NML){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    }

    return(u1_t_ava_rx);
}
