/*-----------------------------------------------------------------------------------------------------------------------------------*/
static U1             u1_s_vdf_eso_gvif_hud;
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Static Function Prototypes                                                                                                       */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Constant Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  void    vd_g_VardefEsOptCfgGvifInit(void)                                                                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefEsOptCfgGvifInit(void)
{
    u1_s_vdf_eso_gvif_hud = (U1)VDF_ESO_GVIF_UNDEF2;
}

/*===================================================================================================================================*/
/*  U1      u1_g_VdfEsoRx_GVIF_HUD(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VdfEsoRx_GVIF_HUD(void)
{
    U1                 u1_t_ava_rx;

    if(u1_s_vdf_eso_gvif_hud == (U1)VDF_ESO_GVIF_LINKNML){
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_ACT;
    }
    else{
        u1_t_ava_rx = (U1)VDF_ESO_AVA_RX_UNK;
    }

    return(u1_t_ava_rx);
}

/*===================================================================================================================================*/
/*  void    vd_g_VdfEsoIn_Put_GVIF_HUD(const U1 u1_a_gvif_sts)                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u1_a_gvif_sts   : gvif link sts                                                                                  */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VdfEsoIn_Put_GVIF_HUD(const U1 u1_a_gvif_sts)
{
    u1_s_vdf_eso_gvif_hud = u1_a_gvif_sts;
}

