/*  void    vd_g_VardefSgaugeBonInit(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefSgaugeBonInit(void)
{
    u1_s_vdf_sguage_rim      = (U1)VDF_SGAUGE_UNK;
    vd_g_Rim_WriteU1(u2_g_VDF_SGAUGE_RIM_U1, u1_s_vdf_sguage_rim);

    u2_s_vdf_sgauge_rx_elpsd = (U2)U2_MAX;
    u1_s_vdf_sgauge_rxc_sta  = u1_g_VDF_SGAUGE_RXC_INT;
    u1_s_vdf_sgauge_rx_las   = (U1)VDF_SGAUGE_1F_NRX;
}
/*===================================================================================================================================*/
/*  void    vd_g_VardefSgaugeRstwkInit(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefSgaugeRstwkInit(void)
{
    U1          u1_t_rim_rx;
    U1          u1_t_rim_chk;

    u1_t_rim_rx  = (U1)VDF_SGAUGE_UNK;
    u1_t_rim_chk = u1_g_Rim_ReadU1withStatus(u2_g_VDF_SGAUGE_RIM_U1, &u1_t_rim_rx) & (U1)RIM_RESULT_KIND_MASK;
    if((u1_t_rim_chk == (U1)RIM_RESULT_KIND_OK) &&
       (u1_t_rim_rx  <= (U1)VDF_SGAUGE_4GAUGE )){
        u1_s_vdf_sguage_rim = u1_t_rim_rx;
    }
    else{
        u1_s_vdf_sguage_rim = (U1)VDF_SGAUGE_UNK;
        vd_g_Rim_WriteU1(u2_g_VDF_SGAUGE_RIM_U1, u1_s_vdf_sguage_rim);
    }

    u2_s_vdf_sgauge_rx_elpsd = (U2)U2_MAX;
    u1_s_vdf_sgauge_rxc_sta  = u1_g_VDF_SGAUGE_RXC_INT;
    u1_s_vdf_sgauge_rx_las   = (U1)VDF_SGAUGE_1F_NRX;
}
/*===================================================================================================================================*/
/*  void    vd_g_VardefSgaugeOpemdEvhk(const U2 u2_a_EOM)                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefSgaugeOpemdEvhk(const U2 u2_a_EOM)
{
    U2                  u2_t_ign;

    u2_t_ign = u2_a_EOM & (U2)VDF_EOM_IGR_ON;
    if(u2_t_ign == (U2)0U){
        u1_s_vdf_sgauge_rxc_sta = u1_g_VDF_SGAUGE_RXC_INT;
        u1_s_vdf_sgauge_rx_las  = (U1)VDF_SGAUGE_1F_NRX;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_VardefSgaugeMainTask(const U2 u2_a_EOM)                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefSgaugeMainTask(const U2 u2_a_EOM)
{
    static const U2     u2_s_VDF_SGAUGE_PERI = ((U2)600U/ (U2)VDF_MAIN_TICK);

    U4                  u4_t_elpsd;

    U2                  u2_t_ign;
    U2                  u2_t_inc;

    U1                  u1_t_rx;
    U1                  u1_t_cnt;

    U2                  u2_t_rxcnt;
    U2                  u2_t_rxcnt_masked;

    u1_t_rx  = (U1)0U;
    u1_t_cnt = u1_g_VardefSgaugeCfgVartrm1chk(&u1_t_rx);
    u2_t_ign = u2_a_EOM & (U2)VDF_EOM_IGR_ON;
    if(u2_t_ign == (U2)0U){
        u1_t_rx = (U1)VDF_SGAUGE_1F_NRX;
    }

    if((u1_t_rx                 > (U1)VDF_SGAUGE_0F_MAX  ) ||
       (u1_s_vdf_sgauge_rxc_sta > u1_g_VDF_SGAUGE_RXC_MAX) ||
       (u1_t_cnt                > u1_g_VDF_SGAUGE_RXC_MAX)){
        u1_s_vdf_sgauge_rxc_sta = u1_t_cnt;
        u1_s_vdf_sgauge_rx_las  = (U1)VDF_SGAUGE_1F_NRX;
    }
    else if(u1_t_rx == u1_s_vdf_sgauge_rx_las){
        u2_t_rxcnt        = (U2)((U2)u1_t_cnt - (U2)u1_s_vdf_sgauge_rxc_sta);
        u2_t_rxcnt_masked = u2_t_rxcnt & (U2)u1_g_VDF_SGAUGE_RXC_MAX;
        u2_t_inc          = u2_t_rxcnt_masked + (U2)1U;
        if(u2_t_inc >= (U2)VDF_SGAUGE_RXC_MAX){
            if((u1_t_rx == (U1)VDF_SGAUGE_00_3GAUGE) ||
               (u1_t_rx == (U1)VDF_SGAUGE_02_3GAUGE)){
                u1_s_vdf_sguage_rim = (U1)VDF_SGAUGE_3GAUGE;
            }
            else{
                u1_s_vdf_sguage_rim = (U1)VDF_SGAUGE_4GAUGE;
            }
            vd_g_Rim_WriteU1(u2_g_VDF_SGAUGE_RIM_U1, u1_s_vdf_sguage_rim);
        }
    }
    else if(u1_t_cnt != u1_s_vdf_sgauge_rxc_sta){
        u1_s_vdf_sgauge_rxc_sta = u1_t_cnt;
        u1_s_vdf_sgauge_rx_las = u1_t_rx;
    }
    else{
        /* do nothing */
    }

    u4_t_elpsd = (U4)u2_s_vdf_sgauge_rx_elpsd + (U4)VDF_NUM_TSLOT;
    if(u4_t_elpsd >= (U4)u2_s_VDF_SGAUGE_PERI){
        u1_s_vdf_sgauge_rxc_sta = u1_t_cnt;
        u1_s_vdf_sgauge_rx_las  = (U1)VDF_SGAUGE_1F_NRX;
        u4_t_elpsd              = (U4)0U;
    }
    u2_s_vdf_sgauge_rx_elpsd = (U2)u4_t_elpsd;
}
/*===================================================================================================================================*/
/*  void    vd_g_VardefSgaugeReset(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefSgaugeReset(void)
{
    u1_s_vdf_sguage_rim = (U1)VDF_SGAUGE_UNK;
    vd_g_Rim_WriteU1(u2_g_VDF_SGAUGE_RIM_U1, u1_s_vdf_sguage_rim);
}
/*===================================================================================================================================*/
/*  U1      u1_g_VardefSgauge(void)                                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VardefSgauge(void)
{
    U1    u1_t_sgauge;

    if(u1_s_vdf_sguage_rim == (U1)VDF_SGAUGE_UNK){
        u1_t_sgauge = (U1)VDF_SGAUGE_3GAUGE;
    }
    else{
        u1_t_sgauge = u1_s_vdf_sguage_rim;
    }
    return(u1_t_sgauge);
}

