/*  void    vd_g_VardefMainTask(void)                                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefMainTask(void)
{
    U2                   u2_t_eom;

    if(u1_s_vdf_tslot_cnt >= (U1)VDF_NUM_TSLOT){
        u1_s_vdf_tslot_cnt = (U1)0U;
    }

    u2_t_eom = u2_g_VardefCfgEomchk();

    vd_g_VardefCfgMainTask(u2_t_eom, u1_s_vdf_tslot_cnt);

    u1_s_vdf_tslot_cnt++;
}
