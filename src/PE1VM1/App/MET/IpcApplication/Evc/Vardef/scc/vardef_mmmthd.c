/*  void    vd_g_VardefMmMthdMainTask(const U2 u2_a_EOM)                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefMmMthdMainTask(const U2 u2_a_EOM)
{
    static const U2           u2_s_VDF_MM_MTHD_TOUT = (U2)20000U / (U2)100U;  /* 20 seconds */

    U1                 u1_t_sts;
    U1                 u1_t_esi_chk;
    U2                 u2_t_mdbit;

    u2_t_mdbit = u2_a_EOM & (U2)VDF_EOM_ACC_ON;
    u1_t_sts = u1_g_VardefMmMthdCfgComSts();

    if(u2_t_mdbit == (U2)0U){
        u2_s_vdf_mmmthd_tocnt = (U2)0U;
    }
    else if((u1_t_sts != (U1)VDF_MMMTHD_STS_COMSCCS      ) && 
       (u1_s_vdf_mmmthd_sts == (U1)VDF_MM_MTHD_UNDEF)){
        u2_s_vdf_mmmthd_tocnt++;
    }
    else{
        if((u1_s_vdf_mmmthd_sts == (U1)VDF_MM_MTHD_ETHER) || 
           (u1_s_vdf_mmmthd_sts == (U1)VDF_MM_MTHD_MMSUBBUS)){
            u2_s_vdf_mmmthd_tocnt = (U2)0U;
        }
        else{
            u1_s_vdf_mmmthd_sts = (U1)VDF_MM_MTHD_ETHER;
            u2_s_vdf_mmmthd_tocnt = (U2)0U;
        }
    }

    if(u2_s_vdf_mmmthd_tocnt >= u2_s_VDF_MM_MTHD_TOUT){
        u1_s_vdf_mmmthd_sts = (U1)VDF_MM_MTHD_MMSUBBUS;
        u2_s_vdf_mmmthd_tocnt = (U2)0U;
    }

#if 0   /* BEV BSW provisionally */
    u1_t_esi_chk = u1_g_ESInspectMdBfield();
#else
    u1_t_esi_chk = (U1)0U;
#endif

    if((u1_s_vdf_mmmthd_sts_nvm != u1_s_vdf_mmmthd_sts) &&
       (u1_t_esi_chk           == (U1)0U              )){
        vd_g_Nvmc_WriteU1(u2_g_MMMTHD_STS__NVMC_U1,u1_s_vdf_mmmthd_sts);
        u1_s_vdf_mmmthd_sts_nvm = u1_s_vdf_mmmthd_sts;
    }
}
