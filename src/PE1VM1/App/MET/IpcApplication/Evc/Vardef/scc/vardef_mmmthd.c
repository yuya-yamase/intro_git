/*  void    vd_g_VardefMmMthdBonInit(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefMmMthdBonInit(void)
{
    U1  u1_t_sts;
    U1  u1_t_data;

    u1_t_data = (U1)0U;

    u1_t_sts = u1_g_Nvmc_ReadU1withSts(u2_g_MMMTHD_STS__NVMC_U1, &u1_t_data);
    if((u1_t_sts & (U1)NVMC_STATUS_KIND_MASK) == (U1)NVMC_STATUS_KIND_OK){
        u1_s_vdf_mmmthd_sts     = u1_t_data;
        u1_s_vdf_mmmthd_sts_nvm = u1_t_data;
    }
    else{
        u1_s_vdf_mmmthd_sts     = (U1)VDF_MM_MTHD_UNDEF;
        u1_s_vdf_mmmthd_sts_nvm = (U1)VDF_MM_MTHD_UNDEF;
    }

    u2_s_vdf_mmmthd_tocnt = (U2)0U;
}
