/*  void    vd_g_VardefNavSpdLmtBonInit(void)                                                                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefNavSpdLmtBonInit(void)
{
    u1_s_vdf_navspdlmt_rx_sta = (U1)u1_g_VDF_NAVSPDLMT_RXC_INT;
    u1_s_vdf_navspdlmt_rx_las = (U1)VDF_NAVSPDLMT_RX_00_UNK;
}
/*===================================================================================================================================*/
/*  void    vd_g_VardefNavSpdLmtRstwkInit(void)                                                                                      */
