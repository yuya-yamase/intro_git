void    vd_g_VardefNavSpdLmtRstwkInit(void)
{
    u1_s_vdf_navspdlmt_rx_sta = (U1)u1_g_VDF_NAVSPDLMT_RXC_INT;
    u1_s_vdf_navspdlmt_rx_las = (U1)VDF_NAVSPDLMT_RX_00_UNK;
}
/*===================================================================================================================================*/
/*  void    vd_g_VardefNavSpdLmtMainTask(const U2 u2_a_EOM)                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefNavSpdLmtMainTask(const U2 u2_a_EOM)
{
    U1  u1_t_sts;
    U1  u1_t_cnt;
    U1  u1_t_rx;
    U2  u2_t_mdbit;

    u2_t_mdbit = u2_a_EOM & (U2)VDF_EOM_ACC_ON;

    if(u2_t_mdbit == (U2)0U){
        u1_s_vdf_navspdlmt_rx_las = (U1)VDF_NAVSPDLMT_RX_00_UNK;
    }
    else{
        u1_t_sts = u1_g_VardefCfgMmMthd();
        if(u1_t_sts == (U1)VDF_MM_MTHD_MMSUBBUS ){
            u1_t_cnt = u1_g_VardefNavSpdLmtCfgMSig(&u1_t_rx);
        }
        else{
            u1_t_cnt = u1_g_VardefNavSpdLmtCfgESig(&u1_t_rx);
        }
        
        if(u1_t_cnt > u1_g_VDF_NAVSPDLMT_RXC_MAX){
        }
        else if(u1_t_cnt != u1_s_vdf_navspdlmt_rx_sta){
            u1_s_vdf_navspdlmt_rx_sta = u1_t_cnt;
            u1_s_vdf_navspdlmt_rx_las = u1_t_rx;
        }
        else{
            /* do nothing */
        }
    }
}
/*===================================================================================================================================*/
/*  U1      u1_g_VardefNavSpdLmtRx(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VardefNavSpdLmtRx(void)
{
    U1    u1_t_sup;

    if(u1_s_vdf_navspdlmt_rx_las == (U1)VDF_NAVSPDLMT_RX_01_USA){
        u1_t_sup = (U1)TRUE;
    }
    else{
        u1_t_sup = (U1)FALSE;
    }
    return(u1_t_sup);
}
