/*  U1      u1_g_VardefZmilrqDispJdg(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_VardefZmilrqDispJdg(void)
{
    U1    u1_t_ava_red;
    U1    u1_t_ava_amber;
    U1    u1_t_dsp_jdg;

    u1_t_ava_red   = u1_g_VardefEsOptAvaByCh((U2)VDF_ESO_CH_ZMILRQ_EVMILRQR);
    u1_t_ava_amber = u1_g_VardefEsOptAvaByCh((U2)VDF_ESO_CH_ZMILRQ_EVMILRQA);

    u1_t_dsp_jdg = (U1)VDF_ZMILRQ_RED;

    if(u1_t_ava_red         == (U1)TRUE){
        u1_t_dsp_jdg = (U1)VDF_ZMILRQ_RED;
    }else if(u1_t_ava_amber == (U1)TRUE){
        u1_t_dsp_jdg = (U1)VDF_ZMILRQ_AMBER;
    }else{
        /*Do Nothing*/
    }

    return(u1_t_dsp_jdg);
}
