/*  U1          u1_g_SbltsyncCfgChmRingKnd(void)                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1          u1_g_SbltsyncCfgChmRingKnd(void)
{
    static const U1 u1_sp_SBLTSYNC_REQ_LIST[SBLTSYNC_NUM_KND] = {
        (U1)WCHIME_REQ_IN_SBLT_FMV,      /* SBLTSYNC_KND_LGL */
        (U1)WCHIME_REQ_IN_SBLT_LV2,      /* SBLTSYNC_KND_LV2 */
        (U1)WCHIME_REQ_IN_SBLT_LV1       /* SBLTSYNC_KND_LV1 */
    };

    U4              u4_t_lpcnt;
    U1              u1_t_ringknd;
    U1              u1_t_chm_req;

    u1_t_ringknd = (U1)U1_MAX;
    u1_t_chm_req = u1_g_wChimeReqSel((U1)WCHIME_CH_03, vdp_PTR_NA);
    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)SBLTSYNC_NUM_KND; u4_t_lpcnt++){
        if(u1_t_chm_req == u1_sp_SBLTSYNC_REQ_LIST[u4_t_lpcnt]){
            u1_t_ringknd = (U1)u4_t_lpcnt;
        }
    }

    return(u1_t_ringknd);
}

