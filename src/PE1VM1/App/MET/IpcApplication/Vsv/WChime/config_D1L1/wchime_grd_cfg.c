/*  U1      u1_g_wChimeCfgMstrReq(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_wChimeCfgMstrReq(void)
{
    U1    u1_t_req;
    U1    u1_t_mstrreq;

    u1_t_req     = (U1)FALSE;

    u1_t_mstrreq = u1_g_HmiWchimeMstrReq();

    if(u1_s_wchime_master_pre != u1_t_mstrreq){
        if((u1_t_mstrreq == (U1)HMIWCHIME_ON1) ||
           (u1_t_mstrreq == (U1)HMIWCHIME_ON2)){
            u1_t_req = (U1)TRUE;
        }
    }
    u1_s_wchime_master_pre = u1_t_mstrreq;

    return(u1_t_req);
}
