/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Constant Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
const U2            u2_g_GAGDST_REVIND_SRC_UNK = (U2)ENGSPD_STSBIT_UNKNOWN;     /* Source Signal Unknown                             */
const U2            u2_g_GAGDST_REVIND_SRC_INV = (U2)ENGSPD_STSBIT_INVALID;     /* Source Signal Invalid                             */

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  void      vd_g_GagdstRevindCfgTh(U2 * u2p_a_fix_th, U2 * u2p_a_opt_th)                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void      vd_g_GagdstRevindCfgTh(U2 * u2p_a_fix_th, U2 * u2p_a_opt_th)
{
    vd_s_GagdstRevindCfgFixedTh(u2p_a_fix_th);
    if((*u2p_a_fix_th) < (U2)U2_MAX){
        vd_s_GagdstRevindCfgOptTh(u2p_a_opt_th);
        if(((*u2p_a_opt_th) < (U2)GAGDST_REVIND_TH_INV) &&
            ((*u2p_a_opt_th) > (*u2p_a_fix_th))         ){
            (*u2p_a_opt_th) = (U2)GAGDST_REVIND_TH_INV;
        }
    }
    else{
        (*u2p_a_opt_th) = (U2)U2_MAX;
    }
}

/*===================================================================================================================================*/
/*  static void     vd_s_GagdstRevindCfgFixedTh(U2 * u2p_a_fix_th)                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void     vd_s_GagdstRevindCfgFixedTh(U2 * u2p_a_fix_th)
{

    U2                  u2_t_redzone;
    U2                  u2_t_fix_th;

    u2_t_fix_th = (U2)U2_MAX;

    u2_t_redzone = u2_g_EngspdVarrzGetRzrpm();
    if( (u2_t_redzone >= (U2)ENGSPD_VARRZ_RZRPM_MIN)   &&
        (u2_t_redzone <= (U2)ENGSPD_VARRZ_RZRPM_MAX)){
        u2_t_fix_th = u2_t_redzone;
    }

    (*u2p_a_fix_th) = u2_t_fix_th;
}

/*===================================================================================================================================*/
/*  static void     vd_s_GagdstRevindCfgOptTh(U2 * u2p_a_opt_th)                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void     vd_s_GagdstRevindCfgOptTh(U2 * u2p_a_opt_th)
{
    U1                  u1_t_opt_cstm;
    U1                  u1_t_opt_th;

    u1_t_opt_cstm = u1_g_McstBf((U1)MCST_BFI_REV_IND);

    if(u1_t_opt_cstm == (U1)MCST_REV_IND_ON){
        u1_t_opt_th = u1_g_McstBf((U1)MCST_BFI_REV_ST);

        if(u1_t_opt_th >= (U1)GAGDST_REVIND_OPTTH_MIN){
            *u2p_a_opt_th = (U2)u1_t_opt_th * (U2)GAGDST_REVIND_MCST_RPM_LSB;
        }
    }
}

