/*  void    vd_g_GagdstAcdcTxInit(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_GagdstAcdcTxInit(void)
{
    U1                                  u1_t_tx;                                /* Transmission data                                 */

    u1_t_tx = (U1)GAGDST_ACDC_TX_FUELWRNG_UNK;
    (void)Com_SendSignal(ComConf_ComSignal_FUELWRNG, &u1_t_tx);
}
/*===================================================================================================================================*/
/*  void    vd_g_GagdstAcdcTxFinhk(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_GagdstAcdcTxFinhk(void)
{
    U1                                  u1_t_tx;                                /* Transmission data                                 */
    U1                                  u1_t_lw_sts;                            /* Low Fuel Warning Status                           */

    u1_t_lw_sts = u1_g_GagdstLowFuel((U1)TRUE);
    if((u1_t_lw_sts <  (U1)GAGDST_LOWFUEL_NUM) &&
       (u1_t_lw_sts != (U1)GAGDST_LOWFUEL_OFF)){
        u1_t_tx = (U1)GAGDST_ACDC_TX_FUELWRNG_ON;
    }
    else{
        u1_t_tx = (U1)GAGDST_ACDC_TX_FUELWRNG_OFF;
    }

    (void)Com_SendSignal(ComConf_ComSignal_FUELWRNG, &u1_t_tx);
}
