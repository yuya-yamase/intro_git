/*===================================================================================================================================*/
/*  U1      u1_g_GagdstRevpeakCfgCstmOn(void)                                                                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_GagdstRevpeakCfgCstmOn(void)
{
    U1                  u1_t_cstm;
    U1                  u1_t_ret;

    u1_t_ret  = (U1)FALSE;

    u1_t_cstm = u1_g_McstBf((U1)MCST_BFI_REV_PK);
    if(u1_t_cstm == (U1)MCST_REV_PK_ON){
        u1_t_ret = (U1)TRUE;
    }

    return(u1_t_ret);
}

