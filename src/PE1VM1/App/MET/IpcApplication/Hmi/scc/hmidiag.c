/*  void    vd_g_HmiDiagInit(void)                                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiDiagInit(void)
{
    U4  u4_t_loop;

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIDIAG_SIG_NUM ; u4_t_loop++){
        u1_sp_hmidiag_swdata[u4_t_loop] = (U1)0U;
        u1_sp_hmidiag_swpredata[u4_t_loop] = (U1)0U;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_HmiDiagMainTask(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiDiagMainTask(void)
{
    U1    u1_t_sts;
    U1    u1_t_kind;
    U2    u2_t_swcnt;

    static const U2 u2_s_HMIDIAG_ID_HUDPOS = (U2)RIMID_U2_DS_22_10A7_MM_HUD_POS;

    u2_t_swcnt   = (U2)0U;

    u1_t_sts  = u1_g_Rim_ReadU2withStatus(u2_s_HMIDIAG_ID_HUDPOS , &u2_t_swcnt);
    u1_t_kind = u1_t_sts & (U1)RIM_RESULT_KIND_MASK;

    if ((u1_t_kind == (U1)RIM_RESULT_KIND_OK) &&
        (u2_t_swcnt < (U2)U2_MAX            ))  {
        if((u1_sp_hmidiag_swdata[HMIDIAG_VIPOS_UP] == (U1)HMIDIAG_SW_OPE) &&
           (u1_sp_hmidiag_swdata[HMIDIAG_VIPOS_DN] == (U1)HMIDIAG_SW_OPE)) {
            /* Do Nothing */
        }
        else if ((u1_sp_hmidiag_swdata[HMIDIAG_VIPOS_UP] != u1_sp_hmidiag_swpredata[HMIDIAG_VIPOS_UP]) &&
           (u1_sp_hmidiag_swdata[HMIDIAG_VIPOS_UP] == (U1)HMIDIAG_SW_OPE                       ))  {
            u2_t_swcnt++;
            vd_g_Rim_WriteU2(u2_s_HMIDIAG_ID_HUDPOS, u2_t_swcnt);
        }
        else if ((u1_sp_hmidiag_swdata[HMIDIAG_VIPOS_DN] != u1_sp_hmidiag_swpredata[HMIDIAG_VIPOS_DN]) &&
                 (u1_sp_hmidiag_swdata[HMIDIAG_VIPOS_DN] == (U1)HMIDIAG_SW_OPE                       ))  {
            u2_t_swcnt++;
            vd_g_Rim_WriteU2(u2_s_HMIDIAG_ID_HUDPOS, u2_t_swcnt);
        }
        else {
            /* Do Nothing */
        }
    }
    else{
        /* Do Nothing */
    }

}

/*===================================================================================================================================*/
/*  void  vd_g_HmiDiagPut(const U1 u1_a_SIG, const U1 u1_a_SIG_ID)                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u1_a_SIG     :  recieve signal                                                                                   */
/*                  u1_a_SIG_ID  :  recieve signal id                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void  vd_g_HmiDiagPut(const U1 u1_a_SIG, const U1 u1_a_SIG_ID)
{
    U1 u1_t_sts;
    U1 u1_t_mmmthd;

    u1_t_sts = u1_g_HmiMM2C118NStsChk();
    u1_t_mmmthd = u1_g_VardefEsOptAvaByCh((U2)VDF_ESO_CH_AISETH);

    if((u1_t_sts != (U1)HMIMM2C118N_STS_NML) && 
        (u1_t_mmmthd == (U1)TRUE)){
        u1_sp_hmidiag_swpredata[u1_a_SIG_ID] = (U1)0U;
        u1_sp_hmidiag_swdata[u1_a_SIG_ID] = (U1)0U;
    }
    else if (u1_a_SIG_ID < (U1)HMIDIAG_SIG_NUM) {
        u1_sp_hmidiag_swpredata[u1_a_SIG_ID] = u1_sp_hmidiag_swdata[u1_a_SIG_ID];
        u1_sp_hmidiag_swdata[u1_a_SIG_ID] = u1_a_SIG;
    }
    else{
        /* Do Nothing */
    }
}

/*===================================================================================================================================*/
/*  U1  u1_g_HmiDiagSwGet(const U1 u1_a_ID)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u1_a_ID  :  recieve signal id                                                                                    */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_HmiDiagSwGet(const U1 u1_a_ID)
{
    U1 u1_t_req;

    u1_t_req = (U1)U1_MAX;

    if (u1_a_ID < (U1)HMIDIAG_SIG_NUM) {
        u1_t_req = u1_sp_hmidiag_swdata[u1_a_ID];
    }

    return(u1_t_req);
}

