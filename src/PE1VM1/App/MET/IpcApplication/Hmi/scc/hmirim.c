/*  void    vd_g_HmiRimMainTask(void)                                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiRimMainTask(void)
{

    const ST_HMIRIM_PRPTY * stp_t_prpry;
    U4 *                    u4p_t_data;
    U4 *                    u4p_t_data_put;
    U4                      u4_t_loop;
    U2                      u2_t_id;
    U4                      u4_t_def;
    U1                      u1_t_sts;
    U1                      u1_t_kind;

    stp_t_prpry     = &st_sp_HMIRIM_PRPTY[0];
    u4p_t_data      = &u4_sp_hmirim[0];
    u4p_t_data_put  = &u4_sp_hmirim_put[0];

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIRIM_NUM ; u4_t_loop++){
        u2_t_id    = stp_t_prpry->u2_id;
        u4_t_def   = stp_t_prpry->u4_def;
        if( (*u4p_t_data) != (*u4p_t_data_put) ){
            vd_g_Rim_WriteU4(u2_t_id , *u4p_t_data_put);
            *u4p_t_data    = *u4p_t_data_put;
        }
        else{
            u1_t_sts   = u1_g_Rim_ReadU4withStatus(u2_t_id , u4p_t_data);
            u1_t_kind  = u1_t_sts & (U1)RIM_RESULT_KIND_MASK;
            if(u1_t_kind != (U1)RIM_RESULT_KIND_OK){
                *u4p_t_data     = u4_t_def;
                *u4p_t_data_put = u4_t_def;
                vd_g_Rim_WriteU4(u2_t_id , u4_t_def);
            }
        }
        stp_t_prpry++;
        u4p_t_data++;
        u4p_t_data_put++;
    }
}

/*===================================================================================================================================*/
/*  U4      u4_g_HmiRim(const U1 u1_a_ID)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_HmiRim(const U1 u1_a_ID)
{
    U4  u4_t_ret;

    u4_t_ret = (U4)U4_MAX;
    if(u1_a_ID < (U1)HMIRIM_NUM){
        u4_t_ret = u4_sp_hmirim[u1_a_ID];
    }
    return(u4_t_ret);
}

/*===================================================================================================================================*/
/*  void      vd_g_HmiRimPut(const U1 u1_a_ID , const U4 u4_a_DATA)                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void      vd_g_HmiRimPut(const U1 u1_a_ID , const U4 u4_a_DATA) 
{
    if(u1_a_ID < (U1)HMIRIM_NUM){
        u4_sp_hmirim_put[u1_a_ID] = u4_a_DATA;
    }
}
