/*===================================================================================================================================*/
/*  void    vd_g_HmiFuelInit(void)                                                                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiFuelInit(void)
{
    u1_s_hmifuel_rmc_act = (U1)HMIFUEL_UPDT_UD2;
    u1_s_hmifuel_rmc_req = (U1)FALSE;
    u2_s_hmifuel_to      = (U2)HMIPROXY_TOC_MAX;
}

/*===================================================================================================================================*/
/*  void    vd_g_HmiFuelMainTask(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiFuelMainTask(void)
{
    static const U2 u2_s_HMIFUEL_TO = (U2)1000U / (U2)100U;
    U1              u1_t_rmc_act;
    U1              u1_t_to;

    u1_t_to = u1_g_HmiProxyToc(&u2_s_hmifuel_to, u2_s_HMIFUEL_TO);

    if(u1_t_to == (U1)FALSE){
        u1_t_rmc_act = u1_g_FuelvolTauEasAct((U1)FUEL_TAU_EAS_ID_AVG_I, (U1)FUEL_TAU_EAS_EDG_CRNT);

        if(u1_t_rmc_act == (U1)TRUE){
            u1_s_hmifuel_rmc_act = (U1)HMIFUEL_UPDT_REQ;
        }
        else{
            u1_s_hmifuel_rmc_act = (U1)HMIFUEL_UPDT_UD2;
        }

        if(u1_s_hmifuel_rmc_req == (U1)TRUE){
            (void)u1_g_FuelvolTauRcmAct();
        }
        else {
            vd_g_FuelvolTauRcmDeAct();
        }
    }
    else{
        vd_g_FuelvolTauRcmDeAct();
        u1_s_hmifuel_rmc_act = (U1)HMIFUEL_UPDT_UD2;
    }
}

/*===================================================================================================================================*/
/*  U1  u1_g_HmiFuelAct(void)                                                                                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1  u1_g_HmiFuelAct(void)
{
    return(u1_s_hmifuel_rmc_act);
}

/*===================================================================================================================================*/
/*  void  vd_g_HmiFuelPut(const U1 u1_a_REQ)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void  vd_g_HmiFuelPut(const U1 u1_a_REQ)
{
    u1_s_hmifuel_rmc_req = u1_a_REQ;
    u2_s_hmifuel_to      = (U2)HMIPROXY_TOC_INI;
}

