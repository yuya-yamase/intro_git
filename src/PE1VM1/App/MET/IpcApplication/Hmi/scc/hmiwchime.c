/*===================================================================================================================================*/
/*  void    vd_g_HmiWchimeInit(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiWchimeInit(void)
{
    U4  u4_t_loop;
    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIWCHIME_NUM ; u4_t_loop++){
        u1_sp_hmiwchime_req[u4_t_loop] = (U1)HMIWCHIME_UNDEF;
    }
}

