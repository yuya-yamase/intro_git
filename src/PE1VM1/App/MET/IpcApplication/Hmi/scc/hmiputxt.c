/*===================================================================================================================================*/
/*  void    vd_g_HmiPuTxtInit(void)                                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiPuTxtInit(void)
{
    U4  u4_t_loop;
    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIPUTXT_NWORD ; u4_t_loop++){
        u4_sp_hmiputxt_req[u4_t_loop] = (U4)0U;
    }
    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIPUTXT_WRNDETAIL_NUM ; u4_t_loop++){
        u2_sp_hmiputxt_detail[u4_t_loop] = (U2)U2_MAX;
    }
    vd_g_HmiPuTxtCfgInit();
}

/*===================================================================================================================================*/
/*  void    vd_g_HmiPuTxtMainTask(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiPuTxtMainTask(void)
{
    U4  u4_t_mask[HMIPUTXT_NWORD];
    U4  u4_t_loop;

    vd_g_HmiPuTxtCfgReq(&u4_sp_hmiputxt_req[0]);
    vd_g_HmiPuTxtCfgDetail(&u2_sp_hmiputxt_detail[0]);

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIPUTXT_NWORD ; u4_t_loop++){
        u4_t_mask[u4_t_loop] = (U4)U4_MAX;
    }
    vd_g_HmiPuTxtCfgVarmask(&u4_t_mask[0]);
    vd_g_HmiPuTxtCfgThevmHidmask(&u4_t_mask[0]);
    vd_g_HmiPuTxtCfgCstmask(&u4_t_mask[0]);

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIPUTXT_NWORD ; u4_t_loop++){
        u4_sp_hmiputxt_req[u4_t_loop] &= u4_t_mask[u4_t_loop];
    }
}

/*===================================================================================================================================*/
/*  void    vd_g_HmiPuTxt(U4 * u4_ap_req , const U1 u1_a_NWORD)                                                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiPuTxt(U4 * u4_ap_req , const U1 u1_a_NWORD)
{
    U4  u4_t_loop;

    if((u4_ap_req   != vdp_PTR_NA           ) &&
       (u1_a_NWORD  >= (U1)HMIPUTXT_NUM)    ){
        for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIPUTXT_NWORD ; u4_t_loop++){
            u4_ap_req[u4_t_loop] = u4_sp_hmiputxt_req[u4_t_loop];
        }
        u4_ap_req[HMIPUTXT_NWORD + 0U] = (U4)u2_sp_hmiputxt_detail[0];
    }

}

