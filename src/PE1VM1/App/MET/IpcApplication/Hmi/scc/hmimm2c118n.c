/*===================================================================================================================================*/
/*  void    vd_g_HmiMM2C118NInit(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiMM2C118NInit(void)
{
    U4  u4_t_loop;    /* loop counter */

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIMM2C118N_ETH_DTA_NUM; u4_t_loop++){
        u4_sp_hmimm2c118n_ethbuf[u4_t_loop] = (U4)0U;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_HmiMM2C118NMainTask(void)                                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiMM2C118NMainTask(void)
{
    U4 u4_t_loop;    /* loop counter */
    U1 u1_t_sig;     /* demand signal */
    U1 u1_t_sts;     /* recieve status */

    u1_t_sts = u1_g_HmiMM2C118NStsChk();

    if(u1_t_sts == (U1)HMIMM2C118N_STS_NO_RX){
        vd_g_HmiMM2C118NInit();
    }

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)u1_g_HMIMM2C118N_SIG_NUM ; u4_t_loop++){
        if(((st_gp_MM2C118N_SIGIF[u4_t_loop].fp_vd_send) != vdp_PTR_NA)  &&
           ((st_gp_MM2C118N_SIGIF[u4_t_loop].fp_u1_read) != vdp_PTR_NA)){
            u1_t_sig = (st_gp_MM2C118N_SIGIF[u4_t_loop].fp_u1_read)((U1)u4_t_loop, &u4_sp_hmimm2c118n_ethbuf[HMIMM2C118N_FIRST_DTA]);
            (st_gp_MM2C118N_SIGIF[u4_t_loop].fp_vd_send)(u1_t_sig);
        }
    }

    vd_g_HmiGatewayPut((U1)u4_sp_hmimm2c118n_ethbuf[HMIMM2C118N_STS_DTA], &u4_sp_hmimm2c118n_ethbuf[HMIMM2C118N_FIRST_DTA]);
}

/*===================================================================================================================================*/
/*  void    vd_g_HmiMM2C118NDataPut(const U4 * u4_ap_REQ)                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u4_ap_REQ   : MM2C118N address                                                                                   */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiMM2C118NDataPut(const U4 * u4_ap_REQ)
{
    U4 u4_t_loop;    /* loop counter */

    if(u4_ap_REQ != vdp_PTR_NA) {

        for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIMM2C118N_ETH_DTA_NUM ; u4_t_loop++){
            u4_sp_hmimm2c118n_ethbuf[u4_t_loop] = u4_ap_REQ[u4_t_loop];
        }
        u4_sp_hmimm2c118n_ethbuf[HMIMM2C118N_STS_DTA] &= (U4)HMIMM2C118N_STS_MASK;
    }
}

/*===================================================================================================================================*/
/*  U1      u1_g_HmiMM2C118NStsChk(void)                                                                                             */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         status result                                                                                                    */
/*===================================================================================================================================*/
U1      u1_g_HmiMM2C118NStsChk(void)
{
    U1 u1_t_result;    /* status result */
    U1 u1_t_norx;      /* Ethernet not recieve status */
    U1 u1_t_tout;      /* Ethernet timeout status */

    u1_t_result = (U1)HMIMM2C118N_STS_NML;
    u1_t_norx = (U1)(u4_sp_hmimm2c118n_ethbuf[HMIMM2C118N_STS_DTA] & (U4)HMIMM2C118N_1BITMASK);
    u1_t_tout = (U1)((u4_sp_hmimm2c118n_ethbuf[HMIMM2C118N_STS_DTA] >> HMIMM2C118N_1BITSHIFT) & (U4)HMIMM2C118N_1BITMASK);

    if(u1_t_norx == (U1)HMIMM2C118N_NORX_STS){                /* Ethernet STATUS is NotRecieve */
        u1_t_result = (U1)HMIMM2C118N_STS_NO_RX;
    }else if (u1_t_tout == (U1)HMIMM2C118N_TOUT_STS) {        /* Ethernet STATUS is TimeOut */
        u1_t_result = (U1)HMIMM2C118N_STS_TIMEOUT;
    }else{
        /* Do Nothing */
    }

    return(u1_t_result);
}

