#define HMIMM2C118N_STS_MASK                        (0x03U)
#define HMIMM2C118N_STS_DTA                         (0U)
#define HMIMM2C118N_FIRST_DTA                       (1U)
#define HMIMM2C118N_NORX_STS                        (1U)
#define HMIMM2C118N_TOUT_STS                        (1U)
#define HMIMM2C118N_1BITSHIFT                       (1U)
#define HMIMM2C118N_1BITMASK                        (0x01U)
