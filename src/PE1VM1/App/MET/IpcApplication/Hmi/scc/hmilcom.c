/*  void     vd_g_HmiLcomInit(void)                                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiLcomInit(void)
{
    U4  u4_t_loop;

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMILCOM_SIG_NUM ; u4_t_loop++){
        u1_sp_hmilcom_swdata[u4_t_loop] = (U1)0U;
        u1_sp_hmilcom_swpredata[u4_t_loop] = (U1)0U;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_HmiLcomMainTask(void)                                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiLcomMainTask(void)
{
    U1    u1_t_sts;
    U1    u1_t_kind;
    U2    u2_t_swcnt;

    static const U2 u2_s_HMILCOM_ID_DSP = (U2)RIMID_U2_DS_22_10A7_MM_MET_DSP;

    u2_t_swcnt   = (U2)0U;

    if ((u1_sp_hmilcom_swpredata[HMILCOM_L_CONT_C] != u1_sp_hmilcom_swdata[HMILCOM_L_CONT_C]) &&
        ((u1_sp_hmilcom_swdata[HMILCOM_L_CONT_C]    == (U1)HMILCOM_SW_LEFT ) ||
         (u1_sp_hmilcom_swdata[HMILCOM_L_CONT_C]    == (U1)HMILCOM_SW_RIGHT)                ))
    {
        u1_t_sts  = u1_g_Rim_ReadU2withStatus(u2_s_HMILCOM_ID_DSP, &u2_t_swcnt);
        u1_t_kind = u1_t_sts & (U1)RIM_RESULT_KIND_MASK;

        if ((u1_t_kind == (U1)RIM_RESULT_KIND_OK) &&
            (u2_t_swcnt < (U2)U2_MAX            ))  {
             u2_t_swcnt++;
             vd_g_Rim_WriteU2(u2_s_HMILCOM_ID_DSP, u2_t_swcnt);
        }
        else{
            /* Do Nothing */
        }
    }
    else{
        /* Do Nothing */
    }

}

/*===================================================================================================================================*/
/*  void  vd_g_HmiLcomPut(const U1 u1_a_SIG, const U1 u1_a_SIG_ID)                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u1_a_SIG     :  recieve signal                                                                                   */
/*                  u1_a_SIG_ID  :  recieve signal id                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void  vd_g_HmiLcomPut(const U1 u1_a_SIG, const U1 u1_a_SIG_ID)
{
    U1 u1_t_sts;
    U1 u1_t_mmmthd;

    u1_t_sts = u1_g_HmiMM2C118NStsChk();
    u1_t_mmmthd = u1_g_VardefMmMthd();

    if((u1_t_sts != (U1)HMIMM2C118N_STS_NML) && 
        (u1_t_mmmthd == (U1)VDF_ESO_METHOD_ETHER)){
        u1_sp_hmilcom_swpredata[u1_a_SIG_ID] = (U1)0U;
        u1_sp_hmilcom_swdata[u1_a_SIG_ID] = (U1)0U;
    }else if (u1_a_SIG_ID < (U1)HMILCOM_SIG_NUM) {
        u1_sp_hmilcom_swpredata[u1_a_SIG_ID] = u1_sp_hmilcom_swdata[u1_a_SIG_ID];
        u1_sp_hmilcom_swdata[u1_a_SIG_ID] = u1_a_SIG;
    }else{
        /* Do Nothing */
    }
}

/*===================================================================================================================================*/
/*  U1  u1_g_HmiLcomSwGet(const U1 u1_a_ID)                                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u1_a_ID  :  recieve signal id                                                                                    */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_HmiLcomSwGet(const U1 u1_a_ID)
{
    U1 u1_t_req;

    u1_t_req = (U1)U1_MAX;

    if (u1_a_ID < (U1)HMILCOM_SIG_NUM) {
        u1_t_req = u1_sp_hmilcom_swdata[u1_a_ID];
    }
    return(u1_t_req);
}

