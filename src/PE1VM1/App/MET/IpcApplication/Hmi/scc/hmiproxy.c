/*===================================================================================================================================*/
/*  void    vd_g_HmiProxyMainTask(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiProxyMainTask(void)
{
    U4                  u4_t_schd;
    const ST_HMIPROXY * stp_t_hmiproxy;
    U4                  u4_t_jdg;

    u4_t_schd      = u4_g_SchdlrTaskActivated();
    stp_t_hmiproxy = &st_gp_HMIPROXY[0];
    while((stp_t_hmiproxy->fp_vd_TASK) != vdp_PTR_NA){
        u4_t_jdg = u4_t_schd & stp_t_hmiproxy->u4_schd;
        if(u4_t_jdg != (U4)0U){
            (stp_t_hmiproxy->fp_vd_TASK)();
        }
        stp_t_hmiproxy++;
    }

    u1_s_hmiproxy_shtdwnok = u1_g_Fpcall_u1_And(&fp_gp_u1_HMIPROXY_SHTDWN_CHK[0], (U2)HMIPROXY_SHTDWN_NUM);

}

