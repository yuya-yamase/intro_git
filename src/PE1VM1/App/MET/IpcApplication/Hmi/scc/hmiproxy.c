/*===================================================================================================================================*/
/*  U1      u1_g_HmiProxyToc(U2 * u2p_a_cnt, const U2 u2_a_TO_THRSH)                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_HmiProxyToc(U2 * u2p_a_cnt, const U2 u2_a_TO_THRSH)
{
    U1  u1_t_to;

    if((*u2p_a_cnt) >= (U2)HMIPROXY_TOC_INI){
        (*u2p_a_cnt) = (U2)0U;
    }
    else if((*u2p_a_cnt) < (U2)HMIPROXY_TOC_MAX){
        (*u2p_a_cnt)++;
    }
    else{
        /* Do Nothing */
    }

    u1_t_to = (U1)TRUE;
    if((*u2p_a_cnt) < u2_a_TO_THRSH){
        u1_t_to = (U1)FALSE;
    }
    return(u1_t_to);
}

