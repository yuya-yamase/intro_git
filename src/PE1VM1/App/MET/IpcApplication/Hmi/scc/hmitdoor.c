/*===================================================================================================================================*/
/*  void    vd_g_HmiTdoorBonRstInit(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiTdoorBonRstInit(void)
{
    u1_s_tdoor_update_cntt = (U1)HMITDOOR_VALUE_INIT;
    u1_s_tdoor_dcty_sts = (U1)HMITDOOR_VALUE_INIT;

    vd_g_Rim_WriteU1((U2)RIMID_U1_TDOOR_DCTY_STS, (U1)HMITDOOR_VALUE_INIT);
}

/*===================================================================================================================================*/
/*  void    vd_g_HmiTdoorWkupInit(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiTdoorWkupInit(void)
{
    U1    u1_t_sts;
    U1    u1_t_dcty_sts;

    u1_s_tdoor_update_cntt = (U1)HMITDOOR_VALUE_INIT;
    u1_t_dcty_sts = (U1)HMITDOOR_VALUE_INIT;

    u1_t_sts = u1_g_Rim_ReadU1withStatus((U2)RIMID_U1_TDOOR_DCTY_STS, &u1_t_dcty_sts) & (U1)RIM_RESULT_KIND_MASK;

    if(u1_t_sts != (U1)RIM_RESULT_KIND_OK)
    {
        u1_s_tdoor_dcty_sts = (U1)HMITDOOR_VALUE_INIT;
    }
    else
    {
        u1_s_tdoor_dcty_sts = u1_t_dcty_sts;
    }
}

/*===================================================================================================================================*/
/*  U1    u1_g_HmiTdoorDctyStsGet(void)                                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1    u1_g_HmiTdoorDctyStsGet(void)
{
   return (u1_s_tdoor_dcty_sts);
}

/*===================================================================================================================================*/
/*  void      vd_g_HmiTdoorCanRxBdb1s01(void)                                                                                        */
/*  Attention : Call timing is when the message is received [Call from RxHook in CAN]                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiTdoorCanRxBdb1s01(void)
{
    U1    u1_t_retval;

    u1_t_retval = (U1)HMITDOOR_VALUE_INIT;
    (void)Com_ReceiveSignal(ComConf_ComSignal_DCTY, &u1_t_retval);

    if(u1_s_tdoor_dcty_sts != u1_t_retval)
    {
        if(u1_s_tdoor_update_cntt < (U1)HMITDOOR_COUNT_MAX)
        {
            u1_s_tdoor_update_cntt++;
        }
        else
        {
            u1_s_tdoor_dcty_sts = u1_t_retval;
            u1_s_tdoor_update_cntt = (U1)HMITDOOR_VALUE_INIT;
            vd_g_Rim_WriteU1((U2)RIMID_U1_TDOOR_DCTY_STS, u1_t_retval);
        }
    }
    else
    {
        u1_s_tdoor_update_cntt = (U1)HMITDOOR_VALUE_INIT;
    }
}

