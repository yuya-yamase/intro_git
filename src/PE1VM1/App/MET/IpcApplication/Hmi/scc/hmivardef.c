/*  Macro Definitions                                                                                                                */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define HMIVARDEF_SIG_NUM                         (5U)

#define HMIVARDEF_NUM_CNTUP                       (1U)
#define HMIVARDEF_CNT_INIT                        (0xFFU)

#define HMIVARDEF_CNT_MAX                         (0x7FU)

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Type Definitions                                                                                                                 */
/*-----------------------------------------------------------------------------------------------------------------------------------*/

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Variable Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Static Function Prototypes                                                                                                       */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static U1 u1_sp_hmivardef_sigdata[HMIVARDEF_SIG_NUM];
static U1 u1_sp_hmivardef_rcvcnt[HMIVARDEF_SIG_NUM];
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Constant Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static const U1 u1_sp_HMIVARDEF_FLAMEID[HMIVARDEF_SIG_NUM] =
{
    (U1)HMIVARDEF_FLM_MM2C100N,
    (U1)HMIVARDEF_FLM_MM2C107N,
    (U1)HMIVARDEF_FLM_MM2C112N,
    (U1)HMIVARDEF_FLM_MM2C118N,
    (U1)HMIVARDEF_FLM_MM2C112N_CTY
};
static const ST_LCOM_RX_CNT u1_sp_HMIVARDEF_RXCNT[HMIVARDEF_SIG_NUM] =
{
    { &u1_g_LcomSpiMm2c100n_rxcnt },
    { &u1_g_LcomSpiMm2c107n_rxcnt },
    { &u1_g_LcomSpiMm2c112n_rxcnt },
    { &u1_g_LcomSpiMm2c118n_rxcnt },
    { &u1_g_LcomSpiMm2c112n_rxcnt }
};
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  void    void    vd_g_HmiVardefInit(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_HmiVardefInit(void)
{
    U4  u4_t_loop;

    for(u4_t_loop = (U4)0U ; u4_t_loop < (U4)HMIVARDEF_SIG_NUM ; u4_t_loop++){
        u1_sp_hmivardef_rcvcnt[u4_t_loop]  = (U1)HMIVARDEF_CNT_INIT;
        u1_sp_hmivardef_sigdata[u4_t_loop] = (U1)U1_MAX;
    }
}
