/*===================================================================================================================================*/
/*  static void u1_s_Mm2c124nReadSig(const U1 u1_a_SIG_IDX, const U4 * u4_ap_REQ)                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u1_a_SIG_IDX  :  signal index number                                                                             */
/*                  u4_ap_REQ     :  ethernet data buffer                                                                            */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U1 u1_s_Mm2c124nReadSig(const U1 u1_a_SIG_IDX, const U4 * u4_ap_REQ)
{
    U1    u1_t_sig;       /* recieve signal */
    U4    u4_t_buf;       /* MM Sub-Bus buffer */
    U1    u1_t_bitpos;    /* bit position */
    U1    u1_t_bitmask;   /* bit mask */

    u4_t_buf      = u4_ap_REQ[st_gp_MM2C124N_ETHCOMVERT[u1_a_SIG_IDX].u4_buf];
    u1_t_bitpos   = st_gp_MM2C124N_ETHCOMVERT[u1_a_SIG_IDX].u1_bitpos;
    u1_t_bitmask  = st_gp_MM2C124N_ETHCOMVERT[u1_a_SIG_IDX].u1_bitmask;

    u1_t_sig      = (U1)((u4_t_buf >> u1_t_bitpos) & (U4)u1_t_bitmask);

    return(u1_t_sig);
}

