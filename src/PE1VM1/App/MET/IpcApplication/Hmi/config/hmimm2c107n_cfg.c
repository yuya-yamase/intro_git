const ST_MM2C107N_ETHCOMVERT st_gp_MM2C107N_ETHCOMVERT[HMIMM2C107N_SIG_NUM] = {
/*  u4_buf    u1_bit    u1_bitmask                 */
    {(U4)0U,  (U1)24U,  (U1)0x03U}    /* EXIST_TEL */
};

const ST_MM2C107N_SIGIF st_gp_MM2C107N_SIGIF[HMIMM2C107N_SIG_NUM] = {
/*  fp_vd_send                       fp_u1_read  */
    {&vd_s_Mm2c107nSendExisttel,     &u1_s_Mm2c107nReadSig}
};

/*-----------------------------------------------------------------------------------------------------------------------------------*/
