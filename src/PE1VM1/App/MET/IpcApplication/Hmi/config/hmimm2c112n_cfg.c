const ST_MM2C112N_ETHCOMVERT st_gp_MM2C112N_ETHCOMVERT[HMIMM2C112N_SIG_NUM] = {
/*  u4_buf     u1_bit   u1_bitmask                    */
    {(U4)14U,  (U1)0U,  (U1)0xFFU     },    /* NAVFUNC */
    {(U4)13U,  (U1)11U, (U1)0x1FU     }     /* CTY_INF */
};

const ST_MM2C112N_SIGIF st_gp_MM2C112N_SIGIF[HMIMM2C112N_SIG_NUM] = {
/*  fp_vd_send                       fp_u1_read         */
    {&vd_s_Mm2c112nSendNavfunc,     &u1_s_Mm2c112mReadSig},
    {&vd_s_Mm2c112nSendCTYINF,      &u1_s_Mm2c112mReadSig}
};

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  static void u1_s_Mm2c112mReadSig(const U1 u1_a_SIG_IDX, const U4 * u4_ap_REQ)                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u1_a_SIG_IDX  :  signal index number                                                                             */
/*                  u4_ap_REQ     :  ethernet data buffer                                                                            */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U1 u1_s_Mm2c112mReadSig(const U1 u1_a_SIG_IDX, const U4 * u4_ap_REQ)
{
    U1    u1_t_sig;       /* recieve signal */
    U4    u4_t_buf;       /* MM Sub-Bus buffer */
    U1    u1_t_bitpos;    /* bit position */
    U1    u1_t_bitmask;   /* bit mask */

    u4_t_buf      = u4_ap_REQ[st_gp_MM2C112N_ETHCOMVERT[u1_a_SIG_IDX].u4_buf];
    u1_t_bitpos   = st_gp_MM2C112N_ETHCOMVERT[u1_a_SIG_IDX].u1_bitpos;
    u1_t_bitmask  = st_gp_MM2C112N_ETHCOMVERT[u1_a_SIG_IDX].u1_bitmask;

    u1_t_sig      = (U1)((u4_t_buf >> u1_t_bitpos) & (U4)u1_t_bitmask);

    return(u1_t_sig);
}

/*===================================================================================================================================*/
/*  static void vd_s_Mm2c112nSendNavfunc(const U1 u1_a_SIG)                                                                          */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      u1_a_SIG  :  recieve signal                                                                                      */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void vd_s_Mm2c112nSendNavfunc(const U1 u1_a_SIG)
{
    vd_g_HmiVardefPut(u1_a_SIG, (U1)HMIVARDEF_SIG_NAVFUNC);
}

/*===================================================================================================================================*/
/*  static void vd_s_Mm2c112nSendCTYINF(const U1 u1_a_SIG)                                                                           */
