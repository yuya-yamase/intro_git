/*===================================================================================================================================*/
/*  U1      u1_g_FspocmnGetPrssUnit(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         U1 u1_t_unit                                                                                                     */
/*===================================================================================================================================*/
U1      u1_g_FspocmnGetPrssUnit(void)
{
    U1 u1_t_unit;

    u1_t_unit = u1_FSPOCMN_GET_UNIT();

    if (u1_t_unit >= (U1)FSPOCMN_UNIT_NUM) {
        u1_t_unit = (U1)FSPOCMN_UNIT_DEF;
    }

    return(u1_t_unit);
}

