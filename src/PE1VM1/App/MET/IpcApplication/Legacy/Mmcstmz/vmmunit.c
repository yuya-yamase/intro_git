/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void  vd_s_VardefMmUnitDispUp(void)
{
    U1  u1_mmsig;
    U1  u1_t_untdb1_mask;
    U1  u1_dspjudg;
    U1  u1_t_loop;
    U1  u1_t_locale_idx;

    u1_mmsig = (U1)0U;
    vd_VMMUNIT_RXMSG_A_UNTCSP(&u1_mmsig);

    if(u1_mmsig >= (U1)VMMUNIT_A_UNTCSP_NUM){
        u1_mmsig = (U1)0U;
    }
    u1_t_untdb1_mask = u1_gp_VMMUNIT_METUNIT_TBL[u1_mmsig];

    u1_dspjudg = u1_s_mmcst_m_unitdb1 & u1_t_untdb1_mask;

    if(u1_dspjudg != (U1)0){
        for(u1_t_loop = (U1)0; u1_t_loop < (U1)VMMUNIT_UNIT_NUM; u1_t_loop++){
            if(u1_gp2_VMMUNIT_CNRTLOCALE[u1_mmsig][u1_t_loop] != (U1)VMMUNIT_A_UNTCSP_UNKNOWN){
                u1_t_locale_idx = u1_gp_VMMUNIT_LOCALEIDX[u1_t_loop];
                vd_g_UnitPut(u1_t_locale_idx,u1_gp2_VMMUNIT_CNRTLOCALE[u1_mmsig][u1_t_loop]);
            }
        }
    }
}

