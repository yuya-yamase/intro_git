/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefMmUnitWkupInit(void)
{
    U1  u1_t_sts;
    U1  u1_t_data;

    u1_t_data = (U1)0U;

    u1_t_sts = u1_g_Rim_ReadU1withStatus((U2)VMMUNIT_RIMID_M_UNITDB1, &u1_t_data);
    if ((u1_t_sts & (U1)RIM_RESULT_KIND_MASK) == (U1)RIM_RESULT_KIND_OK){
        u1_s_mmcst_m_unitdb1 = u1_t_data;
    }
    else{
        u1_s_mmcst_m_unitdb1 = (U1)0U;
    }
}

