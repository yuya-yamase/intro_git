static void  vd_s_VardefMmUnitWrite(void)
{
    static const U1     u1_s_MMCST_SFT  = (U1)3U;
    static const U1     u1_s_MMCST_MASK = (U1)0x0fU;
    U1  u1_t_write_req;
    U1  u1_t_jdgtbl_idx;
    U1  u1_t_tx;

    u1_t_jdgtbl_idx = (u1_s_mmcst_m_unitdb1 >> u1_s_MMCST_SFT) & u1_s_MMCST_MASK;

    u1_t_write_req = u1_gp_VMMUNIT_TX_JUDGE_TBL[u1_t_jdgtbl_idx];

    if(u1_t_write_req == (U1)TRUE){
        u1_t_tx = u1_s_mmcst_m_unitdb1;
    }
    else{
        u1_t_tx = (U1)0U;
    }
        vd_VMMUNIT_WRITE_M_UNTDB1(&u1_t_tx);
}

