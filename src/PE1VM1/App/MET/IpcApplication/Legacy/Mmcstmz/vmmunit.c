/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_VardefMmUnitCstmzTask(void)
{
    U1  u1_t_msg_sts;
    U1  u1_t_sig;
    U1  u1_t_accon;
    U1  u1_t_pattern;
    U1  u1_t_m_untdb1;
    U1  u1_t_loop;

    u1_t_m_untdb1 = (U1)0U;
	u1_t_sig = (U1)0U;

    u1_t_pattern = u1_VMMUNIT_GETSLCTUNIT();
    for(u1_t_loop = (U1)0U; u1_t_loop < (U1)VMMUNIT_NUM_M_UNTDB1; u1_t_loop++){
        if((u1_t_pattern & u1_gp2_VMMUNIT_UNITCONVRT[u1_t_loop][VMMUNIT_KIND_VDF]) != (U1)0U){
            u1_t_m_untdb1 |= u1_gp2_VMMUNIT_UNITCONVRT[u1_t_loop][VMMUNIT_KIND_MMCST];
        }
    }
    u1_s_mmcst_m_unitdb1 = u1_t_m_untdb1;
    vd_g_Rim_WriteU1((U2)VMMUNIT_RIMID_M_UNITDB1, u1_s_mmcst_m_unitdb1);

    u1_t_accon = u1_VMMUNIT_ACCISON();
    if(u1_t_accon == (U1)TRUE){
        u1_t_msg_sts = u1_VMMUNIT_GETMSGSTS_AVN1S21();
        u1_t_msg_sts &= ((U1)VMMUNIT_TIMEOUT | (U1)VMMUNIT_NO_RX);
        if(u1_t_msg_sts == (U1)0U){
            vd_VMMUNIT_RXMSG_A_CSPCHG(&u1_t_sig);
            if(u1_t_sig == (U1)TRUE){
                vd_s_VardefMmUnitDispUp();
            }
        }
        vd_s_VardefMmUnitWrite();
    }
}

