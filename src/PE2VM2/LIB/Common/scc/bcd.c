/*  Literal Definitions                                                                                                              */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define BCD_DIGIT_BASE                           (10U)
#define BCD_DIGIT_LSB_NEXT                       (4U)
#define BCD_DIGIT_BIT_LEAST                      (0x0000000fU)

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Macro Definitions                                                                                                                */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define u4_BCD_MUL_BY_10(i)                      (((i) << 1U) + ((i) << 3U))

