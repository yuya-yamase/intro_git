/*===================================================================================================================================*/
/*  U4      u4_g_BcdToInt(const U4 u4_a_BCD)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_BcdToInt(const U4 u4_a_BCD)
{
    U4      u4_t_int;
    U4      u4_t_bcd;
    U4      u4_t_digit;

    u4_t_int = (U4)U4_MAX;
    if(u4_a_BCD <= (U4)BCD_CNVRT_BCD_MAX){

        u4_t_bcd = u4_a_BCD;
        u4_t_int = (U4)0U;
        u4_t_digit = (U4)1U;
        while(u4_t_bcd > (U4)0U){
            u4_t_int   += ((u4_t_bcd & (U4)BCD_DIGIT_BIT_LEAST) * u4_t_digit);
            u4_t_digit *= (U4)BCD_DIGIT_BASE;
            u4_t_bcd  >>= BCD_DIGIT_LSB_NEXT;
        }
    }

    return(u4_t_int);
}
/*===================================================================================================================================*/
/*  U4      u4_g_IntToBcd(const U4 u4_a_INT)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_IntToBcd(const U4 u4_a_INT)
{
    U4      u4_t_int;
    U4      u4_t_bcd;
    U4      u4_t_wk;
    U4      u4_t_res;
    U4      u4_t_lsb;

    if(u4_a_INT >= (U4)BCD_CNVRT_INT_MAX){
        u4_t_bcd = (U4)BCD_CNVRT_BCD_MAX;
    }
    else if(u4_a_INT > (U4)0U){

        u4_t_bcd = (U4)0U;
        u4_t_int = u4_a_INT;
        u4_t_lsb = (U4)0U;
        do{
            u4_t_wk   = u4_t_int;
            u4_t_int /= (U4)BCD_DIGIT_BASE;
            u4_t_res  = (u4_t_wk - u4_BCD_MUL_BY_10(u4_t_int)); /* calculate residue */
            u4_t_bcd |= (u4_t_res << u4_t_lsb);
            u4_t_lsb += (U4)BCD_DIGIT_LSB_NEXT;
        }while(u4_t_int > (U4)0U);
    }
    else{
        u4_t_bcd = (U4)0U;
    }

    return(u4_t_bcd);
}
