/*===================================================================================================================================*/
/*  uint8   u1_g_oXDoCANAubIfComCtrlOk(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
uint8   u1_g_oXDoCANAubIfComCtrlOk(void)
{
#if (OXDC_CFG_EN_KMPH == 1U)
    static const U2         u2_s_TYDC_RPG_KMPH_MIN = (U2)500U;                     /* 5.00 km/h */

    U1                      u1_t_ret;
    U2                      u2_t_sp1;

    u2_t_sp1 = u2_s_oXDoCANAubIfKmph();
    if((u2_t_sp1 < (U2)OXDC_KMPH_UNK     ) &&
       (u2_t_sp1 > u2_s_TYDC_RPG_KMPH_MIN)){

        u1_t_ret = (U1)OXDC_SAL_PROC_NR_88;
    }
    else{
        u1_t_ret = (U1)0x00U;
    }

    return(u1_t_ret);
#else
    return((U1)0x00U);
#endif /* #if (OXDC_CFG_EN_KMPH == 1U) */
}
