#if (OXDC_CFG_EN_KMPH == 1U)

/*===================================================================================================================================*/
/*  static U2      u2_s_oXDoCANAubIfKmph(void)                                                                                       */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static U2      u2_s_oXDoCANAubIfKmph(void)
{
    static const U2         u2_s_OXDC_KMPH_RX_TOUT = u2_OXCAN_RXTO_THRSH(3600U);

    U2                      u2_t_sp1;
    U2                      u2_t_kmph;
    U1                      u1_t_rx_stat;

    u2_t_sp1     = (U2)0U;
    u1_t_rx_stat = u1_g_oXCANRxdStat((U2)OXCAN_RXD_PDU_CAN_VSC1G13_CH0,
                                     (U2)OXCAN_SYS_IGR,
                                     u2_s_OXDC_KMPH_RX_TOUT) & ((U1)COM_NO_RX | (U1)COM_TIMEOUT);

    (void)Com_ReceiveSignal(ComConf_ComSignal_SP1, &u2_t_sp1);
    if(u1_t_rx_stat >= (U1)COM_TIMEOUT){
        u2_t_kmph = (U2)OXDC_KMPH_UNK;
    }
    else if(u1_t_rx_stat == (U1)0U){
        u2_t_kmph = u2_t_sp1;
    }
    else{
        u2_t_kmph = (U2)0U;
    }

    return(u2_t_kmph);
}
#endif /* #if (OXDC_CFG_EN_KMPH == 1U) */
