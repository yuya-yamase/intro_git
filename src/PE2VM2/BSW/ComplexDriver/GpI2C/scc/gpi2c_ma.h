void    vd_g_GpI2cMaIRQDmaRx(const U1 u1_a_MA_CH);

U1      u1_g_GpI2cMaReqTRx(const ST_GP_I2C_MA_REQ * const st_ap_REQ, const U2 u2_a_NREQ);
                                                                        /* Return : TRUE = request is accepted, else not             */
