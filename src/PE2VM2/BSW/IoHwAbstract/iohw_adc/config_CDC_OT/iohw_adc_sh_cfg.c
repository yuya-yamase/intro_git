/*===================================================================================================================================*/
/*  void    vd_g_IoHwAdcShCfgInit(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_IoHwAdcShCfgInit(void)
{
    /* ----------------------------------------------------------------------------------------------- */
    /* Attention :                                                                                     */
    /* ----------------------------------------------------------------------------------------------- */
    /* u4_sp_iohw_adc_lv[] is initialized to IOHW_ADC_LV_U4_UNKNWN in vd_g_IoHwAdcShInit.              */
    /* Thus, u4_sp_iohw_adc_lv[] doesn't need to be initialized at here.                               */
    /* ----------------------------------------------------------------------------------------------- */

    ehvm_vcc_clear_channel(EHVM_RX_VCCID_VCC_IOHWAD_DATA_TX_VM0);
}
/*===================================================================================================================================*/
/*  U1      u1_g_IoHwAdcShCfgLvRx(U4 * u4_ap_lv_rx)                                                                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_IoHwAdcShCfgLvRx(U4 * u4_ap_lv_rx)
{
    U4                 u4_t_lpcnt;
    U4                 u4_t_nbyte;

    U1                 u1_t_vcc_ok;
    U1                 u1_t_rx_ok;

    u1_t_rx_ok = (U4)FALSE;
    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)IOHW_ADC_SH_LP_MAX; u4_t_lpcnt++){

        u1_t_vcc_ok = ehvm_vcc_receive(EHVM_RX_VCCID_VCC_IOHWAD_DATA_TX_VM0,
                                       u4_ap_lv_rx, ((U4)IOHW_ADC_SH_NUM_WORD << 2U), &u4_t_nbyte);
        if(u1_t_vcc_ok == E_EHVM_OK){ 
            u1_t_rx_ok = (U1)TRUE;
        }
        else if(u1_t_vcc_ok == E_EHVM_RECEIVE_OVERWRITE_OCCURS){
            u1_t_rx_ok = (U4)FALSE;
        }
        else{
            break;
        }
    }

    return(u1_t_rx_ok);
}

