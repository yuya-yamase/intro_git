/*  void    vd_g_Pwm_SetPeriodAndDuty(const U1 u1_a_PWM_CH, const U2 u2_a_PERIOD, const U2 u2_a_DUTY)                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_Pwm_SetPeriodAndDuty(const U1 u1_a_PWM_CH, const U2 u2_a_PERIOD, const U2 u2_a_DUTY)
{
    U4                 u4_t_duty;
    U2                 u2_tp_peri_ch[GPT_D16_START_NUM_CFG];
    U2                 u2_tp_duty_ch[GPT_D16_START_NUM_CFG];
    U2                 u2_t_gpt;

    if(u1_a_PWM_CH < u1_g_PWM_DRV_NUM_CH){

        u2_tp_peri_ch[GPT_D16_START_CTRL] = (U2)GPT_D16_START_CTRL_BIT_TRG_ST;
        u2_tp_peri_ch[GPT_D16_START_PERI] = u2_a_PERIOD;

        u2_tp_duty_ch[GPT_D16_START_CTRL] = (U2)GPT_D16_START_CTRL_BIT_TRG_ST;
        u2_tp_duty_ch[GPT_D16_START_PERI] = (U2)GPT_D16_PERI_MAX;

        if(u2_a_DUTY < (U2)PWM_DRV_DUTY_MAX){

            u4_t_duty = ((U4)u2_a_DUTY * (U4)u2_a_PERIOD) + (U4)PWM_DRV_DUTY_RND;
            u4_t_duty = (u4_t_duty >> PWM_DRV_DUTY_DIV) + (U4)1U;
            u2_tp_duty_ch[GPT_D16_START_PERI] = (U2)u4_t_duty;
        }

        u2_t_gpt = st_gp_PWM_DRV_CFG[u1_a_PWM_CH].u2_gpt;
        if(u2_t_gpt == (U2)PWM_DRV_GPT_D16){

            vd_g_Gpt_D16Start(st_gp_PWM_DRV_CFG[u1_a_PWM_CH].u1_duty_ch, &u2_tp_duty_ch[0]);
            vd_g_Gpt_D16Start(st_gp_PWM_DRV_CFG[u1_a_PWM_CH].u1_peri_ch, &u2_tp_peri_ch[0]);
        }
        else{
            /* do nothing */
        }
    }
}
