/*===================================================================================================================================*/
/*  void    vd_g_Rim_ClrKeyword(void)                                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_Rim_ClrKeyword(void)
{
    U1  u1_t_cnt;
    volatile U4 u4_t_rim_keyword;

    for(u1_t_cnt = (U1)0U; u1_t_cnt < u1_d_RIM_KEYWORD_NUM; u1_t_cnt++){
        *u4_dp2_RIM_KEYWORD_RAM[u1_t_cnt] = (U4)0U;
    }
    
    u4_t_rim_keyword = *u4_dp2_RIM_KEYWORD_RAM[u1_d_RIM_KEYWORD_NUM - (U1)1U];
}

/*===================================================================================================================================*/
/*  U1      u1_g_Rim_KeyIsCrct(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         U1 u1_t_ret : Result                                                                                             */
/*===================================================================================================================================*/
U1      u1_g_Rim_KeyIsCrct(void)
{
    U1  u1_t_ret;
    U1  u1_t_cnt;

    u1_t_ret = (U1)TRUE;

    for(u1_t_cnt = (U1)0U; u1_t_cnt < u1_d_RIM_KEYWORD_NUM; u1_t_cnt++){
        if(*u4_dp2_RIM_KEYWORD_RAM[u1_t_cnt] != u4_d_RIM_KEYWORD_VAL[u1_t_cnt]){
            u1_t_ret = (U1)FALSE;
            break;
        }
    }

    return(u1_t_ret);
}

/*===================================================================================================================================*/
/*  void    vd_g_Rim_SetKey(void)                                                                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_Rim_SetKey(void)
{
    U1  u1_t_cnt;
    volatile U4 u4_t_rim_keyword;
    
    for(u1_t_cnt = (U1)0U; u1_t_cnt < u1_d_RIM_KEYWORD_NUM; u1_t_cnt++){
        *u4_dp2_RIM_KEYWORD_RAM[u1_t_cnt] = u4_d_RIM_KEYWORD_VAL[u1_t_cnt];
    }
    
    u4_t_rim_keyword = *u4_dp2_RIM_KEYWORD_RAM[u1_d_RIM_KEYWORD_NUM - (U1)1U];
}

