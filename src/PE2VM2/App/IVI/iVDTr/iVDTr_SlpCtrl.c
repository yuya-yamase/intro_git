#define   SLPCTL_SLP_RQST_NWORD     (1U)
#define   SLPCTL_SLP_OK_TX          (0x01U)
#define   SLPCTL_FORCESSLP_OK_RX    (0x01U)

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Type Definitions                                                                                                                 */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Variable Definitions                                                                                                             */
