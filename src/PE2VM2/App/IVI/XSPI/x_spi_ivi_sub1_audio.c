/*  void            vd_g_XspiIviSub1AudioAna(const U1 * u1_ap_XSPI_ADD, const U2 u2_a_DATA_SIZE)                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Description:    SubFlame1(MISC) Data Analysis                                                                                    */
/*  Arguments:      u1_ap_XSPI_ADD : SubFlame1 Start Buffer                                                                          */
/*                  u2_a_DATA_SIZE : Data Size                                                                                       */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_XspiIviSub1AudioAna(const U1 * u1_ap_XSPI_ADD, const U2 u2_a_DATA_SIZE)
{
    U1 u1_t_subtype;

    u1_t_subtype = u1_ap_XSPI_ADD[0];

    switch (u1_t_subtype)
    {
    case XSPI_IVI_AUDIO_MUTE_FIN:
        vd_g_PowerIcMuteHook();
        break;
    
    default:
        break;
    }
}

