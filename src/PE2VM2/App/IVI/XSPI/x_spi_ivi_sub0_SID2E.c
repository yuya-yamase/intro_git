/*  U1          u1_g_XspiIviSub0Request_Sid2E(const U1 u1_a_REQID, const U2 u2_a_DID, const U2 u2_a_DTLEN,                           */
/*                                          const U1 * u1_ap_data, U1 * u1_ap_NegativeResponseCode)                                  */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Description:    SID2E Request                                                                                                    */
/*  Arguments:                                                                                                                       */
/*  Return:         u1_t_ret : positive or negative response                                                                         */
/*===================================================================================================================================*/
U1          u1_g_XspiIviSub0Request_Sid2E(const U1 u1_a_REQID, const U2 u2_a_DID, const U2 u2_a_DTLEN,
                                          const U1 * u1_ap_data, U1 * u1_ap_NegativeResponseCode)
{
    U1  u1_t_ret;
    U1  u1_t_power_sts;
    U1  u1_t_oscom_chk;
    U2  u2_t_dtlen;
    U1  u1_tp_data[XSPI_IVI_DIAG_MAX_SIZE];

    u1_t_ret = (U1)E_OK;
    u1_t_power_sts = u1_g_XspiIviSub0PowerSts();
    u1_t_oscom_chk = u1_g_XspiIviSub0OSComBridge();

    if(u1_t_power_sts != (U1)POWER_MODE_STATE_APPON) {
        *u1_ap_NegativeResponseCode = (U1)OXDC_SAL_PROC_NR_22;
        u1_t_ret = (U1)E_NOT_OK;
    } else if(u1_t_oscom_chk == (U1)FALSE) {
        *u1_ap_NegativeResponseCode = (U1)OXDC_SAL_PROC_NR_22;
        u1_t_ret = (U1)E_NOT_OK;
    } else {
        u1_tp_data[0] = (U1)(((U2)XSPI_IVI_SID2E_OPC_2E01 >> XSPI_IVI_SFT_08) & 0x00FFU);
        u1_tp_data[1] = ((U1)XSPI_IVI_SID2E_OPC_2E01 & 0x00FFU);
        u1_tp_data[2] = (U1)((u2_a_DID >> XSPI_IVI_SFT_08) & 0x00FFU);
        u1_tp_data[3] = (U1)(u2_a_DID & 0x00FFU);
        if(u2_a_DTLEN <= (U2)XSPI_IVI_DIAG_MAX_SIZE - (U2)XSPI_IVI_DIAG_OPC_DID_SIZE) {
            vd_g_MemcpyU1(&u1_tp_data[4], &u1_ap_data[0], (U4)u2_a_DTLEN);
            u2_t_dtlen = u2_a_DTLEN + (U2)XSPI_IVI_DIAG_OPC_DID_SIZE;
            vd_g_XspiIviSub0Request(u2_t_dtlen,u1_a_REQID,u1_tp_data,(U1)XSPI_IVI_SID2E);
        }
    }

    return(u1_t_ret);
}

/*===================================================================================================================================*/
/*  void         vd_g_XspiIviSub0Response_SID2E(const U1 * u1_ap_DATA)                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Description:    SubFlame0(DiagCAN Data) Transmission data                                                                        */
/*  Arguments:      u1_ap_DATA : SubFlame0 Data                                                                                      */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void         vd_g_XspiIviSub0Response_SID2E(const U1 * u1_ap_DATA)
{
    U2  u2_t_opc;

    u2_t_opc = (U2)((u1_ap_DATA[0] << XSPI_IVI_SFT_08) | u1_ap_DATA[1]);

    switch(u2_t_opc) {
        case XSPI_IVI_SID2E_OPC_2E02:
            vd_s_XspiIviSub0Response_OPC2E02(&u1_ap_DATA[2]);
            break;
        default:
            break;
    }
}

/*===================================================================================================================================*/
/*  static void     vd_s_XspiIviSub0Response_OPC2E02(const U1 * u1_ap_DATA )                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Description:    SID2E OPC2E04 Response                                                                                           */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void     vd_s_XspiIviSub0Response_OPC2E02(const U1 * u1_ap_DATA )
{
    U1  u1_t_nrc;
    U2  u2_t_did;

    u1_t_nrc = u1_ap_DATA[0];
    u2_t_did = (U2)((u1_ap_DATA[1] << XSPI_IVI_SFT_08) | u1_ap_DATA[2]);

    vd_g_DiagAppResponse_Sid2E(u1_t_nrc, u2_t_did);

}

