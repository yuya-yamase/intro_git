/*===================================================================================================================================*/
/* void            vd_g_VolmuteCfgSetMute(void)                                                                                      */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_VolmuteCfgSetMute(void)
{
     /* MUTE設定 */
     vd_g_PowerIc_SoftMuteSet((U1)POWERIC_SOFTMUTE_STS_ON);
}

/*===================================================================================================================================*/
/* void            vd_g_VolmuteCfgSetUnMute(void)                                                                                    */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_VolmuteCfgSetUnMute(void)
{
     /* MUTE解除設定 */
     vd_g_PowerIc_SoftMuteSet((U1)POWERIC_SOFTMUTE_STS_OFF);
}

/*===================================================================================================================================*/
/* void            vd_g_VolmuteCfgSendTMuteSts(const U1 u1_a_STS)                                                                    */
