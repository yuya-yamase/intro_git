/*===================================================================================================================================*/
/* void            vd_g_VolmuteInit(void)                                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_VolmuteInit(void)
{
    U4  u4_t_loop;  /* ループカウンタ */

    for(u4_t_loop = (U4)0U; u4_t_loop < (U4)VOLMUTE_LSTNUM; u4_t_loop++){
        *(st_sp_VOLMUTE_LST[u4_t_loop].u1p_cyctim)  = (U1)0U;
        *(st_sp_VOLMUTE_LST[u4_t_loop].u1p_smplcnt) = (U1)0U;
        *(st_sp_VOLMUTE_LST[u4_t_loop].dlp_pinsts)  = st_sp_VOLMUTE_LST[u4_t_loop].fp_GETPINSTS();
    }

    vd_g_VolmuteCfgInit();
}

/*===================================================================================================================================*/
/* void            vd_g_VolmuteMainTask(void)                                                                                        */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_VolmuteMainTask(void)
{
    U4              u4_t_lstid;  /* リストID */

    for(u4_t_lstid = (U4)0U; u4_t_lstid < (U4)VOLMUTE_LSTNUM; u4_t_lstid++){
        (*(st_sp_VOLMUTE_LST[u4_t_lstid].u1p_cyctim))++;

        if(*(st_sp_VOLMUTE_LST[u4_t_lstid].u1p_cyctim) >= st_sp_VOLMUTE_LST[u4_t_lstid].u1_SMPLTIM){
            *(st_sp_VOLMUTE_LST[u4_t_lstid].u1p_cyctim) = (U1)0U;

            vd_s_VolmutePinChk(u4_t_lstid);
        }
    }
}

/*===================================================================================================================================*/
/* static void     vd_s_VolmutePinChk(const U1 u1_a_ID)                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void     vd_s_VolmutePinChk(const U1 u1_a_ID)
{
    Dio_LevelType   dl_t_port;  /* Pin読み出し値 */

    dl_t_port = st_sp_VOLMUTE_LST[u1_a_ID].fp_GETPINSTS();

    if(*(st_sp_VOLMUTE_LST[u1_a_ID].dlp_pinsts) != dl_t_port){
        (*(st_sp_VOLMUTE_LST[u1_a_ID].u1p_smplcnt))++;

        if(*(st_sp_VOLMUTE_LST[u1_a_ID].u1p_smplcnt) >= st_sp_VOLMUTE_LST[u1_a_ID].u1_SCNT){
            *(st_sp_VOLMUTE_LST[u1_a_ID].u1p_smplcnt) = (U1)0U;

            if(dl_t_port == dl_g_VOLMUTE_LOW){
                st_sp_VOLMUTE_LST[u1_a_ID].fp_LOWFUNC();
            }
            else if(dl_t_port == dl_g_VOLMUTE_HIGH){
                st_sp_VOLMUTE_LST[u1_a_ID].fp_HIGHFUNC();
            }
            else{
                /* do nothing */
            }
            *(st_sp_VOLMUTE_LST[u1_a_ID].dlp_pinsts)  = dl_t_port;
        }
    }
}
