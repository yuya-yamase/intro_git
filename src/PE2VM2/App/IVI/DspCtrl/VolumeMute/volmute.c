/* サンプリング監視対象 */
#define VOLMUTE_LSTNUM                          (2U)
#define VOLMUTE_LST_BU_DET_H                    (0U)
#define VOLMUTE_LST_T_MUTE                      (1U)

/* /BU-DET-H */
#define VOLMUTE_SMPLTIM_BUVOL                   (VOLMUTE_TASK)          /* サンプリング間隔 */
#define VOLMUTE_SCNT_BUVOL                      (1U)                    /* 同一論理判定確定回数 */

/* /T-MUTE */
#define VOLMUTE_SMPLTIM_TMUTE                   (10U / VOLMUTE_TASK)    /* サンプリング間隔 */
#define VOLMUTE_SCNT_TMUTE                      (3U)                    /* 同一論理判定確定回数 */

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Type Definitions                                                                                                                 */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
typedef struct {
    U1 *                        u1p_cyctim;             /* 前回サンプリングからの経過時間 */
    const U1                    u1_SMPLTIM;             /* サンプリング間隔 */
    U1  (* const                fp_GETPINSTS)(void);    /* PIN状態取得関数アドレス */
    const U1                    u1_SCNT;                /* 同一論理判定確定回数 */
    U1 *                        u1p_smplcnt;            /* サンプリング回数 */
    Dio_LevelType *             dlp_pinsts;             /* PIN状態確定値 */
    void (* const               fp_LOWFUNC)(void);      /* PIN状態：LOWで確定した時の処理関数 */
    void (* const               fp_HIGHFUNC)(void);     /* PIN状態：HIGHで確定した時の処理関数 */
} ST_VOLMUTE_LST;

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Variable Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/* /BU-DET-H */
static U1                       u1_s_volmute_cyctim_buvol;
static U1                       u1_s_volmute_smplcnt_buvol;
static Dio_LevelType            dl_s_volmute_pinsts_buvol;

/* /T-MUTE */
static U1                       u1_s_volmute_cyctim_tmute;
static U1                       u1_s_volmute_smplcnt_tmute;
static Dio_LevelType            dl_s_volmute_pinsts_tmute;

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Static Function Prototypes                                                                                                       */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static void     vd_s_VolmutePinChk(const U1 u1_a_ID);
static void     vd_s_VolmuteBuVolLowFunc(void);
static void     vd_s_VolmuteBuVolHighFunc(void);
static void     vd_s_VolmuteTMuteLowFunc(void);
static void     vd_s_VolmuteTMuteHighFunc(void);

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Constant Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static const ST_VOLMUTE_LST st_sp_VOLMUTE_LST[VOLMUTE_LSTNUM] = {
    /* /BU-DET-H */
    {
        &u1_s_volmute_cyctim_buvol,
        (U1)VOLMUTE_SMPLTIM_BUVOL,
        u1_g_VolmuteCfgGetBuDetH,
        (U1)VOLMUTE_SCNT_BUVOL,
        &u1_s_volmute_smplcnt_buvol,
        &dl_s_volmute_pinsts_buvol,
        vd_s_VolmuteBuVolLowFunc,
        vd_s_VolmuteBuVolHighFunc
    },
    /* /T-MUTE */
    {
        &u1_s_volmute_cyctim_tmute,
        (U1)VOLMUTE_SMPLTIM_TMUTE,
        u1_g_VolmuteCfgGetTMute,
        (U1)VOLMUTE_SCNT_TMUTE,
        &u1_s_volmute_smplcnt_tmute,
        &dl_s_volmute_pinsts_tmute,
        vd_s_VolmuteTMuteLowFunc,
        vd_s_VolmuteTMuteHighFunc
    }
};

