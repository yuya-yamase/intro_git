/*===================================================================================================================================*/
/*  void            vd_g_DiagAppSID28Request(const U1 u1_a_DATA)                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Description:    MainTask                                                                                                         */
/*  Arguments:      u1_ap_DATA : SoCからの受信コマンド                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_DiagAppSID28Request(const U1 u1_a_DATA)
{
     U1   u1_tp_data[3];
     U1   u1_t_cantrx;

     u1_t_cantrx = (U1)0U;

     if(u1_a_DATA == (U1)DIAGAPP_SID28_CAN_ENABLE) {
          u1_t_cantrx = (U1)DIAGAPP_SID28_SPI_ENABLE;
     } else if(u1_a_DATA == (U1)DIAGAPP_SID28_CAN_DISENABLE) {
          u1_t_cantrx = (U1)DIAGAPP_SID28_SPI_DISENABLE;
     } else {
          /*Do Nothing*/
     }

     u1_tp_data[0] = (U1)(((U2)DIAGAPP_SID28_OPC_2801 >> DIAGAPP_SFT_08) & 0x00FFU);
     u1_tp_data[1] = ((U1)DIAGAPP_SID28_OPC_2801 & 0x00FFU);
     u1_tp_data[2] = u1_t_cantrx;

     vd_g_XspiIviSub0Request_SID28((U2)DIAGAPP_SID28_SIZE,(U1)DIAGAPP_REQUESTID_PHYOFF,&u1_tp_data[0]);

}

