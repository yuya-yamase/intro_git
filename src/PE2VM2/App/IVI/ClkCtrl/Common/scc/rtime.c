/*  U4      u4_g_Hhmmss24hTo12h(const U4 u4_a_HHMMSS)                                                                                */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_Hhmmss24hTo12h(const U4 u4_a_HHMMSS)
{
    static const U1    u1_sp_HHMMSS_24H_TO_12H[] = {
        (U1)12U | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 00  00:mm:ss AM */
        (U1)1U  | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 01  01:mm:ss AM */
        (U1)2U  | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 02  02:mm:ss AM */
        (U1)3U  | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 03  03:mm:ss AM */
        (U1)4U  | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 04  04:mm:ss AM */
        (U1)5U  | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 05  05:mm:ss AM */
        (U1)6U  | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 06  06:mm:ss AM */
        (U1)7U  | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 07  07:mm:ss AM */
        (U1)8U  | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 08  08:mm:ss AM */
        (U1)9U  | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 09  09:mm:ss AM */
        (U1)10U | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 10  10:mm:ss AM */
        (U1)11U | (U1)((U4)HHMMSS_12H_BIT_AM >> HHMMSS_LSB_HR),    /* 11  11:mm:ss AM */

        (U1)12U | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 12  12:mm:ss PM */
        (U1)1U  | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 13  01:mm:ss PM */
        (U1)2U  | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 14  02:mm:ss PM */
        (U1)3U  | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 15  03:mm:ss PM */
        (U1)4U  | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 16  04:mm:ss PM */
        (U1)5U  | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 17  05:mm:ss PM */
        (U1)6U  | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 18  06:mm:ss PM */
        (U1)7U  | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 19  07:mm:ss PM */
        (U1)8U  | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 20  08:mm:ss PM */
        (U1)9U  | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 21  09:mm:ss PM */
        (U1)10U | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR),    /* 22  10:mm:ss PM */
        (U1)11U | (U1)((U4)HHMMSS_12H_BIT_PM >> HHMMSS_LSB_HR)     /* 23  11:mm:ss PM */
    };

    U4                 u4_t_hr;
    U4                 u4_t_mi;
    U4                 u4_t_se;

    U4                 u4_t_hhmmss_12h;

 /* u4_t_se  = (u4_a_HHMMSS & (U4)HHMMSS_BIT_SE) >> HHMMSS_LSB_SE; QAC Warning#2985 */
    u4_t_se  = u4_a_HHMMSS & (U4)HHMMSS_BIT_SE;
    u4_t_mi  = (u4_a_HHMMSS & (U4)HHMMSS_BIT_MI) >> HHMMSS_LSB_MI;
    u4_t_hr  = (u4_a_HHMMSS & (U4)HHMMSS_BIT_HR) >> HHMMSS_LSB_HR;

    if((u4_t_se <= (U4)HHMMSS_SE_MAX    ) &&
       (u4_t_mi <= (U4)HHMMSS_MI_MAX    ) &&
       (u4_t_hr <= (U4)HHMMSS_24H_HR_MAX)){

        u4_t_hhmmss_12h  = u4_a_HHMMSS & ((U4)HHMMSS_BIT_MI | (U4)HHMMSS_BIT_SE);
        u4_t_hhmmss_12h |= ((U4)u1_sp_HHMMSS_24H_TO_12H[u4_t_hr] << HHMMSS_LSB_HR);
    }
    else{
        u4_t_hhmmss_12h = (U4)HHMMSS_UNKNWN;
    }

    return(u4_t_hhmmss_12h);
}
