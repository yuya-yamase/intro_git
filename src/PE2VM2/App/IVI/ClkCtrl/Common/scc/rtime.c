/*  U4      u4_g_HhmmssToSeconds(const U4 u4_a_HHMMSS)                                                                               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_HhmmssToSeconds(const U4 u4_a_HHMMSS)
{
    U4                 u4_t_hr;
    U4                 u4_t_mi;
    U4                 u4_t_se;

 /* u4_t_se  = (u4_a_HHMMSS & (U4)HHMMSS_BIT_SE) >> HHMMSS_LSB_SE; QAC Warning#2985 */
    u4_t_se  = u4_a_HHMMSS & (U4)HHMMSS_BIT_SE;
    u4_t_mi  = (u4_a_HHMMSS & (U4)HHMMSS_BIT_MI) >> HHMMSS_LSB_MI;
    u4_t_hr  = (u4_a_HHMMSS & (U4)HHMMSS_BIT_HR) >> HHMMSS_LSB_HR;

    u4_t_se += (u4_t_mi * (U4)HHMMSS_MI_TO_SE);
    u4_t_se += (u4_t_hr * (U4)HHMMSS_HR_TO_SE);

    return(u4_t_se);
}
/*===================================================================================================================================*/
/*  U4      u4_g_SecondsToHhmmss(const U4 u4_a_SE)                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_SecondsToHhmmss(const U4 u4_a_SE)
{
    U4                 u4_t_hr;
    U4                 u4_t_mi;
    U4                 u4_t_se;

    U4                 u4_t_hhmmss;

    if(u4_a_SE <= (U4)HHMMSS_HR_TO_SE_MAX){

        u4_t_hr = u4_a_SE / (U4)HHMMSS_HR_TO_SE;
        u4_t_se = u4_a_SE % (U4)HHMMSS_HR_TO_SE;

        u4_t_mi = u4_t_se / (U4)HHMMSS_MI_TO_SE;
        u4_t_se = u4_t_se % (U4)HHMMSS_MI_TO_SE;

        u4_t_hhmmss  = u4_t_hr << HHMMSS_LSB_HR;
        u4_t_hhmmss |= u4_t_mi << HHMMSS_LSB_MI;
     /* u4_t_hhmmss |= u4_t_se << HHMMSS_LSB_SE; QAC Warning#2985 */
        u4_t_hhmmss |= u4_t_se;
    }
    else{
        u4_t_hhmmss = (U4)HHMMSS_UNKNWN;
    }

    return(u4_t_hhmmss);
}
