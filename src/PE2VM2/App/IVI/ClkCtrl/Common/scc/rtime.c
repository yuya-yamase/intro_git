/*  U1      u1_g_HhmmssFrmtIs24h(const U4 u4_a_HHMMSS, U1 * u1_ap_time)                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U1      u1_g_HhmmssFrmtIs24h(const U4 u4_a_HHMMSS, U1 * u1_ap_time)
{
    U4                 u4_t_hr;
    U4                 u4_t_mi;
    U4                 u4_t_se;

    U1                 u1_t_ans;

 /* u4_t_se  = (u4_a_HHMMSS & (U4)HHMMSS_BIT_SE) >> HHMMSS_LSB_SE; QAC Warning#2985 */
    u4_t_se  = u4_a_HHMMSS & (U4)HHMMSS_BIT_SE;
    u4_t_mi  = (u4_a_HHMMSS & (U4)HHMMSS_BIT_MI) >> HHMMSS_LSB_MI;
    u4_t_hr  = (u4_a_HHMMSS & (U4)HHMMSS_BIT_HR) >> HHMMSS_LSB_HR;

    if((u4_t_se <= (U4)HHMMSS_SE_MAX    ) &&
       (u4_t_mi <= (U4)HHMMSS_MI_MAX    ) &&
       (u4_t_hr <= (U4)HHMMSS_24H_HR_MAX)){

        u1_t_ans = (U1)TRUE;

        if(u1_ap_time != vdp_PTR_NA){
            u1_ap_time[HHMMSS_24H_TIME_SE] = (U1)u4_t_se;
            u1_ap_time[HHMMSS_24H_TIME_MI] = (U1)u4_t_mi;
            u1_ap_time[HHMMSS_24H_TIME_HR] = (U1)u4_t_hr;
        }
    }
    else{
        u1_t_ans = (U1)FALSE;
    }

    return(u1_t_ans);
}
