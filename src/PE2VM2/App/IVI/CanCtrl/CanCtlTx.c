/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Literal Definitions                                                                                                              */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
#define    CANCTLTC_SIGINIT        (0U)

#define    CANCTLTC_TXBYTE0        (0U)
#define    CANCTLTC_TXBYTE6        (6U)

#define    CANCTLTC_2BIT_MAX       (3U)

#define    CANCTLTC_4BIT_SHIFT     (4U)
#define    CANCTLTC_5BIT_SHIFT     (5U)

#define    CANCTLTC_FLYNOP_MASK    (0x9FU)
#define    CANCTLTC_CDSIZE_MASK    (0x0FU)

#define    CANCTLTC_VM_1WORD       (1U)

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Macro Definitions                                                                                                                */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Type Definitions                                                                                                                 */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Variable Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static U1       u1_s_canctltx_flynop;

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Static Function Prototypes                                                                                                       */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
static void     vd_s_CanCtlTx_Chk_FLYNOP(void);

/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Constant Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*  Function Definitions                                                                                                             */
/*-----------------------------------------------------------------------------------------------------------------------------------*/
/*===================================================================================================================================*/
/*  void            vd_g_CanCtlTx_Init(void)                                                                                         */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_CanCtlTx_Init(void)
{
    u1_s_canctltx_flynop = (U1)CANCTLTC_SIGINIT;
}

/*===================================================================================================================================*/
/*  void            vd_g_CanCtlTx_MainTask(void)                                                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_CanCtlTx_MainTask(void)
{
    vd_s_CanCtlTx_Chk_FLYNOP();
}

/*===================================================================================================================================*/
/*  static void     vd_s_CanCtlTx_Chk_FLYNOP(void)                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
static void     vd_s_CanCtlTx_Chk_FLYNOP(void)
{
    U1    u1_t_sts;
    U4    u4_t_read;
    U1    u1_t_sig;

    u4_t_read = (U4)0U;
    u1_t_sig  = (U1)0U;
    u1_t_sts = u1_g_iVDshReabyDid((U2)IVDSH_DID_REA_VM1TO2_FLYNOP, &u4_t_read, (U1)CANCTLTC_VM_1WORD);
    if(u1_t_sts != IVDSH_NO_REA){
        if(u4_t_read <= (U4)CANCTLTC_2BIT_MAX){
            u1_s_canctltx_flynop = (U1)u4_t_read;
        }
        else {
            u1_s_canctltx_flynop = (U1)CANCTLTC_2BIT_MAX;
        }
    }

    (void)Com_ReceiveSignal(ComConf_ComSignal_FLYNOP, &u1_t_sig);
    if(u1_t_sig != u1_s_canctltx_flynop){
        (void)Com_SendSignal(ComConf_ComSignal_FLYNOP, &u1_s_canctltx_flynop);
        Com_TriggerIPDUSend((PduIdType)MSG_AVN1S03_TXCH0);
    }
}

/*===================================================================================================================================*/
/*  void            vd_g_CanCtlTx_SendHk(const U4 u4_a_IPDU_RX, U1 * u1_ap_DATA)                                                     */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void            vd_g_CanCtlTx_SendHk(const U4 u4_a_IPDU_RX, U1 * u1_ap_DATA)
{
    U1 u1_t_sig;
    
    switch(u4_a_IPDU_RX)
    {
        case MSG_AVN1S03_TXCH0:
            u1_t_sig = u1_s_canctltx_flynop;
            u1_ap_DATA[CANCTLTC_TXBYTE0]  = u1_ap_DATA[CANCTLTC_TXBYTE0] & (U1)CANCTLTC_FLYNOP_MASK;
            u1_ap_DATA[CANCTLTC_TXBYTE0] |= (U1)(u1_t_sig << CANCTLTC_5BIT_SHIFT);
            break;

        case MSG_AVN1S97_TXCH0:
            u1_t_sig = u1_g_PictCtl_CdsizeSnd();
            u1_ap_DATA[CANCTLTC_TXBYTE6]  = u1_ap_DATA[CANCTLTC_TXBYTE6] & (U1)CANCTLTC_CDSIZE_MASK;
            u1_ap_DATA[CANCTLTC_TXBYTE6] |= (U1)(u1_t_sig << CANCTLTC_4BIT_SHIFT);
            break;

        default:
            break;
    }
}

