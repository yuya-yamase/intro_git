/*                                                                                             */
/* vd_g_IoHwAdcSeqAct and vd_g_IoHwAdcSeqDeAct shall be invoked at any task which has equal    */
/* priority or higher priority than vd_g_IoHwAdcMainTask.                                      */
/* ------------------------------------------------------------------------------------------- */
void    vd_g_IoHwAdcCyclSeqTask(void);
void    vd_g_IoHwAdcCyclSeqAct(const U1 u1_a_INIT); /* u1_a_INIT = TRUE/FALSE */
