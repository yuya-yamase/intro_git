void    vd_g_IoHwAdcInit(void);
void    vd_g_IoHwAdcMainTask(void);

/* ------------------------------------------------------------------------------------------- */
/* Attention :                                                                                 */
/* ------------------------------------------------------------------------------------------- */
/* vd_g_IoHwAdcSeqAct shall NOT be invoked at anywhere if vd_g_IoHwAdcCyclSeqTask is used and  */
/* activated.                                                                                  */
/*                                                                                             */
/* vd_g_IoHwAdcSeqAct and vd_g_IoHwAdcSeqDeAct shall be invoked at any task which has equal    */
/* priority or lower priority than vd_g_IoHwAdcCvstStart and vd_g_IoHwAdcCvstFinish.           */
/*                                                                                             */
/* vd_g_IoHwAdcSeqAct and vd_g_IoHwAdcSeqDeAct shall be invoked at any task which has equal    */
/* priority or higher priority than vd_g_IoHwAdcMainTask.                                      */
/* ------------------------------------------------------------------------------------------- */
void    vd_g_IoHwAdcSeqAct(const U1 u1_a_INIT); /* u1_a_INIT = TRUE/FALSE */
void    vd_g_IoHwAdcSeqDeAct(void);
U1      u1_g_IoHwAdcSeqActvtd(void);

/* ------------------------------------------------------------------------------------------- */
/* Attention :                                                                                 */
/* ------------------------------------------------------------------------------------------- */
/* vd_g_IoHwAdcCvstStart and vd_g_IoHwAdcCvstFinish are peformed as pair always.               */
/* Following use case is prohibited.                                                           */
/* ex)                                                                                         */
/* 1.vd_g_IoHwAdcCvstStart is performed.                                                       */
/* 2.vd_g_IoHwAdcCvstStart is performed.  <-- vd_g_IoHwAdcCvstFinish shall be called.          */
/* 3.vd_g_IoHwAdcCvstFinish is performed.                                                      */
/*                                                                                             */
/* vd_g_IoHwAdcCvstStart and vd_g_IoHwAdcCvstFinish shall be totally exclusive.                */
/* ------------------------------------------------------------------------------------------- */
void    vd_g_IoHwAdcCvstStart(void);   /* ConVersion STep Start  */
void    vd_g_IoHwAdcCvstFinish(void);  /* ConVersion STep Finish */

/* ------------------------------------------------------------------------------------------- */
/* Attention :                                                                                 */
/* ------------------------------------------------------------------------------------------- */
/* vd_g_IoHwAdcCyclSeqAct and vd_g_IoHwAdcCyclSeqDeAct shall be invoked at any task which has  */
