/*  void    vd_g_oXCANSysEaInit(void)                                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXCANSysEaInit(void)
{
    U4           u4_t_lpcnt;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_OXCAN_SYSEA_NUM_TIM; u4_t_lpcnt++){
        u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_SYSEA_TIM_INA;
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_oXCANSysEaSysEvhk(const U4 u4_a_SYS_ACT)                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXCANSysEaSysEvhk(const U4 u4_a_SYS_ACT)
{
    U4           u4_t_lpcnt;
    U4           u4_t_tim_chk;
    U4           u4_t_tim_run;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_OXCAN_SYSEA_NUM_TIM; u4_t_lpcnt++){

        u4_t_tim_chk = u4_a_SYS_ACT & st_gp_OXCAN_SYSEA_TIM[u4_t_lpcnt].u4_sys_chk; 
        u4_t_tim_run = st_gp_OXCAN_SYSEA_TIM[u4_t_lpcnt].u4_tim_run;
        if(u4_t_tim_chk != u4_t_tim_run){
            u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_SYSEA_TIM_INA;
        }
        else if(u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] >= (U2)OXCAN_SYSEA_TIM_INA){
            u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)0U;
        }
        else{
            /* no processing */
        }
    }

    vd_g_oXCANSysEaEvAct((U1)OXCAN_SYSEA_EAGR_1ST);
}
/*===================================================================================================================================*/
/*  void    vd_g_oXCANSysEaTimElpsd(const U4 u4_a_SYS_ACT)                                                                           */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXCANSysEaTimElpsd(const U4 u4_a_SYS_ACT)
{
    U4           u4_t_lpcnt;
    U4           u4_t_tim_chk;
    U4           u4_t_tim_run;

    for(u4_t_lpcnt = (U4)0U; u4_t_lpcnt < (U4)u1_g_OXCAN_SYSEA_NUM_TIM; u4_t_lpcnt++){

        u4_t_tim_chk = u4_a_SYS_ACT & st_gp_OXCAN_SYSEA_TIM[u4_t_lpcnt].u4_sys_chk; 
        u4_t_tim_run = st_gp_OXCAN_SYSEA_TIM[u4_t_lpcnt].u4_tim_run;
        if(u4_t_tim_chk != u4_t_tim_run){
            u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_SYSEA_TIM_INA;
        }
        else if(u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] >= (U2)OXCAN_SYSEA_TIM_INA){
            u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)0U;
        }
        else if(u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] < (U2)OXCAN_SYSEA_TIM_MAX){
            u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt]++;
        }
        else{
            /* u2_gp_oxcan_sysea_tim_elpsd[u4_t_lpcnt] = (U2)OXCAN_SYSEA_TIM_MAX; */
        }
    }
}
/*===================================================================================================================================*/
/*  void    vd_g_oXCANSysEaEvAct(const U1 u1_a_EAGR)                                                                                 */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_oXCANSysEaEvAct(const U1 u1_a_EAGR)
{
    U4           u4_t_lpcnt;
    U4           u4_t_lpend;
    U2           u2_t_elpsd;

    if(u1_a_EAGR > (U1)OXCAN_SYSEA_EAGR_2ND){
        u4_t_lpcnt = (U4)U4_MAX;
        u4_t_lpend = (U4)0U;
    }
    else if(u1_a_EAGR == (U1)OXCAN_SYSEA_EAGR_2ND){
        u4_t_lpcnt = (U4)u1_g_OXCAN_SYSEA_EA2_BGN;
        u4_t_lpend = (U4)u1_g_OXCAN_SYSEA_NUM_ACT;
    }
    else if(u1_g_OXCAN_SYSEA_EA2_BGN < u1_g_OXCAN_SYSEA_NUM_ACT){
        u4_t_lpcnt = (U4)0U;
        u4_t_lpend = (U4)u1_g_OXCAN_SYSEA_EA2_BGN;
    }
    else{
        u4_t_lpcnt = (U4)0U;
        u4_t_lpend = (U4)u1_g_OXCAN_SYSEA_NUM_ACT;
    }

    while(u4_t_lpcnt < u4_t_lpend){

        u2_t_elpsd = st_gp_OXCAN_SYSEA_ACT[u4_t_lpcnt].u2_tim_idx;
        u2_t_elpsd = u2_gp_oxcan_sysea_tim_elpsd[u2_t_elpsd];
        if((u2_t_elpsd                                  == st_gp_OXCAN_SYSEA_ACT[u4_t_lpcnt].u2_tim_act) &&
           (st_gp_OXCAN_SYSEA_ACT[u4_t_lpcnt].fp_vd_ACT != vdp_PTR_NA                                  )){

            (st_gp_OXCAN_SYSEA_ACT[u4_t_lpcnt].fp_vd_ACT)();
        }
        u4_t_lpcnt++;
    }
}
