/*===================================================================================================================================*/
/*  void BswM_CS_CbkChkWkupSrcInd(void)                                                                                              */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    BswM_CS_CbkChkWkupSrcInd(void)
{
    U4          u4_t_wksrc;
    U4          u4_t_bit;
    U1          u1_t_wkch;

    u4_t_wksrc = u4_s_oxcan_aubif_wksrc;
    u1_t_wkch  = (U1)0U;
    while(u4_t_wksrc != (U4)0U){
        u4_t_bit = u4_t_wksrc & (U4)0x00000001U;
        if(u4_t_bit != (U4)0U){
            ComM_EcuM_WakeUpIndication(u1_t_wkch);
        }
        u4_t_wksrc >>= 1U;
        u1_t_wkch++;
    }
}
