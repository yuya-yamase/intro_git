/*****************************************************************************
  Function      : Mcu_Main_Bon_Init
  Description   : MET/IVIの+B初期化要求
  param[in/out] : none
  return        : none
  Note          : +B OFF→ON時に一度だけCentralからコールするのでRAM初期化などで使用ください。
*****************************************************************************/
void Mcu_Main_Bon_Init( void ){

    /* SYS電源ON/OFF制御 初期化処理 */
    Mcu_Sys_PwrOn_Init();

    /* 非冗長電源ON/OFF制御 初期化処理 */
    Mcu_NoRedun_PwrCtrl_Init();
    
    return;
}

/*****************************************************************************
  Function      : Mcu_Main_Wkup_Init
  Description   : MET/IVIのWakeup初期化要求
  param[in/out] : none
  return        : none
  Note          : Sleep→Wakeup時に一度だけCentralからコールするのでRAM初期化などで使用ください。
*****************************************************************************/
void Mcu_Main_Wkup_Init( void ){

    /* SYS電源ON/OFF制御 初期化処理 */
    Mcu_Sys_PwrOn_Init();

    /* 非冗長電源ON/OFF制御 初期化処理 */
    Mcu_NoRedun_PwrCtrl_Init();

    return;
}

/*****************************************************************************
  Function      : Mcu_Main_Pwrsup_Function
  Description   : MET/IVI定期処理(5ms)
  param[in/out] : none
  return        : none
  Note          : Centralからコール
                  起動/終了処理に関わる処理のみ実施すること。
                  起動/終了処理が完了している区間ではコールされない。
*****************************************************************************/
void Mcu_Main_Pwrsup_Function( void ){

    /* SYS電源ON/OFF制御 メインタスク */
    Mcu_Sys_PwrOn_MainFunction();

    /* 非冗長電源ON/OFF制御 メインタスク */
    Mcu_NoRedun_PwrCtrl_MainFunction();
	
	/* Port更新処理 */	
	Mcu_Dev_Pwron_WritePort();
    
    return;
}

