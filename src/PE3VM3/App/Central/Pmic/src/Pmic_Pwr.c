/*****************************************************************************
  Function      : Pmic_PowerOnEthSwitch
  Description   : PMIC sets the level of the ETH power supply to HIGH
  param[in/out] : none
  return        : none
  Note          : none
*****************************************************************************/
void Pmic_PowerOnEthSwitch( void )
{
    (void)Dio_WriteChannel(DIO_ID_APORT4_CH11, STD_HIGH);
    return;
}


/*****************************************************************************
  Function      : Pmic_PowerOffEthSwitch
  Description   : PMIC sets the level of the ETH power supply to LOW
  param[in/out] : none
  return        : none
  Note          : none
*****************************************************************************/
void Pmic_PowerOffEthSwitch( void )
{
    (void)Dio_WriteChannel(DIO_ID_APORT4_CH11, STD_LOW);
    return;
}
