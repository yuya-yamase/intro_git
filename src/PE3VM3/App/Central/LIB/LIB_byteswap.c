uint16 LIB_htons(uint16 val)
{
	uint16	ret;


	ret  =         (val >> 8U);
	ret |= (uint16)(val << 8U);

	return ret;
}
/* ------------------------------------------------------------------------ */
uint32 LIB_htonl(uint32 val)
{
	uint32	ret;


	ret  = (val >> 24U);
	ret |= (val >>  8U) & 0x0000FF00U;
	ret |= (val <<  8U) & 0x00FF0000U;
	ret |= (val << 24U);

	return ret;
}
/* ------------------------------------------------------------------------ */
uint16 LIB_ntohs(uint16 val)
{
	uint16	ret;


	ret  =         (val >> 8U);
	ret |= (uint16)(val << 8U);

	return ret;
}
