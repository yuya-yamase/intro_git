#define LSB_SRCH_SHFT_1ST                        (1U)
#define LSB_SRCH_SHFT_2ND                        (2U)
#define LSB_SRCH_SHFT_3RD                        (4U)
#define LSB_SRCH_SHFT_4TH                        (8U)
#define LSB_SRCH_SHFT_5TH                        (16U)
#define LSB_SRCH_MASK_1ST                        (0x55555555U)
#define LSB_SRCH_MASK_2ND                        (0x33333333U)
#define LSB_SRCH_MASK_3RD                        (0x0F0F0F0FU)
#define LSB_SRCH_SUBVAL                          (1U)
#define LSB_SRCH_OFFSET                          (1U)
#define LSB_SRCH_MAX_MASK                        (0x0000001FU)

