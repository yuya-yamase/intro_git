/*  U4      u4_g_Crc32(const U4 u4_a_INIT, const U1 * const u1_ap_DATA, const U4 u4_a_NUM_BYTE, const U1 u1_a_FNL_XOR)               */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      --> u4_a_INIT      : CRC32 calculation initialize value                                                          */
/*                  --> *u1_ap_DATA    : CRC32 calculation start address                                                             */
/*                  --> u4_a_NUM_BYTE  : CRC32 calculation size                                                                      */
/*                  --> u1_a_FNL_XOR   : CRC32 calculation final XOR                                                                 */
/*  Return:          U4 u4_t_ret       : CRC32 calculation result                                                                    */
/*===================================================================================================================================*/
U4      u4_g_Crc32(const U4 u4_a_INIT, const U1 * const u1_ap_DATA, const U4 u4_a_NUM_BYTE, const U1 u1_a_FNL_XOR)
{
    U4  u4_t_ret;
    U4  u4_t_cnt;
    U4  u4_t_element;

    u4_t_ret = u4_a_INIT;

    for(u4_t_cnt = (U4)0U; u4_t_cnt < u4_a_NUM_BYTE ; u4_t_cnt++){
        u4_t_element = (u4_t_ret ^ (U4)u1_ap_DATA[u4_t_cnt]) & (U4)CRC32_BITMASK;
        u4_t_ret = u4_sp_CRC32_TABLE[u4_t_element] ^ (u4_t_ret >> CRC32_BITSIZE_U1);
    }

    if(u1_a_FNL_XOR == (U1)TRUE){
        u4_t_ret ^= (U4)CRC32_REVERSE;
    }

    return(u4_t_ret);
}
