/*===================================================================================================================================*/
/*  void    vd_g_Gpt_FrtInit(void)                                                                                                   */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
void    vd_g_Gpt_FrtInit(void)
{
    volatile U4 *            u4_tp_word;

    u4_tp_word = (volatile U4 *)GPT_DRV_FRT_RA_BASE;
    vd_REG_U4_WRITE(u4_tp_word[GPT_DRV_FRT_RO_RMSK], (U4)FALSE);
    vd_REG_U4_WRITE(u4_tp_word[GPT_DRV_FRT_RO_TCS],  (U4)TRUE);
    vd_s_SYNCP_W(&u4_tp_word[GPT_DRV_FRT_RO_CSTR]);
}
/*===================================================================================================================================*/
/*  U4      u4_g_Gpt_FrtGetUsElapsed(U4 * u4_ap_frtstamp)                                                                            */
/* --------------------------------------------------------------------------------------------------------------------------------- */
/*  Arguments:      -                                                                                                                */
/*  Return:         -                                                                                                                */
/*===================================================================================================================================*/
U4      u4_g_Gpt_FrtGetUsElapsed(U4 * u4_ap_frtstamp)
{
    U4                  u4_t_elapsed;

    u4_t_elapsed = u4_REG_READ(*u4p_g_GPT_FRT_RA);
    if(u4_ap_frtstamp != vdp_PTR_NA){
        u4_ap_frtstamp[GPT_FRT_USELPSD_CRRNT]  = u4_t_elapsed;
        u4_t_elapsed                          -= u4_ap_frtstamp[GPT_FRT_USELPSD_BASE];
    }

    return(u4_t_elapsed);
}

