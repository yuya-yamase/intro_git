#define ICU_WK_ELC_NUM_WORD                     (6U)             /* 4 bits x 41 channel = 164 bits / 32 bits = 6 words */
typedef struct{
    U4                      u4p_elc[ICU_WK_ELC_NUM_WORD];    /* edge/level configuration                           */
    U4                      u4p_ena[ICU_WK_NUM_GR];
    U4                      u4p_clr[ICU_WK_NUM_GR];
}ST_ICU_WK;

typedef struct{
    U4                      u4_wrqbit;                           /* wake-up req. bit                                   */
    U2                      u2_ro;                               /* register address offset                            */
                                                                 /* base/FCLACTL0 = 0xffed4600U                        */
    U1                      u1_irq_off;                          /* IRQ Channel offset                                 */
                                                                 /* base/INTP0 = 544                                   */
    U1                      u1_clsb;                             /* edge/level configuration lsb                       */
}ST_ICU_EL;

typedef struct{
    const ST_ICU_EL *       stp_EL;
    U4                      u4_wrqbit;
    U4                      u4_erqbit;
    U2                      u2_num_el;
    U2                      u2_ro;                               /* register address offset                            */
                                                                 /* base/STBC_WUF0_A0 = 0xFF98E000U                    */
}ST_ICU_WK_GR;

typedef struct{
    U4                      u4_raddr;
    U4                      u4_bit;
}ST_ICU_WK_INVLD;

